

<h3>TM Dashboard</h3>

<p>
	TM Dashboard is a service plugin. It will help you to update template version easily.
</p>

<p>After plugin installation it adds two new blocks: <strong>Dashdoard</strong> and <strong>Updates</strong>.</p>


