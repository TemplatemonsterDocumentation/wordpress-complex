<h3>Easy Twitter Feed Widget Plugin</h3>

<p>
	<a href="https://wordpress.org/plugins/easy-twitter-feed-widget/">Easy Twitter Feed Widget</a> to display tweets on your WordPress site without creating an API .  the plugin provides a nice interface to implement your tweets in a quick and easy way.

	If you are planning to display the tweets on your blogsidebar without bells and whistles, our plugin can be an ideal choice.
</p>

