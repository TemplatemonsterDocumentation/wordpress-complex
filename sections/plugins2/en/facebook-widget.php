<h3>Facebook Widget</h3>
<p>
	This <a href="https://wordpress.org/plugins/facebook-pagelike-widget/">widget</a> ensures the most simple and effective way to display Facebook page likes on your WordPress sidebar. It is very easy to configure it right from the Admin area. You just need to activate the plugin and drag this widget to the area you need to add it. Just add the application ID from your created Facebook application into the  widget and also the URL of your Facebook page. You also have other options like show faces , show Data Stream and Header.
</p>
