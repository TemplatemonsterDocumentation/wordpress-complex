<h4>General Settings</h4>

<p>Here you can set the effect for the flipbox animation and access the height and border settings.</p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/blocks/ab2.png">
  	</figure>

<ul class="marked-list">
<li><p><b>Animated Effect</b></p>

<ul class="marked-list">
<li>flip vertical / horizontal;</li>
<li>fall up / right;</li>
<li>slide down / ght;</li>
<li>flip vertical 3D;</li>
<li>flip horizontal 3D.</li></ul></li>

<li><b>Height</b> - here you can set the custom height for the flipbox in px using the control.</li>
<li><b>Boder Radius</b> - change the border radius value to make the border angles more smooth and round.</li></ul>



<h4>Front / Back Settings</h4>

<p>In these blocks you can change the appearance of the flipbox from the front and back side.</p>

<ul class="marked-list">
<li><p><b>Front / Back Title Settings</b></p>

<ul class="marked-list">
<li><b>Heading Tag</b> - choose the tag to use for the flipbox heading (<b>H1 - H6</b> headings).</li>
<li><b>Size</b> - here you can define the size of the title text <b>(S (small)</b>, <b>M (medium)</b>, <b>L (large)</b> and <b>XL (extra large)</b>. Click Reset button to reset the settings to the default ones.</li>
<li><b>Custom Size</b> - use the control to set the size of the title.</li>
<li><b>Color</b>- here you can specify the color of the title using color palette or color picker tool.</ul></li></ul>



<ul class="marked-list">
<li><p><b>Front / Back Description Settings</b></p>

<ul class="marked-list">
<li><b>Heading Tag</b> - choose the tag to use for the flipbox description (<b>H1 - H6</b> headings).</li>
<li><b>Size</b> - here you can define the size of the description text <b>(S (small)</b>, <b>M (medium)</b>, <b>L (large)</b> and <b>XL (extra large)</b>. Click Reset button to reset the settings to the default ones.</li>
<li><b>Custom Size</b> - use the control to set the size of the description.</li>
<li><b>Color</b>- here you can specify the color of the description using color palette or color picker tool.</ul></li></ul>

<ul class="marked-list">
<li><p><b>Front / Back Panel Color</b> - here you can set the color to use for the flipbox block as a background for its content.</p></li></ul>

<ul class="marked-list">
<li><p><b>Front / Back Panel Border</b></p>
<ul class="marked-list">
<li><b>Border Width</b> - here you can specify the width of the border to use for the flipbox.</li>
<li><b>Border Color</b> - here you can set the color to apply for the flipbox border  using color palette or color picker tool.</li></ul></li></ul>

<ul class="marked-list">
<li><p><b>Front / Back Panel Image</b></p>
<ul class="marked-list">
<li><b>Select Image</b> - use this button to upload an image to use in the flipbox as a background from your hard drive or select an image from the media library.</li>
<li><b>Image Size</b> - here you can set the size to use for the image (there are only the default image sizes that are available in the theme oneâ€™s using).</li>














