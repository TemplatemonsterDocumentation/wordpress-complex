<h4>General Settings</h4>

<p>In this block you can customize the content that should be displayed in the Image Comparison block: the <b>Before</b> and <b>After</b> images.</p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/blocks/ic2.png">
  	</figure>

<h5>Image Before</h5>

<ul class="marked-list">
<li><b>Label Before</b> - here you can specify the title to show up for the image that is displayed in Before block area.</li>

<li><b>Select Image Before</b> - click this option in order to access the media library and select the image from the already downloaded ones, or upload it to your site. This image will be displayed in the left Before part of the block.</li></ul>

<h5>Image After</h5>
<ul class="marked-list">
<li><b>Label After</b> - here you can specify the title to show up for the image that is displayed in After block area.</li>

<li><b>Select Image After</b> - click this option in order to access the media library and select the image from the already downloaded ones, or upload it to your site. This image will be displayed in the right After part of the block.</li>

<li><b>Start Position</b> - use the switcher to set the initial position of the Before/After control (by default, it is set to 50 and shows 50% from the left Before area and 50% from the After area on the right).</li></ul>

<h4>Style Settings</h4>

<p>Open the <b>Style</b> settings tab to access the color controls, apply a border, change labels appearance using the visual editing mode.</p>

<h5>Controls</h5>
<ul class="marked-list">
<li><b>Controls Color</b> - here you can set one of the color available in the palette or use color picker tool to define the color of the control that separates the left part from the right one and enables visitors to drag it in order to view more of the Before or After image to compare them.</li></ul>

<h5>Border</h5>
<ul class="marked-list">
<li><b>Border Width</b> - here you can specify the width of the border to use for the Before/After images container that will be applied around the block containing both of the images.</li>

<li><b>Color</b> - here you can specify the color to use for the border using color picker or the color palette.</li></ul>

<h5>Labels</h5>
<ul class="marked-list">
<li><b>Labels color</b> - here you can set the custom color to use for the label which marks the image as the one set for Before or After block.</li>

<li><b>Labels Background </b>- here you can specify the color to use for the labels background.</li>

<li><b>Labels Opacity</b> - here you can set the opacity for the labels to make them more transparent in percents.</li></ul>
