<h2>Progress Bar</h2>

<h6>
Progress bar block allows adding attractive progress bars to showcase the progress of the process you’re displaying. With this block you’ll be able to change the progress value, change the layout style by selecting one of the 6 styles, and customize the style of the bar.
</h6>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/blocks/pb1.png">
  	</figure>

<h4>General Settings</h4>

<p>Here you can change the content shown in the progress bar and the general layout of the progress bar line.</p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/blocks/pb2.png">
  	</figure>

<ul class="marked-list">
<li><b>Progress Value</b> - here you can define the percentage value that will be displayed in the progress bar. Use the control to set the needed number of percents.</li>

<li><b>Style</b> - here you can select one of the 6 different stylish layouts for the progress bar in order to position the number of percents, the bar title and the bar itself according to your needs.</li></ul>

<h5>Sizing Settings</h5>
<ul class="marked-list">
<li><b>Progress Bar Height</b> - here you can change the height for the bar in px using the control.</li></ul>

<h4>Style Settings</h4>

Title
<ul class="marked-list">
<li><b>Size</b> - here you can define the size of the title text (S (small), M (medium), L (large) and XL (extra large). Click Reset button to reset the settings to the default ones.</li>

<li><b>Custom Size</b> - use the control to set the size of the title font.</li>

<li><b>Color</b> - here you can specify the color of the title using color palette or color picker tool.</li></ul>

<h5>Value</h5>
<ul class="marked-list">
<li><b>Size</b> - here you can define the size of the value text (S (small), M (medium), L (large) and XL (extra large). Click Reset button to reset the settings to the default ones.</li>

<li><b>Custom Size</b> - use the control to set the size of the value characters.</li>

<li><b>Color</b> - here you can specify the color of the value characters using color palette or color picker tool.</li></ul>

<h5>Progress Bar</h5>
<ul class="marked-list">
<li><b>Progress Bar Status Color</b> - here you can specify the color that is used when the bar is filled.</li>

<li><b>Progress Bar Color</b> - here you can set the color to use for the empty part of the progress bar.</li></ul>
