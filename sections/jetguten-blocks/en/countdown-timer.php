<h2>Countdown Timer</h2>



<h6>
Countdown Timer block allows to display the countdown to the set date in the future using the simple countdown that displays the days, hours, minutes and seconds. The block has easily manageable settings, allowing to change the size of the timer, display the separator, change value, label and panel settings.
</h6>

<h4>Creating Countdown Block</h4>

<p>If you click on the block saettings you will see the pop-up window where you can set the labels: <b>days, hours, minutes</b> and <b>seconds</b>.</p>

<h5>General Settings</h5>

<ul class="marked-list">
<li><b>Size</b> - here you can set the size of the block. Just  drag the control to change the value.</li>
<li><b>Show Separator</b> - if you click this option the separator (a colon between days, hours, minutes and seconds) will be shown in the block.</li></ul>

<h5>Value Settings</h5>
<li><b>Size</b> - here you can set the blockâ€™s size. There are S, M, L and XL sizes, you can choose one of them. If you click Reset, the settings will be canceled.</li>
<li><b>Custom Size</b> - this option allows to set customized size.  Just  drag the control to change its value.</li>
<li><b>Color</b> - here you can choose the color of the numbers and the words: days, hours, minutes and seconds (if Show labels option is active).</li></ul>

<h5>Label Settings</h5>
<li><b>Show labels</b> - if you want days, hours, minutes and seconds labels to be displayed in the block,  you need to click here.</li>
<li><b>Size</b> - here you can choose the appropriate size of the labels. There are S, M, L and XL sizes, you can choose one of them. If you click Reset, the setting will be canceled.</li>
<li><b>Custom Size</b> - this option allows to set customized size.  just  drag the control to change its value.</li>
<li><b>Color</b> - here you can choose the color of the labels.</li></ul>
<h5>Panel Settings</h5>
<li><b>Panel Color</b> - here you can choose the color of background.</li>
<li><b>Border Radius</b> - set the radius of the borders.  Just  drag the control to change its value.</li>
<li><b>Border Width</b> - set the width of the borders.  Just  drag the control to change its value.</li>
<li><b>Border Color</b> - choose the color of the borders from the variety of colors.</li></ul>
