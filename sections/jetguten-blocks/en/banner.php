<h4>Content</h4>


<ul class="marked-list">
<li><p><b>Upload Image</b> - click the button to access the media library and select the image to use for the banner, or upload one from your hard drive.</p></li></ul>

<h4>General Settings</h4>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/blocks/b2.png">
  	</figure>

<ul class="marked-list">
<li><b>Image Size</b> - here you can select the size of the image to use for the banner (the sizes are pulled from the available size list in the theme).</li>

<li><b>Effect</b> - here you can select the effect to use for the banner from 11 different animation effects.</li></ul>

<h5>Title Settings</h5>

<ul class="marked-list">
<li><b>Heading Tag</b> - choose the tag to use for the banner heading (H1 - H6 headings).</li>

<li><b>Size</b> - here you can define the size of the banner title text (S (small), M (medium), L (large) and XL (extra large). Click Reset button to reset the settings to the default ones.</li>

<li><b>Custom Size</b> - use the control to set the size of the title.</li>


<li><b>Color</b> - here you can specify the color of the title using color palette or color picker tool.</li></ul>

<h5>Description Settings</h5>

<ul class="marked-list">
<li><b>Size</b> - here you can define the size of the banner description text (S (small), M (medium), L (large) and XL (extra large). Click Reset button to reset the settings to the default ones.</li>

<li><b>Custom Size</b> - use the control to set the size of the description.</li>

<li><b>Color</b> -  here you can specify the color of the description using color palette or color picker tool.</li></ul>

<h4>Overlay Settings</h4>

<ul class="marked-list">
<li><b>Overlay Opacity</b> - here you can define the transparency percentage of the color overlay set for the banner.</li>

<li><b>Overlay Color</b> - here you can specify the color to use for the overlay.</li></ul>
