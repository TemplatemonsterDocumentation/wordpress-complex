<h2>Map</h2>

<h6>
Map block is a perfect solution if you’re looking for an easy way to add a Google map to the page built with Gutenberg. The block provides access to the general settings, such as Address, Map or Satellite view, zoom level, etc.
</h6>


<h4>General Settings</h4>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/blocks/m1.png">
  	</figure>

<p>Here you can specify the content that is to be shown in the Map block.</p>

<ul class="marked-list">
<li><b>Address</b> - fill in the needed  address to display it on the map.</li>

<li><b>Content</b> - here you can specify if you want to show the map using the basic map mode or the satellite layout.</li>

<li><b>Zoom Level</b> - here you can specify the initial zoom to apply for the map (the visitor will be able to zoom the map in and out according to his wishes, however, when the map opens it will have the scale you’ve set.</li>

<li><b>Height</b> - here you can change the height of the map to use in px. Drag the control to set it according to your needs.</li></ul>
