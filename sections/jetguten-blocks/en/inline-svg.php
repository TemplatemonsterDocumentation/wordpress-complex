<h4>General</h4>

<p>In this section you can select the SVG image to be shown on the page built with Gutenberg without using any extra plugins for uploading and integrating SVG format into WordPress.</p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/blocks/svg2.png">
  	</figure>

<ul class="marked-list">
<li><b>Select image</b> - click this option to upload an SVG image to the media library or pick one from it if it was already uploaded.</li></ul>

<h4>Style Settings</h4>

<p>In this section you can set the width and height of the SVG image to the preferable ones, and set the different coloring for the image.</p>

<ul class="marked-list">
<li><b>Image Width</b> - here you can customize the width value of the SVG image.</li>

<li><b>Image Height</b> - here you can change the height value to apply for the SVG image.</li></ul>

