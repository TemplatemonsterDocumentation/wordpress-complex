<h4>

<h4>General Settings</h4>

<p>Here you can set up the content of the block to display it with Circle Progress.</p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/blocks/cp2.png">
  	</figure>

<ul class="marked-list">
<li><b>Value Number</b> - here you can set the value of the progress thatâ€™s already accomplished.</li>

<li><b>Max Value</b> - here you can define the maximum value to use for the circle progress (by default it is set to 100 (100%), however, you can define your own value according to your needs.</li>

<li><b>Value Number Prefix</b> - add the text or any characters to show up before the progress value.</li>

<li><b>Value Number Suffix</b>  - add the text or any characters to display after the progress value.</li></ul>

<h5>Circle Settings</h5>

<ul class="marked-list">
<li><b>Circle Size</b> - use the controls to define the size of the circle in px.</li>

<li><b>Stroke Color</b> - here you can specify the color to use for the filled part of the circle what stands for the accomplished progress.</li>

<li><b>Stroke Width</b> - use the control to define the preferable width of the stroke in px.</li>

<li><b>Background Stroke Color</b> - set the color to use for the circle background that shows up in the area where the circle is not filled.</li>

<li><b>Background Stroke Width</b> - here you can specify the width of the background stroke of the empty part of the circle.</li>

<li><b>Circle Fill Color</b> - add the fill to use the custom color inside the circle progress.</li></ul>

<h5>Value Settings</h5>

<ul class="marked-list">
<li><b>Value Position</b> - here you can set the preferable position of the progress value:
<ul class="marked-list">
<li>Inside progress circle;</li>
<li>Below progress circle.</li></ul></li>

<li><b>Number Font Size</b> - here you can define the size of the value text (S (small), M (medium), L (large) and XL (extra large). Click Reset button to reset the settings to the default ones.</li>

<li><b>Custom Size</b> -  use the control to set the size of the progress value.</li>

<li><b>Color</b> -  here you can specify the color of the progress value using color palette or color picker tool.
</li>
<li><b>Prefix Font Size</b> - here you can define the size of the prefix text (S (small), M (medium), L (large) and XL (extra large). Click Reset button to reset the settings to the default ones.</li>

<li><b>Custom Size</b> - use the control to set the size of the prefix.</li>

<li><b>Prefix Color</b> - here you can specify the color of the prefix using color palette or color picker tool.</li>

<li><b>Suffix Font Size</b> - here you can define the size of the suffix text (S (small), M (medium), L (large) and XL (extra large). Click Reset button to reset the settings to the default ones.</li>

<li><b>Custom Size</b> - use the control to set the size of the suffix.</li>

<li><b>Suffix Color</b> -  here you can specify the color of the suffix using color palette or color picker tool.</li></ul>

<h5>Label Settings</h5>

<ul class="marked-list">
<li><b>Title Tag</b> - choose the tag to use for the circle progress title (H1 - H6 headings).</li>

<li><b>Title / Subtitle Font Size</b> - here you can define the size of the title text (S (small), M (medium), L (large) and XL (extra large). Click Reset button to reset the settings to the default ones.
</li>
<li><b>Title / Subtitle Custom Size</b> -  use the control to set the size of the title.</li>

<li><b>Title / Subtitle Color</b> -  here you can specify the color of the title using color palette or color picker tool.</li></ul>
