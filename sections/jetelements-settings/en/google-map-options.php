<h3>Google Map Options</h3>

<p>In this block you can input an API key needed for the proper work of Advanced Map widget. </p>

<ul class="marked-list">
<p><li><b>API Key</b> - here you can input an API key to use it for the Advanced Map widget. In the case You don't have it, please, navigate <a href="https://developers.google.com/maps/documentation/javascript/get-api-key" rel="nofollow">here</a>.</li></p>
<p><li><b>Disable Maps API JS File</b> - check this option in order to disable the Google Maps API JS file in the case it already exists in another plugin or theme. </li></p></ul>

