<h3>Mailing List Manager</h3>

<h5>In this block you can add a MailChimp API key to use it in Subscribe Form widget.</h5>

<ul class="index-list">
<p><li>First, let’s add the <b>MailChimp List ID</b> and <b>API Key</b> to JetElements to connect the Subscribe Form widget to it. Navigate to <b>Elementor &gt; JetElements Settings</b> tab on the left of the Dashboard.</li></p>

 	<figure class="img-polaroid">
        <img src="projects/jetelements/img/widgets/subscr3.png">
  	</figure>

<p><li>Here switch to <b>Mailing List Manager</b> settings block.</li></p>

<p><li>Add the <b>MailChimp API key</b> and the <b>MailChimp List ID</b> in the corresponding fields.</li></p>

<p><li>If you don’t know where to get this information, please, feel free to read <a href="https://zemez.io/wordpress/support/how-to/how-to-add-mailchimp-widget-to-sidebar/" target="_blank">this</a> tutorial.</li></p>

<p><li>After that you need to navigate to the Elementor page editor and drop the <b>Subscribe Form</b> widget to the section where you need to add it.</li></p>

</ul>


<ul class="marked-list">
	<p><li><b>Double opt-in</b> - enable this option if you want to send contacts an opt-in confirmation email when they subscribe to your list.</li></p></ul>
