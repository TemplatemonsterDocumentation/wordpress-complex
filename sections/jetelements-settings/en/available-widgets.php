<h3>Available Widgets</h3>

<p>In this block you can change the list of available widgets that will show up in JetElements block on Elementor live page builder editing page.</p>

<p>This option is useful for you when:</p>


<ul class="marked-list">
<p><li>you want to conceal the rarely used addons to access the most frequently used ones more quickly;</li></p>
<p><li>you want Elementor work faster - in this case disabling some of the rarely used addons will speed up the loading time.</li></p></ul>

<p>You might also consider disabling some of the rarely used addons when you're facing the Memory Limit Exhausted error, and you can't boost up the memory limit on your server.</p>


<p>Just remove the checks for those addons you want to disable and click <b>Save</b> button to save the changes.</p>

