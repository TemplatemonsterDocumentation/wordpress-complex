<h2>TM Timeline</h2>
<p>
Use this professionally designed plugin to create and manage your events
and display them via custom crafted layouts. You're able to group your events by tags, which
allow you to display individual events in a multiple timelines. Simply generate a shortcode
on a Settings page and paste it into your post and you will get a nice timeline widget.
You're also able to customize the dates and show or hide anchor to an event.
</p>
