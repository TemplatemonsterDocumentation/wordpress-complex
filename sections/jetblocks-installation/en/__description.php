<h3>JetBlocks Installation</h3>

<h5>This block will give you information on how to install <b>JetBlocks</b> plugin to Your website manually. </h5>

<ol class="index-list">
<p><li>First, make sure that you’ve installed and activated <a href="https://wordpress.org/plugins/elementor/" target="_blank">Elementor</a> page builder before proceeding to JetBlocks installation. If you have problems with installing Elementor feel free to read the detailed <a href="https://docs.elementor.com/" target="_blank">Elementor documentation</a>.</li></p>

<p><li>Log in to <b>Wordpress Dashboard</b> using Your login credentials and navigate to <b>Plugins</b> tab in the left column.

</li></p>

<p><li>Click on <b>Add new</b> button to upload JetBlocks plugin from Your hard drive.

 	<figure class="img-polaroid">
        <img src="projects/jetelements/img/inst/1.png">
  	</figure>
</li></p>

<p><li>Click on <b>Upload</b> button, which can be found at the top of <b>Plugins</b> page. Browse the path to JetBlocks plugin and start installation. </li></p>

 	<figure class="img-polaroid">
        <img src="projects/jetelements/img/inst/2.png">
  	</figure>
</li></p>

<p><li>When JetBlocks plugin is uploaded and installed you’ll need to activate it. Navigate to <b>Plugins</b> tab and locate JetBlocks plugin in the list of installed plugins. Click on <b>Activate</b> link under JetBlocks. As a result the plugin will become active.

 	<figure class="img-polaroid">
        <img src="projects/jetelements/img/inst/3.png">
  	</figure>
</li></p>
