</p>

<h4>Downloads</h4>

	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/quicksart/downloads.png">
  	</figure>

<p>In this <a href="https://account.crocoblock.com/#downloads" target="_blank">block</a> you can find all the products available for using and installing with your CrocoBlock subscription under the terms of your current license.</p>

<p><b>Quick Installation Pack</b> - this pack contains the must-have products if you need the simple installation and are planning to use <b>Kava</b> theme along with <b>Jet plugins</b> and <b>demo content</b>. It includes:</p>

<ul class="marked-list">
<p><li><b>Kava theme;</b></li></p>
<p><li><b>Kava Child Theme</b> (recommended to install and use);</li></p>
<p><li><b>Jet Plugins Wizard</b> - the tool which installs the plugins and demo content automatically.</li></p>

<p><li><b>Plugins</b> - the block contains the latest version of plugins which are now available for installation and usage. The list can be updated with time as more plugins roll out.</li></p>

<p><li><b>Bonus Images</b> - the block contains the high quality image packages on different topics available for usage.</li></p></ul>


