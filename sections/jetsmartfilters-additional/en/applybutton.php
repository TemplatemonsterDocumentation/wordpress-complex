<h2>Apply Button</h2>

<h6>Apply Button widget allows adding apply button functionality to the filters. You can use Apply button widget in case you have several filters and want to adjust apply button with the same settings to them.</h6>

<h4>Content</h4>

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/applybutton1.png">
    </figure></li></p>

<ul class="marked-list">
<p><li><b>This filter for</b> - here you need to select the listing or archive for which you want to apply the current filter.</li>
<p><li><b>Apply Type</b> - here you can choose which type is preferable for the apply button. An AJAX type allows apply button using AJAX method without reloading the page, which makes viewing the products much faster for the visitors. The Page Reload filter reloads the page each time the visitor uses the apply button.</li>
<p><li><b>Apply button text</b> - here you can specify the text of the apply button.</li>
<p><li><b>Query ID</b> - in case you’re using more than one filter and display more than one listing grid, products grid, etc., on the page, you can use the Query ID option to define which filter is applied to which widget.</li></p></ul>

