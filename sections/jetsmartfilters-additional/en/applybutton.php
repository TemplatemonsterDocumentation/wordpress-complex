<h4>Button</h4>
<ul class="marked-list">
<p><li><b>Typography</b> -  turn the option on to view the typography settings.</li>

<p>Switch between <b>Normal</b> and <b>Hover</b> modes to customize the appearance of both.</p>

<p><li><b>Text Color</b> - here you can select the color for the text on the apply button.</li>
<p><li><b>Background color</b> - here you can set the color to apply for apply button.</li>
<p><li><b>Border type</b> - here you can specify the type of the border to apply for the apply button. After that, you’ll get access to border color and width.</li>
<p><li><b>Border radius</b> - specify the responsive border to apply for the apply button.</li>
<p><li><b>Box shadow</b> - enable this option if you want to access the shadow advanced settings for apply button, and need to apply shadow for it.</li>
<p><li><b>Padding</b> - here you can set the responsive padding for the apply button.</li>
<p><li><b>Margin</b> - here you can specify the margins values to set them for the apply button.</li>
<p><li><b>Alignment</b> - here you can change the apply button’s alignment to left, center or right.</li></p>
