<h2>Pagination</h2>


<h6>
In case you want to create the AJAX-based pagination and allow using it for the specific widget listing the products or posts, the Pagination widget can assist you in adding such an element to the page and provides multiple stylization options for the pagination elements.

</h6>


<h4>Content</h4>

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/w-15.png">
    </figure></li></p>


<ul class="marked-list">
<p><li><b>Pagination for</b> - here you can select for which element you want to add the pagination element.</li>
<p><li><b>Apply Type</b> - here you can choose which type is preferable for this pagination element. An AJAX type allows pagination using AJAX method without reloading the page, which makes viewing the products much faster for the visitors. The Page Reload filter reloads the page each time the visitor uses the pagination.</li>
<p><li><b>Query ID</b> - in case you’re using more than one filter and display more than one listing grid, products grid, etc., on the page, you can use the Query ID option to define which filter is applied to which widget.</li></p></ul>



<h4>Style</h4>

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/w-16.png">
    </figure></li></p>

<h4>Pagination</h4>
<ul class="marked-list">
<p><li><b>Background color</b> - here you can set the color to apply for the background.</li>
<p><li><b>Border type</b> - here you can specify the type of the border to apply for the pagination. After that, you’ll get access to border color and width.</li>
<p><li><b>Border radius</b> - specify the responsive border to apply for the pagination.</li>
<p><li><b>Box shadow</b>- apply the Shadow option for the pagination block.</li>
<p><li><b>Padding</b> - here you can set the responsive padding for the block.</li>
<p><li><b>Margin</b> -  here you can set the responsive margin for the block.</li>

<h4>Items</h4>
<ul class="marked-list">
<p><li><b>Typography</b> -  turn the option on to view the typography settings. They are similar to the ones for the Checkbox label.</li>

<p>Switch between <b>Normal</b>, <b>Hover</b> and <b>Current</b> modes to customize the appearance of both.</p>

<p><li><b>Background color</b> - here you can set the color to apply for the item background.</li>
<p><li><b>Text color</b> - here you can specify the color to use for the text.</li>
<p><li><b>Padding</b> - here you can set the responsive padding for the item.</li>
<p><li><b>Gap between items</b> - here you can specify the gap distance to set between the items.</li>
<p><li><b>Border type</b> - here you can specify the type of the border to apply for the item. After that, you’ll get access to border color and width.</li>
<p><li><b>Border radius</b> - specify the responsive border to apply around the item.</li>

<p><li><b>Alignment</b> - here you can change the widget’s alignment to left, center or right.</li></p>
