<h2>Active Filters</h2>

<h6>
Use this widget to enable the visitors apply the specific filters only and disable the filters from being applied.
</h6>

<h4>Content</h4>

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/w-17.png">
    </figure></li></p>

<ul class="marked-list">
<p><li><b>Show active filters for</b> - here you can select for which element you want to show the active filters.</li>
<p><li><b>Apply Type</b> - here you can choose which type is preferable for this filter. An AJAX type allows filtering using AJAX method without reloading the page, which makes viewing the products much faster for the visitors. The Page Reload filter reloads the page each time the visitor uses the filter.</li>
<p><li><b>Query ID</b> - in case you’re using more than one filter and display more than one listing grid, products grid, etc., on the page, you can use the Query ID option to define which filter is applied to which widget.</li></ul>

