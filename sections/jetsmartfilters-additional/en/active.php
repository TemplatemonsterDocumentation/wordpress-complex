<p><li><b>Color</b>- here you can specify the color to use for the content.</li>
<p><li><b>Margin</b> - here you can set the responsive margin for the element.</li></ul>

<h4>Filters</h4>
<ul class="marked-list">
<p><li><b>Filters Position</b> - here you can select the type of position applied for the filter: the inline or columns.</li>
<p><li><b>Vertical Offset</b> - here you can specify the offset between the values.</li>
<p><li><b>Background color</b> - here you can select the proper background color to use for the filter.</li>
<p><li><b>Border type</b> - here you can  specify the type of the border to apply for the element.</li>
<p><li><b>Border radius</b> -  here you can specify the type of the border to apply for the element. After that, you’ll get access to border color and width.</li>
<p><li><b>Box shadow</b> - apply the Shadow option for the element.</li>
<p><li><b>Padding</b> - here you can set the responsive padding for the element.</li></p></ul>


