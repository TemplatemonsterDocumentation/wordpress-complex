<h2>Working with Custom Meta Boxes</h2>

<h5>From this block you’ll learn how to create meta boxes to add any custom meta fields you need to the default posts, taxonomies and custom post types. </h5>


<p>From this video you can learn more about creating custom meta fields with JetEngine.</p>


