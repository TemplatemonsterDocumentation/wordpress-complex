<h4>Adding Meta Boxes</h4>

<ol class="index-list">
<p><li>To add a new meta box to one of the post types or taxonomy type you need to navigate to <b>JetEngine > Meta Boxes</b> block on the left of WordPress Dashboard. Here click <b>Add New</b> button to add a new meta box.

  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/mb/1.png">
  	</figure></li></p>

<p><li>After that you’ll see <b>Add New Meta Box</b> block. It is divided into two sections: <b>General</b> and <b>Meta fields</b>. Let’s take a closer look at them. </li></p></ol>

<h5>General</h5>

  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/mb/2.png">
  	</figure>

<ul class="marked-list">
<li><b>Name</b> - here you need to specify the title for the meta box. </li>

<li><b>Meta Box for</b> - here you have to select the post type or taxonomy, or multiple items for which you want to set this meta box. Depending on the type you’ve selected you’ll be able to set the meta box to specific post type or taxonomy.</li></ul>

<h5>Meta Fields</h5>

  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/mb/3.png">
  	</figure>

<p>Here you can create the meta fields to display for custom post types or taxonomies. </p>

<ol class="index-list">
<p><li>To create a new meta field click <b>+ New Meta Field</b> option. </li></p>

<p><li>In the newly opened block input these kinds of information:

<ul class="marked-list">
<li><b>Title</b> - here you should type in the title that will be shown for the meta field; </li>

<li><b>Name / ID</b> - here you have to provide an ID for the meta field that will be recognized with Elementor widgets when you select the needed meta field to display; </li>

<li><b>Type</b> - here you have to specify the type of content that will be available for adding with this meta field. </li></ul></p>

<p>There are such kinds of possible content available for the meta field:

<ul class="marked-list">
<li><b>Text</b> - will perfectly fit if you want to add an extra field with custom text to the items.</li>
<li><b>Date</b> - will help display the date set for the item as a meta field. You can also save the date as a timestamp to allow to query items by it. </li>
<li><b>Time</b> - allows to add the time marker for the item.</li>
<li><b>Datetime</b> - provides the convenient way to add time and date to the item. You can also save it as a timestamp. </li>
<li><b>Textarea</b> - use this content type to add the text area to the item.</li>
  <li><b>WYSIWYG</b> - use this content type to add the basic editor in the custom field.</li>
<li><b>Switcher</b> - allows displaying switcher for the item. </li>
<li><b>Checkbox</b> - adds the checkboxes for the items. </li>
<li><b>Iconpicker</b> - the tool provides ability to select the icons for displaying them for the item.</li>
<li><b>Media</b> - will help add the images for the item.</li>
  <li><b>Gallery</b> - will help add and display the gallery of multiple images.</li>
<li><b>Radio</b> - allows to add the radio options for choosing when creating the item.</li>
<li><b>Select</b> - use this content type to add dropdown selector for the item.</li>
<li><b>Colorpicker</b> - will add a colorpicker tool for the item. </li>
<li><b>Repeater</b> - will help add the repeating content types for every item of the given type. </li></ul></p></li>

<p><li>After everything is set click <b>Add Meta Box</b> button to save the changes.</li></p>

<p><li>You’ll see the new meta field in the list of fields available for the item to which it was added.

