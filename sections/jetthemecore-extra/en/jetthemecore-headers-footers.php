<h3>Creating Headers & Footers</h3>

<p>From this block one can learn how to create header & footer templates and assign them for different pages. </p>


<ol class="index-list">
<p><li>First, click <b>Add New</b> button located on the right of the <b>Theme Parts</b> title. </li></p>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/core/creating3.png">
  	</figure>

<p><li>In the newly opened window select the template type from the dropdown menu (<b>page, header, footer, section</b>). </li></p>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/core/creating4.png">
  	</figure>

<p><li>Type in the template name in the corresponding field. </li></p>

<p><li>Use Elementor page editor to create a new unique  header / footer. Note, that you also can use the pre-styled headers available in the headers / footers list. In order to access the pre-styled headers / footers click <b>Magic button</b>. </li></p>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/core/creating5.png">
  	</figure>

<p><li>After that navigate to <b>Headers / Footers</b> tab and here  select the header / footer you want to use as a template on your websiteâ€™s pages. </li></p>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/core/creating6.png">
  	</figure>

<p><li>You can use the <b>JetBlocks</b> widgets in order to add the authorization links, login forms, site logos, search or WooCommerce Cart to the header. Feel free to check <a href="http://documentation.zemez.io/wordpress/index.php?project=jetblock&lang=en&section=jetblocks-widgets#" target="_blank"><b>JetBlocks documentation</b></a> which contains the detailed explanations on how the widgets work. </li></p>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/core/creating7.png">
  	</figure>

<p><li>When the header or footer is created navigate to <b>Settings</b> block (located in the bottom left corner).
Here open <b>Conditions</b> sub menu. </li></p>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/core/creating8.png">
  	</figure>

<p><li>In <b>General</b> dropdown menu select to which pages you want to assign this header / footer. </li></p>

<p><li>The header / footer can be assigned: </p>

<ul class="marked-list">
<li>To the entire site;</li>
<li>To Single pages, such as:
<ul class="marked-list">
<li>front page;</li>
<li>post type;</li>
<li>post, post category;</li>
<li>posts from tags;</li>
<li>pages child pages;</li>
<li>page templates (such as Elementor Canvas, Elementor Full width);</li>
<li>404 page;</li></ul>

<li>Archive:
<ul class="marked-list">
<li>all archives;</li>
<li>post type archives;</li>
<li>category archives;</li>
<li>tag archives.</li></p></li></ul></li></li></ul>

<p><li>After everything is done click <b>Publish</b> button in order to preserve the changes and set the header / footer to the selected pages. </li></p>


