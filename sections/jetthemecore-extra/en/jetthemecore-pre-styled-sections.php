<h3>Using Pre-Styled Sections</h3>

<p>From this block you can learn how to use the pre-styled sections available when clicking on <b>Magic Button</b>.</p>



<ol class="index-list">
<p><li>In order to access the pre-made stylish sections open Elementor editor.</li></p>

<p><li>Here you can see the three buttons. The <b>Magic Button</b> is located on the left of Add new section button. Click it to view all the pre-styled content available for usage.</li></p>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/core/creating5.png">
  	</figure>

<p><li>The content is separated into <b>Pages, Headers, Footers, Sections</b> and <b>My Library</b>. Let’s overview each of the sections.</li></p>


<ul class="marked-list">
<p><li><b>Pages</b> - the block contains different pages on various topics available for usage (such as <b>Home, About, Services,</b> etc). You can see the page categories on the left.</p>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/core/creating9.png">
  	</figure>

<p>You can also search the page using the keywords. To do it input the keywords into the <b>Search</b> field at the top of the block.</p>

<p>Click on the page to preview it. You can also insert it to the page you’re currently editing, and clone it to the library.</p></li>

<p><li><b>Headers</b> - the block contains all the headers available for your use. Please, note, that in order to apply the headers to different pages you need to create a header template and assign it to the needed pages in <b>Settings > Conditions</b>.</li></p>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/core/creating6.png">
  	</figure>

<p><li><b>Footers</b> - the block  contains all the footers available for your use. Please, note, that in order to apply the footers to different pages you need to create a footer template and assign it to the needed pages in <b>Settings > Conditions</b>.</li></p>

<p><li><b>Sections</b> - here you can select the sections you want to use on the page. You can use one or more sections, mix and match them in order to get the necessary results.</li></p>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/core/creating10.png">
  	</figure>

<p><li><b>My Library</b> - the block contains the templates you’ve saved into your library.</li></p></p></ul>

<p><li>Feel free to enrich the sectins with your own content, use different other widgets, changte the sectin styles according to your needs. After everything is done click <b>Publish</b> button to save all the changes.</li></p></ol>

