<li><p>Check <b>Enable custom archive page</b> option to allow using the newly created custom archive template. </p></li>

<li><p>Select the newly created template in <b>Products Archive Template</b> dropdown. </p></li>

<li><p>Save the changes by clicking <b>Save</b> button. </p></li>

