<h4>Adding Content to Product Archive Template</h4>

<ol class="index-list">
<p><li>
  Once you’ve created a template and navigated to Elementor editing page by clicking on <b>Edit with Elementor</b> button, the standard <b>Elementor editor</b> opens.


</li></p>

<p><li>
  You can create any page structure you need by adding the sections, add columns to the sections, duplicate sections and columns, add templates you’ve already created, and use now only the widgets added by <b>JetWooBuilder</b>, but also any other widget you want to place to the template, which is available in the <b>Elements</b> block.
</li></p>

<p><li>
  To add a new section click <b>Add New Section</b> button. Click <b>Edit Section</b> option in order to set the content width, section background style, paddings and margins, etc.

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/templ/1.png">
    </figure>

        <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/templ/3.png">
    </figure>

</li></p>

<p><li>
  Select the preferable columns structure for the section. You can also drag the column borders in order to set the custom columns width. You can also edit the columns width, set the content position, etc, by editing the column.

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/templ/2.png">
    </figure>

</li></p>

<p><li>
  After that navigate to <b>Elements</b> list and here scroll down to see <b>JetWooBuilder</b> widgets.

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/templ/6.png">
    </figure>

</li></p>

<p><li>
  Drag and drop the widgets to the columns in order to place them on the page.

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/templ/7.png">
    </figure>

</li></p>



<p><li>To access <b>Jet Woo Archive Template Settings</b> click the <b>Settings</b> option located in the bottom left corner. Here you can change the general settings and the specific template settings for the newly created template.

<p><b>General Settings</b></p>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/upd/nt3.png">
    </figure>


<ul class="marked-list">
<p><li><b>Title</b> - here you need to specify the title to apply for the Archive template. After that you’ll be able to select this template from the dropdown using its title. </li></p>

<p><li><b>Status</b> - here you can select one of the statues for the template (draft, pending review, private or published). </li></p></ul>

<p><b>Template Settings</b></p>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/upd/nt4.png">
    </figure>

<ul class="marked-list">
<p><li><b>Use Custom Columns Count</b> - enable this option in order to enable the custom count for the columns in the archive template (you can change the columns count and then you won’t have to use bootstrap to add or remove the columns and change the layout). </li></p>

<p><li><b>Template Columns</b> - here you can specify the number of columns to use for this template and change the number for different devices. </li></p>


<p><li><b>Template Columns Horizontal Gutter</b> - here you can specify the size of the horizontal gutter between the columns in px and make it responsive. </li></p>


<p><li><b>Template Columns Vertical Gutter</b> - here you can set the vertical gutter value in px and make it responsive. </li></p></ul></li></p>

<li><p>After everything is done please click <b>Save (Update)</b> button. Now the template is saved and ready for use!</p></li></ol>


