<h3>JetMenu Installation</h3>

<h5>From this block you can learn how to install JetMenu addon for Elementor manually. </h5>

<div class="alert alert-info">First, make sure that you’ve installed and activated Elementor page builder before proceeding to JetMenu installation. If you have problems with installing Elementor feel free to read the detailed <a href="https://docs.elementor.com/" target="_blank">Elementor</a> documentation.</div>


<ol class="index-list">
<p><li>Log in to WordPress Dashboard using Your login credentials and navigate to <b>Plugins</b> tab in the left column.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/1.png">
  	</figure>

</li></p>
<p><li>Click on <b>Add new</b> button to upload <b>JetMenu</b> plugin from Your hard drive.</li></p>
<p><li>Click on <b>Upload</b> button, which can be found at the top of <b>Plugins</b> page. Browse the path to <b>JetMenu</b> plugin and start installation.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/2.png">
  	</figure>

</li></p>
<p><li>When <b>JetMenu</b> plugin is uploaded and installed you’ll need to activate it.</li></p>
<p><li>Navigate to <b>Plugins</b> tab and locate JetMenu plugin in the list of installed plugins.</li></p>
<p><li>Click on <b>Activate</b> link under JetMenu. As a result the plugin will become active.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/3.png">
  	</figure>

</li></p>
