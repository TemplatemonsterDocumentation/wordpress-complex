<h3>Template configuration at Customizer</h3>

<p>
	Now your theme can be configured with the convenient intuitive WYSIWYG editor interface. Our template extends its functionality, so you have additional settings available. 
</p>
<p>
 	There are two ways to start configuring. First is to navigate to <strong>Appearance > Customize</strong> section. Second way is to press on <strong>"Customize"</strong> at the top left corner of site. This way allows you to edit the page you're currently viewing. And the first way initially shows the site front page by default.
</p>
<p>
	Left side of the site interface lists the available settings. Page template is displayed at the right side of the screen. After adding the changes you can save them by pressing the <strong>"Save &amp; Publish"</strong> button.
</p>

