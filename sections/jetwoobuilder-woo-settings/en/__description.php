<h3>JetWooBuilder WooCommerce Settings</h3>

<p>From this block you can learn more about the ways to assign the templates built with JetWooBuilder to WooCommerce Single product pages, Product Archive, Shop page.</p>

<p>You’ll also be able to use the Archive template to show the products when one searches for the specific products, or wants to display them using the shortcode, or with Upsells or Cross-sells widgets in Elementor-built single product templates.</p>

<ol class="marked-list">
<p><li>In order to access the <b>WooCommerce JetWooBuilder Settings</b>, please, navigate to <b>WooCommerce > Settings > Jet Woo Builder</b> tab in WordPress Dashboard. Here you can see the list of options available for JetWooBuilder templates.
Let’s take a closer look at them.

<p><b>Shop Page</b></p>


	  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/upd/woo-settings2.png">
    </figure>

<ul class="marked-list">
<li><p><b>Custom Shop Page</b> - enable this option in order to use the Custom Product Archive template created with JetWooBuilder.</p></li>

<li><p><b>Products Shop Template</b> - here you need to select the template built with JetWooBuilder to use it as global shop template.</p></li>


<p><b>Single Product</b></p>


	  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/upd/woo-settings3.png">
    </figure>

<ul class="marked-list">
<li><p><b>Custom Single Product</b> - enable this option in order to use the Single Product template created with JetWooBuilder.</p></li>

<li><p><b>Single Product Template</b> - here you need to select the template built with JetWooBuilder to use for all the single products existing on the site.</p></li>

<p><b>Products Archive</b></p>


	  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/upd/woo-settings4.png">
    </figure>

<ul class="marked-list">


<li><p><b>Custom Products Archive</b> - here you can enable the usage of the custom archive that you've created with JetWooBuilder to show instead of the default one.</p></li>

<li><p><b>Products Archive Template</b> - here you can select the archive template to show on the Shop page instead of the default products archive.</p></li>

<li><p><b>Search Page Template</b> - here you can select the archive to show up instead of the default one when one displays the search results on the Search page.</p></li>

<li><p><b>Products Shortcode Template</b> - here you can define the archive to display when one adds the WooCommerce shortcode in order to display the products on the pages built with Elementor using the default <a href="https://docs.woocommerce.com/document/woocommerce-shortcodes/">WooCommerce shortcodes</a>. E.g., <b>[products]</b> shortcode displays the products list.</p></li>

<li><p><b>Related and Upsells Products Template</b> - here you can select the template that will be shown when one adds the Single Upsells or Single Related widget to the Single Product page to add upsells or relared products.</p></li>

<li><p><b>Cross-sells Products Template</b> - here you can select the template that will be shown when one wants to display the cross-selling products.</p></li>


<p><b>Categories Archive</b></p>


	  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/upd/woo-settings5.png">
    </figure>

<ul class="marked-list">

<li><p><b>Custom Categories Archive</b> - here you can enable the usage of the custom categories that you've created with JetWooBuilder to show instead of the default one.</p></li>

<li><p><b>Categories Archive Template</b> - here you can select the template to use it as global categories archive template.</p></li>


<p><b>Other Options</b></p>


	  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/upd/woo-settings6.png">
    </figure>

<ul class="marked-list">


<li><p><b>Use Native Templates</b> - check this option if you've previously customized the templates and the appearance of the pages breaks when you use the newly created templates built with JetWooBuilder.</p></li>

<li><p><b>Number Related Products to Show</b> - here you can define the number of related products to display.
</p></li>

<li><p><b>Number Up Sells Products to Show</b> - here you can define the number of up sells products to display.
</p></li>

<li><p><b>Number Cross-sells products to show</b> - here you can define the number of cross-selling products to display. </p></li></ul></p></li>


<p><li>After you’ve specified the templates to use for the different WooCommerce pages, you can save the changes. Click <b>Save</b> button.</li></p></ol>
