<h6>Title</h6>

<ul class="marked-list">
 

  <li><p><b>Typography</b> — turn the option on to view the typography settings for the title text.
    <ul class="marked-list">
      <li><b>Size</b> — define the font size of the  title text.</li>
      <li><b>Family</b> — here you can set the font family for the title text.</li>
      <li><b>Weight</b> — in this block you can select the suitable font weight.</li>
      <li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</li>
      <li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li>
      <li><b>Line Height</b> — in this field you can set the height of the title text.</li>
      <li><b>Letter Spacing</b> — here you can set the space between the title text characters.</li>
    </ul>



<p>Switch from <b>Normal</b> to <b>Hover</b> or <b>Disabled</b> modes to set the color scheme and box shadow for the title when in normal state, on mouse hover or when the purchase is disabled for the customers.</p>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can set the color of the title label text using the color picker.</p></li>

	<li><p><b>Background Color</b> - here you can set the background color to use for the title.</p></li>

	<li><p><b>Box Shadow</b> - click on the pencil icon to access the shadow option and apply the shadow for the title.</p>

	

	<li><p><b>Border Type</b> - here you can select one of the available border types to use for the title: none, solid, double, dotted, dashed or groove.</p>

		<ul class="marked-list">
			<li><b>Width</b> - here you can define the width of the border in px.</li>
			<li><b>Color</b> - here you can change the color of the borders.</li>
			
		</ul>
	</li>

    <li><p><b>Border Radius</b> - here you can set the border radius for the chosen border to make the border angles more round.</p></li>


	<li><p><b>Padding</b> - here you can set the padding values for the text placed inside the title.</p></li>
	<li><p><b>Margin</b> - here you can define the margins applied for the whole button contained in the column.</p></li>
 
   <li><p><b>Alignment</b> - here you can specify the alignment of the title (left, center, right).</p></li>
</ul>

