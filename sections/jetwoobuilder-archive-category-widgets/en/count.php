<h5>Count </h5>



<ul class="marked-list">

    <li><p><b>Count Position</b> - select either Block or Inline position of count. </p></li>

	<li><p><b>Typography</b> - here you can edit the typography settings. They are the same as available for other JetWooBuilder widgets. </p></li>

	<li><p><b>Color</b> - here you need to specify the color of the count. </p></li>

	<li><p><b>Background Color</b> - here you can set the needed color to use for the background of the count. </p></li>

    <li><p><b>Box Shadow</b> -here you can apply the shadow for the item. Click the Edit pencil option to open the additional shadow settings. </p></li>

    <li><p><b>Border Type</b> - here you can define the type of the border to apply for the count (or apply no border, according to your needs). </p></li>

    <li><p><b>Border Radius</b> - here you can set the border radius values to make the angles more smooth and round. </p></li>

	<li><p><b>Padding</b> - here you can define the padding values for the count. </p></li>

	<li><p><b>Margin</b> - here you can apply the specific margin values for the count and make it responsive. </p></li>

    <li><p><b>Alignment</b> -  here you can change the count alignment. </p></li></ul>

