<h2>Count</h2>

<h6>
This widget allows to display the number of products applied to certain category.
</h6>


<h4>Content</h4>

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/count.png">
    </figure>


<ul class="marked-list">
	<li><p><b>Count Before Text</b> - here you can add or remove symbols before count. </p></li>

	<li><p><b>Count After Text</b> - here you can add or remove symbols after count. </p></li></ul>

<h4>Style</h4>

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/count-style.png">
