<h6>Thumbnail</h6>

<ul class="marked-list">
	<li><p><b>Background Color</b> - here you can change the color of the thumbnail imageusing the color picker tool. </p></li>

	<li><p><b>Border Type</b> - here you can change the margins to use them for the thumbnail image attributes.</p></li>

	<li><p><b>Border Radius</b> - input the padding values in order to use the custom paddings for the attributes thumbnail image.</p></li>

	<li><p><b>Box Shadow</b> - input the padding values in order to use the custom paddings for the attributes thumbnail image.</p></li>

	<li><p><b>Margin</b> - here you can apply the specific margin values for the thumbnail and make it responsive.</p></li>

	<li><p><b>Alignment</b> - here you can set the preferable (right, left, center) alignment for the thumbnail image.</p></li>
</ul>

