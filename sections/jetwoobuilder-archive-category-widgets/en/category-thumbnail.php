<h2>Thumbnail</h2>


<h6>
This widget is used to add thumbnail image to the product on Archive Products page.
</h6>

<h4>Content</h4>

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/category-thumbnail.png">
    </figure>

<ul class="marked-list">
	<li><p><b>Add Link To Thumbnail</b> - enable/disable adding link to the product thumbnail.</p></li>
	<li><p><b>Thumbnail Size</b> - select the size of thumbnail from the drop-down list. </p></li>
</ul>

<h4>Style</h4>

    <figure class="img-polaroid">
