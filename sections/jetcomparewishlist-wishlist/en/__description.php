<h3>Working with Products Wishlist</h3>

<h4>From this block you can learn how to set the products wishlist with JetCompareWishlist plugin for Elementor.</h4>

<h5>1. Create a page</h5>

<p>In the <b>Dashboard > Pages</b> block create the page, where you want to display the products wishlist.</p>

<h5>2. Enable Wishlist functionality</h5>

<p>Then go to <b>Elementor > JetCompareWishlist settings > Wishlist</b> block and move the toggle near <b>Enable Wishlist</b> option.</p>

<figure class="img-polaroid">
        <img src="projects/jetcomparewishlist/img/wishlistsettings.png">
    </figure>

<h5>3. Set the conditions</h5>
<p>Enable <b>Save for logged users</b> option if you want to provide logged users only with an opportunity to save their wishlists.</p>

<h5>4. Select a page</h5>

<p>You need to define the wishlist page, where you want to apply wishlist functionality in <b>Wishlist Page</b> option. Also, you can add <b>Wishlist button</b> to default <b>WooCommerce Single</b> and <b>Archive</b> templates. </p>


<h5>5. Adding widgets</h5>

<h6>Here you can learn how to add <b>Wishlist Button</b>, <b>Wishlist Count Button</b> and <b>Wishlist</b> widgets to Elementor sections.</h6>

<ol class="index-list">
<p><li>First, let’s open the page where you want to add the <b>Wishlist Button</b>, <b>Wishlist Count Button</b> or <b>Wishlist</b> widget using Elementor live page builder. To do it click <b>Edit with Elementor</b> button. </li></p>

<p><li>Add a new section to the page using <b>Add New Section</b> button.</li></p>

  <figure class="img-polaroid">
        <img src="projects/jetsearch/img/5.png">
    </figure>

<p><li>Select the preferable columns structure for the section. You can also drag the column borders in order to set the custom columns width. Feel free to edit the columns width, set the content position, etc, by editing the column.</li></p>
<figure class="img-polaroid">
        <img src="projects/jetsearch/img/6.png">
    </figure>

<p><li>On the left you can see the list of the available widgets which you can drop to the column you’ve created. Find <b>Wishlist Button</b>, <b>Wishlist Count Button</b>, or <b>Wishlist</b> widget and drop it to the needed area.</li></p>

  <figure class="img-polaroid">
        <img src="projects/jetcomparewishlist/img/wishlistsample.png">
    </figure>

<p><li>After the page is built click <b>Update</b> button to preserve the changes.</li></p>
</li></p>
</ol>
