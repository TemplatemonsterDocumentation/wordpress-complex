<h3>Wishlist Count Button</h3>

<h6>Wishlist Count Button widget allows showing the number of products added to the wishlist. It can be added in header, footer, etc.</h6>

<h4>Content</h4>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/10.png">
    </figure>

<ul class="marked-list">
<li><p><b>Button Icon</b> - here you can define an icon for the wishlist count button.</p></li>
<li><p><b>Button Label Text</b> - here you can insert a label text of the wishlist count button.</p></li></ul>

<h6>Icon</h6>
<ul class="marked-list">
<li><p><b>Use Icon</b> - enable this option if you want to display an icon on the wishlist count button.</p></li>
<li><p><b>Icon Position</b> - here you can specify position of the icon on the wishlist count button (left, top, right, bottom).</p></li></ul>

<h6>Count</h6>
<ul class="marked-list">
<li><p><b>Show Count</b> - enable this option if you want to showcase the number of added products in a wishlist.</p></li>
<li><p><b>Format</b> - here you can specify format of the count items added to the wishlist.</p></li>
<li><p><b>Position</b> - here you can set position of the count in wishlist count button.</p></li></ul>


