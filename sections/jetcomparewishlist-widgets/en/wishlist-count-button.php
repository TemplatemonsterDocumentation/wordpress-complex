<h5>General</h5>
<ul class="marked-list">
<li><p><b>Typography</b> - turn the option on to view the typography settings.</p></li>
<li><p><b>Custom Size</b> - enable this potion if you want to set the custom size of the wishlist count button.</p></li>
<li><p><b>Custom Width</b> - specify the preferable width of the wishlist count button in pixels or percentage.</p></li>
<li><p><b>Custom Height</b> - specify the preferable height of the wishlist count button in pixel or percentage.</p></li>
<p>Here you can switch from customizing <b>Normal</b> to <b>Hover</b> general style settings. Just click on the button <b>Hover</b> and proceed to customizing the elements on hover.</p>
<li><p><b>Color</b> - here you can pick color for the label text and count of the wishlist count button.</p></li>
<li><p><b>Background Color</b> - here you can select background color of the wishlist count button.</p></li>
<li><p><b>Border Color</b> - here you can pick color of the borders on hover.</p></li>
<li><p><b>Border Type</b> - here you can define the type of the border you need to use for the element.</p></li>
<li><p><b>Width</b> - here you can specify width of the borders.</p></li>
<li><p><b>Color</b> - here you can pick color for the borders of the wishlist count button.</p></li>
<li><p><b>Border Radius</b> - here you can define the value of the border radius to make the angles more smooth and round.</p></li>
<li><p><b>Alignment</b> - here you can define the preferable alignment of the wishlist count button. It can be placed to the left, to the right, or remain centered.</p></li>
<li><p><b>Padding</b> - here you can set the preferable custom padding for the wishlist count button. Fill in the values for the top, bottom, right and left padding in pixels to apply your custom padding.</p></li>
<li><p><b>Margin</b> - here you can apply your custom margins in pixels or percentage to set them for the wishlist count button.</p></li></ul>

