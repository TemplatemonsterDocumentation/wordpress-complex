<h3>Available Wisgets</h3>

<p>In this block you can change the list of available widgets that will show up in JetCompareWishlist block on Elementor page builder editing page.</p>

<p>This option is useful for you when:
<ul class="marked-list">
<li><p>you want to conceal the rarely used widgets to access the most frequently used ones more quickly;</p></li>
<li><p>you want Elementor work faster - in this case disabling some of the rarely used widgets will speed up the loading time.</p></li></ul>

<p>You might also consider disabling some of the rarely used widgets when you're facing the Memory Limit Exhausted error, and you can't boost up the memory limit on your server.</p>

<p>Just remove the checks for those addons you want to disable and click <b>Save</b> button to save the changes.</p>

