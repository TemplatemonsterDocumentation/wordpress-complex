<h3>Compare Button</h3>

<h6>Compare Button widget allows adding products to the comparison list. It can be added on custom Single and Archive WooCommerce templates.</h6>

<h4>Content</h4>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/comparebuttoncontent.png">
    </figure>

<h5>Compare</h5>
<ul class="marked-list">
<p>Here you can switch from customizing <b>Normal</b> to <b>Added</b> content settings. Just click on the button <b>Added</b> to proceed to customizing added elements.</p>
<li><p><b>Button Icon</b> - here you can define an icon for the compare button in Normal and Added status of the product.</p></li>
<li><p><b>Button Label Text</b> - here you can paste a label text of the compare button in Normal and Added status of the product.</p></li></ul>

<h6>Icon</h6>
<ul class="marked-list">
<li><p><b>Use Icon</b> - enable this option if you want to display an icon on the compare button.</p></li>
<li><p><b>Icon Position</b> - here you can specify the position of the icon on the compare button (left, top, right, bottom).</p></li></ul>


<h4>Style</h4>
  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/comparebuttonstyle.png">
    </figure>

<h5>Compare</h5>
<ul class="marked-list">
<li><p><b>Typography</b> - turn the option on to view the typography settings.</p></li>
<li><p><b>Custom Size</b> - enable this option if you want to set the custom size of the compare button.</p></li>
<li><p><b>Custom Width</b> - specify the preferable width of the compare button in pixels or percentage.</p></li>
<li><p><b>Custom Height</b> - specify the preferable height of the compare button in pixel or percentage.</p></li>
<p>Here you can switch from customizing <b>Normal</b> to <b>Hover</b> and <b>Added</b> Compare style settings. Just click on the button <b>Hover</b> and <b>Added</b> to proceed to customizing hover and added elements.</p>
<li><p><b>Color</b> - here you can pick color for the label text and icon of the compare button.</p></li>
<li><p><b>Background Color</b> - here you can select background color of the compare button.</p></li>
<li><p><b>Border Color</b> - here you can choose color of the border of the compare button.</p></li>
<li><p><b>Border Type</b> - here you can define the type of the border you need to use for the element.</p></li>
<li><p><b>Width</b> - here you can specify width of the borders.</p></li>
<li><p><b>Color</b> - here you can pick color for the borders of the compare button.</p></li>
<li><p><b>Border Radius</b> - here you can define the value of the border radius to make the angles more smooth and round.</p></li>
<li><p><b>Alignment</b> - here you can define the preferable alignment of the compare button. The compare button can be placed to the left, to the right, or remain centered.</p></li>
<li><p><b>Padding</b> - here you can set the preferable custom padding for the compare button. Fill in the values for the top, bottom, right and left padding in pixels to apply your custom padding.</p></li>
<li><p><b>Margin</b> - here you can apply your custom margins in pixels or percentage to set them for the compare button.</p></li></ul>

<h6>Icon</h6>
<ul class="marked-list">
<p>Here you can switch from customizing <b>Normal</b> to <b>Added</b> and <b>Hover</b> Icon style settings. Just click on the button <b>Added</b> and <b>Hover</b> to proceed to customizing icon hover and added elements.</p>
<li><p><b>Color</b> - here you can pick color of the icon on the compare button.</p></li>
<li><p><b>Font Size</b> - here you can define font size of the icon on the compare button.</p></li>
<li><p><b>Margin</b> - here you can apply your custom margins in pixels or percentage to set them for the icon on the compare button.</p></li></ul>



