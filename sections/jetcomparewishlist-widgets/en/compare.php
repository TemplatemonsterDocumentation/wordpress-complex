<h6>Compare Table</h6>
<ul class="marked-list">
<p>Click on the button <b>Add item</b> to add new features to the comparison table. You can add as many features as you want, and customize each one of them. Click on the newly added item to customize it.</p>
<li><p><b>Data Type</b> - here you can specify type of the data displayed in the comparison table.</p></li>
<li><p><b>Title</b> - here you can specify title of the features added to the comparison table.</p></li>
<p>In the case you’ve selected <b>Remove Button</b> data type, you’ll need to adjust the additional settings.
<ul class="marked-list">
<li><p><b>Button Text</b> - here you can specify text of the button.</p></li>
<li><p><b>Button Icon</b> - here you can select icon of the button.</p></li></ul>
<p>In the case you’ve selected <b>Thumbnail</b> data type, you’ll need to adjust the additional setting.
<ul class="marked-list">
<li><p><b>Image Size</b> - here you can specify size of the image.</p></li></ul>
<p>In the case you’ve selected <b>Rating</b> data type, you’ll need to adjust the additional setting.
<ul class="marked-list">
<li><p><b>Rating Icon</b> - here you can define the rating icon.</p></li></ul></p></p></p></ul>

<h5>Settings</h5>
<ul class="marked-list">
<li><p><b>Empty Compare Text</b> - here you can insert text appearing when there aren't any comparison lists yet.</p></li>
