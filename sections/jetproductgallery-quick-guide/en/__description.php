<h3>JetProductGallery Quick Guide</h3>

<p>Here you can learn more about the way one can use the JetProductGallery widgets. It explains the process of adding the product gallery to the Single Product page template in details.</p>

<h4>Adding Images to Display with the Gallery</h4>

<ol class="index-list">
<li><p>First, make sure you've added the images to the product gallery and have set the featured image for the product. To do it, please, open the WordPress Dashboard, and here proceed to products page. </p></li>

<li><p>Open one of the existing products for editing, or create a new one. Then, scroll the page down and locate <b>Product Gallery</b> block. Here you need to upload the images that display all the benefits and strong sides of the product.</p></li></ol>


<h4>Creating New Single Product Template</h4>


<p>After all the information about the product is filled we can create a new <b>Single Product</b> page template. Follow the steps described below to learn how this can be done.</p>

<ol class="index-list">
<li><p>Let’s navigate to <b>WooCommerce > JetWoo Templates</b> in the Dashboard.

	    <figure class="img-polaroid">
        <img src="projects/jetwoobuilder/img/add-templ/1.png">
    </figure>
</p></li>


<li><p>Here click <b>Add New Template</b> button.

	    <figure class="img-polaroid">
        <img src="projects/jetwoobuilder/img/add-templ/2.png">
    </figure>
</p></li>

    </p></li>

 <li><p>The button opens the popup window showing the set of pre-designed <b>Archive/Single Product/Category/Shop</b> pages. You can easily select one of them to use. Or you can start creating the <b>Single Product</b> template from scratch. </p></li>

<p><li>Also, in this popup window you’ll have to specify:

	<ul class="marked-list">

<li><b>The template name</b> - here you have to type in the title of the template you’re creating;</li>
<li><b>This template for</b> - in this dropdown you need to select Single in order to start creating Single Product page template. </li></ul></li></p>

<li><p>After everything is done click <b>Create Template</b> button. </p></li></p>

<h4>Adding Content to Single Product Template</h4>

<ol class="index-list">
<p><li>
  Once you’ve created a template and navigated to Elementor editing page by clicking on <b>Edit with Elementor</b> button, the standard <b>Elementor editor</b> opens.


</li></p>

<p><li>
  You can create any page structure you need by adding the sections, add columns to the sections, duplicate sections and columns, add templates you’ve already created, and use now only the widgets added by <b>JetProductGallery</b>, but also any other widget you want to place to the template, which is available in the <b>Elements</b> block.
</li></p>

<p><li>
  To add a new section click <b>Add New Section</b> button. Click <b>Edit Section</b> option in order to set the content width, section background style, paddings and margins, etc.

    <figure class="img-polaroid">
        <img src="projects/jetwoobuilder/img/templ/1.png">
    </figure>


</li></p>

<p><li>
  Select the preferable columns structure for the section. You can also drag the column borders in order to set the custom columns width. You can also edit the columns width, set the content position, etc, by editing the column.

    <figure class="img-polaroid">
        <img src="projects/jetwoobuilder/img/templ/2.png">
    </figure>

</li></p>

<p><li>
  After that navigate to <b>Elements</b> list and here scroll down to see <b>JetProductGallery</b> widgets.


</li></p>

<p><li>
  Drag and drop the widgets to the columns in order to place them on the page.


</li></p>

<p><li>
  After the page is built click <b>Publish</b> button to preserve the changes.

</li></p></ol>

