

<h2>Shortcodes</h2>

<p>The shortcode displays the product list with the set attributes. Shortcode attributes:
</p>

<ul class="marked-list">
    <li>    
        <dl class="inline-term">
            <dt>    
               super_title (default = '')
            </dt>
                <dd>
                   additional block title with posts listing above the main one
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               title (default = '')
            </dt>
                <dd>
                    main block title with products list
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               subtitle (default = '') 
            </dt>
                <dd>
                   additional block title with products list under the main
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               columns (default = 3)
            </dt>
                <dd>
                   number of columns for desktop  (6 max)
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               columns_tablet (default = 2)
            </dt>
                <dd>
                   number of columns for tablets
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               columns_phone (default = 1)
            </dt>
                <dd>
                   number of columns for phones
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               posts_per_page (default = 6) 
            </dt>
                <dd>
                   number of posts per page
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               category (default = '')
            </dt>
                <dd>
                   use slugs to pick posts from the category. If you need to display more than 1 category, slugs are separated by commas  
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               id (default = '')
            </dt>
                <dd>
                   display posts by IDs
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               more (default = true)
            </dt>
                <dd>
                   Show/hide button under the products list in the shortcode
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               more_text (default = 'More')
            </dt>
                <dd>
                   text of the displayed button 
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               more_url (default = '#')
            </dt>
                <dd>
                   link of the displayed button
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               ajax_more (default = true)
            </dt>
                <dd>
                   use the button for  loading additional posts by AJAX (if the attribute =  true, more_url attribute is ignored)
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               pagination (default = false)
            </dt>
                <dd>
                   show/hide pagination. If more = true the attribute is ignored
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               show_filters (default = false)
            </dt>
                <dd>
                  show /hide AJAX filter by groups in shortcode before posts list. If group attribute has several groups - only they  will be displayed in the filter.    
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               show_title (default = true)
            </dt>
                <dd>
                   show/hide post title in the listing
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               show_media (default = true)
            </dt>
                <dd>
                   show/hide media content (icon, image)
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               show_content (default = true)
            </dt>
                <dd>
                   show/hide short description
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               excerpt_length (default = 20
            </dt>
                <dd>
                   max word length in short description
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               image_size (default = 'thumbnail')
            </dt>
                <dd>
                   image size displayed in post list 
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               template (default = 'default')
            </dt>
                <dd>
                   choose the template for displaying items in posts list 
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               use_space (default = true)
            </dt>
                <dd>
                   add horizontal 30px padding between columns in posts list 
                </dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               use_rows_space (default = true)
            </dt>
                <dd>
                   add vertical 30px padding between columns in posts list 
                </dd>
        </dl>
    </li>
</ul>
