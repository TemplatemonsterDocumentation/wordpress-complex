<h2>Cherry_projects shortcode</h2>

<p>
The shortcode displays Category and Tag sections content listing with set parameters ( <strong>[cherry_projects]</strong> ). Shortcodes include:
</p>

 <ul class="marked-list">
        <li>    
            <dl class="inline-term">
                <dt>    
                   Projects
                </dt>
        </li>
        <li>    
            <dl class="inline-term">
                <dt>    
                   Project terms
                </dt>
        </li>
    </ul>

 <p>
    In the <strong>'Project'</strong> section shortcodes are used to display the projects list with set parameters:
