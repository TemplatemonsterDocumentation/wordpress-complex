<h2>WooCommerce Sale Products</h2>

<h6>
WooCommerce Sale Products widget helps you to display sale products on your website’s page,  and sort them using custom order.
</h6>

<div class="alert alert-info">
Before using WooCommerce Sale Products widget make sure that you've installed and activated <a href="https://wordpress.org/plugins/woocommerce/" target="_blank">WooCommerce</a> plugin.
</div>

<h4>Content</h4>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widget/sale2.png">
