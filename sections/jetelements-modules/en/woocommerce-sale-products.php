<ul class="marked-list">
<p><li><b>Products per Page</b>  — in this block you can set how many products you want to display per one page using this widget. Set “0” value to display all the existing products.</li></p>
<p><li><b>Columns</b>  — here you can define how many columns you want to display in the widget to organize products. You can set from 1 to 6 columns.   </li></p>
<p><li><b>Order By</b>  — here you can define the criterion by which the products will be sorted. You can sort products by date, ID, author, title, name, slug, comments count or menu order. You can as well apply random order. </li></p>
<p><li><b>Order</b>  — here you can set ascending or descending order for the products.</li></p></ul>


