<h4>Style 1</h4>


 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widget/style1.png">
  	</figure>


<ul class="marked-list">
<p><li>Let’s navigate to <b>Pages > Add New</b> tab on the left of the Dashboard and type in the name for the page which will include the contact form. Then click on <b>Edit with Elementor</b> button. </li></p>
<p><li>Click on <b>Add New Section</b> button to add the section where you want to place the contact form. </li></p>
<p><li>Select <b>Contact Form 7</b> widget from the <b>Elements</b> menu on the left and drop it to the section area. </li></p>
<p><li>Select the form you’ve created from the dropdown list in the <b>Content</b> block. </li></p>
<p><li>Now let’s switch to <b>Style</b> tab in order to style up the form to make it look like on live demo. </li></p></ul>

<h5>Form Texts</h5>

<ul class="marked-list">
<p><li>Paste the  <b>#2f2f2f</b> color HEX code in the <b>Color</b> field. </li></p>
<p><li>Switch on <b>Typography</b> option to view the <b>typography settings</b>. </li></p>
<p><li>In the <b>Size</b> field input <b>20 px</b> value. </li></p>
<p><li>Select <b>Georgia</b> font family in the <b>Family</b> section. </li></p>
<p><li>The <b>weight</b> for the font should be <b>400</b>. </li></p>
<p><li>Leave <b>Transform, Style</b> fields, <b>Line Height</b> and <b>Letter Spacing</b> controls with the default values. </li></p></ul>

<h5>Not Valid Notices</h5>

<ul class="marked-list">
<p><li>The color for the not valid notices should be <b>#f16334</b>. </li></p>
<p><li>Turn on <b>Typography</b> option to view the <b>typography settings</b>. </li></p>
<p><li>The <b>font size</b> should be <b>14px</b>. </li></p>
<p><li>Leave the default values for all the other fields. </li></p>
<p><li><b>Turn off</b> the linking values together in the <b>Margin</b> block and define <b>-21px</b> for the <b>top margin</b>. </li></p>
<p><li>Set the <b>right alignment</b> in the <b>Alignment</b> block. </li></p></ul>

<h5>Controls</h5>

<ul class="marked-list">
<p><li>Use the <b>Classic background type</b>. </li></p>
<p><li>In the <b>Color</b> area set <b>#7a7a7a</b> HEX color value. </li></p>
<p><li>The <b>placeholder color</b> should be <b>#9d9c9c</b>.</li></p>
<p><li>Turn on <b>Typography options</b>. </li></p>
<p><li>The <b>font size</b> should be <b>16px</b>. </li></p>
<p><li>Use <b>Georgia font family</b>. </li></p>
<p><li>All the other fields should be left with default values. </li></p>
<p><li>Link the <b>Padding</b> values together and set <b>19px</b> padding value for <b>top, right, bottom</b> and <b>left</b>. </li></p>
<p><li>In the <b>margin</b> block set <b>19px top margin</b> and <b>20px bottom margin</b>. </li></p>
<p><li>Select <b>Solid</b> for the <b>border type</b> from the dropdown menu. </li></p>
<p><li>Link together the <b>border radius</b> values and set <b>5px</b> for <b>top, left, bottom</b> and <b>right</b> border radius. </li></p>
<p><li>Enable <b>Box Shadow</b> and specify <b>10</b> for the <b>blur</b>. Leave all the remaining controls with <b>0</b> value. </li></p>
<p><li>Define <b>Outline</b> in the <b>Position</b> block. </li></p>
<p><li>The <b>Text area minimal height</b> should be <b>171</b>. </li></p></ul>


<h5>Submit Button</h5>

<ul class="marked-list">
<p><li>Set <b>#00bbd4</b> HEX value in the <b>Color</b> field. </li></p>
<p><li>Turn on <b>Typography</b> and set <b>20px</b> in the <b>font size</b>. </li></p>
<p><li>Select <b>Georgia</b> in the <b>Font Family</b> block. </li></p>
<p><li>The <b>font weight</b> should be <b>700</b>. </li></p>
<p><li>Select <b>Normal</b> in the <b>Transform</b> field. </li></p>
<p><li>Leave the <b>Default</b> value for the <b>style</b>. </li></p>
<p><li>The <b>line height</b> should be <b>1.7EM</b>. </li></p>
<p><li>Now proceed to editing <b>padding</b> and <b>border radius</b> values. </li></p>
<p><li>Set the <b>padding</b> values as follows: <b>top 12px, right 50px, bottom 12px, left 50px</b>. </li></p>
<p><li>The <b>border radius</b> should be <b>7px</b> for the <b>top, bottom, right</b> and <b>left</b>. </li></p>
<p><li>Choose <b>Solid border type</b>. </li></p>
<p><li>Enable <b>Fullwidth Button</b> option. </li></p></ul>

<h5>Alerts</h5>

<ul class="marked-list">
<p><li>Now let’s customize <b>alerts</b> for this contact form. </li></p>
<p><li>Enable <b>Typography</b> option. </li></p>
<p><li>Select <b>16px font size</b>. </li></p>
<p><li>Use <b>Default font family</b>. </li></p>
<p><li>The <b>font weight</b> should be <b>400</b>. </li></p>
<p><li>In the <b>Border Radius</b> block use <b>7px border radius</b> for the <b>top, bottom, right</b> and <b>left</b>. </li></p>
<p><li>Select <b>centered alignment</b>. </li></p>
<p><li>Let’s specify the colors for the different kinds of alerts. </li></p>
<p><li>In the <b>Sent Success Background Color</b> area specify <b>#5bb966</b> HEX color code. </li></p>
<p><li>Use <b>#f16334</b> HEX color code for the <b>main color</b> of the <b>Sent Error</b> and <b>Not Valid</b> alerts. </li></p>
<p><li>Now the contact form is ready!</li></p></ul>



