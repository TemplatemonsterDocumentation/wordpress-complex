<h6>General</h6>

<ul class="marked-list">
	<p><li><b>Items Gap</b> - here you can define the appropriate gap between the items.</li></p></ul>

<h6>Cards</h6>

<ul class="marked-list">
	<p><li><b>Border type</b> - here you can define the needed border type for the card block. It can be groove, dotted, dashed, double or solid.</li></p>
	<p><li><b>Border Radius</b> - here you can set the radius for the chosen border to make the border angles more round.</li></p>
    <p><li><b>Padding</b> - type in the custom padding values to add the responsive padding for the block.</li></p>
	<p><li><b>Spacing</b> - here you can define the spacing between cards and other items in the timeline.</li></p>

<p>Here you can switch from customizing <b>Normal</b>, <b>Hover</b> or <b>Active</b> style settings of event cards. Just click on the button <b>Hover</b> or <b>Active</b> to proceed to customizing hover or active elements.</p>

<ul class="marked-list">
	<p><li><b>Background Color</b> - here you can specify the color to apply for the card box background.</li></p>
	<p><li><b>Border Color</b> - here you can select the color of the cards border.</li></p>
	<p><li><b>Box Shadow</b> - enable this option if you want to access the shadow advanced settings for the box, and need to apply shadow to it.</li></p>

<p><b>Arrow</b></p>
	<p><li><b>Size</b> - here you can define the size of the cards arrows.</li></p>
	<p><li><b>Offset</b> - here you can tune the cards arrows horizontal offset.</li></p></ul>

<h6>Cards Content</h6>

<ul class="marked-list">
	<p><li><b>Alignment</b> - arrange the event cards content using the top, middle and bottom alignment types.</li></p>

<p><b>Image</b></p>
	<p><li><b>Margin</b> - fill in the custom margin values to add a responsive margin around the image.</li></p>
	<p><li><b>Border Radius</b> - here you can set the radius for the image border to make the border angles more round.</li></p>
	<p><li><b>Stretch Image</b> - enable this option if you want to stretch the image.</li></p>

<p><b>Title</b></p>
	<p><li><b>Typography</b> - turn the option on to view the typography settings. They are similar to the typography settings of other JetElements modules.</li></p>
	<p><li><b>Margin</b> - fill in the custom margin values to add a responsive margin around the title.</li></p>
<p>Switch between <b>Normal</b>, <b>Hover</b> and <b>Active</b> editing modes in order to alter the appearance of the event card title on hover, when active and in the normal state.</p>
    <p><li><b>Color</b> - here you can select the color of the title.</li></p>

<p><b>Description</b></p>
	<p><li><b>Typography</b> - turn the option on to view the typography settings. They are similar to the typography settings of other JetElements modules.</li></p>
	<p><li><b>Margin</b> - fill in the custom margin values to add a responsive margin around the description.</li></p></ul>
<p>Switch between <b>Normal</b>, <b>Hover</b> and <b>Active</b> editing modes in order to alter the appearance of the event card description on hover, when active and in the normal state.</p>
    <p><li><b>Color</b> - here you can select the color of the description text.</li></p>

<p><b>Orders</b></p>
	<p><li><b>Image Order</b> - here you can define the order of the image comparatively to title and description.</li></p>
	<p><li><b>Title Order</b> - here you can define the order of the title comparatively to image and description.</li></p>
	<p><li><b>Description Order</b> - here you can define the order of the title comparatively to title and description.</li></p>
</ul>

<h6>Meta</h6>

<ul class="marked-list">
	<p><li><b>Typography</b> - turn the option on to view the typography settings. They are similar to the typography settings of other JetElements modules.</li></p>
	<p><li><b>Border type</b> - here you can define the type of the border you need to use for the element.</li></p>
	<p><li><b>Border Radius</b> - here you can set the radius for the meta border to make the border angles more round.</li></p>
	<p><li><b>Padding</b> - type in the custom padding values to add the responsive padding for the block.</li></p>
	<p><li><b>Spacing</b> - here you can define the spacing of meta data in the timeline.</li></p>
<p>Switch between <b>Normal</b>, <b>Hover</b> and <b>Active</b> editing modes in order to alter the appearance of the meta information on hover, when active and in the normal state.</p>
	<p><li><b>Color</b> - use Color picker tool to specify the color of the meta text.</li></p>
	<p><li><b>Background Color</b> - select the preferable color using Color Picker tool to apply if for the meta text background to make it more readable.</li></p>
	<p><li><b>Box Shadow</b> - enable this option if you want to access the shadow advanced settings for the meta data, and need to apply shadow to it.</li></p></ul>

<h6>Point</h6>

<p>Switch between <b>Text</b> and <b>Icon</b> modes in order to customize the appearance of the text point type or icon point type (depending on what type you've selected in Content block.</p>

<ul class="marked-list">
	<p><li><b>Typography</b> - turn the option on to view the typography settings. They are similar to the typography settings of other JetElements modules.</li></p>
	<p><li><b>Icon Size</b> - here you can specify the size of the icon which indicates the event on the timeline.</li></p>
	<p><li><b>Point Size</b> - here you can specify the size of the point that marks the event on the timeline.</li></p>
	<p><li><b>Offset</b> - here you can customize the horizontal offset of the point.</li></p>
	<p><li><b>Border Type</b> - here you can define the needed border type for the point block. It can be groove, dotted, dashed, double or solid.</li></p>
	<p><li><b>Width</b> - type in the border width in pixels in order to set the width for the box top, bottom, left and right borders.</li></p>
	<p><li><b>Color</b> - here you can specify the color of the border using Color picker tool.</li></p>
	<p><li><b>Border Radius</b> - here you can set the radius for the point border to make the border angles more round.</li></p>

<p>Switch between <b>Normal</b>, <b>Hover</b> and <b>Active</b> editing modes in order to alter the appearance of the event point on hover, when active and in the normal state.</p>

	<p><li><b>Color</b> - here you can specify the color of the point using Color picker tool.</li></p>
	<p><li><b>Background Color</b> - select the preferable color using Color Picker tool to apply if for the point background to make it more readable.</li></p>
	<p><li><b>Border Color</b> - here you can select the color of the point border on hover and when in the active state.</li></p>
</ul>


<h6>Line</h6>

