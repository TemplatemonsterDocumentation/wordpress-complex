<h2>Horizontal Timeline</h2>


<h6>
<b>Horizontal Timeline</b> widget fits perfectly for demonstrating the project's milestones, upcoming events and roadmaps. Add stages of the project and events plan on the pages built in Elementor page builder using an attractive Horizontal Timeline widget!
</h6>

<h4>Content</h4>

<h5>Cards</h5>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/htl1.png">
  	</figure>

<p>Click <b>Add Item</b> button in order to create a new event card in the Horizontal Timeline chain. After that, you'll be able to edit the event card, place content into it. You can add as many event cards as you need to make the event timelines longer or shorter.</p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/htl2.png">
  	</figure>


<ul class="marked-list">

    <p><li><b>Active</b> - enable this option in order to show the current state of the event.</li></p>
	<p><li><b>Show Image</b> - enable this option in order to add an image to be shown in the Event card.</li></p>
	<p><li><b>Image</b> - here you can select the image from the media library or upload one from your hard drive to use it for an event card.</li></p>
	<p><li><b>Title</b> - type in the preferable title to display for the event card.</li></p>
	<p><li><b>Meta</b> - add the meta information for the event card to specify the date or timing of the event.</li></p>
	<p><li><b>Description</b> - fill in the Description field to provide more information about the event.</li></p>
	<p><li><b>Point Content Type</b> - select if you want to use the icon or the text event point type to place the icon or the textual event point (in the text field one can type in numbers or words that specify the currently edited event stage or step).</li></p>
	<p><li><b>Point Icon</b> - here you can set the icon for the event or project stage using Icon Picker tool.</li></p>
	<p><li><b>Point Text</b> - here you can fill in the number or the textual mark for the stage or the step on which this event will be held.</li></p>
	<p><li><b>Title HTML Tag</b> - here you can select the HTML tag to apply for the cards titles.</li></p>
	<p><li><b>Show Cards Arrows</b> - enable this option if you want to add arrows to the cards' bottom.</li></p>

<h5>Layout</h5>

<ul class="marked-list">

	<p><li><b>Columns</b> - here you can select the number of columns on the row will be displayed in the timeline.</li></p>
	<p><li><b>Layout</b> - choose the appropriate position of the elements in the timeline. They can be located on the top, on the bottom or in chessboard order.</li></p>
	<p><li><b>Alignment</b> - arrange the event cards horizontally using the left, center and right alignment types.</li></p>
	<p><li><b>Navigation Type</b> - here you can adjust how you want to navigate among the cards in the timeline: by scrolling or with the help of arrows.</li></p>
	<p><li><b>Arrow Type</b> - here you can select the arrow design from the drop-down list.</li></p>

</ul>


<h4>Style</h4>

