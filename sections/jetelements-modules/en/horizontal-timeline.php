	<p><li><b>Scrollbar Height</b> - specify how high should be the scrollbar.</li></p>
	<p><li><b>Scrollbar Offset</b> - define the horizontal offset of the scrollbar.</li></p>
	<p><li><b>Border Radius</b> - here you can set the radius for the scrollbar border to make the border angles more round.</li></p>
