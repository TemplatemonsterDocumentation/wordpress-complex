<h4>Style</h4>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/svg2.png">
  	</figure>

<ul class="marked-list">
<p><li><b>Use Custom Width</b> - makes SVG responsive and allows to change its width.</li></p>
<p><li><b>Use Aspect Ratio</b> - this option allows your SVG item to be scaled up exactly as your bitmap image, at the same time saving its width compared to the height.</li></p>
<p><li><b>Use Custom Color</b> - here you can specify color of all SVG elements that have a fill or stroke color set. When the option is active you'll get access to the Color and Hover Color settings.</li></p>
<p><li><b>Remove Inline CSS</b> - you can use this option to delete the inline styles in the loaded SVG.</li></p>
<p><li><b>Alignment</b> - here you can define the preferable button alignment. The SVG item can be placed to the left, to the right, or remain centered.</li></p>
</ul>

