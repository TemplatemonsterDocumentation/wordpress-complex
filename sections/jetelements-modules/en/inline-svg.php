<h2>Inline SVG</h2>

<h6>
<b>Inline SVG</b> widget is made for adding the imagery with scalable vector graphics format (SVG). SVG files can be scaled without loses in quality. To use them in webpage's design one has only to upload such a file into Media Library and choose if for displaying with Inline SVG widget.
</h6>

