<p>In this block you can change style and appearance settings for Advanced Carousel module, define items padding, alignment, additional elements color, etc. </p>


<h6>Column</h6>

<ul class="marked-list">
<p><li><b>Column Padding</b> — here you can set the preferable custom padding for the whole column where Advanced Carousel module is displayed. Fill in the values for the top, bottom, right and left padding in pixels to apply your custom padding. </li></p></ul>

<h6>Simple Items</h6>


<p>Here you can switch from customizing <b>Normal</b> to <b>Hover</b> Simple Items style settings. Just click on the button <b>Hover</b> to proceed to customizing hover elements. </p>

<ul class="marked-list">
<p><li><b>Background Type</b> — in this block you can choose, whether you want to use classic or gradient type for the simple items. The customization options vary for each type. </li></p></ul>


<h6>Classic Background Type</h6>


<ul class="marked-list">
<p><li><b>Color</b> — here you can set background color for the simple items using color picker tool.</li></p>
<p><li><b>Image</b> — here you can choose the image you want to use as a background. Select the image from the media library or upload it to define it as a background in this block. When the image is uploaded, you’ll be able to define its position, alignment, turn on repeat and set the necessary size.</li></p>
<p><li><b>Opacity</b> — here you can define the value for the opacity of the background. </li></p></ul>

<h6>Gradient Background Type</h6>


<ul class="marked-list">
<p><li><b>Color</b> — here you can set background color for the simple items using color picker tool.</li></p>
<p><li><b>Location</b> — here you can define the proportion for the first color in the gradient background. </li></p>
<p><li><b>Second Color</b> — here you can set the second color to use in the gradient background. </li></p>
<p><li><b>Location</b> — here you can define the proportion for the second color in the gradient background. </li></p>
<p><li><b>Type</b> — define, if you want to use linear or radial type of the gradient. </li></p>
<p><li><b>Angle</b> — in this block you can set the angle for the gradient (in degrees). </li></p>
<p><li><b>Opacity</b> — here you can change background opacity, where 1 is 100% opacity, and 0 — is 0% opacity. </li></p></ul>


<p><li><b>Border Type</b> — here you can define the type of the border you need to use for the element. </li></p>
<p><li><b>Box Shadow</b> — enable this option if you want to access the shadow advanced settings for this module, and need to apply shadow for it.</li></p></ul>

<b>Shadow Settings:</b>

<ul class="marked-list">
<p><li><b>color</b> — here you can set the color for the the shadow (use color picker tool to do it);</li></p>
<p><li><b>blur</b> — in this block you can define the blur settings for the shadow;</li></p>
<p><li><b>spread</b> — here you can set how large the shadow will be;</li></p>
<p><li><b>horizontal</b> — here you can move the shadow horizontally (from right to left);</li></p>
<p><li><b>vertical</b> — here you can move the shadow vertically (from top to bottom).</li></p></ul>

<ul class="marked-list">
<p><li><b>Items Padding</b> — here you can set the custom padding for the slides. Fill in the values for the top, bottom, right and left padding in pixels to apply your custom padding. </li></p>
<p><li><b>Items Alignment</b> — set right, left or center alignment to use it for the slides content. </li></p></ul>

<h6>Banner Items</h6>

<p>You can customize this block if you’ve chosen <b>Banner layout</b> in <b>Items Layout</b> settings block. Here you can switch from customizing <b>Normal</b> to <b>Hover</b> style settings. Just click on the button <b>Hover</b> to proceed to customizing hover elements. </p>

<ul class="marked-list">
<p><li><b>Background Type</b> — in this block you can choose, whether you want to use classic or gradient type for the banner slides background. The customization options vary for each type. The options are similar to Simple Items Background type</li></p></ul>


<h6>Items Title Typography</h6>

<ul class="marked-list">
<p><li><b>Items Title Color</b> — here you can select the preferable color of the slides title using color picker. </li></p>
<p><li><b>Typography</b> — turn the option on to view the typography settings.
<ul class="marked-list">
<p><li><b>Size</b> — define the font size of the slide title. </li></p>
<p><li><b>Family</b> — here you can set the font family for the title. </li></p>
<p><li><b>Weight</b> — in this block you can select the suitable font weight.</li></p>
<p><li><b>Transform</b> — here you can choose from the dropdown menu, if you want the title to be shown in uppercase, lowercase, capitalize or normal way.</li></p>
<p><li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right). </li></p>
<p><li><b>Line Height</b> — in this field you can set the height of the slide title line. </li></p>
<p><li><b>Letter Spacing</b> — here you can set the space between letters. </li></p></ul></li></p></ul>

