<h4>Content</h4>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/brand2.png">
  	</figure>

<ul class="marked-list">
<p><li><b>Columns</b> — here you can select from 1 to 6 columns to showcase brands.</li></p>
<p><li><b>Add Item</b> — click on the button to add new brand. </li></p>
<p><li><b>Company Logo</b> — here you can upload or choose from the media library your brand image to showcase in this block.</li></p>
<p><li><b>Company Name</b> — in this field you need to input the name of the company brand. </li></p>
<p><li><b>Company Description</b> — here you can input the short description of the brand to give the visitors impression about it. </li></p>
<p><li><b>Company URL</b> — fill in the brand URL link. When clicking on the brand the visitor will be redirected to the brand page.</li></p></ul>

<h4>Style</h4>

<h6>Company Logo</h6>

<ul class="marked-list">
<p><li><b>Border Radius</b> - here you can set the border radius for the company logo.</li></p>

<p><li><b>Border Type</b> — here you can define the type of the border you need to use for the company logo. </li></p>

<p><li><b>Box Shadow</b> — enable this option if you want to access the shadow advanced settings for this module, and need to apply shadow for it.</li></p>

<p><li><b>Alignment</b> — here you can define the preferable logo alignment. The logo can be placed to the left, to the right, or remain centered. </li></p></ul>


<h6>Company Name</h6>

<ul class="marked-list">
<p><li><b>Title Color</b> — here you can define the suitable color for the brands title. Use color picker tool to select the necessary color. </li></p>

<p><li><b>Typography</b> — turn the option on to view the typography settings.
<ul class="marked-list">
<p><li><b>Size</b> — define the font size of the company name.</li></p>
<p><li><b>Family</b> — here you can set the font family for the company name.</li></p>
<p><li><b>Weight</b> — in this block you can select the suitable font weight.</li></p>
<p><li><b>Transform</b> — here you can choose from the dropdown menu, if you want the title to be shown in uppercase, lowercase, capitalize or normal way.</li></p>
<p><li><b>Style</b> — in this block you can choose from the dropdown menu the style for the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li></p>
<p><li><b>Line Height</b> — in this field you can set the height of the company name line.</li></p>
<p><li><b>Letter Spacing</b> — here you can set the space between letters.</li></p></ul></li></p>
<p><li><b>Alignment</b> — here you can define the preferable title alignment. The title can be placed to the left, to the right, or remain centered. </li></p></ul>


<h6>Company Description</h6>


<ul class="marked-list">
<p><li><b>Title Color</b> — here you can define the suitable color for the brands description. Use color picker tool to select the necessary color. </li></p>

<p><li><b>Typography</b> — turn the option on to view the typography settings. The options ate similar to Company Name Typography.</li></p>

<p><li><b>Alignment</b> — here you can define the preferable description alignment. The text can be placed to the left, to the right, or remain centered. </li></p></ul>


