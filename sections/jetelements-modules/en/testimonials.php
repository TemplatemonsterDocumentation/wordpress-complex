<h6>Settings</h6>

<ul class="marked-list">
<p><li><b>Slides to show</b> - here you can specify how many slides you want to show per section column (use <b>1</b> if you want to create a <b>testimonials slider</b>, or specify the number of testimonials to create a <b>carousel</b>). </li></p>
<p><li><b>Slides to Scroll</b> - here you can define how many slides are to scroll when scrolling.</li></p>
<p><li><b>Pause on hover</b> - the slider/carousel will be paused when on hover.</li></p>
<p><li><b>Autoplay</b> - enable this option in order to play the testimonials automatically.</li></p>
<p><li><b>Autoplay Speed</b> - here you can set the autoplay speed in ms.</li></p>
<p><li><b>Infinite Loop</b> - enable this option if you need the testimonials to play infinitely. </li></p>
<p><li><b>Effect</b> - when you use the testimonials slider, you can choose the animation effect (<b>slide, fade</b>).</li></p>
<p><li><b>Animation Speed</b> - here you can set the speed of animation in ms.</li></p>
<p><li><b>Show Arrows Navigation</b> - enable this option if you’re using testimonials slider. The navigation arrows will be added to right and left sides of the slider. You can also choose the arrow icons from the navigation icons list.</li></p>
<p><li><b>Show Dots Navigation</b> - enable this option if you’re using testimonials carousel.</li></p></ul>


<h6>Items </h6>

<p>Click <b>Add Item</b> button to add a new testimonial to the module.</p>

<ul class="marked-list">
<p><li><b>Image</b> - here you can add the photo of the feedback author.</li></p>
<p><li><b>Icon</b> - here you can select the icon to use for the testimonial.</li></p>
<p><li><b>Title</b> - input the title of the testimonial.</li></p>
<p><li><b>Comment</b> - here you need to add the testimonial text. </li></p>
<p><li><b>Name</b> - here you should input the name of the testimonial author.</li></p>
<p><li><b>Position</b> - here you should add the testimonial author’s work responsibilities.</li></p>
<p><li><b>Date</b> - here you can add the date when the testimonial was published. </li></p></ul>

