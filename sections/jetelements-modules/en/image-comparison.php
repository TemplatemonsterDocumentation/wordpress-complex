<h6>General</h6>

<ul class="marked-list">
<p><li><b>Border type</b> - here you can define the type of the border you need to use for the element.</li></p>
<p><li><b>Border radius</b> - here you can set the border radius to make the angles more smooth and round.</li></p>
<p><li><b>Padding</b> -  here you can set the preferable custom padding for the whole column where the widget is displayed. Fill in the values for the top, bottom, right and left padding in pixels to apply your custom padding.</li></p>
<p><li><b>Box shadow</b> - enable this option if you want to access the shadow advanced settings for this widget, and need to apply shadow for it.</li></p></ul>


<h6>Label</h6>

<p>Switch between <b>Before</b> and <b>After</b> blocks in order to customize styles for each of the text labels. </p>

<ul class="marked-list">
<p><li><b>Color</b> - here you can set the text color for the image label.</li></p>
<p><li><b>Typography</b> -  turn the option on to view the typography settings. They are similar to the typography settings of other JetElements widget.</li></p>
<p><li><b>Background type</b> -  in this block you can choose, whether you want to use classic or gradient type for the label background. The customization options vary for each type. The settings are similar to the common background type settings for other JetElements widget.</li></p>
<p><li><b>Margin</b> - input the values for the alerts margins in the required fields.</li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding for the labels. Fill in the values for the top, bottom, right and left padding in pixels to apply your custom padding.</li></p>
<p><li><b>Horizontal alignment</b> - select central, left or right alignment for the label.</li></p>
<p><li><b>Vertical alignment</b> - select central, left or right alignment for the label.</li></p></ul>


<h6>Handle</h6>

<p>In this block you can customize <b>Image Comparison handle</b>. It is an element that allows enlarging the image areas by dragging the borders between the two compared images.</p>


<ul class="marked-list">
<p><li><b>Control width</b> - here you can specify the width of the handle in px for different devices (desktop, tablet, mobile).</li></p>
<p><li><b>Weight</b> - here you can apply your custom height for the handle elements.</li></p></ul>

<p>Switch between <b>Normal</b> and <b>Hover</b> blocks in order to customize the style for the handle when on hover or in the normal mode.</p>

<ul class="marked-list">
<p><li><b>Background type</b> -  in this block you can choose, whether you want to use classic or gradient type for the handle background. The customization options vary for each type. The settings are similar to the common background type settings for other JetElements widget.</li></p>

<p><li><b>Box shadow</b> - enable this option if you want to access the shadow advanced settings for the handle, and need to apply shadow for it.</li></p>


<p><li><b>Border radius</b> - here you can set the border radius to make the angles more smooth and round.</li></p></ul>



<h6>Heading divider </h6>

<ul class="marked-list">
<p><li><b>Divider width</b> - here you can specify the divider width in px for different kinds of devices (desktop, tablet, mobile). </li></p>
<p><li><b>Divider color</b> - here you can select the color to apply to the divider using color picker tool. </li></p></ul>


<h6>Heading Arrow</h6>

<ul class="marked-list">
<p><li><b>Prev arrow icon</b> - here you can select the icon type to use for the Prev handle element. There are several icon types for you to choose (<b>angle, chevron, angle  double, arrow, caret, long arrow, arrow circle,  chevron circle, caret square</b>). </li></p>
<p><li><b>Next arrow icon</b> - here you can select the icon type to use for the Next handle element.</li></p>

<p><li><b>Icon size</b> - use the controls to define the needed icon size of fill in the size value in the corresponding field in px.</li></p>
<p><li><b>Margin</b> - here you can define the margins to use between the arrow icons.</li></p></ul>


<p>Switch between <b>Normal</b> and <b>Hover</b> modes to use different icon colors when on hover. </p>

<ul class="marked-list">
<p><li><b>Icon color</b> - here you can specify the color to use it for the handle icon. </li></p></ul>


<h6>Arrows</h6>

<p>Switch between <b>Normal</b> and <b>Hover</b> modes to apply different style setting for the navigation arrows when on hover, or when in the normal state. </p>

<ul class="marked-list">
<p><li><b>Font color</b> - here you can select the color to use for the navigation arrows using color picker tool.</li></p>
<p><li><b>Background type</b> -  in this block you can choose, whether you want to use classic or gradient type for the harrows background. The customization options vary for each type. The settings are similar to the common background type settings for other JetElements widget.</li></p>

<p><li><b>Font size</b> - here you need to specify the size of the arrows.</li></p>
<p><li><b>Box size</b> - here you can set the size of the navigation arrow box.</li></p>
<p><li><b>Border type</b> - here you can define the type of the border you need to use for the element.</li></p>
<p><li><b>Border radius</b> - here you can set the border radius to make the angles more smooth and round.</li></p>
<p><li><b>Box shadow</b> - enable this option if you want to access the shadow advanced settings for the arrows, and need to apply shadow for them.</li></p></ul>



<h6>Prev arrow position</h6>

<ul class="marked-list">
<p><li><b>Vertical position by</b> - define the vertical position for the prev arrow. It can be positioned at the top or at the bottom.</li></p>
<p><li><b>Top indent</b> -  define the vertical position indent in pixels, or use controls to define the suitable position automatically.</li></p>
<p><li><b>Horizontal position by</b> -  define the horizontal position for the prev arrow. It can be positioned on the left or on the right.</li></p>
<p><li><b>Left indent</b> - define the horizontal position indent in pixels, or use controls to define the suitable position automatically.</li></p></ul>


<h6>Next arrow position</h6>

<ul class="marked-list">
<p><li><b>Vertical position by</b> - define the vertical position for the next arrow. It can be positioned at the top or at the bottom.</li></p>
<p><li><b>Top indent</b> - define the vertical position indent in pixels, or use controls to define the suitable position automatically.</li></p>
<p><li><b>Horizontal position by</b> -  define the horizontal position for the next arrow. It can be positioned on the left or on the right.</li></p>
<p><li><b>Right indent</b> - define the horizontal position indent in pixels, or use controls to define the suitable position automatically.</li></p></ul>


<h6>Dots</h6>

<p>Here you can switch between <b>Hover, Active</b> and <b>Normal</b> modes to style up them separately.</p>

<ul class="marked-list">
<p><li><b>Font color</b> - define the color of the pagination dots using color picker tool.</li></p>
<p><li><b>Background type</b> -  in this block you can choose, whether you want to use classic or gradient type for the simple items. The customization options vary for each type.</li></p>
<p><li><b>Font size</b> - here you can select the size of the dot pagination controls in pixels.</li></p>
<p><li><b>Box size</b> - here you can select the size of the dot controls in pixels.</li></p>
<p><li><b>Border type</b> - here you can define the type of the border you need to use for the dots border.</li></p>
<p><li><b>Border radius</b> - here you can set the border radius to make the angles more smooth and round.</li></p>

<p><li><b>Box shadow</b> - enable this option if you want to access the shadow advanced settings for dots, and need to apply shadow for it.</li></p>

<p><li><b>Gap</b> - here you can define the gap between pagination dots in pixels, or use controls to set it automatically.</li></p>
<p><li><b>Dots box margin</b> - here you can define the margins for the Dots box.</li></p>
<p><li><b>Alignment</b> - set right, left or center alignment to use it for the dots.
</li></p></ul>


