<h6>General</h6>
<ul class="marked-list">
<p><li><b>Image</b>  — here you can upload or choose from the media library any image to use as a banner.
</li></p>
<p><li><b>Image Size</b>  — here you can set the preferable size for the banner module.
</li></p>
<p><li><b>Title</b> — in this block you need to input the title to use it for the banner.</li></p>
<p><li><b>Description</b> — here you have to add content to the banner. You can use HTML markup to separate text lines and quotes.
</li></p>
<p><li><b>Link</b> — here you need to place the link, that will redirect the visitors to another webpage when they click on the banner area. </li></p>
<p><li><b>Settings</b> — here you can select the animation effect you want to use for the banner. Choose one of the effects from the dropdown list to view it on the page builder screen. </li></p></ul>


<h4>Style</h4>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/ban3.png">
  	</figure>


<p>You can switch from customizing <b>Normal</b> to <b>Hover</b> style settings. Just click on the button Hover to proceed to customizing hover elements.
</p>

<ul class="marked-list">
<p><li><b>Additional Elements Color</b> — here you can define the color to use for additional elements of the banner. To apply a custom color select it using color picker.
</li></p>
<p><li><b>Background Type</b> — in this block you can choose, whether you want to use classic or gradient type for the bannerbackground. The customization options vary for each type.</li></p></ul>

<h6>Classic Background Type</h6>

<ul class="marked-list">
<p><li><b>Color</b> — here you can set background color for the banner using color picker tool.
</li></p>
<p><li><b>Image</b> — here you can choose the image you want to use as a background. Select the image from the media library or upload it to define it as a background in this block. When the image is uploaded, you’ll be able to define its position, alignment, turn on repeat and set the necessary size.
</li></p>
<p><li><b>Opacity</b> — here you can define the value for the opacity of the background.</li></p></ul>

<h6>Gradient Background Type</h6>

<ul class="marked-list">
<p><li><b>Color</b> — here you can set background color for the banner using color picker tool.
</li></p>
<p><li><b>Location</b> — here you can define the proportion for the first color in the gradient background.</li></p>
<p><li><b>Second Color</b> — here you can set the second color to use in the gradient background.</li></p>
<p><li><b>Location</b> — here you can define the proportion for the second color in the gradient background.
</li></p>
<p><li><b>Type</b> — define, if you want to use linear or radial type of the gradient.
</li></p>
<p><li><b>Angle</b> — in this block you can set the angle for the gradient (in degrees).</li></p>
<p><li><b>Opacity</b> — here you can change background opacity, where 1 is 100% opacity, and 0 — is 0% opacity.</li></p></ul>

<h6>Banner Title Typography</h6>


<ul class="marked-list">
<p><li><b>Title Color</b> — here you can define the color of the banner title using color picker tool. </li></p>
<p><li><b>Typography</b> — turn the option on to view the typography settings.
<ul class="marked-list">
<p><li><b>Size</b> — define the font size of the animated box title.</li></p>
<p><li><b>Family</b> — here you can set the font family for the title.</li></p>
<p><li><b>Weight</b> — in this block you can select the suitable font weight.</li></p>
<p><li><b>Transform</b> — here you can choose from the dropdown menu, if you want the title to be shown in uppercase, lowercase, capitalize or normal way.</li></p>
<p><li><b>Style</b> — in this block you can choose from the dropdown menu the style for the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li></p>
<p><li><b>Line Height</b> — in this field you can set the height of the title line.
<p><li><b>Letter Spacing</b> — here you can set the space between letters.</li></p></ul></li></p></ui>

<h6>Description Typography </h6>

<ul class="marked-list">
<p><li><b>Description Color</b> — here you can set the color for the banner content text. </li></p>
<p><li><b>Typography</b> — turn the option on to view the typography settings (they are similar to banner title typography settings).</li></p></ul>
