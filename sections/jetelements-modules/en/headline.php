<p><li><b>Background type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the headline’s second part. The customization options vary for each type. The settings are similar to the common background type settings for other <b>JetElements</b> modules.</li></p>
<p><li><b>Border radius</b> - here you can set the border radius for the chosen border to make the border angles more round.</li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding for the second part of the headline. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Margin</b> - here you can define the margins for the second part of the headline.</li></p>
<p><li><b>Alignment</b> - here you can define the preferable alignment for the second part of the text. It can be placed to the <b>left</b>, to the <b>right</b>, or remain <b>centered</b>.</li></p></ul>

<h6>Decorative Elements</h6>

<b>Before Deco Element</b>

<ul class="marked-list">
<p><li><b>Before Deco type</b> - here you can select the needed decoration type to place it before the headline element. It can be <b>Icon element, Image element</b>, or you can select <b>None</b> to use neither of these decorations.</li></p>
<p><li><b>Before Icon</b> - when you select <b>Icon deco type</b>, you can specify the icon in this field.</li></p>
<p><li><b>Before Icon Color</b> -  here you can specify the color of the icon using color picker tool.</li></p>
<p><li><b>Before Icon Size</b> - use the controls to change the icon size or type in the size value in px in the corresponding field.</li></p>
<p><li><b>Margin</b> - here you can define the margins for the icon.</li></p>
<p><li><b>Alignment</b> -  here you can define the preferable icon alignment. It can be placed to the left, to the right, or remain centered.</li></p>
<p><li><b>Before image</b> - in the case you’ve chosen <b>Image deco type</b>, you’ll be able to upload or select from the media library an image to use it as a decoration of the headline.</li></p>
<p><li><b>Before image size</b> - here you can define the size of the selected image.</li></p></ul>

<b>After Deco Element</b>

<ul class="marked-list">
<p><li><b>After Deco Type</b> - here you can select the needed decoration type to place it after the headline element. It can be <b>Icon element, Image element</b>, or you can select <b>None</b> to use neither of these decorations.</li></p>
<p><li><b>After Icon color</b> - here you can specify the color of the icon using color picker tool.</li></p>
<p><li><b>After icon size</b> - use the controls to change the icon size or type in the size value in px in the corresponding field.</li></p>
<p><li><b>Margin</b> - here you can define the margins for the icon.</li></p>
<p><li><b>Alignment</b> - here you can define the preferable icon alignment. It can be placed to the left, to the right, or remain centered.</li></p>
<p><li><b>After image</b> - in the case you’ve chosen <b>Image deco type</b>, you’ll be able to upload or select from the media library an image to use it as a decoration of the headline.</li></p>
<p><li><b>After image size</b> - here you can define the size of the selected image.</li></p></ul>
