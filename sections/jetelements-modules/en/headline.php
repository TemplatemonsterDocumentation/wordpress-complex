<h6>General</h6>

<ul class="marked-list">
<p><li><b>Direction</b> - here you can set the preferable headline direction. It can arrange text using <b>horizontal</b> or <b>vertical</b> alignment.</li></p>
<p><li><b>Space between</b> - when using horizontal alignment, you can choose whether it is necessary to add the space between the two parts of the heading.</li></p>
<p><li><b>Alignment</b> - here you can select the needed alignment for the headline text (<b>left, center, right</b>).</li></p>
<p><li><b>Background type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the headline. The customization options vary for each type. The settings are similar to the common background type settings for other <b>JetElements</b> modules.</li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding for the headline. Fill in the values for the <b>top, bottom, right</b> and </b>left</li> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Margin</b> - input the values for the headline margins in the required fields.
<p><li><b>Border type</b> - here you can define the needed border type for the headline block. It can be <b>dotted, dashed, double</b> or <b>solid</b>.</li></p>
<p><li><b>Border radius</b> - here you can set the border radius for the chosen border to make the border angles more round.</li></p></ul>

<h6>First part</h6>

<ul class="marked-list">
<p><li><b>Text color</b> - here you can change the first part of the headline text color using color picker.</li></p>
<p><li><b>Typography</b> - turn the option on to view the typography settings. They are similar to the typography settings of other <b>JetElements</b> modules.</li></p>
<p><li><b>Background type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the headline’s first part. The customization options vary for each type. The settings are similar to the common background type settings for other <b>JetElements</b> modules.</li></p>
<p><li><b>Border type</b> - here you can define the type of the border you need to use for the element.</li></p>
<p><li><b>Border radius</b> - here you can set the border radius for the chosen border to make the border angles more round.</li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding for the first part of the headline. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Margin</b> - here you can define the margins for the first part of the headline.</li></p>
<p><li><b>Alignment</b> - here you can define the preferable alignment for the first part of the text. It can be placed to the left, to the right, or remain centered.</li></p></ul>

