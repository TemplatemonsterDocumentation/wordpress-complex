<h6>Items</h6>

<ul class="marked-list">
<p><li><b>Add item</b> - click this button in order to add a new <b>Scroll Navigation</b> item to the block.</li></p>
<p><li><b>Icon (hint/dot)</b> - here you need to specify the icons to use in this module section from the list of the available icons.</li></p>
<p><li><b>Label</b> - specify the section label in the corresponding field.</li></p>
<p><li><b>Section ID</b> - here you can set the specific <b>section ID</b> (it can contain letters and numbers, be sure not to use spaces).</li></p></ul>

<h6>Settings</h6>

<ul class="marked-list">
<p><li><b>Position</b> - here you can change content position (<b>left</b> or <b>right</b>).</li></p>
<p><li><b>Scroll speed</b> - here you can set the necessary scrolling speed in ms.</li></p>
<p><li><b>Full section switch</b> - enable this option to switch between the whole sections when scrolling, or disable it to scroll slowly from section to section.</li></p></ul>

