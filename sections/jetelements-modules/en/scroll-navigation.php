<h6>General</h6>

<ul class="marked-list">
<p><li><b>Background type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the module. The customization options vary for each type. The settings are similar to the common background type settings for other <b>JetElements</b> modules.</li></p>
<p><li><b>Border type</b> - here you can define the needed border type for the module. It can be <b>dotted, dashed, double</b> or <b>solid</b>.</li></p>
<p><li><b>Border radius</b> - here you can set the border radius for the chosen border to make the border angles more round.</li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding for the module. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Margin</b> - input the values for the margins in the required fields.</li></p>
<p><li><b>Box shadow</b> - enable this option if you want to access the shadow advanced settings for the module, and need to apply shadow for it.</li></p></ul>

<h6>Hint</h6>

<ul class="marked-list">
<p><li><b>Background type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the module. The customization options vary for each type. The settings are similar to the common background type settings for other <b>JetElements</b> modules.</li></p>
<p><li><b>Border type</b> - here you can define the needed border type for the module hint. It can be <b>dotted, dashed, double</b> or <b>solid</b>.</li></p>
<p><li><b>Border radius</b> - here you can set the border radius for the chosen border to make the border angles more round.</li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding for the hint. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Box shadow</b> - enable this option if you want to access the shadow advanced settings for the hint, and need to apply shadow for it.</li></p></ul>


<b>Hint Icon</b>

<p>Here you can set the style of the hint icon.</p>

<ul class="marked-list">
<p><li><b>Icon color</b> - here you can specify the color of the icon using color picker tool.</li></p>
<p><li><b>Icon size</b> - use the controls to change the icon size or type in the size value in px in the corresponding field.</li></p>
<p><li><b>Margin</b> - input the values for the margins in the required fields.</li></p></ul>


<b>Hint Label</b>

<p>Here you can specify the style to use it for the hint label.</p>

<ul class="marked-list">
<p><li><b>Text color</b> - here you can change the first part of the label text color using color picker.</li></p>
<p><li><b>Typography</b> - turn the option on to view the typography settings. They are similar to the typography settings of other <b>JetElements</b> modules.</li></p></ul>


<h6>Dots</h6>

<p>Switch between <b>Normal, Hover</b> and <b>Active</b> mode to customize <b>Dots style</b> according to your needs when the elements are active, or in the normal state.</p>

<ul class="marked-list">
<p><li><b>Font color</b> - define the color of the dots using color picker tool.</li></p>
<p><li><b>Background type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the dots. The customization options vary for each type.</li></p>
<p><li><b>Font size</b> - here you can select the size of the dot controls in pixels.</li></p>
<p><li><b>Box size</b> - here you can select the size of the dot controls box in pixels.</li></p>
<p><li><b>Border type</b> - here you can define the needed border type for the dots. It can be <b>dotted, dashed, double</b> or <b>solid</b>.</li></p>
<p><li><b>Border radius</b> - here you can set the border radius for the chosen border to make the border angles more round.</li></p>
<p><li><b>Box shadow</b> - enable this option if you want to access the shadow advanced settings for the dots, and need to apply shadow for them.</li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding for the dots. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Dots margin</b> - input the values for the dots margins in the required fields.</li></p></ul>

