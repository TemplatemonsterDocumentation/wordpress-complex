<h5>Audio </h5>

<ul class="marked-list">
<p><li><b>Audio Source</b> - here you can specify the source of your audio file. There are <b>Self Hosted</b> and <b>External</b> options, which means you can either upload an audio file from your hard drive or input the external link. </li></p>
<p><li><b>URL</b> - here you paste the external link or upload the file from your computer.</li></p></ul>

<h6>Audio Options</h6>

<ul class="marked-list">
<p><li><b>Loop</b> - enable this option in order for the audio to play without ending.</li></p>
<p><li><b>Muted</b> - this option allows you to put the song on mute.</li></p></ul>

<h6>Controls Options</h6>

<ul class="marked-list">
<p><li><b>Progress Bar</b> - as soon as you enable this option, the progress bar appears. </li></p>
<p><li><b>Current Time</b> - enable this option so that the listeners know how much time they’ve already spent listening to the song. </li></p>
<p><li><b>Duration Time</b> - enable this option to showcase the total duration of the song. </li></p>
<p><li><b>Volume</b> - with enabling this option you allow the listeners to select the preferable volume. </li></p>
<p><li><b>Hide Volume On Touch Devices</b> - enabling this option means, if you view this widget with a tablet or a mobile, the volume icon and bar will not be visible. </li></p>
<p><li><b>Volume Bar</b> - enable or disable this option to show or hide the volume bar, where one can define the comfortable volume. </li></p>
<p><li><b>Volume Bar Layout</b> - here you can pick either Horizontal or Vertical position of the volume bar. </li></p>
<p><li><b>Start Volume</b> - choose the volume the song will start with at the beginning. </li></p></ul>


