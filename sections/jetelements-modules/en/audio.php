<h5>General</h5>


<ul class="marked-list">
<p><li><b>Width</b> - specify the preferable width of the widget in pixels.</li></p>
<p><li><b>Alignment</b> - set right, left or center alignment to use it for the widget.</li></p></ul>

<h5>Play-Pause Button and Time</h5>

<h6>Play-Pause Button</h6>

<ul class="marked-list">
<p><li><b>Font size</b>  -  here you can select the size of the “Play” button icon in pixels when Normal and on Hover.</li></p>
<p><li><b>Color</b>  - here you are allowed to pick the color of the “Play” button icon when Normal and on Hover.</li></p>
<p><li><b>Background color</b>  - here you can select the background color of the “Play” button.</li></p>
<p><li><b>Padding</b>  - here you can set the preferable custom padding for the widget. Fill in the values for the top, bottom, right and left padding in pixels to apply your custom padding.</li></p>
<p><li><b>Border Radius</b>  - here you can set the border radius to make the angles more smooth and round.</li></p>
<p><li><b>Border Type</b>  - here you can define the type of the border you need to use for the element.</li></p>
<p><li><b>Box Shadow</b>  - enable this option if you want to access the shadow advanced settings for the items, and need to apply shadow for them.</li></p></ul>

<h6>Time</h6>

<ul class="marked-list">
<p><li><b>Typography</b> - turn the option on to view the typography settings. They are similar to the typography settings of other JetElements widgets.</li></p>
<p><li><b>Color</b> - pick the preferable color of the time digits.</li></p></ul>

