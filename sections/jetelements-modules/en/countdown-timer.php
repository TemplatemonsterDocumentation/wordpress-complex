<h4>Content</h4>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/count2.png">
  	</figure>
<ul class="marked-list">
<p><li><b>Due Date</b> — here you need to input the date to finish the countdown according to your time zone. </li></p>
<p><li><b>Days</b> — in this block you can enable or disable the days counter. When the option is disabled, the countdown will consist of hours, minutes and seconds only. </li></p>
<p><li><b>Days Label</b> — here you can change standard days label to your custom name. </li></p>
<p><li><b>Hours</b> — here you can enable or disable the hours counter. When the option is disabled, the countdown will consist of days, minutes and seconds only. </li></p>
<p><li><b>Hours Label</b> — here you can change standard hours label to your custom name. </li></p>
<p><li><b>Minutes</b> — here you can enable or disable minutes counter. When the option is disabled, the countdown will display days, hours and seconds only. </li></p>
<p><li><b>Minutes Label</b> — here you can change standard minutes label to your custom name. </li></p>
<p><li><b>Seconds</b> — here you can enable or disable seconds counter. When the option is disabled, the countdown will display days, hours and minutes only. </li></p>
<p><li><b>Seconds Label</b> — here you can change standard seconds label to your custom name.</li></p>
<p><li><b>Blocks Separator</b> — here you can input your custom separator to divide days, hours, minutes and seconds. </li></p></ul>




