<h6>Item Styles</h6>



<ul class="marked-list">
<p><li><b>Items Size</b> — here you can select, if you want to use auto size, or you would rather use fixed size for the items. If you choose the fixed width for the items, you’ll be able to set the width and height in pixels in the blocks below. </li></p>
<p><li><b>Width</b> — here you can select the width of the items. </li></p>
<p><li><b>Height</b> — here you can select the height of the items. </li></p>


<p><li><b>Background Type</b> — in this block you can choose, whether you want to use classic or gradient type for the background. The customization options vary for each type.</li></p></ul>


<h6>Classic Background Type</h6>


<ul class="marked-list">
<p><li><b>Color</b> — here you can set background color using color picker tool.</li></p>
<p><li><b>Image</b> — here you can choose the image you want to use as a background. Select the image from the media library or upload it to define it as a background in this block. When the image is uploaded, you’ll be able to define its position, alignment, turn on repeat and set the necessary size.</li></p>
<p><li><b>Opacity</b> — here you can define the value for the opacity of the background.</li></p></ul>


<h6>Gradient Background Type</h6>



<ul class="marked-list">
<p><li><b>Color</b> — here you can set background color using color picker tool.</li></p>
<p><li><b>Location </b> — here you can define the proportion for the first color in the gradient background.</li></p>
<p><li><b>Second Color</b> — here you can set the second color to use in the gradient background.</li></p>
<p><li><b>Location</b> — here you can define the proportion for the second color in the gradient background.</li></p>
<p><li><b>Type</b> — define, if you want to use linear or radial type of the gradient.</li></p>
<p><li><b>Angle</b> — in this block you can set the angle for the gradient (in degrees).</li></p>
<p><li><b>Opacity</b> — here you can change background opacity, where 1 is 100% opacity, and 0 — is 0% opacity.</li></p>



<p><li><b>Border Type</b> — here you can define the type of the border you need to use for the animated box. </li></p>


<p><li><b>Box Shadow</b> — enable this option if you want to access the shadow advanced settings for this module, and need to apply shadow for it.</li></p></ul>

<h6>Labels</h6>


<ul class="marked-list">
<p><li><b>Color</b> — here you can define the suitable color for the countdown timer. Use color picker tool to select the necessary color. </li></p>
<p><li><b>Typography</b> — turn the option on to view the typography settings.

<ul class="marked-list">
<p><li><b>Size</b> — define the font size of the countdown timer text.</li></p>
<p><li><b>Family</b> — here you can set the font family for the countdown timer text.</li></p>
<p><li><b>Weight</b> — in this block you can select the suitable font weight.</li></p>
<p><li><b>Transform</b> — here you can choose from the dropdown menu, if you want the countdown timer text to be shown in uppercase, lowercase, capitalize or normal way.</li></p>
<p><li><b>Style</b> — in this block you can choose from the dropdown menu the style for the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li></p>
<p><li><b>Line Height</b> — in this field you can set the height of the countdown timer text line.</li></p>
<p><li><b>Letter Spacing</b> — here you can set the space between letters.</li></p></ul></li></p>

<p><li><b>Box Shadow</b> — enable this option if you want to access the shadow advanced settings for this module, and need to apply shadow for it.</li></p>

</ul>


<h6>Digits </h6>


<p>In this block you can set your custom styles for the countdown timer digits. The style options are similar to the general style options. </p>


<h6>Separator Styles</h6>


<ul class="marked-list">
<p><li><b>Color</b> — here you can select the suitable color for the separator (usually it is a colon between the digits). </li></p>
<p><li><b>Size</b> — in this block you can select the size of the separator in pixels.</li></p>
<p><li><b>Font </b> — in this block you can define the font in which the separator will be shown.</li></p></ul>

<h6>Order</h6>

<p>In this block you can set the custom order for the digits and labels.</p>
