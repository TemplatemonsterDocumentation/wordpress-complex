<h6>General</h6>

<p>In this block you can customize Pricing Table general content settings.</p>

<ul class="marked-list">
<p><li><b>Icon</b> — here you can select the suitable icon to use in the widget. Select one of the available icons from the dropdown list. </li></p>
<p><li><b>Title</b> — fill in the field with the Pricing Table widget title.</li></p>
<p><li><b>Subtitle</b> — here you should enter the subtitle for the pricing table.</li></p>
<p><li><b>Is Featured?</b> — turn on the option if you need to apply additional featured badge, use a custom position of the featured badge, etc.


<h6>Price</h6>

<ul class="marked-list">
<p><li><b>Price Prefix</b> — in this field you need to input the prefix for the price, usually it is a currency symbol (you can as well leave the field empty if you don’t want to use any currency prefixes). </li></p>
<p><li><b>Price Value</b> — in this field you need to define the price value to be shown. </li></p>
<p><li><b>Price Suffix</b> — here you have to define the suffix for the price (usually it indicates the frequency of payment, e.g., “per month”, “per year”, etc.).</li></p></ul>

<h6>Features </h6>

<ul class="marked-list">
<p><li><b>Add Item</b> — here you can add a new feature to be displayed in the pricing table. </li></p>
<p><li><b>Text</b> — type in the feature title in this block.</li></p>
<p><li><b>Is Included?</b> — enable the option if you want to show that the feature is included for the product, or disable it to show that the product or service doesn’t have this feature included. </li></p></ul>

<h6>Action Button</h6>

<ul class="marked-list">
<p><li><b>Text Before Action Button</b> — here you can specify the text you want to show before the Action Button. </li></p>
<p><li><b>Button Text</b> — here you can input your custom Action button text (e.g., “purchase now”, “buy”, etc.).</li></p>
<p><li><b>Button URL</b> — in this field you need to define the URL link that will redirect the customers to the purchase page or shopping cart. </li></p>
<p><li><b>Text After Action Button</b> — if you need to add the text below the Action button, just type in it into this field. </li></p></ul></li></p></ul>

