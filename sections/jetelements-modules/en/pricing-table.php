<h6>Pricing </h6>


<p>In this block you can  apply custom appearance settings to the pricing background, prefix, values and suffix typography and colors. </p>


<ul class="marked-list">
<p><li><b>Background Color</b> — here you can select the color to use it for the pricing general background.</li></p>
<p><li><b>Price Prefix Color</b> — here you can select the preferable color for the price prefix.</li></p>
<p><li><b>Price Suffix Color</b> — here you need to specify the color of the price suffix.</li></p>
<p><li><b>Price Color</b> — in this block you can define the color to use for the price.</li></p>
<p><li><b>Typography</b> — turn the option on to view the typography settings. The typography settings are similar for Title, Subtitle, Pricing, Features, Action Box and Action Button blocks.</li></p>
<p><li><b>Vertical Alignment</b> — here you can define the specifics of the prefix vertical alignment.</li></p>
<p><li><b>Prefix Display</b> — here you can define, if you want the prefix to be displayed inline, or as a separate block.</li></p>
<p><li><b>Suffix Display</b> — here you can define, if you want the suffix to be displayed inline, or as a separate block.</li></p>
<p><li><b>Border Type</b> — here you can define the type of the border you need to use for the element.</li></p>
<p><li><b>Alignment</b> — here you can define the preferable alignment. The element can be placed to the left, to the right, or remain centered.</li></p></ul>


<h6>Features</h6>


<p>In this block you can customize pricing table features, such as features background color and alignment. </p>


<ul class="marked-list">
<p><li><b>Background Color</b> — here you can define the background color to use it in the features background.</li></p>
<p><li><b>Alignment</b> — here you can define the preferable features alignment. The features can be placed to the left, to the right, or remain centered.</li></p>
<p><li><b>Typography</b> — turn the option on to view the typography settings. The typography settings are similar for Title, Subtitle, Pricing, Features, Action Box and Action Button blocks.</li></p>
<p><li><b>Included (Excluded) Feature Color</b> — here you can specify the color for the excluded and included features.</li></p>
<p><li><b>Included Bullet Icon</b> — here you can define the icon to use for the included features.</li></p>
<p><li><b>Excluded Bullet Icon</b> — here you can select the icon you want to use for the excluded features.</li></p>
<p><li><b>Text Decoration</b> — for the excluded features you can enable such text decoration element, as line through.</li></p>
<p><li><b>Bullet Color</b> — here you can select the color you want to use for the included of excluded features icons.</li></p>
<p><li><b>Icon Size</b> — here you can define the size of the excluded of included features icons.</li></p>
<p><li><b>Features Divider</b> — enable the option if you want to separate features using a divider to make them stand out.</li></p>
<p><li><b>Divider Style</b> — here you can select the style to use for the divider. It can be solid, double, dotted or dashed.</li></p></ul>
<p>You can also define the weight, the width and the gap for the divider using the corresponding controls.</p>


