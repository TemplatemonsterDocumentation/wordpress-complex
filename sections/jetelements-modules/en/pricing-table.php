<h6>Table</h6>


<p>Here you can customize the pricing table style. </p>


<ul class="marked-list">
<p><li><b>Background Type</b> — in this block you can choose, whether you want to use classic or gradient type for the pricing table background. The customization options vary for each type.

<h6>Classic Background Type</h6>


<ul class="marked-list">
<p><li><b>Color</b> — here you can set background color for the animated box using color picker tool.</li></p>
<p><li><b>Image</b> — here you can choose the image you want to use as a background. Select the image from the media library or upload it to define it as a background in this block. When the image is uploaded, you’ll be able to define its position, alignment, turn on repeat and set the necessary size.</li></p></ul>

<h6>Gradient Background Type</h6>


<ul class="marked-list">
<p><li><b>Color</b> — here you can set background color for the widget using color picker tool.</li></p>
<p><li><b>Location</b> — here you can define the proportion for the first color in the gradient background.</li></p>
<p><li><b>Second Color</b> — here you can set the second color to use in the gradient background.</li></p>
<p><li><b>Location</b> — here you can define the proportion for the second color in the gradient background.</li></p>
<p><li><b>Type</b> — define, if you want to use linear or radial type of the gradient.</li></p>
<p><li><b>Angle</b> — in this block you can set the angle for the gradient (in degrees).</li></p>
<p><li><b>Border Type</b> — here you can define the type of the border you need to use for the element.</li></p>
<p><li><b>Box Shadow</b> — enable this option if you want to access the shadow advanced settings for this widget, and need to apply shadow for it.</li></p></ul></li></p></ul>


<h6>Header</h6>


<p>In this block you can apply custom colors and typography settings to the pricing table header.</p>


<ul class="marked-list">
<p><li><b>Background Color</b> — here you can define the suitable color for the header background. Use color picker tool to select the necessary color.</li></p>
<p><li><b>Title Color</b> — here you can define the suitable color for the header title. Use color picker tool to select the necessary color.</li></p>
<p><li><b>Subtitle Color</b> — here you can define the suitable color for the header subtitle. Use color picker tool to select the necessary color.</li></p>

<p><li><b>Typography</b> — turn the option on to view the typography settings. The typography settings are similar for Title, Subtitle, Pricing, Features, Action Box and Action Button blocks.

<ul class="marked-list">
<p><li><b>Size</b> — define the font size of the animated box title.</li></p>
<p><li><b>Family</b> — here you can set the font family for the title.</li></p>
<p><li><b>Weight</b> — in this block you can select the suitable font weight.</li></p>
<p><li><b>Transform</b> — here you can choose from the dropdown menu, if you want the title to be shown in uppercase, lowercase, capitalize or normal way.</li></p>
<p><li><b>Style</b> — in this block you can choose from the dropdown menu the style for the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li></p>
<p><li><b>Line Height</b> — in this field you can set the height of the title line.</li></p>
<p><li><b>Letter Spacing</b> — here you can set the space between letters.</li></p></ul></li></p>
<p><li><b>Border Type</b> — here you can define the type of the border you need to use for the element. </li></p>
<p><li><b>Alignment</b> — here you can define the preferable title alignment. The title can be placed to the left, to the right, or remain centered.</li></p></ul>


