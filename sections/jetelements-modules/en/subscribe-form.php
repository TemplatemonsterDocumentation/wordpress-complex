<h6>General</h6>

<ul class="marked-list">
<p><li><b>Layout</b> - here you can select the preferable Subscribe form layout (<b>Inline</b> or <b>Block</b>) from the dropdown list.</li></p>
<p><li><b>Width</b> - use the controls or type in the needed value in pixels to make the widget width responsive.</li></p>
<p><li><b>Background Color</b> - use this option to apply the custom background color for the whole widget using color picker tool.</li></p>
<p><li><b>Padding</b> - input your custom padding values in the corresponding fields to change the Subscribe form padding (the form text field).</li></p>
<p><li><b>Margin</b> - input your custom margin values in px in order to apply the margins for the widget.</li></p>
<p><li><b>Border Radius</b> -  here you can define the values for the radius of the selected border to make its angles more smooth.</li></p>
<p><li><b>Border Type</b> - here you can define the needed border type for the controls. It can be dotted, dashed, double or solid.</li></p>
<p><li><b>Box Shadow</b> - enable this option if you want to access the shadow advanced settings for the widget, and need to apply shadow for it.</li></p></ul>


<h6>Input</h6>

<ul class="marked-list">
<p><li><b>Width</b> - here you can change the form field width to make its appearance responsive.</li></p>
<p><li><b>Padding</b> - input your custom padding values to apply them to the form field.</li></p>
<p><li><b>Margin</b> - input your custom margin values to apply margins for the form field.</li></p>
<p><li><b>Border Radius</b> - here you can define the values for the radius of the selected border to make its angles more smooth.</li></p></ul>


<p>Switch from <b>Normal</b> to <b>Error</b> modes to customize the input form field appearance in its normal state of when the provided email address is incorrect.</p>

<ul class="marked-list">
<p><li><b>Background Color</b> - here you can select the color to use for the form input field background using color picker tool.</li></p>
<p><li><b>Text Color</b> - here you can select the color of the text that will be placed in the input form field.</li></p>
<p><li><b>Typography</b> -  turn the option on to view the typography settings. They are similar to the typography settings of other JetElements widget.</li></p>
<p><li><b>Border Type</b> -  here you can define the needed border type for the element. It can be dotted, dashed, double or solid.</li></p>
<p><li><b>Box Shadow</b> -  enable this option if you want to access the shadow advanced settings for the input form field, and need to apply shadow for it.</li></p></ul>


