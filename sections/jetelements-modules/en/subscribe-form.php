<h2>Subscribe Form</h2>


<h6>
Subscribe Form widget will assist you in creating a Subscription form that will work with your MailChimp list. This widget is really easy to use, allowing to change the Subscription form layout, background color, width, add borders, shadows, and there are still many more exquisite options.
</h6>

