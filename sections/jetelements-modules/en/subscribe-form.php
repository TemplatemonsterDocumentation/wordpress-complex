<h4>Connecting Subscribe Form to MailChimp</h4>

<ul class="index-list">
<p><li>First, let’s add the <b>MailChimp List ID</b> and <b>API Key</b> to JetElements to connect the Subscribe Form widget to it. Navigate to <b>Elementor > JetElements Settings</b> tab on the left of the Dashboard.</li></p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widget/subscr3.png">
  	</figure>

<p><li>Here switch to <b>Mailing List Manager</b> settings block.</li></p>

<p><li>Add the <b>MailChimp API key</b> and the <b>MailChimp List ID</b> in the corresponding fields.</li></p>

<p><li>If you don’t know where to get this information, please, feel free to read <a href="https://zemez.io/wordpress/support/how-to/how-to-add-mailchimp-widget-to-sidebar/" target="_blank">this</a> tutorial.</li></p>

<p><li>After that you need to navigate to the Elementor page editor and drop the <b>Subscribe Form</b> widget to the section where you need to add it.</li></p>

<p><li>Let’s overview the <b>Subscribe Form</b> settings.</li></p></ul>


