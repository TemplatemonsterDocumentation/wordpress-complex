<h6>Settings</h6>

<ul class="marked-list">
<p><li><b>Submit Text</b>  - in this field you need to type in the button label text you want to show up on <b>Submit</b> button.</li></p>
<p><li><b>Input Placeholder</b> - here you need to type in the text that will be shown as the placeholder in the field where the visitor has to enter his email address.</li></p></ul>


