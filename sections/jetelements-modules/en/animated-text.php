<h6>General</h6>
<ul class="marked-list">
<p><li><b>Before Text</b> — here you can add the text passage that will go before animated text. You can also leave the field empty if you want animated text to show up at the beginning of the line. </li></p>
<p><li><b>Add Item</b> — click on the button to add animated text. In the Text field type in the text you want to animate. You can  as well add several items of animated text. In this case item 1 will be replaced with item 2 in loops. </li></p>
<p><li><b>After Text</b> — in this field you need to input the text passage, that goes after animated text. You can also leave the field empty if you don’t want to continue animated text with the plain text. </li></p></ul>

<h6>Settings</h6>

<ul class="marked-list">
<p><li><b>Animation Effect</b> — here you can select the preferable animation type from the dropdown menu. </li></p>
<p><li><b>Animation Delay</b> — define the value for the animation delay (the value sets the time for which the text will stay on the screen without animation). Input the time in ms (the minimum value is 500 ms). </li></p>
<p><li><b>Split Type</b> — here you can define, how the text will split when animated (in whole words, or in symbols). </li></p></ul>



<h4>Style</h4>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/text3.png">
  	</figure>

<p>In this block you can define styles for these blocks:</p>

<ul class="marked-list">
<p><li><b>Before Text</b> — the text that goes before the animated text passage;</li></p>
<p><li><b>Animated Text</b> — the text that has animation effects;</li></p>
<p><li><b>After Text</b> — the text that goes after animated text passage. </li></p></ul>
<p>The style settings for these three blocks are similar. </p>

<h6>Style Settings</h6>

<ul class="marked-list">
<p><li><b>Color</b> — here you can define the color of the text using color picker tool.</li></p>
<p><li><b>Background Color</b> — here you need to set the background color to increase text readability. </li></p>
<p><li><b>Typography</b> — turn the option on to view the typography settings.
<ul class="marked-list">
<p><li><b>Size</b> — define the font size of the animated box content.</li></p>
<p><li><b>Family</b> — here you can set the font family for the content.</li></p>
<p><li><b>Weight</b> — in this block you can select the suitable font weight.</li></p>
<p><li><b>Transform</b> — here you can choose from the dropdown menu, if you want the content to be shown in uppercase, lowercase, capitalize or normal way.</li></p>
<p><li><b>Style</b> — in this block you can choose from the dropdown menu the style for the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li></p>
<p><li><b>Line Height</b> — in this field you can set the height of the content text lines.</li></p>
<p><li><b>Letter Spacing</b> — here you can set the space between letters.</li></p></li></p><ul>
<p><li><b>Alignment</b> — here you can define the preferable content alignment. The title can be placed to the left, to the right, or remain centered. </li></p></ul>
