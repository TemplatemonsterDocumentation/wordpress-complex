<h5>Title</h5>

<ul class="marked-list">

<li><b>Color</b> – specify the color of the title with color picker tool.</li>
<li><b>Typography</b> – turn the option on to view the typography settings. They are similar to the typography settings of other JetElements widget.</li>
<li><b>Alignment</b> – here you can set the weather region's alignment. It can be located on the left, in the center or on the right.</li>
<li><b>Margin</b> – fill in the custom margin values to add a responsive margin around the block.</li>
<li><b>Padding</b> – type in the custom padding values to add the responsive padding for the block.</li>
<li><b>Border Type</b>– select how borders of the title will be looked (solid, double, dotted, dashed and groove types).</li>
<li><b>Text Shadow</b> – use the shadow settings (such as shadow color, blur, horizontal, vertical) if you want to make the title’s text highlighted.</li></ul>

<h5>Current Weather / Forecast Weather / Details Weather</h5>

<p>These blocks contain similar style settings. They are easily manageable and allow to apply different stylization for the current, forecasted and detailed weather.</p>

    <h6>Container</h6>

<ul class="marked-list">

<li><b>Margin</b> – fill in the custom margin values to add a responsive margin around the block.</li>
<li><b>Padding</b> – define in the custom padding values to add the responsive padding for the block.</li>
<li><b>Border Type</b> – select how the borders of block will be looked (solid, double, dotted, dashed and groove types).</li>
<li><b>Width</b> - here you can define the width of the container's border.</li>
<li><b>Color</b> - here select the color of the container's border using color picker tool.</li></ul>

    <h6>Temperature</h6>

<ul class="marked-list">

<li><b>Color</b> – select the temperature’s color with color picker tool.</li>
<li><b>Typography</b> – turn the option on to view the typography settings. They are similar to the typography settings of other JetElements widget.</li></ul>

<h6>Icon</h6>

<ul class="marked-list">

<li><b>Color</b> – here you can specify the icon’s color using Color picker tool.</li>
<li><b>Font Size</b> – here you can set the icon’s size.</li></ul>

    <h6>Description</h6>

<ul class="marked-list">

<li><b>Color</b> – here you can choose the description’s color using color picker tool.</li>
<li><b>Typography</b> – turn the option on to view the typography settings. They are similar to the typography settings of other JetElements widget.</li>
<li><b>Gap</b> – set the space between the description and the icon.</li></ul></li></ul>

