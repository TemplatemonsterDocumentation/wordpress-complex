<h5>Weather</h5>

<ul class="marked-list">
<li><b>Location</b> - indicate the location which weather you want to display.</li>
<li><b>Units</b> – here you can define in what system to display the temperature (Metric  – °C or Imperial – °F).</li></ul>

<h5>Settings</h5>

<ul class="marked-list">

<li><b>Title HTML Tag</b> – here you can select the HTML tag  (div, or heading) to apply for the title.</li>
<li><b>Show country name</b> – this option allows showing the country’s name.</li>
<li><b>Show current weather</b> – here you can set if the current weather will be displayed or not. </li>
<li><b>Show current weather details</b> – this option makes it possible to showcase the details of current weather (the day, dawn/ sunset time, min/ max temperature, precipitation). </li>
<li><b>Show forecast weather</b> – enable this option if you want to display the weather forecast for some days and select the amount of days that will be showcased. </li></ul></ul>


