<h6>Item</h6>


<p>Here you can customize the Pricing List style. </p>


<ul class="marked-list">
<p><li><b>Space Between Items (px)</b> — here you can set the needed space between the items in px and make it responsive.</li></p>
<p><li><b>Border Type</b> — here you can define the type of the border you need to use for the element. It can be solid, double, dotted, dashed, groove.</li></p>
<p><li><b>Color</b> — set the color of the border.</li></p>
<p><li><b>Width</b> — set the width of the border in px.</li></p>
<p><li><b>Border Radius</b> — here you can define the radius of the border you use for the element.</li></p>
<p><li><b>Box Shadow</b> — enable this option if you want to access the shadow advanced settings for the item, and need to apply shadow for it.</li></p>
<p><li><b>Padding</b> — here you can define the padding of the border you need to use for the element.</li></p>
<p><li><b>Content Vertical Alignment</b> — here you can define the preferable content alignment. The content can be placed to the top, in the middle, or at the bottom.</li></p></ul>

<h6>Title</h6>


<ul class="marked-list">
<p><li><b>Typography</b> — turn the option on to view the typography settings.</li></p>

<ul class="marked-list">
<p><li><b>Family</b> — here you can set the font family for the title.</li></p>
<p><li><b>Size</b> — define the font size of the title title.</li></p>
<p><li><b>Weight</b> — in this block you can select the suitable font weight.</li></p>
<p><li><b>Transform</b> — here you can choose from the dropdown menu, if you want the title to be shown in uppercase, lowercase, capitalize or normal way.</li></p>
<p><li><b>Style</b> — in this block you can choose from the dropdown menu the style for the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li></p>
<p><li><b>Decoration</b> — in this field you can set the decoration of the title. It can be underline, overline and lone through.</li></p>
<p><li><b>Line Height</b> — in this field you can set the height of the title line.</li></p>
<p><li><b>Letter Spacing</b> — here you can set the space between letters.</li></p></ul></li></p>

<p><li><b>Color</b> — in this block you can define the color to use for the title.</li></p>
<p><li><b>Alignment</b> — here you can define the preferable text alignment. The text can be placed to the top, in the middle or in the bottom.</li></p></ul></ul>


<h6>Price</h6>


<ul class="marked-list">
<p><li><b>Price Minimal Width (px)</b> — here you can set the minimal width for the price.</li></p>
<p><li><b>Typography</b> — turn the option on to view the typography settings.</li></p>
<p><li><b>Color</b> — in this block you can define the color to use for the price.</li></p>
<p><li><b>Background Color</b> — here you can select the color to use it for the price general background. </li></p>
<p><li><b>Border Type</b> — here you can define the type of the border you need to use for the element.</li></p>
<p><li><b>Border Radius</b> — here you can define the radius of the border you need to use for the element.</li></p>
<p><li><b>Box Shadow</b> — enable this option if you want to access the shadow advanced settings for the item, and need to apply shadow for it.</li></p>
<p><li><b>Padding</b> — here you can define the padding of the border you need to use for the element.</li></p></ul></li></p></ul>
<p><li><b>Vertical Alignment</b> — here you can define the preferable price alignment. The price can be placed to the top, to the middle, or in the bottom.</li></p>
<p><li><b>Text Alignment</b> — here you can define the preferable text alignment. The text can be placed to the left, in the center or to the right.</li></p></ul>

<h6>Description</h6>


<p>In this block you can set the description settings.</p>


<ul class="marked-list">
<p><li><b>Typography</b> — turn the option on to view the typography settings.</li></p>
<p><li><b>Color</b> — in this block you can define the color to use for the description.</li></p>
<p><li><b>Margin</b> — here you can define the margin of the border you need to use for the element.</li></p>
<p><li><b>Alignment</b> — here you can define the preferable description alignment. The description can be placed to the left, in the centre, to the right and justified.</li></p></ul>


<h6>Separator</h6>


<p>Here you can customize separator settings. </p>


<ul class="marked-list">
<p><li><b>Separator Type</b> — here you can choose whether you want to use solid, double, dotted, dashed or groove type for the separator.</li></p>
<p><li><b>Separator Width</b> — in this block you can define the width of separator.</li></p>
<p><li><b>Color</b> — here you can set the color to use it for the separator.</li></p>
<p><li><b>Vertical Alignment</b> — here you can define the preferable price alignment. The price can be placed to the top, to the middle, or in the bottom.</li></p>
<p><li><b>Margin</b> — here you can define the margin of the border you need to use for the element.</li></p></ul>

<h6>Image</h6>


<ul class="marked-list">
<p><li><b>Image Offset (px)</b> — here you can set the preferable offset between the image and title, price and description.</li></p>
<p><li><b>Image Width</b> — in this block you can define the width to use it for the image.</li></p></ul>
<p><li><b>Border Type</b> — here you can define the type of the border you need to use for the element. It can be solid, double, dotted or groove type. </li></p>
<p><li><b>Border Radius</b> — here you can define the radius of the border you need to use for the element.</li></p>
<p><li><b>Box Shadow</b> — here you can define the shadow of the border you need to use for the element.</li></p></ul>


