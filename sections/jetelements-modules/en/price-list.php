<h6>General</h6>

<p>In this block you can customize Price List general content settings.</p>

<p>To add the item, click <b>Add Item</b> button and then click <b>Item #1</b> block to input the content: its title, price, description, select the suitable icon to use in the module and add URl link.</p>

<ul class="marked-list">
	<p><li><b>Title</b> - here you need to input the desirable title to apply for this service or project / product.</li></p>
	<p><li><b>Price</b> - here you can add the price to use for the item.</li></p>
	<p><li><b>Description</b> - here you need to add more details about the item (service or project).</li></p>
	<p><li><b>Image</b> - choose the image from the media library or upload one from your hard drive to add it for the service or product / project. </li></p>
	<p><li><b>URL</b> - fill in the link to the service or product page in this field to apply it for the service or product.</li></p></ul>


