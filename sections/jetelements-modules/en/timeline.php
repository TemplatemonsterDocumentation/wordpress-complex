<h6>Cards</h6>

<ul class="marked-list">
	<p><li><b>Border type</b> - here you can define the needed border type for the card block. It can be dotted, dashed, double or solid.</li></p>
	<p><li><b>Width</b> - type in the border width in pixels in order to set the width for the box top, bottom, left and right borders.</li></p>
	<p><li><b>Color</b> - here you can specify the color of the border using Color picker tool.</li></p>
	<p><li><b>Border Radius</b> - here you can set the border radius for the chosen border to make the border angles more round.</li></p>
	<p><li><b>Padding</b> - type in the custom padding values to add the responsive padding for the block.</li></p></ul>

<p>Switch between Normal, Hover and Active editing modes in order to alter the appearance of the event cards on hover, when active and in the normal state.</p>

<ul class="marked-list">
	<p><li><b>Background Color</b> - here you can specify the color to apply for the card box background.</li></p>
	<p><li><b>Box Shadow</b> - enable this option if you want to access the shadow advanced settings for the box, and need to apply shadow to it.</li></p></ul>

<h6>Image</h6>

