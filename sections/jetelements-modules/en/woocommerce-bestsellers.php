<h2>WooCommerce Bestsellers</h2>

<h6>
WooCommerce Bestsellers widget is extremely helpful if you need to show the most sold products on your website’s page, and arrange them in columns.
</h6>

<div class="alert alert-info">
Before using WooCommerce Bestsellers widget make sure that you've installed and activated <a href="https://wordpress.org/plugins/woocommerce/" target="_blank">WooCommerce</a> plugin.
</div>

