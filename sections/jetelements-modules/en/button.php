<h4>Content</h4>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widget/but2.png">
  	</figure>

<p>Here you can switch from customizing <b>Normal</b> to <b>Hover</b> content settings. Just click on the button Hover to proceed to customizing hover elements.</p>

<ul class="marked-list">
<p><li><b>Button icon</b> - here you can select one of the available icons from the icons list to use it for the button.</li></p>
<p><li><b>Button label text</b> - type in your custom text to use it as a button text.</li></p>
<p><li><b>Button link</b> - here you can paste your custom link to apply it to the button.</li></p></ul>

<h6>Settings</h6>

<ul class="marked-list">
<p><li><b>Hover effect</b> - here you can pick one of the hover effects to use it for the button (<b>none, fade, down slide, left slide, right slide, up scale, down scale</b>, etc.). </li></p>
<p><li><b>Use icon</b> - enable this option if you want to display icon for the button.</li></p>
<p><li><b>Icon position</b> - here you can specify the icon button position (<b>left, top, right, bottom</b>).</li></p></ul>

<h4>Style</h4>

 	<figure class="img-polaroid">
