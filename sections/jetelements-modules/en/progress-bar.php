<h6>Progress Bar</h6>

<ul class="marked-list">
	<p><li><b>Progress Height</b> - here you can specify the height of the progress bar for different devices.</li></p>
	<p><li><b>Margin</b> - here you can set the margins on different sides of the progress bar.</li></p>
	<p><li><b>Background Type</b> - here you can set your custom Classic or Gradient background for the progress bar.The customization options vary for each type.


<p><b>Classic Background Type</b></p>

<ul class="marked-list">
	<p><li><b>Color</b> — here you can set the background color for the progress bar using color picker tool.</li></p>
	<p><li><b>Image</b> — here you can choose the image you want to use as a background. Select the image from the media library or upload it to define it as a background in this block. When the image is uploaded, you’ll be able to define its position, alignment, turn on repeat and set the necessary size.</li></p>
</ul>
<p><b>Gradient Background Type</b></p>

<ul class="marked-list">
	<p><li><b>Color</b> — here you can set the background color for the progress bar using color picker tool.</li></p>
	<p><li><b>Location</b> — here you can define the proportion for the first color in the gradient background.</li></p></li></p>
	<p><li><b>Second Color</b> — here you can set the second color to use in the gradient background.</li></p>
	<p><li><b>Location</b> — here you can define the proportion for the second color in the gradient background.</li></p>
	<p><li><b>Type</b> — define, if you want to use the linear or radial type of the gradient.</li></p>
	<p><li><b>Angle</b> — in this block you can set the angle for the gradient (in degrees).</li></p>
	<p><li><b>Border Type</b> - here you can select the type of the border to use for the progress bar. </li></p>
	<p><li><b>Width</b> - here you can specify the width of the border around the bar.</li></p>
	<p><li><b>Color</b> - set the color of the border using color picker. </li></p>
	<p><li><b>Border Radius</b> - here you can specify the radius values to make the border angles more smooth and round.</li></p>
</ul>
	<p><li><b>Box Shadow</b> - here you can define if you want to apply the shadow for the progress bar. </li></p>
</ul>

<h6>Status Bar</h6>

<ul class="marked-list">
	<p><li><b>Background Type</b> - here you can select the type of the status bar, which can be classic or gradient. The options for both types vary. </li></p>
<p><li><b>Border Radius</b> - here you can specify the border radius values in order to make the border angles more round and smooth. </li></p></ul>

<h6>Title</h6>

<ul class="marked-list">
	<p><li><b>Title Alignment</b> - here you can select, if you want to set the title to the left, to the right, or center it. </li></p>

	<p><li><b>Background Type</b> - here you can change the background type for the title of the progress bar. You can select Classic or Gradient background type. The options vary for each of them. </li></p>

	<p><li><b>Border Type</b> - here you can select the type of the border to use for the title.</li></p>
	<p><li><b>Width</b> - here you can specify the width of the border around the bar.</li></p>
	<p><li><b>Color</b> - set the color of the border using color picker. </li></p>
	<p><li><b>Border Radius</b> - here you can specify the radius values to make the border angles more smooth and round.</li></p>
	<p><li><b>Box Shadow</b> - here you can define if you want to apply the shadow for the title. </li></p>
	<p><li><b>Padding</b> - here you can specify the custom padding for the title to use it on different devices.</li></p>

	<p><li><b>Margin</b> - here you can define the margin values to use them for the title.</li></p></ul>

<h6>Icon</h6>

<ul class="marked-list">
	<p><li><b>Position</b> - here you can set the icon to the left or place it at the top of the title. </li></p>

	<p><li><b>Icon Color</b> - here you can select the preferable icon color to use. </li></p>

	<p><li><b>Icon Size</b> - here you can set the icon size to make it responsive for different devices. </li></p></ul>


<h6>Text</h6>

<ul class="marked-list">
	<p><li><b>Text Color</b> - here you can set the color to use it for the title text.</li></p>
	<p><li><b>Typography</b> - turn the option on to view the typography settings.</li></p>
	<p><li><b>Size</b> — define the font size of the progress bar title.</li></p>
	<p><li><b>Family</b> — here you can set the font family for the title.</li></p>
	<p><li><b>Weight</b> — in this block you can select the suitable font weight.</li></p>
	<p><li><b>Transform</b> — here you can choose from the dropdown menu, if you want the title to be shown in uppercase, lowercase, capitalize or normal way.</li></p>
	<p><li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li></p>
	<p><li><b>Line Height</b> — in this field you can set the height of the slide title line.</li></p>
	<p><li><b>Letter Spacing</b> — here you can set the space between letters.</li></p></ul>




<h6>Percent</h6>

<ul class="marked-list">
	<p><li><b>Percent Width </b>- here you can specify the width of the percents block to make it responsive and look good on different devices.</li></p>
	<p><li><b>Background Type</b> - here you can set the Classic or Gradient background type to use it for the percents in the progress bar. The style settings for each type vary.</li></p>
	<p><li><b>Border Type</b> - here you can set the border type you want to use for the percents. </li></p></li></p>
	<p><li><b>Width</b> - in this block you can specify the border width in px. </li></p>
	<p><li><b>Color</b> - here you can define the color of the border. </li></p>
	<p><li><b>Border Radius</b> - here you can set the radius of the border to make its angles more smooth and round. </li></p>
	<p><li><b>Box Shadow</b> - here you can set the shadow to apply it to the percents.</li></p>
	<p><li><b>Margin</b> - here you can specify the margin to use it for the percents on different devices. </li></p>
	<p><li><b>Padding</b> - here you can apply your custom padding values to the percents. </li></p>
	<p><li><b>Text Color</b> - here you can change the color of the percents using the color picker. </li></p>
	<p><li><b>Typography</b> - turn the option on to view the typography settings. They are similar to all of the Typography blocks.</li></p>
	<p><li><b>Prefix Font Size</b> - here you can define the size of the % sign to make it responsive for all devices and screens. </li></p>
	<p><li><b>Percent Suffix Alignment</b> - here you can change the suffix alignment to top, center or bottom. </li></p></ul>
