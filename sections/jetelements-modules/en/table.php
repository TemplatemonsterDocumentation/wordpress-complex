<ul class="marked-list">
<li><b>Color</b> - here you can select the color of the text in the column using color picker tool.</li>
<li><b>Background Color</b> - here you can select the background color of the column using color picker tool.</li></ul>


<h5>Settings</h5>

<ul class="marked-list">
    <p><li><b>Sorting Table</b> - enable this option if you want the table to be sorted.</li></p>
    <p><li><b>Responsive Table</b> - enable this option if you want to set the table’s responsiveness for different devices.</li></p>
    <p><li><b>Responsive On</b> - here you can define devices, where the table will be responsive (mobile, tablet, desktop).</li></p></ul></ul>


<h4>Style</h4>

<h5>General</h5>

<ul class="marked-list">
<li><b>Table Width</b> - set the table’s width in percents or pixels.</li>
<li><b>Column Width</b> - here you can choose auto or fixed (equal) width of the column.</li>
<li><b>Table Alignment</b> - here you can set the table’s alignment (on the left, in the center, on the right). This proiperty is responsive.</li>
<li><b>Border Radius</b> - here you can define the radius of the table’s border to make its angles more round.</li>
<li><b>Box Shadow</b> - enable this option if you want to access the shadow advanced settings for the table, and need to apply shadow to it.</li></ul>

<h5>Table Header</h5>

<ul class="marked-list">
<li><b>Typography</b> - turn the option on to view the typography settings. They are similar to the typography settings of other JetElements widget.</li></ul>

    <p>You can switch from customizing <b>Normal</b> to <b>Hover</b> style settings. Just click on the button Hover to proceed to customizing the style in hover mode.</p>
   <ul class="marked-list">
<li><b>Color</b> - select the header’s text color using color picker tool.</li>
<li><b>Background Color</b> - select the background color of the header using color picker tool.</li>
<li><b>Padding</b> - type in the custom padding values to add a responsive padding for the block.</li>
<li><b>Border Type</b> - here you can define the needed border type for the block. It can be solid, double, dotted, dashed or groove.</li>
<li><b>Hidden border for header container</b> - here you can hide the border of the header’s container.</li>
<li><b>Alignment</b> - here you can set the header’s test alignment (on the left, in the center, on the right).</li>
<li><b>Vertical Alignment</b> - here you can set the header’s alignment (on the top, in the center, to the bottom).</li>
<li><b>Icon</b> - here you can set the icon’s settings (font size, color and gap).</li>
<li><b>Image</b> - here you can adjust the image’s settings (width, gap and border radius).</li>
<li><b>Sorting Icon</b> - this option allows to style the sorting icon (font size and color).</li></ul>

<h5>Table Body</h5>

<ul class="marked-list">
<li><b>Typography</b> - turn the option on to view the typography settings. They are similar to the typography settings of other JetElements widget.</li></ul>


    <p>You can switch from customizing <b>Normal</b> to <b>Hover</b> style settings. Just click on the button Hover to proceed to customizing in hover mode.</p>

    <ul class="marked-list">
<li><b>Color</b> - select the heading’s color using color picker tool.</li>
<li><b>Row Color</b> - here you can select the row text color using color picker tool.</li>
<li><b>Row Background Color</b> - here you can define the color of the row’s background.</li>
<li><b>Striped rows</b> - enable this option to use the different style settings for the rows that are in even count (this way the rows going one under another will have different colors.</li>
<li><b>Even Row Color</b> - here you can specify the text color to use for the even row.</li>
<li><b>Even Row Background Color</b> - here you can specify the color to use for the even row background.</li>
<li><b>Link color</b> - here you can select the color to apply for the links.</li>
<li><b>Padding</b> - type in the custom padding values to add a responsive padding for the block.</li>
<li><b>Border Type</b> - here you can define the needed border type for the block. It can be solid, double, dotted, dashed or groove.</li>
<li><b>Hidden border for body container</b> - here you can add the hidden border of the body’s container.</li>
<li><b>Alignment</b> - here you can set the header’s alignment (on the left, in the center, on the right).</li>
<li><b>Vertical Alignment</b> - here you can set the vertical position of the header (on the top, in the middle or on the bottom).</li>
<li><b>Icon</b> - here you can set the icon’s settings (font size, color and gap).</li>
<li><b>Image</b> - here you can adjust the image’s settings (width, gap and border radius).</li></ul></ul>















