<h5>Table Header</h5>


<p>Click <b>Add item</b> button in order to add a new table's column heading to the table structure. </p>

<p>Each of the columns hasa its own specific <b>Content, Style</b> and <b>Advanced</b> settings.</p>


    <p><b>Content</b></p>

<ul class="marked-list">
<li><b>Text</b> - here you can define the text label for the column’s heading.</li>
<li><b>Add icon / image</b> - here you can add an icon or an image to the column’s heading and adjust its settings.</li></ul>

<p><b>Advanced</b></p>

<ul class="marked-list">
<li><b>Column Span</b> - here you can set the span of the column’s heading.</li>
<li><b>Column Width</b> - here you can set the width of the column’s heading on different devices.</li></ul>


    <b><p>Style</p></b>

<ul class="marked-list">
<li><b>Color</b> - here you can set the color for the column’s heading using color picker tool.</li>
<li><b>Background Color</b> - in this block you can choose the background color of the column’s heading using color picker tool.</li></ul></ul>

<h5>Table Body</h5>

<p>Click <b>Add Item</b> button to add a new table item (you can assign each new item as a start row or a new cell).</p>

<p>Please, note, that in order to add a new row with cells you should start with adding a new start row ,and then you'll be able to fill it  with new cells. Depending on the number of cells, the table will have more or less columns.</p>

<ul class="marked-list">
<li><b>Action</b> - here you can add a new row or cell in the table and customize it.</li>


<h6>Add New Row</h6>

<p>Enable <b>Add Custom Style</b> option in order to access the row style settings, such as content text color and background color.</p>

<h6>Add New Cell</h6>

<p><b>Content</b></p>

<ul class="marked-list">
<li><b>Text</b> - here you can define the  text that is to be shown in the specific cell.</li>
<li><b>Link</b> - here you can add the link and adjust the link’s settings.</li>
<li><b>Add icon/ image</b> - here you can add an icon or an image to the column’s heading and adjust its settings (select an icon, change its position, customize image size and position, etc.</li></ul>

<p><b>Advanced</b></p>

<ul class="marked-list">
<li><b>Column Span</b> - here you can define the column’s span.</li>
<li><b>Row Span</b> - here you can define the row’s span the cell occupies.</li>
<li><b>This cell is Table Heading?</b> - enable this option if you want to combine the cell and the table heading. For this cell are applied table heading cell styles.</li></ul>

    <p><b>Style</b></p>


