<h6>General</h6>
<ul class="marked-list">
<p><li><b>Map Center</b> — here you need to define the location, which will be used as the center of the map. </li></p>
<p><li><b>Initial Zoom</b> — here you can set the value for the initial zoom of the map.</li></p>
<p><li><b>Scrollwheel Zoom</b> — enable this option if you want the visitors to be able to zoom the map when scrolling with the mouse wheel. </li></p>
<p><li><b>Zoom Controls</b> — enable this option if you need to show zoom controls for the map.</li></p>
<p><li><b>Street View Controls</b> — here you can enable controls for the street view. </li></p>
<p><li><b>Map Type Controls</b> — here you can enable the switching between map and satellite map types.</li></p>
<p><li><b>Is Map Draggable?</b> — turn on the option if you want the map to be draggable. </li></p></ul>




<h6>Map Style</h6>

<ul class="marked-list">
<p><li><b>Map Height</b> — define the value of the map height in pixels. </li></p>
<p><li><b>Map Style</b> — here you can choose from the dropdown menu the style for the map appearance.</li></p>
<p>In case you select <b>Custom</b> map style, you can add custom style json file below in order to apply your own map style.</p></ul>



<h6>Pins</h6>
<ul class="marked-list">
<p><li><b>Add Item</b> — here you can add new pins to the map to be displayed on it. </li></p>
<p><li><b>Pin Address</b> — here you need to input the address to be shown on the map. </li></p>
<p><li><b>Pin Description</b> — in this field you have to fill in the description of the place you’ve selected to be pinned.</li></p>
<p><li><b>Pin Icon</b> — here you can select from the media library or upload your custom pin icon. </li></p>
<p><li><b>Initial Size</b> — here you can choose, if you want the initial size to be visible or hidden. </li></p></ul>


<h4>Adding API Key to Advanced Map Widget</h4>

<ul class="marked-list">
<p><li>To add an API key please navigate to <b>Elementor > Jet Elements Settings</b> tab on the left of the Dashboard.</li></p>

<p><li>Here you need to paste <b>API key</b> to the <b>API Key</b> field.</li></p>

<p><li>Let's get <b>API key</b> for the map widget. Go to <b>Google Developers</b> page and select <b>Maps Embed API</b>.</li></p>

<p><li>Here click on <b>Create Key</b> button and select the project to which you want to add a key. If you still don't have any projects, click on <b>Create a New Project</b> button.</li></p>

<p><li>Name the project and click on <b>Create and Enable API</b>.</li></p>

<p><li>Copy the key to the clipboard and paste to the <b>API Key</b> field in your Dashboard. </li></p>

<p><li>Save the changes. </li></p></ul>

