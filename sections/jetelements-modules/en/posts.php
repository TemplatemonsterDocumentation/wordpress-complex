<h5>General</h5>

<p>In this block you can customize Post General settings, such as posts sorting type; define information to show (meta, post excerpts, featured images, etc.).</p>

<ul class="marked-list">
<p><li><b>Posts Number</b> — here you need to define how many posts you need to display with this module. </li></p>
<p><li><b>Columns</b> — in this block you need to choose the number of columns to use from the dropdown menu. You can select from 1 to 6 columns to show per block. </li></p>
<p><li><b>Query Posts By</b> — here you can specify, if you want to filter and show  posts by categories, or by IDs. You can also show only the latest posts. </li></p>
<p><li><b>Show Posts Title</b> — enable this option if you want to display post title in the module. </li></p>
<p><li><b>Show Posts Featured Image</b> — enable this option to display featured images for the posts that are shown in the module.</li></p>

<p><li><b>Show Posts Featured Image As</b> — here you can select, if you want to show the feratured image as a simple image, or the box background image.</li></p>


<p><li><b>Featured Image Size</b> — here you need to define the size of the featured image (usually it can be medium, large and thumbnail). </li></p>
<p><li><b>Show Posts Excerpt</b> — here you can enable displaying the excerpts for the posts shown in the module. </li></p>
<p><li><b>Excerpt Length</b> — here you can specify the number of words from the post content you want to display as a post excerpt. </li></p>
<p><li><b>Show Posts Meta</b> — enable the option if you want to show such meta information, as post author, publish date.</li></p>

<p><li><b>Show Posts Author</b> — enable the option if you want to show the author of the post. </li></p>

<p><li><b>Show Posts Date</b> — enable the option if you want to show the date when the post was published. </li></p>

<p><li><b>Show Posts Comments</b> — enable the option if you want to show the number of comments to the posts. </li></p>


<p><li><b>Show Read More Button</b> — here you can enable <b>Read More</b> button to ensure comfortable navigation from post excerpt to the full version of the post. </li></p>

<p><li><b>Read More Button Icon</b> — here you can select the icon to use it for the  <b>Read More</b>. </li></p>


<p><li><b>Read More Button Text</b> — in this field you need to fill in the text for the Read More button title. </li></p></ul>

<p>You can also add gaps between columns and rows. To do it enable the corresponding option in the <b>General</b> block. </p>


<h5>Carousel</h5>


<p>In this block you can customize animation, enable carousel slider and post slider, etc.</p>

<ul class="marked-list">
<p><li><b>Enable Carousel</b> — turn on this option to create post slider or carousel. </li></p>
<p><li><b>Slides to Scroll</b> — here you need to define how many slides you want to scroll when autoplay or using navigation arrows. </li></p>
<p><li><b>Show Arrows Navigation</b> — here you can enable or disable navigation arrows to provide easier navigation from slide to slide for the visitors. </li></p>

<p><li><b>Prev Arrow Icon</b> — here you can select the type of icon to use for the previous control arrow. </li></p>

<p><li><b>Next Arrow Icon</b> — here you can select the type of icon to use for the next control arrow. </li></p>


<p><li><b>Show Dots Navigation</b> — here you can enable or disable pagination dots at the bottom of the carousel slider to ensure easier navigation. </li></p>
<p><li><b>Pause on Hover</b> — enable this option if you want autoplay to pause when the visitor puts the mouse to the carousel area. </li></p>
<p><li><b>Autoplay</b> — enable this option if you want the slides to play automatically. </li></p>
<p><li><b>Autoplay Speed</b> — here you can set the speed of switching between slides in ms. </li></p>
<p><li><b>Infinite Loop</b> — enable this option if you want the slides to repeat continuously. </li></p>
<p><li><b>Animation Speed</b> — here you can define the speed of animation when the transition between slides takes place (in ms).</li></p></ul>

