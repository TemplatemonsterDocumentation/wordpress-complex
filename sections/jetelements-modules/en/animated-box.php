<h6>Front Side Content</h6>

<p>In this block you can add content to the front side of the animated box. </p>

<ul class="marked-list">
<li><p><b>Icon</b> — here you can set the icon to be shown above the title of the animated box content. Select the icon you want to use from the dropdown list, or click on Delete button to remove the icon if you don’t want to use it. </p></li>
<p><li><b>Title</b> — here you need to input the title for the front side content of the animated box.</li></p>
<p><li><b>Subtitle</b> — here you need to enter the subtitle for the front side content. </li></p>
<p><li><b>Description</b> — in this block you can add the content to the front side of the animated box.  Use HTML tags to separate lines and define quotes, etc. </li></p></ul>


<h6>Back Side Content</h6>
<p>In this block you can add content to the back side of the animated box. </p>

<ul class="marked-list">
<p><li><b>Icon</b> — here you can set the icon to be shown above the title of the animated box content. Select the icon you want to use from the dropdown list, or click on Delete button to remove the icon if you don’t want to use it. </li></p>
<p><li><b>Title</b> — here you need to input the title for the back side content of the animated box.</li></p>
<p><li><b>Subtitle</b> — here you need to enter the subtitle for the back side content.</li></p>
<p><li><b>Description</b> — in this block you can add the content to the back side of the animated box.  Use HTML tags to separate lines and define quotes, etc. </li></p>
<p><li><b>Button Text</b> — here you need to input the text you want to use for the button.</li></p>
<p><li><b>Button Link</b> — in this field you need to paste the URL link for the button. </li></p></ul>


<h6>Settings</h6>

<ul class="marked-list">
<p><li><b>Animation Effect</b> — here you can choose one of the multiple effects from the dropdown list to use it for widget animation. The effect will define how the front side of the box will switch to the back side. </li></p></ul>


