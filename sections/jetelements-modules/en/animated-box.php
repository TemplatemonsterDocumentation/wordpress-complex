<h6>General </h6>

<p>Here you can switch from customizing Front to Back Animated Box settings. Just click on the button Back to proceed to customizing the back side of the Animated Box widget elements. </p>

<ul class="marked-list">
<p><li><b>Height</b> — here you can select the height value (in pixels) for the animated box widget. The height is the general parameter for the front and back sides of the Animated Box. </li></p>
<p><li><b>Background Type</b> — in this block you can choose, whether you want to use classic or gradient type for the animated box background. The customization options vary for each type. </li></p></ul>

<h6>Classic Background Type</h6>

<ul class="marked-list">
<p><li><b>Color</b> — here you can set background color for the animated box using color picker tool.</li></p>
<p><li><b>Image</b> — here you can choose the image you want to use as a background. Select the image from the media library or upload it to define it as a background in this block. When the image is uploaded, you’ll be able to define its position, alignment, turn on repeat and set the necessary size.</li></p></ul>

<h6>Gradient Background Type</h6>

<ul class="marked-list">
<p><li><b>Color</b> — here you can set background color for the widget using color picker tool.</li></p>
<p><li><b>Location</b> — here you can define the proportion for the first color in the gradient background. </li></p>
<p><li><b>Second Color</b> — here you can set the second color to use in the gradient background. </li></p>
<p><li><b>Location</b> — here you can define the proportion for the second color in the gradient background. </li></p>
<p><li><b>Type</b> — define, if you want to use linear or radial type of the gradient. </li></p>
<p><li><b>Angle</b> — in this block you can set the angle for the gradient (in degrees).</li></p>
<p><li><b>Border Type</b> — here you can define the type of the border you need to use for the animated box. </li></p>
<p><li><b>Box Shadow</b> — enable this option if you want to access the shadow advanced settings for this widget, and need to apply shadow for it.</li></p></ul>

<b>Shadow Settings:</b>

<ul class="marked-list">
<p><li><b>color</b> — here you can set the color for the the shadow (use color picker tool to do it);</li></p>
<p><li><b>blur</b> — in this block you can define the blur settings for the shadow;</li></p>
<p><li><b>spread</b> — here you can set how large the shadow will be;</li></p>
<p><li><b>horizontal</b> — here you can move the shadow horizontally (from right to left);</li></p>
<p><li><b>vertical</b> — here you can move the shadow vertically (from top to bottom).</li></p></ul>

<h6>Icon</h6>

<p>Here you can switch to customizing <b>Front</b> and <b>Back</b> Icon. To switch to customizing them you need to click on the corresponding button.</p>

<ul class="marked-list">
<p><li><b>Icon Color</b> — here you can select the color to use it for the icon. </li></p>
<p><li><b>Icon Background Color</b> — in this block you can set the color to use it in the icon background.</li></p>
<p><li><b>Icon Font Size</b> — here you can define the value of icon size in pixels.</li></p>
<p><li><b>Icon Box Size</b> — in this block you can input the value for icon box size (in pixels).</li></p>
<p><li><b>Border Type</b> — here you can select the type of the border from the dropdown menu. </li></p></ul>

<p><b>Here is the list of available border types:</b></p>

<ul class="marked-list">
<li><b>none;</b></li>
<li><b>solid;</b></li>
<li><b>doubled;</b></li>
<li><b>dotted;</b></li>
<li><b>dashed.</b></li></ul>

<ul class="marked-list">
<p><li><b>Box Shadow</b> — enable this option if you want to access the shadow advanced settings for this widget, and need to apply shadow for it.</li></p>
<p><li><b>Alignment</b> — here you can define the preferable icon alignment. The icon can be placed to the left, to the right, or remain centered. </li></p></ul>

<h6>Title</h6>

<ul class="marked-list">
<p><li><b>Title Color</b> — here you can define the suitable color for the animated box title. Use color picker tool to select the necessary color. </li></p>
<p><li><b>Typography</b> — turn the option on to view the typography settings.
<ul class="marked-list">
<p><li><b>Size</b> — define the font size of the animated box title.</li></p>
<p><li><b>Family</b> — here you can set the font family for the title.</li></p>
<p><li><b>Weight</b> — in this block you can select the suitable font weight.</li></p>
<p><li><b>Transform</b> — here you can choose from the dropdown menu, if you want the title to be shown in uppercase, lowercase, capitalize or normal way.</li></p>
<p><li><b>Style</b> — in this block you can choose from the dropdown menu the style for the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li></p>
<p><li><b>Line Height</b> — in this field you can set the height of the title line.</li></p>
<p><li><b>Letter Spacing</b> — here you can set the space between letters.</li></p></ul></li></p>
<p><li><b>Alignment</b> — here you can define the preferable title alignment. The title can be placed to the left, to the right, or remain centered. </li></p></ul>



