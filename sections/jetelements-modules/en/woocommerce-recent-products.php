<h2>WooCommerce Recent Products</h2>

<h6>
WooCommerce Recent Products module helps you to showcase the products on your website’s page  and sort them using custom order.
</h6>

<div class="alert alert-info">
Before using WooCommerce Recent Products module make sure that you've installed and activated <a href="https://wordpress.org/plugins/woocommerce/" target="_blank">WooCommerce</a> plugin.
</div>

<h4>Content</h4>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/recent2.png">
  	</figure>

<ul class="marked-list">
<p><li><b>Products per Page</b>  — here you can set how many products you want to display per one page using this module. Set “0” value to display all the existing products.</li></p>
<p><li><b>Columns</b>  — here you can define how many columns you want to display in the module to organize products. You can set from 1 to 6 columns. </li></p>
<p><li><b>Order By</b>  — here you can define the criterion by which the products will be sorted. You can sort products by date, ID, author, title, name, slug, comments count or menu order. You can as well apply random order.</li></p>
<p><li><b>Order</b>  — here you can set ascending or descending order for the products.</li></p></ul>


