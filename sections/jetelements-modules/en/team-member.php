<h4>Content</h4>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/team-member2.png">
  	</figure>

<ul class="marked-list">
<p><li><b>Member Image</b> - select the image for the team member from the <b>media library</b> or upload one from the hard drive.</li></p>
<p><li><b>First Name</b> - input the team member’s first name.</li></p>
<p><li><b>Last name</b> - type in the team member’s last name. </li></p>
<p><li><b>Position</b> - add the team member’s work responsibilities.</li></p>
<p><li><b>Description</b> - here you can add the description of the work your team member does, the details about him.</li></p>
<p><li><b>Add Item</b> - click the button to add a new social network link to the team member. You’ll need to select the <b>social network icon</b>, type in its <b>label</b>, add a <b>link</b> leading to the member’s profile and <b>enable the label</b>. </li></p>
<p><li><b>Button Text</b> - input the text for the button.</li></p>
<p><li><b>Button URL</b> - here you can add the URL link to the team member’s page on this site. </li></p></ul>


