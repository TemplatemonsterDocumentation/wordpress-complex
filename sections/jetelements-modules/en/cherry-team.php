<ul class="marked-list">
<p><li><b>Super Title</b> — here you need to input the super title for Cherry Team module. </li></p>
<p><li><b>Title</b> — in this field you can fill in the title for Cherry Team. </li></p>
<p><li><b>Subtitle</b> — here you need to define the subtitle to use for the module. </li></p>
<p><li><b>Desktop Columns</b> — here you need to define the number of columns in which the team members information will be displayed for the desktop resolution. You can set from 1 to 6 columns per one line. </li></p>
<p><li><b>Laptop Columns</b> — here you need to define the number of columns in which the team members information will be displayed for the laptop resolution. You can set from 1 to 6 columns per one line. </li></p>
<p><li><b>Tablet Columns</b> — here you need to define the number of columns in which the team members information will be displayed for the tablet resolution. Try to reduce the number of columns to ensure the best responsiveness. </li></p>
<p><li><b>Phone Columns</b> — here you need to define the number of columns in which the team members information will be displayed for the phone screen resolution. Try to reduce the number of columns to ensure the best responsiveness. </li></p>
<p><li><b>Posts per Page</b> — here you can define how many posts you want to display per one page. The maximum number of posts is 50. Set “0” value to display all the existing posts per one page. </li></p>
<p><li><b>Show More Button</b> — here you can enable/disable More button and define button text and button URL. </li></p>
<p><li><b>AJAX Load More</b> — here you can enable or disable AJAX Load More button. </li></p>
<p><li><b>Pagination</b> — enable this option if you want to show pagination circles at the bottom of the module to provide more comfortable navigation. </li></p>
<p><li><b>Show Person Name</b> — enable this option if you want to display the names of the team members. </li></p>
<p><li><b>Show Person Photo</b> — enable this option if you want to display team members photos. </li></p>
<p><li><b>Show Person Bio</b> — enable this option if you want to display team members information. </li></p>
<p><li><b>Show Person Position</b> — enable this option if you want to display where the team members work. </li></p>
<p><li><b>Show Person Social Links</b> — enable this option to show social links for each team member. </li></p>
<p><li><b>Show Filter by Groups Before Team Listing</b> — here you can enable group links at the top of the module. </li></p>
<p><li><b>Listing Item Image Size</b> — here you can select the size of the images used in the module (large, medium, small or thumbnail). </li></p>
<p><li><b>Listing Item Template</b> — here you can define the listing type you want to use for the module. It can be default, single or grid—box listing. </li></p></ul>
<p>You can also add space between Team columns and rows using the corresponding options.</p>

