<h5>Chart Data</h5>
<ul class="marked-list">
<p>Click on the <b>Add item</b> button to add new features to the chart. You can add as many features as you want, and customize each one of them. Click on the newly added item to customize it.</p>
<p><li><b>Label</b> — here you can specify the label of the chart item.</li></p>
<p><li><b>Value</b> — here you can define the value of the chart item.</li></p>
<p><li><b>Color</b> — here you can pick the color for the chart item.</li></p>
<p><li><b>Chart Title</b> - here you can add title of the chart.</li></p>
<p><li><b>Title HTML Tag</b> - here you can select the HTML tag to apply for the items titles.</li></p>
<p><li><b>Title Position</b> - here you can set the position of the title. It can be located before or after the chart.</li></p></ul>

<h5>Settings</h5>
<ul class="marked-list">
<p><li><b>Chart Height</b> — here you can specify the height of the chart.</li></p>
<p><li><b>Cutout Percentage</b> — here you can set the percentage of the cutout in the middle of the chart.</li></p></ul>

<h6>Animations</h6>
<ul class="marked-list">
<p><li><b>Duration</b> — here you need to select the speed of animations for the widget.</li></p>
<p><li><b>Easing</b> — here you can change the type of animation easing.</li></p>
<p><li><b>Animate Scale</b> — enable this option if you want to animate scale of the chart from the center outward.</li></p></ul>

<h6>Legend</h6>
<ul class="marked-list">
<p><li><b>Display</b> — enable this option if you want to display legend on the chart.</li></p>
<p><li><b>Position</b> — here you can specify the position of the legend.</li></p>
<p><li><b>Revers</b> — enable this option if you want to show datasets in reverse order.</li></p></ul>

<h6>Tooltips</h6>
<ul class="marked-list">
<p><li><b>Enabled</b> — this option allows to add tooltips to the chart.</li></p></ul>

