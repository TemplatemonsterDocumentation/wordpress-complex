<h5>Chart</h5>
<ul class="marked-list">
<p><li><b>Border Width</b> — here you can specify the required border width for the chart.</li></p>
<p><li><b>Border Color</b> — here you can select the color of the borders of the chart.</li></p></ul>

<h5>Title</h5>
<ul class="marked-list">
<p><li><b>Typography</b> — turn the option on to view the typography settings.</li></p>
<p><li><b>Color</b> —  specify the color of the title with color picker tool.</li></p>
<p><li><b>Margin</b> — here you can apply your custom margins in pixels or percentage to set them for the title.</li></p>
<p><li><b>Padding</b> — type in the custom padding values to add responsive padding for the block.</li></p>
<p><li><b>Text Shadow</b> — use the shadow settings (such as shadow color, blur, horizontal, vertical) if you want to make the title highlighted.</li></p></ul>

<h5>Legend</h5>
<ul class="marked-list">
<p><li><b>Box Width</b> — here you can specify the width of the boxes of the legend.</li></p>
<p><li><b>Font Family</b> — here you can set the font family for the legend.</li></p>
<p><li><b>Font Size</b> — define the font size of the legend.</li></p>
<p><li><b>Font Weight</b> — in this block you can select the suitable font weight.</li></p>
<p><li><b>Font Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li></p>
<p><li><b>Font Color</b> — here you can specify the color of the font.</li></p></ul>

