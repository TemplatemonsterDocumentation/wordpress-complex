<h5>Video</h5>

<ul class="marked-list">
<p><li><b>Video Type</b> - here you can specify the video file type. It can be YouTube, Vimeo or Self Hosted type (in case you want to showcase the video that’s previously uploaded to your server).</li></p></ul>

<h7>YouTube Video Options</h7>

<ul class="marked-list">
<p><li><b>YouTube URL</b> - input the YouTube video URL address in this field.</li></p>
<p><li><b>Start Time</b> (in seconds) - define the time you want the video to start at.</li></p>
<p><li><b>End Time</b> (in seconds) - define the specific time you want the video to finish at.</li></p>
<p><li><b>Aspect Ratio</b> - this option allows your video item to be scaled according to your needs.</li></p></ul>

<h7>Vimeo Video Options</h7>

<ul class="marked-list">
<p><li><b>Vimeo URL</b> - paste the Vimeo video URL address in this field.</li></p>
<p><li><b>Start Time</b> (in seconds) - define the time you want the video to start at.</li></p>
<p><li><b>Aspect Ratio</b> - this option allows your video item to be scaled according to your needs.</li></p></ul>

<h7>Self Hosted Video Options</h7>

<ul class="marked-list">
<p><li><b>Player</b> - select the preferable video player among the Default HTML 5 and the MediaElement Player.</li></p>
<p><li><b>Self Hosted URL</b> - here you are able to upload the video from your hard drive to embed it to the page built with Elementor.</li></p>
<p><li><b>Start Time</b> (in seconds) - define the time you want the video to start at.</li></p>
<p><li><b>End Time</b> (in seconds) - define the specific time you want the video to finish at.</li></p></ul>

<h6>Video Options</h6>

<ul class="marked-list">
<p><li><b>Autoplay</b> - enable this option if you want the video to play automatically.</li></p>
<p><li><b>Mute</b> - this option allows you to put the video on mute.</li></p>
<p><li><b>Loop</b> - turn this option on if you don’t want the video to stop (the start and end time options will cease to affect the video).</li></p>
<p><li><b>Player Controls</b> - enable or disable this option in order to show or hide the player controls.</li></p></ul>

<h7>Additional Youtube Options</h7>

<ul class="marked-list">
<p><li><b>Modest Branding</b> - enable this option to display the Youtube logo.</li></p>
<p><li><b>Info</b> - enable this option to showcase the title of the video.</li></p>
<p><li><b>Suggested Videos</b> - enable or disable this option in order to reveal or hide the list of suggested videos.</li></p>
<p><li><b>Privacy Mode</b> - when you turn on privacy mode, YouTube won't store information about visitors on your website unless they play the video.</li></p></ul>

<h7>Additional Vimeo Options</h7>

<ul class="marked-list">
<p><li><b>Intro Title</b> - enable this option in order to showcase the title of the video.</li></p>
<p><li><b>Intro Portrait</b> - enable this option in order to showcase the Vimeo profile picture of the person who owns this video.</li></p>
<p><li><b>Intro Byline</b> - enable this option in order to showcase the username of the video owner at Vimeo.</li></p>
<p><li><b>Controls Color</b> - pick the color of the video controls.</li></p></ul>

<h7>Self Hosted Video Options</h7>

<ul class="marked-list">
<p><li><b>Download Button</b> - enable this option to reveal the “Download” button so that the viewers will be able to download the video.</li></p>
<p><li><b>Controls Visibility</b> - here you can specify which controls you want to be visible or hidden.</li></p>
<p><li><b>Poster</b> - upload the poster for the video from your computer to use it in case the video file can’t be played at the time (the poster will be shown instead of the video).</li></p></ul>

<h5>Thumbnail Overlay</h5>

<ul class="marked-list">
<p><li><b>Show Custom Thumbnail</b> - enable this option to display the thumbnail of the video. It will be shown before the video is played.</li></p>
<p><li><b>Image Size</b> - pick the preferable thumbnail size.</li></p>
<p><li><b>Overlay Color</b> - choose the overlay color with the color picker tool.</li></p></ul>

<h5>Play Button</h5>

<ul class="marked-list">
<p><li><b>Show Play Button</b> - enable this option to show the “play” button.</li></p>
<p><li><b>Play Button Type</b> - select the desirable button type, which can be either an icon or an image.</li></p>
<p><li><b>Icon</b> - pick the appropriate icon from the list of icons.</li></p>
<p><li><b>Image</b> - upload the image, which will be the “Play” button.</li></p></ul>

