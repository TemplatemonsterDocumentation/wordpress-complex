<h6>Values </h6>

<p>In this block you can specify the values of the circle progress bar.</p>

<ul class="marked-list">
<p><li><b>Progress Values Type</b> — here you can define the type of progress. It can be either absolute type (for common numbers) or percent type (for progress bar that uses percentage). </li></p>
<p><li><b>Current Percent</b> — here you can define the value to be displayed in the progress bar (in percents). The option is active when you’ve previously set percent progress values type. </li></p>
<p><li><b>Current Value</b> — here you can set the current value to be shown in the progress bar. The option is active when you’ve defined absolute progress values type. </li></p>
<p><li><b>Max Value</b> — the option defines the maximum value for the circle progress  counter. </li></p></ul>

<h6>Settings</h6>




<ul class="marked-list">
<p><li><b>Circle Size</b> — here you can select the size for the circle progress bar. The minimal size is 100 px, the maximum size is 600 px. </li></p>
<p><li><b>Value Stroke Width</b> — here you need to select the width of the circle counter outline (stroke), which displays complete progress, in pixels.</li></p>
<p><li><b>Background Stroke Width</b> — here you need to select the width of the circle counter outline (stroke), which displays the remaining progress, in pixels.</li></p>
<p><li><b>Animation Duration</b> — here you need to select the speed of animation for the module. </li></p></ul>


<h6>Content</h6>




<ul class="marked-list">
<p><li><b>Value Number Prefix</b> — here you need to set the positive or negative value number prefix.</li></p>
<p><li><b>Value Number Suffix</b> — here you need to define the positive or negative value number suffix.</li></p>
<p><li><b>Show Thousand Separator in Value</b> — here you can enable visual separating thousands in the counter.</li></p>
<p><li><b>Counter Title</b> — here you can fill in the title for the counter progress bar.</li></p>
<p><li><b>Counter Subtitle</b> — here you need to input the subtitle for the circle progress bar.</li></p>
<p><li><b>Counter Content position</b> — here you can select, if you want to position circle progress bar content inside of the circle, or outside of it. </li></p></ul>


