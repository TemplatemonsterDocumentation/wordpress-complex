<h6>Progress Circle Style</h6>



<ul class="marked-list">
<p><li><b>Background Stroke Type</b> — here you can define the type of the stroke to use for the background outlines of the circle progress bar. It can be Classic or Gradient. Choose the one that is more suitable for you.</li></p>

<h6>Classic Stroke Type</h6>


<p><li><b>Background Stroke Color</b> — here you can define the color to use for the background outlines of the circle progress bar.</li></p>

<h6>Gradient Stroke Type</h6>


<p><li><b>Background Stroke Color A</b> — here you can define the first color to use for the background outlines of the circle progress bar.</li></p>

<p><li><b>Background Stroke Color B</b> — here you can define the second color to use for the background outlines of the circle progress bar.</li></p>

<p><li><b>Background Stroke Gradient Angle</b> — here you can define the angle between first and second gradients.</li></p>

<h6>Value Classic Stroke Color</h6>

<p><li><b>Value Stroke Color</b> — here you need to specify the color for the outlines of the completed progress.</li></p>


<h6>Value Gradient Stroke Color</h6>

<p><li><b>Value Stroke Color A</b> — here you can define the first color to use for the value outlines of the circle progress bar.</li></p>

<p><li><b>Value Stroke Color B</b> — here you can define the second color to use for the value outlines of the circle progress bar.</li></p>

<p><li><b>Value Stroke Gradient Angle</b> — here you can define the angle between first and second gradients.</li></p>


<p><li><b>Circle Fill Color</b> — here you need to specify the color that will be shown inside of the circle progress bar.</li></p>
<p><li><b>Progress Line Endings</b> — here you need to define, if you want the progress stroke line endings to be sharp or rounded, more smooth. </li></p></ul>


<h6>Content Style</h6>


<p>In this block you can define style for these elements:</p>

<ul class="marked-list">
<li><b>number;</b></li>
<li><b>title;</b></li>
<li><b>subtitle.</b></li></ul>

<p>Style settings for these three blocks are similar.</p>



<ul class="marked-list">
<p><li><b>Color</b> — here you can define the suitable color for the Circle Progress module elements. Use color picker tool to select the necessary color.</li></p>
<p><li><b>Typography</b> — turn the option on to view the typography settings.
<ul class="marked-list">
<p><li><b>Size</b> — define the font size of the Circle Progress module element.</li></p>
<p><li><b>Family</b> — here you can set the font family for the Circle Progress module element.</li></p>
<p><li><b>Weight</b> — in this block you can select the suitable font weight.</li></p>
<p><li><b>Transform</b> — here you can choose from the dropdown menu, if you want the Circle Progress module element to be shown in uppercase, lowercase, capitalize or normal way.</li></p>
<p><li><b>Style</b> — in this block you can choose from the dropdown menu the style for the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li></p>
<p><li><b>Line Height</b> — in this field you can set the height of the Circle Progress module element lines.</li></p>
<p><li><b>Letter Spacing</b> — here you can set the space between letters.</li></p></ul></li></p></ul>


