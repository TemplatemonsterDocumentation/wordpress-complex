<h6>Instagram Settings</h6>

<ul class="marked-list">
	<p><li><b>What to show</b> - here you can specify what you want to display in the module (the photos with the specific <b>hashtag</b>, or the photos from your Instagram account.</li></p></ul>

<p>First, let’s see what options are available when you set <b>Tagged Photos</b>.</p>

<ul class="marked-list">
<p><li><b>Hashtag</b> - here you need to input the hashtag by which the photos will be displayed.</li></p>

<p><li><b>Cache Timeout</b> - in this block you can select the timeout of the Instagram photo cache (<b>minute, hour, day, week,</b> or <b>none</b>).</li></p></ul>

<p>Now let’s see what options are available when you set <b>My Photos</b> in <b>What to show</b>.</p>

<ul class="marked-list">
	<p><li><b>Username</b> - type in your <b>Instagram username</b> to display the photos from your Instagram account.</li></p></ul>

<p>The options mentioned below are the same for both <b>My Photos</b> and <b>Tagged Photos</b> options.</p>

<ul class="marked-list">
	<p><li><b>Photo size</b> - here you can set the desirable size to use it for the photos. Note, that in some cases the original photo size may vary for different photos, so you may need to set another size. Here you can see the available sizes to use for the photo publications:
<ul class="marked-list">
	<li><b>Standard</b> (640x640px);</li>
<li><b>Low</b> (320x320px);</li>
<li><b>Thumbnail</b> (150x150px);</li>
<li><b>Original.</li></b></ul></li>

<p><li><b>Number of Instagram posts</b> - here you can set the needed number of instagram publications to display.</li></p>
<p><li><b>Enable linking photos</b> - enable this option in order to use linking.</li></p>
<p><li><b>Enable caption</b> - enable this option to show the caption of the Instagram publication (you can see it on hover).</li></p>
<p><li><b>Caption length</b> - here you can specify the needed length to use for the publication caption (the number of symbols to show in a caption).</li></p>
<p><li><b>Enable comments count</b> - enable this option in order to show the number of comments to Instagram publications on hover.</li></p>
<p><li><b>Enable likes count</b> - enable this option to see the number of likes for these Instagram publications on hover.</li></p></ul>

<h6>Layout settings</h6>

<ul class="marked-list">
	<p><li><b>Layout type</b> - here you can specify the Instagram publications layout you want to apply (<b>Masonry, Grid, List</b>).</li></p>
<p><li><b>Columns</b> - here you can define how many columns you want to use for the Instagram publications (from 1 to 6). You can also change the number of columns for different devices to make the module content responsive.</li></p></ul>

