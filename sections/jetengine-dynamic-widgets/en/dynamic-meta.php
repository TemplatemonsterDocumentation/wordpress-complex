<h5>General</h5>

<ul class="marked-list">
<li><b>Alignment</b> - here you can specify the needed alignment (left, right or center) to apply for the meta data.</li></ul>

<h5>Items</h5>

<ul class="marked-list">
<li><b>Typography</b> - here you can change the typography settings to the ones suitable. These settings are the same as the ones available for other JetEngine widgets.</li></ul>

<p>Switch between <b>Normal</b> and <b>Hover</b> modes in order to change the meta data appearance on hover and in the normal state.</p>

<ul class="marked-list">
<li><b>Background Color</b> - here you can specify the color to use for the meta data background.</li>

<li><b>Text Color</b> - here you can specify the color to use for the meta data text using color picker tool.</li>

<li><b>Padding</b> - here you can apply your own responsive custom padding values.</li>

<li><b>Margin</b> - here you can apply the custom margin values to use for the meta data.</li>

<li><b>Border Type</b> - here you can select the type of the border to use for the meta data (solid, dotted, dashed, etc.)</li>

<li><b>Color</b> - here you can define the color of the border using color picker tool.</li>

<li><b>Width</b> - here you can set the needed responsive width for the border.</li>

<li><b>Border Radius</b> - apply your custom border radius values to make the border angles more round and smooth.</li>

<li><b>Box Shadow</b>- here you can set the shadow for the meta data block and set its settings, such as blur, spread, color, etc.</li></ul>

<h5>Icon</h5>

<ul class="marked-list">
<li><b>Color</b> - here you can set the desirable color to apply for the icon.</li>

<li><b>Size</b> - here you can set the preferable size to use for the icon.</li>

<li><b>Gap</b> - here you can set the custom gap width between the icon and the meta data text.</li></ul>

<h5>Items Value</h5>

<p>Switch between <b>Normal</b> and <b>Hover</b> modes to change the values appearance on hover and in the normal state.</p>

<ul class="marked-list">
<li><b>Background Color</b> - here you can set the background color to apply for the meta data values.</li>

<li><b>Text Color</b> - here you can specify the color to use for the meta data values text.</li>

<li><b>Padding</b> - here you can apply the custom padding values to apply a responsive padding for the item values.</li>

<li><b>Margin</b> - here you can specify the margin values to create a responsive margins around the meta data values.</li>

<li><b>Border Type</b> - here you can select the type of the border to apply for the meta data values (the border can be solid, dotted, dashed, etc).</li>

<li><b>Color</b> - here you can change the color to use for the border.</li>

<li><b>Width</b> - here you can set the custom width values to apply for the border.</li>

<li><b>Border Radius</b> - here you can set the border radius to make the border angles more smooth and rounded.</li>

<li><b>Box Shadow</b> - here you can specify if you want to enable the shadow and set its blur, spread and color.</li></ul>

<h5>Prefix</h5>

<ul class="marked-list">
<li><b>Left Gap</b> - here you can set the left gap distance to apply it before the prefix and the meta data.</li>

<li><b>Right Gap</b> - here you can set the distance to apply it between the prefix and the meta data.</li></ul>

<h5>Suffix</h5>

<ul class="marked-list">
<li><b>Left Gap </b>- here you can specify the distance to use it between the meta data and the suffix.</li>

<li><b>Right gap</b> - here you can specify the distance to use it between the suffix and the other content block on the right.</li></ul>
