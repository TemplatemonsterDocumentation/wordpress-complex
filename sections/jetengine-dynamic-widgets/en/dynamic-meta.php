<h2>Dynamic Meta</h2>


<h6>
Dynamic Meta widget allows displaying the default meta information (usually needed for the posts), such as the publishing date, author and information about comments.
</h6>

<h4>Content</h4>

  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/dm1.png">
  	</figure>

<ul class="marked-list">
<li><b>Add Item</b> - click <b>Add Item</b> to add a new item to the dynamic meta information block displayed with this widget (by default, there are already 3 meta data items: date, author and comments).</li>

<li><b>Type</b> - here you can select which meta content you want to display with this block: date, comments count or information about the author.</li>

<li><b>Icon</b> - here you need to select the icon to apply for the item.</li>

<li><b>Prefix</b> - here you can fill in the text that will show up before the meta data pulled from the post.</li>

<li><b>Suffix</b> -  here you can fill in the text that will show up after the meta data pulled from the post.</li>

<li><b>Layout</b> - here you can select if you want to arrange the meta data items in line or as a vertical list.</li></ul>

<h5>Date Settings</h5>

<ul class="marked-list">
<li><b>Format</b> - type in the needed date format  to apply for the date. You can check how to format date <a href="https://codex.wordpress.org/Formatting_Date_and_Time" target="_blank">here</a>.</li>

<li><b>Link</b> - here you can make the date linked to Archive, post or disable the link.</li></ul>

<h5>Author Settings</h5>

<ul class="marked-list">
<li><b>Link</b> - here you can set the link for the author block to lead the visitors to the author archives, to the posts, or you can disable the link at all.</li></ul>

<h5>Comments Settings</h5>

<ul class="marked-list">
<li><b>Link</b> - here you can specify the link that will lead the visitors from Comments count to the post itself.</li>

<li><b>Zero Comments format</b> - here you can set the format to apply in the case there are no comments for the post.</li>

<li><b>One Comments Format</b> - here you can set the format to apply in the case there is 1 comment for the post.</li>

<li><b>More Comments Format</b>  - here you can set the format to apply in the case there is more than 1 comment for the post.</li></ul>

