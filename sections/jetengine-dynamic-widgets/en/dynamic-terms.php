<h5>General</h5>

<ul class="marked-list">
<li><b>Alignment</b> - here you can specify the preferable alignment to use for the terms (left, right, center). </li></ul>

<h5>Icon </h5>

<ul class="marked-list">
<li><b>Color</b> - here you can set the color to use for the icon.</li>

<li><b>Size</b> - here you can select the size of the icon and make it responsive.</li>

<li><b>Gap</b> - here you can specify the gap distance to use between the icn and the terms.</li></ul>


<h5>Links</h5>

<ul class="marked-list">
<li><b>Typography</b> - here you can apply the typography settings to the terms text. These settings are the same as the ones available for the other JetEngine widgets. </li></ul>

<p>Switch between <b>Normal</b> and <b>Hover</b> modes to specify the looks of the terms links on hover and in the normal state.</p>

<ul class="marked-list">
<li><b>Background Type</b> - here you can set type of the background to use for the terms links.</li>

<li><b>Text Color</b> - here you need to set the color you want to apply to the terms links text.</li>

<li><b>Padding</b> - here you can apply the custom padding values to apply a responsive padding for the item links.</li>
<li><b>Margin</b> - here you can specify the margin values to create a responsive margins around the links.</li>


<li><b>Border Type</b> - here you can set the type of the border to apply for the item (e.g., solid, dashed, dotted type).</li>

<li><b>Color</b> - here you can choose the color to apply for the border.</li>

<li><b>Width</b> - here you can select the width to use for the border and make it responsive.</li>

<li><b>Border Radius</b> - here you can set the border radius values to make the border angles more smooth and round.</li>

<li><b>Box Shadow</b> - enable this option to access the shadow settings for the item, such as color, blur and spread).</li></ul>

<h5>Delimiter</h5>

<ul class="marked-list">
<li><b>Color</b> - here you need to set the color to apply for the delimiter separator.</li>

<li><b>Size</b> - here you need to specify the size to apply to the delimiter.</li>

<li><b>Left Gap</b> - here you have to set the gap distance to use between the term on the left and the delimiter.</li>

<li><b>Right Gap</b> - here you need to set the distance between the delimiter and the term on the right from it.</li></ul>

