<h5>General</h5>

<ul class="marked-list">
<li><b>Alignment</b> - here you can specify the preferable alignment to use for the terms (left, right, center). </li></ul>

<h5>Icon </h5>

<ul class="marked-list">
<li><b>Color</b> - here you can set the color to use for the icon.</li>

<li><b>Size</b> - here you can select the size of the icon and make it responsive.</li>

<li><b>Gap</b> - here you can specify the gap distance to use between the icn and the terms.</li></ul>

<h5>Labels</h5>

<ul class="marked-list">
<li><b>Typography</b> -  turn the option on to view the typography settings.They are similar to the typography settings of other JetEngine widgets.</li>
<p>Switch between <b>Normal</b> and <b>Hover</b> modes to specify the looks of the labels on hover and in the normal state.</p>
<li><b>Background Type</b> - here you can set type of the background to use for the labels.</li>

<p>Classic Background Type</p>

<ul class="marked-list">
<li><b>Color</b> - here you can specify the color you want to apply to the background of the labels.</li>
<li><b>Image</b> - here you can add the image to the background of the labels.</li>
<li><b>Text Color</b> - here you need to set the color you want to apply to the labels text.</li>
<li><b>Padding</b> - here you can apply the custom padding values to apply a responsive padding for the labels.</li>
<li><b>Margin</b> - here you can specify the margin values to create a responsive margins around the labels.</li>
<li><b>Border Type</b> - here you can set the type of the border to apply for the item (e.g., solid, dashed, dotted, double, groove type).</li>
<li><b>Width</b> - here you can specify the width of the borders.</li>
<li><b>Color</b> - here you can choose the color of the borders.</li>
<li><b>Border Radius</b> - here you can set the border radius values to make the border angles more smooth and round.</li>
<li><b>Box Shadow</b> - enable this option to access the shadow settings for the item.</li></ul>

<p>Gradient Background Type</p>

<ul class="marked-list">
<li><b>Color</b> — here you can set the background color for the labels using color picker tool.</li>
<li><b>Location</b> — here you can define the proportion for the first color in the gradient background.</li>
<li><b>Second Color</b> — here you can set the second color to use in the gradient background.</li>
<li><b>Location</b> — here you can define the proportion for the second color in the gradient background.</li>
<li><b>Type</b> — define, if you want to use the linear or radial type of the gradient.</li>
<li><b>Angle</b> — in this block you can set the angle for the gradient (in degrees).</li>
<li><b>Text Color</b> - there you can define the color of the text.</li>
<li><b>Border Color</b> - here you can define the color of the borders on hover.</li>
<li><b>Padding</b> - here you can apply the custom padding values to apply a responsive padding for the labels.</li>
<li><b>Margin</b> - here you can specify the margin values to create a responsive margins around the labels.</li>
<li><b>Border Type</b> - here you can select the type of the border to use for the total progress bar.</li>
<li><b>Width</b> - here you can specify the width of the borders.</li>
<li><b>Color</b> - here you can choose the color of the borders.</li>
<li><b>Border Radius</b> - here you can specify the radius values to make the border angles more smooth and round.</li>
<li><b>Box Shadow</b> - enable this option to access the shadow settings for the item.</li></ul>

<h5>Links</h5>

<ul class="marked-list">
<li><b>Typography</b> - here you can apply the typography settings to the terms text. These settings are the same as the ones available for the other JetEngine widgets. </li></ul>

<p>Switch between <b>Normal</b> and <b>Hover</b> modes to specify the looks of the terms links on hover and in the normal state.</p>

<ul class="marked-list">
<li><b>Background Type</b> - here you can set type of the background to use for the terms links.</li>

<li><b>Text Color</b> - here you need to set the color you want to apply to the terms links text.</li>

<li><b>Padding</b> - here you can apply the custom padding values to apply a responsive padding for the item links.</li>
<li><b>Margin</b> - here you can specify the margin values to create a responsive margins around the links.</li>


<li><b>Border Type</b> - here you can set the type of the border to apply for the item (e.g., solid, dashed, dotted type).</li>

<li><b>Color</b> - here you can choose the color to apply for the border.</li>

<li><b>Width</b> - here you can select the width to use for the border and make it responsive.</li>

<li><b>Border Radius</b> - here you can set the border radius values to make the border angles more smooth and round.</li>

<li><b>Box Shadow</b> - enable this option to access the shadow settings for the item, such as color, blur and spread.</li></ul>

<h5>Delimiter</h5>

<ul class="marked-list">
<li><b>Color</b> - here you need to set the color to apply for the delimiter separator.</li>

<li><b>Size</b> - here you need to specify the size to apply to the delimiter.</li>

<li><b>Left Gap</b> - here you have to set the gap distance to use between the term on the left and the delimiter.</li>

<li><b>Right Gap</b> - here you need to set the distance between the delimiter and the term on the right from it.</li></ul>

<h5>Text before</h5>

<ul class="marked-list">
<li><b>Typography</b> - turn the option on to view the typography settings.</li>

<li><b>Color</b> - here you can select the color to apply for the text before the terms list.</li>

<li><b>Gap</b> - here you have to set the gap distance to use between the text and the terms list.</li></ul>

<h5>Text after</h5>

<ul class="marked-list">
<li><b>Typography</b> - turn the option on to view the typography settings.</li>

<li><b>Color</b> - here you can select the color to apply for the text after the terms list.</li>

<li><b>Gap</b> - here you have to set the gap distance to use between the text and the terms list.</li></ul>



