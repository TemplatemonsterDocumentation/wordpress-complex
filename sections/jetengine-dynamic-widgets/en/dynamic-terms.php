<h2>Dynamic Terms</h2>


<h6>
Dynamic Terms widget provides opportunity to add the taxonomies to the custom post types. Use this widget to display the terms that are applied to the needed custom post listing.
</h6>

<h4>Content</h4>

  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/dt1.png">
  	</figure>

<ul class="marked-list">
<li><b>From Taxonomy</b> - here you can specify the taxonomy type from which you want to pull the terms. </li>

<li><b>Show All Terms</b> - enable this option to show all the terms in this taxonomy.

In the case the option is deactivated, youâ€™ll be able to specify the number of terms to show.</li>

<li><b>Delimiter</b> - here you can add a delimiter to separate the terms from one another. </li>

<li><b>Terms Icon</b> - here you can select the icon to use for the  terms. </li></ul>

