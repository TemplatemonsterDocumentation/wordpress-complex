<h5>Image</h5>
<ul class="marked-list">
<li><b>Border Type</b> - here you can set the border for the image. Select one of the available border types to apply it. </li>

<li><b>Width</b> - type in the needed values to set the width of the border for different devices. </li>

<li><b>Color</b> - here you can specify the color to apply for the border. </li>

<li><b>Border Radius</b> - change the border radius values to make the border angles more round and smooth. </li></ul>
