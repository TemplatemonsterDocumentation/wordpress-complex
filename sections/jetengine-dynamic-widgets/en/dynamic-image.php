<h6>General</h6>
<ul class="marked-list">
<li><b>Source</b> - here you can set the source from which you need to pull the thumbnail image (e.g., <b>Thumbnail, Post Thumbnail</b>). </li>

<li><b>Custom Meta Field Key</b> - in order to display the image that is added to the meta field in the form of Media, input the ID of the meta field in this block.</li>

<li><b>Image Size</b> - here you can specify the size of the image to use for the set image (the list of sizes depends on the sizes available for the theme you’re using). </li>

<li><b>Linked Image</b> - enable this option to add a link to the image. </li>

<li><b>Link Source</b> - here you can select the link that will be applied for the image (e.g., permalink). In the case you can’t find the field to which you want to link the image, just input the ID of the meta field which you want to be linked to the image.</li>

<li><b>Alignment</b> - choose one of the alignments to apply for the image (center, left, right). </li></ul>

