<h5>General</h5>

<ul class="marked-list">
<li><b>Typography</b> - here you can change the typography settings that will be applied for this link (they are the same as for other JetEngine widgets for Elementor). </li></ul>

<p>Switch between <b>Normal</b> and <b>Hover</b> modes to customize the appearance of the link on hover and in its normal state.</p>

<ul class="marked-list">
<li><b>Background Type</b> - select the type of the background you want to use for the link (especially in the case you want to create a button). The background can be <b>Classic</b> (in the case you want to apply a single solid color or an image) or a <b>Gradient</b> background.

<p>Classic Background Type</p>

<ul class="marked-list">
<li><b>Color</b> — here you can set background color for the background using color picker tool.</li>
<li><b>Image</b> — here you can choose the image you want to use as a background. Select the image from the media library or upload it to define it as a background in this block. When the image is uploaded, you’ll be able to define its position, alignment, turn on repeat and set the necessary size.</li></ul>

<p>Gradient Background Type</p>

<ul class="marked-list">
<li><b>Color</b> — here you can set background color for the widget using color picker tool.</li>
<li><b>Location</b> — here you can define the proportion for the first color in the gradient background.</li>
<li><b>Second Color</b> — here you can set the second color to use in the gradient background.</li>
<li><b>Location</b> — here you can define the proportion for the second color in the gradient background.</li>
<li><b>Type</b> — define, if you want to use linear or radial type of the gradient.</li>
<li><b>Angle</b> — in this block you can set the angle for the gradient (in degrees).</li></ul>
</li></ul>

<ul class="marked-list">
<li><b>Text Color</b> - here you can specify the color to apply for the link label text.</li>

<li><b>Icon Color</b> - here you can set the different color to apply for the icon that is assigned for the link.</li>

<li><b>Padding</b> - here you can specify the custom padding values to apply for the link to make the paddings responsive.</li>

<li><b>Margin</b> - here you can set the margin values to apply the custom responsive margins for the link.</li>

<li><b>Border Type</b> - here you can select the type of the border to use for the dynamic link block.</li>

<li><b>Width</b> - here you can set the width of the border in px to make it responsive.</li>

<li><b>Color</b> - here you can specify the color of the border.</li>

<li><b>Border Radius</b> - set the border radius values to make the border angles more smooth and round.</li>

<li><b>Icon Position</b> - here you can set the preferable icon position (before link label, or after link label).</li>

<li><b>Icon Size</b> - use the control to change the icon size to the needed one. This value is also responsive.</li></ul>
