Items

Space Between - here you can set the space between the checkboxes and apply different spaces between them for mobile, tablet and desktop devices.
Children Offset Left - use the control to change the offset for the

Checkbox

Size - here you can specify the size to apply for the checkbox container.
Select Normal or Checked mode to edit the appearance of the checked and unchecked checkboxes.
