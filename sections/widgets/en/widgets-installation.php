	<h3>Widgets import</h3>

	<h4>In order to import the widgets to the website, you need to install "Widget Importer & Exporter" plugin</h4>

	<p>Navigate to <strong>"Tools" = > "Widget Importer & Exporter"</strong> section after the successful plugin installation and activation. To start the import select the file in <strong>.wie</strong> format. It is located in "Widget Dummy Content" folder in template package.</p>

	<p>When the widgets are successfully imported, you will see a list of widgets with installation status. Then you'll be able to start using them on the site.</p>

