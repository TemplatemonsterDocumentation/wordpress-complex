        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show page logo</dt>
            <dd>
                This property shows/hides page logo
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show image</dt>
            <dd>
                This property shows/hides images
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show likebox</dt>
            <dd>
                Here you can select whether to show/hide the likebox
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Open links in new tab</dt>
            <dd>
                This property allows to open links in new tab
