<h2>Quick Guide: Working with Prestyled Sections</h2>


<h5>From this block you can learn how to use the pre-styled sections available when clicking on <b>Magic Button</b>.</h5>

<h5>Please make sure you have <b>Elementor</b> and <b>JetElements</b> plugins installed!</h5>

<ol class="index-list">
<p><li>In order to access the pre-made stylish sections open the page, which you want to insert the template to in <b>Elementor editor</b>.</li></p>

<p><li>Here you can see the three buttons. The <b>Magic Button</b> is located on the left of Add new section button. Click it to view all the pre-styled content available for usage.</li></p>

	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/prestyled/1.png">
    </figure></li></p>

<p><li>The content is separated into <b>Homepages, Sub Pages, Sections</b>.</li></p>


<ul class="marked-list">
<p><li><b>Homepages</b> - the block contains different Homepages on various topics available for usage.
