<h3>Getting Started</h3>

<h5>From this block you can learn more about installing JetParallax and creating parallax backgrounds for the sections using JetParallax plugin. </h5>

<h4>Installation</h4>

<ol class="index-list">
  <p><li>First, make sure that you’ve installed and activated <a href="https://wordpress.org/plugins/elementor/" target="_blank">Elementor</a> page builder before proceeding to JetParallax installation. If you have problems with installing Elementor feel free to read the detailed <a href="https://docs.elementor.com/" target="_blank">Elementor documentation</a>.</li></p>

  <p><li>Log in to <b>Wordpress Dashboard</b> using Your login credentials and navigate to <b>Plugins</b> tab in the left column.

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/4.png">
    </figure>

  </li></p>



  <p><li>Click on <b>Add new</b> button to upload JetParallax plugin from Your hard drive.

      <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/1.png">
    </figure></li></p>

  <p><li>Click on <b>Upload</b> button, which can be found at the top of <b>Plugins</b> page. Browse the path to JetParallax plugin and start installation.

        <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/2.png">
    </figure>
</li></p>


  <p><li>When JetParallax plugin is uploaded and installed you’ll need to activate it. Navigate to <b>Plugins</b> tab and locate JetParallax plugin in the list of installed plugins. Click on <b>Activate</b> link under JetParallax. As a result the plugin will become active.</li></p>

</ol>

<h4>Accessing JetParallax</h4>

<ol class="index-list">
<p><li>First, let’s access the Elementor section background settings. To do it, please, navigate to <b>Elementor</b> page editor and click <b>Add New Section</b> button.</li></p>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/par2.png">
    </figure>

<p><li>Select the section structure and add content modules to it (e.g., spacers, headings, etc.).</li></p>

<p><li>Hover on the upper border of the section and click <b>Edit Section</b> option in order to access the section settings.</li></p>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/par3.png">
    </figure>

<p><li>In <b>Layout settings</b> locate <b>JetParallax</b> tab. Open it and click <b>Add Item</b> button to add a new item with the Parallax effect.</li></p>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/par4.png">
    </figure>

<p><li>Prepare the objects to place in the section's background. You can place as many of them as you need and apply different effects to them in order to create the illusion of depth and 3D effect.</li></p>
