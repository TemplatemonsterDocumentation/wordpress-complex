<h3>Template configuration at Customizer</h3>

<p>
	Now your theme can be configured with the convenient intuitive WYSIWYG editor interface. Our template extends its functionality, so you have additional settings available. You'll easily adapt your settings.
</p>
<p>
 	There are two ways to start configuring, first is to navigate to <strong>Appearance > Customize</strong> section. Second way is to press on <strong>"Customize"</strong> at the top left corner on site frontend. This way allows you to edit the page you're currently viewing. And the first way initially shows site front page by default.
</p>
<p>
	Left side of site interface lists settings available, and at the right side of the screen page template is displayed. After adding the changes you can save them by pressing on the <strong>"Save &amp; Publish"</strong> button.
</p>

