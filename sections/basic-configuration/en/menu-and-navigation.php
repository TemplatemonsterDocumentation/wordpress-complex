<p>Then you can define the menu position and check the result with the help of customizer tool.</p>

<p>
	In order to show the list of social icons you need to create a separate menu with <strong>"Social Menu"</strong> title. Then please create menu items for the required social networks, and place this menu at the similar "Social" section. Social icons will be automatically applied. In order to change them to custom ones, you just need to replace the images with the same filenames and save changes. These icons will be also used at the <strong>"Follow"</strong> block.
</p>

