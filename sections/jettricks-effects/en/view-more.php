<h3>View More</h3>

<p>
	This widget allows displayind sections and hiding them under the cut stylishly, making the page's content more visually compact.
</p>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/showmore.png">
    </figure>

<h4>Content </h4>

<h5>Sections </h5>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/vm.png">
    </figure>

<ul class="marked-list">
    <p><li><b>Add Item</b> - here you can add the section IDs you want to stay hidden until the user clicks the View More button. The first item opens on the first click, the second section is shown on the second click, etc. </li></p></ul>

<p>Please, note that before adding the section IDs one needs to create the sections and specify the section IDs in the <b>Edit Section > Advanced > CSS ID</b> field. </p>

<p>The ID You entered in the field mentioned above should be placed in the <b>Section ID</b> field of the item. </p>

<ul class="marked-list">
    <p><li><b>Section ID</b> - here you need to input the Section ID found in the Edit Section > Advanced > CSS ID field.</li></p></ul>

<h5>Settings </h5>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/vm1.png">
    </figure>

<ul class="marked-list">
    <p><li><b>Icon</b> - here you can change the icon to display in the View More button. </li></p>

    <p><li><b>Label</b> - here you can change the button label text.</li></p>

    <p><li><b>Show All Sections</b> - enable this option in order to show all the sections on first click.</li></p>

    <p><li><b>Show Effect</b> - here you can change the effect to use when the sections are displayed:

<ul class="marked-list">
    <li>Move Up;</li>
    <li>Fade;</li>
    <li>Foom In;</li>
    <li>Zoom Out;</li>
    <li>Fall Perspective;</li>
    <li>None.</li>
</li></p></ul>

<h4>Style </h4>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/vm2.png">
    </figure>

<h5>General </h5>

<ul class="marked-list">
    <p><li><b>Padding</b> - here you can change the paddings to use for the View More button. </li></p>

    <p><li><b>Margin</b> - here you can define the custom margins to apply them for the button. </li></p>

    <p><li><b>Border Radius</b> - change the border radius values to make the button angles more smooth and round. </li></p>

<p>Switch between <b>Normal </b>and <b>Hover </b>modes in order to customize both the button appearance when it is in normal state, or when on hover. </p>

<ul class="marked-list">
    <p><li><b>Text Color</b> - here you can change the button text color. </li></p>

    <p><li><b>Typography</b> - here you can make changes in the button text typography. </li></p>
    <p><li><b>Size</b> — define the font size of the text.</li></p>
    <p><li><b>Family</b> — here you can set the font family for the text.</li></p>
    <p><li><b>Weight</b> — in this block you can select the suitable font weight.</li></p>
    <p><li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</li></p>
    <p><li><b>Style </b>— in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li></p>
    <p><li><b>Line Height </b>— in this field you can set the height of the text.</li></p>
    <p><li><b>Letter Spacing </b>— here you can set the space between the text characters.</li></p>

    <p><li><b>Icon Color </b>- here you can change the color of the button icon to the suitable one.
</li></p>
    <p><li><b>Icon Size </b>- here you can change the size of the ixon to use for the button. </li></p>

    <p><li><b>Background Type</b> - here you can set the preferable background type to use for the button. It can be Classic or Gradient background.</li></p></ul>

<p><b>Classic Background Type</b></p>

<ul class="marked-list">
    <p><li><b>Color</b> - here you can set the specific color to use for the button.</li></p>
    <p><li><b>Image </b>- click the image box and upload an image in order to use it as a background for the button.</li></p>
    <p><li><b>Position </b>- here you can position the image used for the background according to your needs.</li></p>
    <p><li><b>Attachment</b> - here you can make the image fixed or allow it to be scrolled.</li></p>
    <p><li><b>Repeat </b>- here you can allow the image to be repeated horizontally, vertically, tiled or restrict repeating.</li></p>
    <p><li><b>Size </b>- select Cover, Contain or Auto image sizing in order to make it fitted into the button.</li></p></ul>

<p><b>Gradient Background Type</b></p>

<ul class="marked-list">
    <p><li><b>Color </b>- add the first color to use for the gradient background of the button.</li></p>
    <p><li><b>Location </b>- here you can change the location of the first gradient color.</li></p>
    <p><li><b>Second Color </b>- add the second color to use for the gradient background.</li></p>
    <p><li><b>Location </b>- here you can change the location of the second gradient color.</li></p>
    <p><li><b>Type </b>- here you can change the gradient location from linear to radial.</li></p>
    <p><li><b>Angle </b>- if the gradient type is lineer, you can change the angle to use for it.</li></p>
    <p><li><b>Position</b> - if the gradient is radial, here you can change the position of its center to the preferable one.</li></p>
    <p><li><b>Padding</b> - here you can set the padding for the button.</li></p>
    <p><li><b>Border type </b>- here you can change the border type to the one needed (solid, dotted, dashed, double, groove or none).</li></p>
    <p><li><b>Width </b>- here you can set the width of the border in px.</li></p>
    <p><li><b>Color</b> - here you can choose the color to use for the border.</li></p>
    <p><li><b>Border Radius</b> - here you can apply a radius for the border angles to make them more smooth.</li></p>
    <p><li><b>Border Type </b>- here you can define if the button has a border (none, solid, dotted, dashed, double, groove).</li></p>

    <p><li><b>Width </b>- here you can specify the required border width for the button.</li></p>

    <p><li><b>Color</b> - here you can set the needed color to use for the border.</li></p>

    <p><li><b>Box Shadow</b> - here you can set the shadow options for the button in order to make it stand out more on the image. You’ll be able to customize such shadow options, as:
<ul class="marked-list">
    <li>Color;</li>
    <li>Horizontal;</li>
    <li>Vertical;</li>
    <li>Blur;</li>
    <li>Spread.</li></ul>
</li></p></ul>
