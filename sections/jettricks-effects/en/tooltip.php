<h3>Tooltip for Widget</h3>

<p><b>Tooltip </b> effect is necessary whenever you need to add an informational hint tip to one of the widgets. </p>

<p>In order to use <b>Tooltip</b> effect click <b>Edit Widget</b> option and then open <b>Advanced Settings</b>. Here open <b>JetTricks</b> block and locate <b>Tooltip</b>.</p>

<ul class="marked-list">
<li><p><b>Use Tooltip</b> - here you can enable or disable the <b>Tooltip</b> effect for the widget. After enabling this effect you’ll be able to change its settings and style. </p></li></ul>

