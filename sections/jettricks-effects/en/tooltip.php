<h4>Settings</h4>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/tooltips2.png">
    </figure>

<ul class="marked-list">
<li><p><b>Description</b> - here you need to add the text to be shown inside the tooltip.</p></li>

<li><p><b>Placement</b> - here you can select the needed position of the tooltip (top, bottom, left, right).</p></li>

<li><p><b>Animation</b> - here you can select one of the available animation effects.</p></li>

<li><p><b>Offset</b> - here you can specify the offset for the tooltip.</p></li>

<li><p><b>Distance</b> - here you can set the distance for the tooltip to be shown where it is needed and positioned according to one’s needs. </p></li>

<li><p><b>Z-Index</b> - here you can make the tooltip be shown before or after the widget’s content.</p></li></ul>

<h4>Style </h4>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/tooltips3.png">
    </figure>


<ul class="marked-list">
<li><p><b>Width</b> - here you can set the custom tooltip block’s width in px.</p></li>

<li><p><b>Typography</b> -  here you can customize the typography options for the tooltip text element. The typography settings are similar to the ones available for other textual elements used in JetTricks.</p></li>

<li><p><b>Text Color</b> - here you can specify the color of the tooltip text.</p></li>

<li><p><b>Text Alignment</b> - here you need to set the preferable alignment for the tooltip text.</p></li>

<li><p><b>Background Type</b> - here you can change the type of the background of the tooltip. It can be gradient or classic background. The settings vary for each one and are similar to the ones available for other JetTricks widgets.</p></li>

<li><p><b>Arrow Color</b> - here you can change the color of the tooltip arrow to the one needed.</p></li>

<li><p><b>Padding</b> - here you can set the preferable padding values for the tooltip and make the paddings responsive.</p></li>

<li><p><b>Border Type</b> - here you can apply the custom border type for the tooltip. In the case the border is selected, you can also change the border width and color.</p></li>

<li><p><b>Border Radius</b> - here you can set the radius values to make the border angles more smooth.</p></li>

<li><p><b>Box Shadow</b> - here you can set the shadow for the tooltip and customize its appearance (color, blur, alignment).</p></li></ul>

