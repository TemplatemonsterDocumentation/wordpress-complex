<h3>Parallax Element</h3>

<p>
	This functionality enables you to add parallax effect for versatile widgets and change parallax speed, enable or disable parallax for different devices, etc.
</p>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/parell.png">
    </figure>

<ol class="index-list">
<li><p>Click on <b>Edit Widget</b> option and then proceed to <b>Advanced</b> settings. </p></li>

<li><p>Here open <b>JetTricks</b> menu and enable <b>Parallax</b> option in order to apply parallax for the specific widget. </p></li>

<li><p>Use the control in order to set the parallax speed (in percents).</p></li>

<li><p>Enable <b>Invert</b> option in order to switch the initial position of the widget to the opposite one. </p></li>

<li><p><b>Active on</b> - here you can define, on which devices the parallax effect should work for the widget. </p></li></ol>


