<h3>Hotspots</h3>

<p><b>Hotspots</b> widget allows adding the stylish hotspots to the specific image to mark the important details.</p>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/hotspots.png">
    </figure>

<h4>Content</h4>


<h5>Image</h5>


 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/hsp1.png">
    </figure>


<p>Here you can select the image to which you’re going to add hotspots from the media library or upload it from your hard drive.</p>

<h5>Hotspots</h5>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/hsp2.png">
    </figure>

<ol class="index-list">
<li><p>Click <b>Add Item</b> button in order to add a new hotspot to the image.</p></li>

<li><p>Switch between <b>Content</b> and <b>Position</b> tabs in order to specify the content you want to display and place the hotspot where it is needed.</p>

<ul class="marked-list">
<li><p><b>Icon</b> - here you can set the icon to use it for the hotspot marker.</p></li>

<li><p><b>Text</b> - here you can add the text to show it beside the icon.</p></li>

<li><p><b>Description</b> - here you can add a text to display when one hovers on the hotspot in an additional tip.</p></li>

<li><p><b>Link</b> - here you can specify a link which will be opened when one clicks on the hotspot marker.</p></li>

<li><p><b>Horizontal Position</b> - use the control in order to position the marker using the horizontal x axis (in percents, assuming that 100% is the full width of an image).</p></li>

<li><p><b>Vertical Position</b> - use the control in order to position the marker using the vertical y axis (in percents, assuming that 100% is the full height of an image).</p></li></ul></li></ol>

<h5>Tooltip</h5>


 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/hsp3.png">
    </figure>


<ul class="marked-list">
<li><p><b>Placement</b> - here you can select, where you want the tooltip with a description text to be displayed (top, bottom, left or right position).</p></li>

<li><p><b>Use Arrow</b> - here you can enable or disable the usage of an arrow pointing to the hotspot to which the tooltip is related.</p></li>

<li><p><b>Arrow Type</b> - here you can select which arrow type you want to apply for the tooltip (sharp or round).</p></li>

<li><p><b>Arrow Size</b> - here you can define, how large or small the arrow should be (normal, small, skinny, large, wide).</p></li>

<li><p><b>Trigger</b> - here you can specify what action triggers the tooltip to show up (hover or click).</p></li>

<li><p><b>Show duration</b> - here you can set how fast or slow the tooltip is to appear.</p></li>

<li><p><b>Hide duration</b> - here you can specify how slow or fast the tooltip is to disappear.</p></li>

<li><p><b>Delay</b> - here you can define the tooltip delay time in ms.</p></li>

<li><p><b>Distance</b> - here you can set the distance between the original hotspot and the tooltip.</p></li>

<li><p><b>Animation</b> - here you can select the suiting animation effect which will be used when the tooltip appears or disappears (shift-away, shift-toward, fade, scale, perspective).</p></li></ul>

<h4>Style</h4>


 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/hsp4.png">
    </figure>


<h5>Hotspot</h5>

<ul class="marked-list">
<li><p><b>Padding</b> - here you can set the custom paddings to use for the content contained inside the hotspot.</p></li>

<li><p><b>Border Type</b> - here you can define if the hotspot has a border (none, solid, dotted, dashed, double, groove).</p></li>

<li><p><b>Width</b> - here you can specify the required border width for the hotspot.</p></li>

<li><p><b>Color</b> - here you can set the needed color to use for the border.</p></li>

<li><p><b>Border Radius</b> - define the values of the border radius to make the border angles more round.</p></li>

<li><p><b>Box Shadow</b> - here you can set the shadow options for the hotspot in order to make it stand out more on the image. You’ll be able to customize such shadow options, as:</p>
<p>
<ul class="marked-list">
<li>Color;</li>
<li>Horizontal;</li>
<li>Vertical;</li>
<li>Blur;</li>
<li>Spread.</li></p></li>

<li><p><b>Typography</b> - here you can make changes in the hotspot content typography.
<li><p><b>Size</b> — define the font size of the hotspot text.</p></li>
<li><p><b>Family</b> — here you can set the font family for the hotspot text.</p></li>
<li><p><b>Weight</b> — in this block you can select the suitable font weight.</p></li>
<li><p><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</p></li>
<li><p><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</p></li>
<li><p><b>Line Height</b> — in this field you can set the height of the hotspot text.</p></li>
<li><p><b>Letter Spacing</b> — here you can set the space between the hotspot text characters.</p></li></ul>


<p>Switch between <b>Normal</b> and <b>Hover</b> modes in order to customize the hotspot marker appearance on hover and in its normal state.</p>

<ul class="marked-list">
<li><p><b>Icon Size</b> - here you can select the necessary size to apply it for the hotspot icon.</p></li>

<li><p><b>Icon Color</b> - here you can define the color you want to apply for the icon.</p></li>

<li><p><b>Text Color</b> - here you can specify the color to use for the text.</p></li>

<li><p><b>Background Type</b> - here you can set the preferable background type to use for the hotspot. It can be Classic or Gradient background.</p></li>

<p><b>Classic Background Type</b></p>

<ul class="marked-list">
<li><p><b>Color</b> - here you can set the specific color to use for the hotspot.</p></li>
<li><p><b>Image</b> - click the image box and upload an image in order to use it as a background for the hotspot.</p></li>
<li><p><b>Position</b> - here you can position the image used for the background according to your needs.</p></li>
<li><p><b>Attachment</b> - here you can make the image fixed or allow it to be scrolled.</p></li>
<li><p><b>Repeat</b> - here you can allow the image to be repeated horizontally, vertically, tiled or restrict repeating.</p></li>
<li><p><b>Size</b> - select Cover, Contain or Auto image sizing in order to make it fitted into the hotspot.</p></li></ul>

<p><b>Gradient Background Type</b></p>

<ul class="marked-list">
<li><p><b>Color</b> - add the first color to use for the gradient background of the hotspot.</p></li>
<li><p><b>Location</b> - here you can change the location of the first gradient color.</p></li>
<li><p><b>Second Color</b> - add the second color to use for the gradient background.</p></li>
<li><p><b>Location</b> - here you can change the location of the second gradient color.</p></li>
<li><p><b>Type</b> - here you can change the gradient location from linear to radial.</p></li>
<li><p><b>Angle</b> - if the gradient type is lineer, you can change the angle to use for it.
<li><p><b>Position</b> - if the gradient is radial, here you can change the position of its center to the preferable one.</p></li>
<li><p><b>Padding</b> - here you can set the padding for the hotspot.</p></li>
<li><p><b>Border type</b> - here you can change the border type to the one needed (solid, dotted, dashed, double, groove or none).</p></li>
<li><p><b>Width</b> - here you can set the width of the border in px.</p></li>
<li><p><b>Color </b>- here you can choose the color to use for the border.</p></li>
<li><p><b>Border Radius</b> - here you can apply a radius for the border angles to make them more smooth.</p></li></ul>

