<h4>Settings</h4>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/sat2.png">
    </figure>

<ul class="marked-list">

<li><p><b>Type</b> - here you can select if you want to add a textual satellite element to the widget or an image satellite element. Depending on what you choose the following settings and the needed styles will vary. </p></li>
<li><p><b>Text</b> - type in the text you need to use as a satellite for the widget in this field.</p></li>
<li><p><b>Image</b> - here you can select one of the images from the media library or upload it in order to use it as a satellite for the widget. </p></li>
<li><p><b>Position</b> - here you can select the position of the element to use it as a satellite (top, middle, bottom, center, right or left). </p></li>
<li><p><b>X-Offset</b> - here you can apply the custom horizontal offset for the satellite element. </p></li>
<li><p><b>Y-Offset</b> - here you can set the custom vertical offset for the satellite element. </p></li>
<li><p><b>Rotate</b> - use the controls to rotate the satellite element using 360 degrees.</li></p>
<li><p><b>Z-Index</b> - - here you can define the position of the satellite element before or after the widget’s content.</li></p>


</ul>

<h4>Style</h4>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/sat3.png">
    </figure>


<ul class="marked-list">
<li><p><b>Color</b> - here you can set the color to apply for the text using color picker tool. </p></li>

<li><p><b>Typography</b> - here you can customize the typography options for the text element. The typography settings are similar to the ones available for other textual elements used in JetTricks. </p></li>

<li><p><b>Text Shadow</b> - here you can set the shadow for the text and customize its appearance (color, blur, alignment).</p></li>


<li><p><b>Width</b> - here you can apply the custom width for the image used as a satellite for the widget.</p></li>

<li><p><b>Height</b> - here you can set the custom height for the image used as a satellite.</p></li>

<li><p><b>CSS Filters</b> - here you can set the additional style settings for the satellite element.</p></li>

<li><p><b>Blur</b> - use this control to make the element blurred.</p></li>
<li><p><b>Brightness</b> - use the control to make the element’s brightness lower or higher. </p></li>
<li><p><b>Contrast</b> - here you can set the contrast to maximum or minimum according to your needs. </p></li>
<li><p><b>Saturation</b> - use the control to make the element saturation higher or lower. </p></li></ul>



