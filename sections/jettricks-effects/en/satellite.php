<h3>Satellite Element</h3>

<p><b>Satellite</b> effect can be applied for one of the existing widgets to add a textual or image element before or after the widget, styling it up and positioning it according to one’s needs. </p>

<ol class="index-list">
<li><p>In order to use <b>Satellite</b> effect click <b>Edit Widget</b> option and then open <b>Advanced Settings</b>. </p></li>

<li><p>Here open <b>JetTricks</b> block and locate <b>Satellite</b>.</p></li>

</ol>

<p>You can create a satellite element for the widget and change its settings and appearance using the settings listed below.</p>

<ul class="marked-list">
<li><p><b>Use Satellite</b> - here you can enable or disable the <b>Satellite</b> effect for the widget. After enabling this effect you’ll be able to change its settings and style. </p></li></ul>

