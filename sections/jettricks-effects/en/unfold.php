<h5>Container</h5>

<p>Switch between <b>Fold</b> and <b>Unfold</b> modes in order to be able to customize appearance of both hidden and expanded containers.</p>

<ul class="marked-list">
    <p><li><b>Background type</b> - here you can set the preferable background type to use for the folded or unfolded container. It can be Classic or Gradient background.</li></p></ul>


<p><b>Classic Background Type</b></p>

<ul class="marked-list">
    <p><li><b>Color</b> - here you can set the specific color to use for the container.</li></p>
    <p><li><b>Image </b>- click the image box and upload an image in order to use it as a background for the container.</li></p>
    <p><li><b>Position</b> - here you can position the image used for the background according to your needs.</li></p>
    <p><li><b>Attachment</b> - here you can make the image fixed or allow it to be scrolled.</li></p>
    <p><li><b>Repeat </b>- here you can allow the image to be repeated horizontally, vertically, tiled or restrict repeating.</li></p>
    <p><li><b>Size</b> - select Cover, Contain or Auto image sizing in order to make it fitted into the container.</li></p></ul>

<b><p>Gradient Background Type</p></b>

<ul class="marked-list">
    <p><li><b>Color</b> - add the first color to use for the gradient background of the container.</li></p>
    <p><li><b>Location</b> - here you can change the location of the first gradient color.</li></p>
    <p><li><b>Second Color</b> - add the second color to use for the gradient background.</li></p>
    <p><li><b>Location </b>- here you can change the location of the second gradient color.</li></p>
    <p><li><b>Type </b>- here you can change the gradient location from linear to radial.</li></p>
    <p><li><b>Angle</b> - if the gradient type is lineer, you can change the angle to use for it.</li></p>
    <p><li><b>Position</b> - if the gradient is radial, here you can change the position of its center to the preferable one.</li></p>
    <p><li><b>Padding</b> - here you can set the padding for the container.</li></p>
    <p><li><b>Border type</b> - here you can change the border type to the one needed (solid, dotted, dashed, double, groove or none).</li></p>
    <p><li><b>Width</b> - here you can set the width of the border in px.</li></p>
    <p><li><b>Color</b> - here you can choose the color to use for the border.</li></p>
    <p><li><b>Border Radius</b> - here you can apply a radius for the border angles to make them more smooth.</li></p>
    <p><li><b>Border Type</b> - here you can define if the container has a border (none, solid, dotted, dashed, double, groove).</li></p>
<ul class="marked-list">
    <p><li><b>Width</b> - here you can specify the required border width for the container.</li></p>

    <p><li><b>Color </b>- here you can set the needed color to use for the border.</li></p>

    <p><li><b>Border Radius</b> - define the values of the border radius to make the border angles more round.</li></p>

    <p><li><b>Box Shadow</b> - here you can set the shadow options for the container in order to make it stand out more on the image. You’ll be able to customize such shadow options, as:

<ul class="marked-list">
    <li>Color;</li>
    <li>Horizontal;</li>
    <li>Vertical;</li>
    <li>Blur;</li>
    <li>Spread.</li></p></ul>

<ul class="marked-list">
    <p><li><b>Padding</b> - here you can add your own custom padding values in order to add the paddings to me content encased in the container.</li></p>

    <p><li><b>Margin</b> - here you can add your custom margins to use for the Fold/Unfold container.</li></p>

    <p><li><b>Border Radius</b> - here you can define the values of the border radius to make the border angles more round.</li></p></ul>

