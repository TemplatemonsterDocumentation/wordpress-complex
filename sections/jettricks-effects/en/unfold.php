<h4>Content</h4>

<h5>Settings</h5>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/fold.png">
    </figure>

<ul class="marked-list">
    <p><li><b>Fold</b> - enable this option in order to make the initial content shown fully on the page. If the option is disabled, the content will stay hidden unless the user clicks on the Unfold button.</li></p>

    <p><li><b>Closed Height</b> - here you can specify the height of the unfolding content box when it is closed.</li></p></ul>

<p>Switch between <b>Fold</b> and <b>Unfold</b> modes to customize the behavior of the Fold and Unfold actions.</p>

<ul class="marked-list">
    <p><li><b>Duration</b> - here you can define, how fast or slow the Fold/Unfold option will close or open the content box.</li>

    <p><li><b>Easing</b> - here you can change the type of easing used when the content is folded or unfolded:
<ul class="marked-list">
    <li>Linear;</li>
    <li>Sine;</li>
    <li>Expo;</li>
    <li>Circ;</li>
    <li>Back;</li>
    <li>InOutSine;</li>
    <li>InOutExpo;</li>
    <li>InOutCirc;</li>
    <li>InOutBack.</li></ul>


