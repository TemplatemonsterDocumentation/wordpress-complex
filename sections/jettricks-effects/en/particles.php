<h3>Section Particles</h3>

<p><b>Section Particles</b> effect allows to add the stylish animated particles to the section’s background using the JSON code. </p>

<p>You can check the code samples using this <a href="https://vincentgarreau.com/particles.js/#default" target="_blank">link.</a></p>

<p>Use the block in the top right corner to customize the <b>number</b> of particles, their <b>color</b>, <b>shape</b>, <b>size</b>, etc. </p>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/part.png">
    </figure>

<p>Then click <b>Download Current Config</b> option in .json format and open the file in any text editor.</p>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/part2.png">
    </figure>

<p>Copy its content and paste in to the <b>Particles JSON </b> field. The animated particles will be added to the section’s background.</p>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/part1.png">
    </figure>

<p>Note, that you can also create your own animated particles or elements to place to the section’s background.</p>








