<h3>Sticky Column </h3>

<p><b>Sticky Column</b> is an additional functionality which makes it possible to make the columns "sticky" when one scrolls the section up and down. </p>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/sticky1.png">
    </figure>

<ol class="index-list">
    <li><p>First, add a new section and select the needed columns layout. Place the needed content into the column and the rest of the columns, which will be scrolled. Cick <b>Edit Column</b> option for the column you want to make sticky.</p></li>

    <li><p>After that open <b>JetTricks</b> tab. Here turn on <b>Sticky Column</b> option. This will make the Sticky Column effect options appear on the page.</p></li></ol>

     <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/stick.png">
    </figure>

<ul class="marked-list">
    <li><p><b>Top Spacing</b> - add the top spacing value in order to make the starting point when scrolling a sticky column a bit lower.</p></li>

    <li><p><b>Bottom Spacing</b> - add the bottom spacing value in order to make the ending point of the sticky column when it scrolls a bit higher.</p>

<p>Please, note that the value set for Top and Bottom spacing should be larger than 0.</p></li>

    <li><p><b>Sticky on</b> - here you can select on which devices the sticky option will be applied (e.g., desktop, tablet).</p></li></ul>

