<h3>Sorting Search Results by Relevance</h3>

<h5>From this block you can learn how to control the search results that the posts to be showed by relevance.</h5>

<ul class="index-list">

<li><p>First of all, check the publications, which you want to be displayed in the search results, they must include the proper keywords.</p>

<li><p>You can manage the order of the search results. Depending on the location and frequency of mentioning the search request will have the appropriate position in the results area. </p></li>
<li><p>There search variant will be showed in such sequence:</p>
<p>On the top the publications with keywords in their title, excerpt and content are presented.</p> 
<p>Then the posts with keywords in their content and excerpt are displayed.</p>
<p>The less relevant results are also showed in the results area if they have the search keywords either in their title or content or excerpt.</p></li>
<li><p>Now adjust the needed settings in Ajax Search widget.</p></li>
<li><p>Open the page, where you want to add <b>Ajax Search</b> widget and click <b>Edit with Elementor</b> button.</p></li>
<li><p>Then, find Ajax Search widget and insert in the section.</p></li>
<li><p>In the <b>Content > Search Settings</b> tab select <b>Relevance</b> option from <b>Sort Results by</b> dropdown list.</p></li>
<li><p>Below in the <b>Filter Results by</b> setting choose the descending or ascending order of the search results.</p></li>

