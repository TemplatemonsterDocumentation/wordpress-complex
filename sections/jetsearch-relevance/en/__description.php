<h3>Sorting Search Results by Relevance</h3>

<h5>From this block you can learn how to control the search results that the posts to be showed by relevance.</h5>

<ol class="index-list">

<li><p>From this block you can learn how to control the search results, making sure that the posts are sorted according to the relevance.</p>

<li><p>You can manage the order of the search results and change the way they are showcased, which result should be the first, and which is to be the last one.</p>
<p>Each request will be handled  depending on the location and frequency of mentioning the search words. This is how the position of the result is defined.
<ul class="marked-list">
<li>The first search result should include the search words matching the request in the title, excerpt and content.</p></li>

<li><p>The second result should contain the matching search words in the content and excerpt.</p></li>
<li><p>The results with the lowest relevance should have the search keywords either in their title, or content, or excerpt.</p></li>
</ul>
<li><p>Now adjust the needed settings in <b>Ajax Search</b> widget.</p></li>
<li><p>Open the page, where you want to add <b>Ajax Search</b> widget and click <b>Edit with Elementor</b> button.</p></li>
<li><p>Add the <b>Ajax Search</b> widget in the section.</p></li>
<li><p>In the <b>Content > Search Settings</b> tab select <b>Relevance</b> option from <b>Sort Results by</b> dropdown list.</p></li>
<li><p>Below, in the <b>Filter Results</b> set the descending or ascending order of the search results.</p></li>

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/relevance.png">
    </figure></li></p>

<li><p>Click the <b>Update</b> button.</p></li>

</ol>


