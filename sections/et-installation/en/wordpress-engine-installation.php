<h5 id="useful-tips:"<strong>Step 2. Upload the extracted files to hosting server:</strong></h5>
<p>WordPress engine files should be uploaded to the root directory on your server. It is usually named <strong>www</strong> or <strong>public_html</strong>.</p>

<li>The following tutorials will help you:</li>
<ol><ol class="marked-list"><p>
	<li><a href="https://zemez.io/wordpress/support/knowledge-base/uploading-files-to-a-server-using-ftp/">Uploading Files to a Server Using FTP;</a></li>
