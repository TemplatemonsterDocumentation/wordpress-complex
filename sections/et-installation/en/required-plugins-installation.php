<h3>Required Plugins Installation</h3>

<p>In order to install plugins, required in your Elementor Template, you need to follow the steps below:</p>


<p>1. Log in to administration panel and enter <strong>Plugins</strong> tab, which you can see in the left column. Click on <strong>Add New</strong> button. As a result, you will be able to add plugins manually using <strong>Upload</strong> button at the top of the page.</p>

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/pl1.png">
    </figure>

<p>2. Use the <strong>Search</strong> field and search for the plugins you want to install. Just input keywords into it, and the search will start.</p>

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/pl3.png">
    </figure>


<p>3. If you have the plugins you want to install on your hard drive, then click on Upload button, which usually can be found at the top of <strong>Plugins</strong> page (assuming you still have Administration panel opened). Otherwise, you can download them to your hard drive first.</p>
