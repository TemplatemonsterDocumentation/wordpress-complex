<h3>Elementor Template Installation</h3>

<p>There are two main ways how to install Elementor Template. You are free to select the most convenient one.</p>

<h4>Installation via Elementor</h4>

<p>1. Open Elementor editing page and locate <strong>Add Template</strong> button. Click on it.</p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/3-1.png">
    </figure>
<p>2. In the opened Library window navigate to <strong>My Templates</strong>.</p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/3-2.png">
    </figure>

<p>3. To import your template click corresponding icon.</p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/3-3.png">
    </figure>

<p>4. In the opened window you will be offered to either drag and drop your file or select it from your hard drive by clicking <strong>Select File</strong> button.</p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/3-4.png">
    </figure>


<p>5. Once you selected json file you will find it in end of the list in your Library.</p>
  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/3-5.png">
    </figure>

<p>6. Click <strong>Insert</strong> button to embed it to your page.</p>
  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/3-6.png">
    </figure>

    <p>Now you can proceed with Template customization in Elementor!</p>

<h4>Installation via Dashboard</h4>


<p>1. Navigate to <strong>WordPress Dashboard > Elementor > My Templates</strong>.</p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/3-7.png">
    </figure>
<p>2. Locate <strong>Import Templates</strong> button and click on it. </p>
<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/3-8.png">
    </figure>

<p>3. Click <strong>Choose File</strong> button and select json file from your hard drive. Click <strong>Import Now</strong> button.</p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/3-9.png">
    </figure>

<p>4. Your template will be added to the list. Click <strong>Edit with Elementor</strong> to start customizing. </p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/3-10.png">
    </figure>

    <p>That's all! You are free to customize your Template in Elementor.</p>