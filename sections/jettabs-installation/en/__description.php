<h3>JetTabs Installation</h3>

<h5>From this block one can learn more about how to install <b>JetTabs</b> plugin using WordPress Dashboard. </h5>

<ol class="index-list">
<p><li>First, navigate to WordPress Dashboard. Here open <b>Plugins > Add New</b> tab and click <b>Upload</b> button located at the top of the opened page.</li></p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/install2.png">
  	</figure>

<p><li>Browse the archive containing <b>JetTabs</b> plugin and click <b>Install Now</b> button.

	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/inst1.png">
  	</figure>

<div class="alert alert-info">Note, that before this step you need to unpack the package downloaded from the marketplace. It contains documentation and the very <b>.zip</b> archive you need to download in order to install JetTabs.</div>

</li></p>

<p><li>After that click <b>Activate Plugin</b> button to activate the plugin on Your site.

<div class="alert alert-info">Please, make sure that both <b>JetTabs</b> and <b>Elementor</b> plugins are active on Your site. To do it navigate to <b>Plugins > Installed Plugins</b> and view the list of installed and active plugins.</div>

</li></p>
</ol>
