<h3>JetMenu Presets</h3>

<h5>This block contains the detailed information on how to work with JetMenu presets and apply different style presets to different menu locations.

</h5>

<h3>What is a Preset</h3>

<p><b>Preset</b> is the number of previously set menu style settings which are saved and can be updated or applied to one of the menu locations on the site.
</p>

<h3>Working with Presets
</h3>

<ol class="index-list">
<p><li>In order to create a new preset, first, navigate to WordPress Dashboard and open <b>JetMenu</b>.
Here you can change the menu style according to your needs. </li></p>


<p><li>After that open <b>Presets Manager</b> tab and type in the preferable preset name in the <b>Create Preset</b> field. Then click <b>Save</b> button. You’ll see <b>New preset was created. Page will be reloaded</b> notice under the <b>Create Preset</b> option.


  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/preset/1.png">
    </figure>
</li></p>

<p><li>You can return to the saved preset anytime you need to make changes in it. To do it select the needed preset to which you want to apply changes in <b>Update Preset</b> dropdown list, and click <b>Update</b> button. Then you’ll be able to make changes in the preset you’ve chosen.
</li></p>

<p><li>To use the selected preset for all of the menu locations on your site, please, select the needed preset from the <b>Apply This Preset Globally</b> list, and then click <b>Load</b> button. The selected preset will be set for all of the menu locations on your site.
</li></p>

<p><li>If you don’t need one of the presets anymore, then select it in the <b>Delete Preset</b> list and click <b>Delete</b> button.
</li></p>

<p><li>In order to apply one of the presets to the specific menu location, please, navigate to <b>Appearance > Menus</b>.
</li></p>

<p><li>Here scroll the page down to select the locations for the menu. Check the locations for the menu to which you want to apply different presets depending on its location.


  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/preset/2.png">
    </figure>
  </li></p>

<p><li>After that locate <b>JetMenu Settings</b> block. Here you can enable JetMenu style settings for different locations. Just below the <b>Enable JetMenu</b> option you can see <b>Select menu preset</b> dropdown list. Here you can select the style preset for the specific menu location.

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/preset/3.png">
    </figure>
</li></p>

<p><li>In the case you’re working with <b>JetMenu Mega Menu</b> module, you can select different menu presets for each of the menus. To do it, please, add the <b>Mega Menu</b> module to the page created with Elementor.
In <b>Content</b> block you can select the menu style preset in the <b>Menu Preset</b> dropdown list.

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/preset/4.png">
    </figure>
</li></p></ol>

