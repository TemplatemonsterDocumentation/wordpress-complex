<h3>Enabling Ajax Search for Custom Post Types</h3>

<h5>If you have different posts types and you are looking for the way for the post types to have a connection to the search results, then JetSearch plugin is what you really need.</h5>

<H5>Let’s walk through the process of limiting search results to specific post types.</H5> 

<ul class="index-list">
<li><p>Log in to the WordPress Dashboard and proceed to the Pages directory.</p></li>
<li><p>Here, open the page where you want to add the search bar and click the <b>Edit with Elementor</b> option.</p></li>
<li><p>After that, find <b>Ajax Search</b> widget drag and drop it to page’s canvas.</p></li>
<li><p>There are the <b>Content</b>, <b>Style</b> and <b>Advanced</b> blocks. Open the <b>Content > Search Settings</b> tab in order to define that the search is to be made only in the specific posts type.
<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/search-settings.png">
    </figure></li></p>


<li><p>Hover over the <b>Search in</b> field and then the dropdown list with available posts results will appear. The posts type results there depend on what posts types you have got. For example, it might be the default <b>Posts</b> or <b>Products</b> types.</p></li>
<li><p>Set the needed post type and Ajax search bar will automatically hunt for the results within the posts type you’ve chosen.</p></li>
<li><p>Please, pay attention that if you haven’t chosen any posts type, the search will be looking for the results within the entire site.</p></li>
<li><p>The very last step is to click the <b>Update button</b> to apply the search results to the site.</p></li></li></ul>

