<h3>Working with Products Comparison</h3>

<h4>From this block you can learn how to set the products comparison with JetCompareWishlist plugin for Elementor.</h4>

<h5>1. Create a page</h5>

<p>In the <b>Dashboard > Pages</b> block create the page, where you want to display the compared products.</p>

<h5>2. Enable Comparison functionality</h5>

<p>Then go to <b>Elementor > JetCompareWishlist settings > Compare</b> block and move the toggle near <b>Enable Compare</b> option.</p>

<figure class="img-polaroid">
        <img src="projects/jetcomparewishlist/img/comparesettings.png">
    </figure>

<h5>3. Set the conditions</h5>

<p>Enable <b>Save for logged users</b> option if you want to provide logged users only with an opportunity to save their comparison lists.</p>

<h5>4. Select a page</h5>

<p>You need to define the comparison page, where you want to apply comparison functionality in <b>Compare Page</b> option and number of the items shown in Compare widget in <b>Count products to compare</b> option. Also, you can add <b>Compare button</b> to default <b>WooCommerce Single</b> and <b>Archive</b> templates. </p>


<h5>5. Adding widgets</h5>

<p>Here you can learn how to add <b>Compare Button</b>, <b>Compare Count Button</b> and <b>Compare</b> widgets to Elementor sections.</p>

<ol class="index-list">
<p><li>First, let’s open the page where you want to add the <b>Compare Button</b>, <b>Compare Count Button</b> or <b>Compare</b> widget using Elementor live page builder. To do it click <b>Edit with Elementor</b> button. </li></p>

<p><li>Add a new section to the page using <b>Add New Section</b> button.</li></p>

  <figure class="img-polaroid">
        <img src="projects/jetsearch/img/5.png">
    </figure>

<p><li>Select the preferable columns structure for the section. You can also drag the column borders in order to set the custom columns width. Feel free to edit the columns width, set the content position, etc, by editing the column.</li></p>
<figure class="img-polaroid">
        <img src="projects/jetsearch/img/6.png">
    </figure>

<p><li>On the left you can see the list of the available widgets which you can drop to the column you’ve created. Find <b>Compare Button</b>, <b>Compare Count Button</b>, or <b>Compare</b> widget and drop it to the needed area.</li></p>

  <figure class="img-polaroid">
        <img src="projects/jetcomparewishlist/img/widgetsample.png">
    </figure>

<p><li>After the page is built click <b>Update</b> button to preserve the changes.</li></p>
</li></p>
</ol>


