
<h3> Authorization Links Widget</h3>

<p>The widget allows adding the authorization links to the sections created with Elementor. </p>

<h4>Content</h4>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/authlinks.png">
  	</figure>

<h5>Login Link</h5>

<ul class="marked-list">
<p><li><b>Show Login Link</b> - enable this option in order to enable the users not to only register, but also login to the site using the provided link. </li></p>

<p><li><b>Login Page URL</b> - here you need to input the URL which will open when one clicks on the link. </li></p>

<p><li><b>Login link Text</b> - in this field one needs to input his custom text linked to the login page.</li></p>

<p><li><b>Login Icon</b> - here you can set the preferable icon to use for the login link. </li></p>

<p><li><b>Login Prefix</b> - in this field one needs to specify the text which you want to be shown before the login link and icon. </li></p></ul>

<h5>Logout Link </h5>

<ul class="marked-list">
<p><li><b>Show Logout Link</b> - enable this option in order to allow the users to use it to log out from the site. </li></p>

<p><li><b>Logout Link Text</b> - here you can specify the text to display it for the logout link. </li></p>

<p><li><b>Logout Icon</b> - this option allows to set the icon for the Logout link. </li></p>

<p><li><b>Redirect After Logout</b> - here you can set where you want to redirect the users after they logged out from the site (in the case they won’t have access to the pages where they’ve previously been (allows them to remain on the current page, redirect to the home page or specify your own custom link). </li></p>

<p><li><b>Logout prefix</b> - here you can set your own text to be displayed before the logout link. </li></p></ul>

<h5>Register Link </h5>
<ul class="marked-list">
<p><li><b>Show Register Link</b> - here you can set if you want the register link to be shown. </li></p>

<p><li><b>Register Page URL</b> - here you need to input the URL address of the page where the new user needs to register. </li></p>

<p><li><b>Register Link Text</b> - here you can set the text to be shown for the Register link. </li></p>

<p><li><b>Register Icon</b> - here you can assign the needed icon to be shown on the left of Register link.</li></p>

<p><li><b>Register Prefix</b> - in this field you can specify the text to display it before the Register link and icon.</li></p></ul>

<h5>Registered Link </h5>
<ul class="marked-list">
<p><li><b>Show Registered Link</b> - here you can define, whether you need the Registered link to be shown. </li></p>

<p><li><b>Registered page URL</b> - here you can set the URL address for the Registered page. </li></p>

<p><li><b>Registered Link Text</b> - here you can set the custom text to display for the Registered link.</li></p>

<p><li><b>Registered Icon</b> - here you can assign the needed icon to be shown on the left of Registered link.</li></p>

<p><li><b>Registered Prefix</b> -  in this field you can specify the text to display it before the Registered link and icon.</li></p></ul>

<h4>General </h4>

<h5>Order </h5>

<p>Here you can define the preferable order for the displayed links (login/logout, register/registered).</p>

