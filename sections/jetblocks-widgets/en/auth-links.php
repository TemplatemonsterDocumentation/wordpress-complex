<h5>General Styles</h5>
<ul class="marked-list">
<p><li><b>Auth Links Alignment</b> - here you can set the links alignment ot the most convenient one (left, center, right).</li></p></ul>

<h5>Login Link Styles </h5>

<h6>Link</h6>
<ul class="marked-list">
<p><li><b>Typography</b> - turn the option on to view the typography settings for the login link text.</li></p>
<p><li><b>Size</b> — define the font size of the text.</li></p>
<p><li><b>Family</b> — here you can set the font family for the text.</li></p>
<p><li><b>Weight</b> — in this block you can select the suitable font weight.</li></p>
<p><li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</li></p>
<p><li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li></p>
<p><li><b>Line Height</b> — in this field you can set the height of the text line.</li></p>
<p><li><b>Letter Spacing</b> — here you can set the space between letters.</li></p></ul>


<p>Switch between <b>Normal</b> and <b>Hover</b> modes in order to be able to customize the links appearance in normal state and when on mouse hover.</p>

<ul class="marked-list">
<p><li><b>Text Color</b> - here you can set the color of the text using the color picker.</li></p>

<p><li><b>Background Color</b> - here you can set the background color to use for the element.</li></p>

<p><li><b>Border Type</b> - here you need to specify the type of the border to use for the element.</li></p>
<p><li><b>Width</b> - here you can set the needed width to use for the border.</li></p>
<p><li><b>Color</b> - here you can specify the color to use for the border.</li></p>
<p><li><b>Border Radius</b> - here you can set the border radius for the chosen border to make the border angles more round.</li></p>
<p><li><b>Box Shadow</b> - click on the pencil icon to access the shadow options and apply the shadow for the element. The shadow settings are similar for different elements of JetBlocks widgets.</li></p>

<p><li><b>Padding</b> - here you can select the paddings to use for the links added into the widget.</li></p>

<p><li><b>Margin</b> - here you can select the margin values to use for the links added into the widget.</li></p></ul>

<h5>Prefix</h5>
<ul class="marked-list">
<p><li><b>Color</b> - here you can select the color to use for the Prefix text. </li></p>

<p><li><b>Typography</b> - turn the option on to view the typography settings for the text. The typography settings are similar to the other ones available for JetBlocks elements included into the widgets.</li></p></ul>


