<h3>Login Form </h3>

<p>This widget allows to add and customize a login form to the pages built with Elementor. </p>

<h4>Content </h4>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/login.png">
  	</figure>

<ul class="marked-list">
<p><li><b>Username Label</b> - here you can change the username label text to your custom one.</li></p>

<p><li><b>Password Label</b> - here you can change the password label to the one more suitable.</li></p>

<p><li><b>Remember Me Label</b> - here you can specify the Remember Me label according to your needs.</li></p>

<p><li><b>Log In Button Label</b> - here you can change the Log In button label to your custom one.</li></p>

<p><li><b>Redirect After Login</b> - here you can select to which page the user needs to be redirected after login (home page, stay on the current page, specify your custom URL).</li></p>

<p><li><b>Logged in Message</b> - here you can set your preferable message to make is shown when the user is logged in.</li></p></ul>

