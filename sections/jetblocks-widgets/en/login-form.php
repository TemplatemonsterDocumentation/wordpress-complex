<h5>Fields</h5>

<ul class="marked-list">
<p><li><b>Background Color</b> - here you can set the preferable color to use for the background.</li></p>

<p><li><b>Text Color</b> - here you can set the color to use for the text.</li></p>

<p><li><b>Typography</b> - turn the option on to view the typography settings for the text. The typography settings are similar to the other ones available for JetBlocks elements included into the widgets.
</li></p>
<p><li><b>Padding</b> - here you can change the padding setting to the custom ones making the paddings responsive.</li></p>

<p><li><b>Margin</b> - here you can specify the margins to apply them for the elements making them responsive.</li></p>

<p><li><b>Border Type</b> - here you can specify the suiting border type to use for the element (solid, dotted, double, dashed, groove or none).</li></p>

<p><li><b>Width</b> - here you can set the width to use for the border in px.</li></p>
<p><li><b>Color</b> - here you can specify the color to use for the border.</li></p>
<p><li><b>Border Radius</b> - here you can apply the radius for the borders to make the angles more round and smooth.</li></p>

<p><li><b>Box Shadow</b> - here you can set the shadow and customize its settings.</li></p></ul>

