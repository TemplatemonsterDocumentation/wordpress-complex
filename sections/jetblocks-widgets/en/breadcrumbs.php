<h3>Breadcrumbs</h3>

<p>This widget allows adding breadcrumbs in order to display the path to the opened page ina stylish way.</p>

<h4>Content</h4>

<h5>General Settings</h5>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/brcr.png">
  	</figure>

<ul class="marked-list">
<p><li><b>Show on Front Page</b> - check this option to enable the breadcrumbs on front page. </li></p>

<p><li><b>Show Page Title</b> - add a page title to the breadcrumbs.</li></p>

<p><li><b>Title HTML Tag</b> - here you can set the tag to use for the title (heading, div).</li></p>

<p><li><b>Show Prefix</b> - add a prefix word or symbol to be shown before the breadcrumbs path.</li></p>

<p><li><b>Prefix for the breadcrumb path</b> - input the needed prefix inside the field to use it as a prefix.</li></p>

<p><li><b>Separator Type</b> - here you can set the icon or your custom separator type which will separate each breadcrumb link from the other. If the Icon type is selected, youâ€™ll be able to set the needed icon from the icons list. In the case there is a custom separator needed, just input it in the corresponding field.</li></p>

<p><li><b>Path Type</b> - here you can choose if you want to use the full path to be shown in breadcrumbs, or the minified path showing only the first and the last breadcrumb.</li></p>

<p><li><b>Alignment</b> - here you can change the breadcrumbs alignment according to your needs. It can be left, centered, right or justified.</li></p>

<p><li><b>Order</b> - here you can specify the breadcrumbs order: the page title is to be shown before or after the breadcrumbs.</li></p></ul>

