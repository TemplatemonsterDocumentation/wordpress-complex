<h5>Settings</h5>

<ul class="marked-list">
<p><li><b>Label</b> - input the text in the corresponding field to use it as a label.</li></p>

<p><li><b>Icon</b> - here you can change the icon shown for the Cart to the one more suitable from the list of available icons.</li></p>

<p><li><b>Show Products Count</b> - here you can enable the products count to be displayed.</li></p>

<p><li><b>Products Count Format</b> - here you can change the format of the product count to the one more preferable.</li></p>

<p><li><b>Show Cart Total</b> - enable this option if you want the users to be able to see the total value of the products added into cart.
</li></p>
<p><li><b>Cart Total Format</b> - here you can specify the format of the total cart value.</li></p>

<p><li><b>Cart Dropdown Label</b> - here you can specify the text to be shown as the card dropdown label.</li></p></ul>

