<h3>Changelog</h3>

<h5>In this block you can view the latest updates for JetEngine plugin.</h5>


<h4>Version 1.2.4 (December 20, 2018)</h4>
<ul class="marked-list">
<li>Added an ability to manage Admin columns;</li>
<li>Added Text before and Text after controls for Dynamic terms widget;</li>
<li>Added an ability to enable or disable links for terms in Dynamic terms widget;</li>
<li>Added %queried_term% macros for Listing Grid widget to display related posts for the specific terms;</li>
<li>Added translations for months in Listing Calendar widget.</li>


<h4>Version 1.2.2 (November 6, 2018)</h4>
<ul class="marked-list">
<li>Added rel and target controls to Dynamic Image link;</li>
<li>Added an ability to add meta boxes only for specific posts/pages;</li>
<li>Added improvements for Checkbox meta fields processing.</li>

<h4>Version 1.2.1 (October 19, 2018)</h4>
<ul class="marked-list">
<li>Added an ability to use HTML tags in textarea meta field.</li>

<h4>Version 1.2.0 (October 18, 2018)</h4>
<ul class="marked-list">
<li>Added Calendar widget;</li>
<li>Added Posts relationships;</li>
<li>Added Columns gap for Listing Grid widget;</li>
<li>Added 'QR Code' callback to filter Dynamic Field output;</li>
<li>Added 'Format number' callback to filter Dynamic Field output;</li>
<li>Added Date query controls for Posts query settings in Listing Grid widget;</li>
<li>Added fixes for Dynamic image tag.</li>
</ul>

<h4>Version 1.1.0 (August 28, 2018)</h4>
<ul class="marked-list">
<li>Added Allow to filter values in repeater items;</li>
<li>Added Gallery meta field;</li>
<li>Added %current_id%, %current_tags% and %current_categories% macros;</li>
<li>Added WYSIWYG meta field;</li>
<li>Added Slider and grid output for gallery meta fields;</li>
<li>Added new options for Dynamic Repeater widget;</li>
<li>Added new options for Dynamic Link widget;</li>
<li>Added Embed URL callback;</li>
<li>Added  functionality for using dynamic terms images on taxonomy archives;</li>
<li>Added functionality for setting output multiple values for Select field;</li>
<li>Prevented errors on archive pages;</li>
<li>Added fixes for Meta boxes registration;</li>
<li>Added fixes for Meta fields parsing of dynamic widgets options.</li>
</ul>
