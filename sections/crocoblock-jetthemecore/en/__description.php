<h3>JetThemeCore </h3>

<p><b>JetThemeCore</b> is a powerful plugin which is aimed to help you create custom headers and footers, sections, as well as archive and single post page templates.</p>

<p>This plugin provides an access to a huge library with pre-styled content blocks via Magic Button option. You will be able to insert them into pages in a few clicks. It will be possible to create a website from scratch selecting pre-made entire pages and applying ready-to-use section templates on different topics using <b>JetThemeCore</b> functionality.</p>

