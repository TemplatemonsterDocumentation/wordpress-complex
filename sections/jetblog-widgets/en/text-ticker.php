<h6>General</h6>

<ul class="marked-list">
<li><p><b>Widget Title</b> - here you can fill in the widget title in order to give a name to the text ticker.</li></p>
<li><p><b>Title Tag</b> - select the appropriate widget title tag to use it when displaying the title (H1, H2, H3, H4, H5, H6 or div tag).</li></p>
<li><p><b>Show Current Date</b> - enable this option in order to display the current date.</li></p>
<li><p><b>Date Format</b> - here you can define the date format to use for the displayed date (e.g., F j, Y; you can learn more about displaying the date <a href="https://codex.wordpress.org/Formatting_Date_and_Time" target="_blank">here</a>). </li></p>
<li><p><b>Date Icon</b> - here you can define what icon to use for the date.</li></p>
<li><p><b>Posts Number to Show</b> - input the number of posts you want to display in Text Ticker block.</li></p>
<li><p><b>Query Posts By</b> - here you can select the query type you want to use for this widget’s content (Categories, Tags or All).</li></p>
<li><p><b>Get posts from categories</b> - here you can select which post categories you want to display using the Text Ticker widget.</li></p>

<li><p><b>Exclude Posts by IDs</b> - here you can specify the post IDs you want to exclude from showing up.</li></p>

<li><p><b>Show Post Thumbnail</b> - enable this option in order to display the thumbnail image for the post.</li></p>
<li><p><b>Thumbnail Size</b> - here you can specify the image thumbnail size in pixels.</li></p>
<li><p><b>Show Post Author</b> - enable this option in order to display the post author in the ticker.</li></p>
<li><p><b>Author Icon</b> - when the previous option is active you can select the author icon to show it on the left from the author’s username.</li></p>
<li><p><b>Show Post Date</b> - enable this option in order to display the date when the post is published.</li></p>
<li><p><b>Post Date Format</b> - here you can set the preferable date format (e.g., H:s).</li></p>
<li><p><b>Date Icon</b> - when the previous option is active, you can select the icon to display it on the left from the date when the post is published.</li></p></ul>

<h6>Slider Settings</h6>

<ul class="marked-list">
<li><p><b>Typing Effect</b> - enable this option in order to apply the typing animated effect to the text ticker line.</li></p>
<li><p><b>Typing Cursor Char</b> - here you can set the typing cursor symbol to use it in the ticker line.</li></p>
<li><p><b>Show Controls Arrows</b> - enable this option to make the arrow controls visible in order to make the navigation through the news ticker more comfortable for the visitors.</li></p>
<li><p><b>Select Control Arrows Type</b> - there you can choose which arrow controls symbols you want to use.</li></p></ul>

