<article><h2>Posts Pagination</h2>


<h6>
Posts Pagination widget makes it possible to add the pagination in order to make an easier navigation through the posts.
It is specially created for using in the templates and archives in Elementor Pro v.2.0 and with CrocoBlock.
</h6>

<h4>Content</h4>

<ul class="marked-list">
<li><p><b>Add the Previous and Next page links</b> - enable this option in order to add the links to the widget.</li></p>
<li><p><b>The older posts link text</b> - here you can change the text that will be shown as the link leading to the older posts.</li></p>
<li><p><b>The older posts link icon</b> - in this block you can specify the icon to use for the older posts link.</li></p>
<li><p><b>The newer posts text</b> - here you can change the text that will be shown as the link leading to the newer posts.</li></p>
<li><p><b>The newer posts link icon</b> - in this block you can specify the icon to use for the newer posts link.
</li></p></ul>

<h4>Style</h4>


<p>Select the most preferable alignment for the pagination elements.</p>

<p>Select the <b>Normal</b>, <b>Hover</b> or <b>Active</b> mode in order to change the background, text color and other properties when the navigation element is on hover or active.</p>

<ul class="marked-list">
<li><p><b>Background Color</b> - here you can set the specific color to use for the background using the color picker tool.</li></p>
<li><p><b>Text Color</b> - here you can pick the color for the pagination item text using the color picker tool.</li></p>
<li><p><b>Typography</b> - turn the Typography option on to view the typography settings. They are similar to the typography settings of other JetBlog modules.</li></p>
<li><p><b>Item Min Width</b> - here you can set the minimal width of the items on different devices to make them look responsive.</li></p>
<li><p><b>Padding</b> - here you can specify the padding values for the pagination for different devices.</li></p>
<li><p><b>Gap Between Items</b> - here you can specify the precise gap width between the items.</li></p>
<li><p><b>Border Type</b> - here you can select the border to apply it for the items.</li></p>
<li><p><b>Border Radius</b> - set the border radius values to make the angles of the elements more smooth.</li></p></ul>

<h5>Prev / Next Icons</h5>

<ul class="marked-list">
<li><p><b>Background Color</b> - here you can set the specific color to use for the background of the icons using the color picker tool.</li></p>
<li><p><b>Text Color</b> - here you can pick the color for the pagination items dots using the color picker tool.</li></p>
<li><p><b>Icon Box Size</b> - here you can set the size of the icon box for different devices.</li></p></li></p>
<li><p><b>Border Type</b> - here you can set the border to use it for the icon.</li></p>
<li><p><b>Border Radius</b> - here you can change the icon border radius to make its angles more smooth.</li></p>
<li><p><b>Gap Between Text and Icon</b> - here you can set the specific gap value to use it to set the space between the text and the icon itself.</li></p></ul>

<p>The Advanced settings of the Posts Pagination module are similar to the ones of the other JetBlog modules.</p>
