<h6>Items</h6>

<ul class="marked-list">
<p><li><b>Add Item</b> - click this button in order to add a new item to the playlist. </li></p>
<p><li><b>Title</b> - here you need to input the title of the video to show it in the playlist. </li></p>
<p><li><b>URL</b> - here you need to paste the video URL link in the corresponding field in order to display it in the playlist. </li></p></ul>

<h6>Settings</h6>

<ul class="marked-list">
<p><li><b>Playlist Height</b> - here you can input your preferable playlist height in pixels. </li></p>
<p><li><b>Thumbnails Orientation</b> - here you can change the video playlist thumbnails orientation from vertical to horizontal. </li></p>
<p><li><b>Thumbnails Position</b> - here you can change the thumbnail position in the playlist (left or right). </li></p>
<p><li><b>Thumbnails List Width (%)</b> - here you can drag the controls in order to change the thumbnails list width value. </li></p>
<p><li><b>Show Thumbnails Heading</b> - enable this option in order to display the thumbnails block heading. </li></p>
<p><li><b>Heading Text</b> - here you can input your custom text in the Thumbnails Heading block. </li></p>
<p><li><b>Heading Icon</b> - here you can select the ion to display it on the left from the thumbnail list title text. </li></p>
<p><li><b>Show Videos Counter</b> - enable this option in order to show how many videos are displayed from the whole playlist. </li></p>
<p><li><b>Counter Suffix</b> - here you can apply your custom text to show up as the custom counter suffix. </li></p>
<p><li><b>Show Item Number and Status</b> - enable this option in order to display the video number and status. </li></p>
<p><li><b>Show Item Duration</b> - enable this option in order to display the video duration. </li></p></ul>

