<h3>RTL Support</h3>



<p>From 1.0.2. Monstroid 2 theme supports RTL (right-to-left) localization. </p>

<p>This means that Administration panel in your theme can be translated in languages, that need RTL to be viewed correctly.</p>

<ul class="marked-list">

<p><li>To enable RTL localization proceed to Administration panel. Click on <strong>Settings</strong> tab in the left column and choose <strong>General</strong>. A <strong>General Settings</strong> window will open.</li></p>

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/basic-settings/2.png">
    </figure>

<p><li>Find <strong>Site language</strong> settings and choose a language you need to apply to your theme. </li></p>

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/basic-settings/3.png">
    </figure>

<p><li>Click on <strong> Save changes</strong> button for your changes to be applied and saved. </li></p>
</ul>

