<h3>Step 1. Theme Installation</h3>



<p>
    If you have Cherry Theme Wizard plugin installed you can proceed to the first step of your theme installation. The process is rather quick and easy, it won’t take long.
</p>

<p>
    Go to the <strong>Administration panel > Tools > Cherry Theme Wizard</strong> and a window will appear offering you to install a new theme. To start the installation process fill in the fields <strong>Template ID</strong> and <strong> Order ID </strong>.
</p>

 <figure class="img-polaroid">
        <img src="projects/cherry-wizard/img/images/2.png" alt="" >
</figure>

<p>
    The uploading and installation process will begin. It may take some time, so wait for several minutes before the theme will be completely installed to your Wordpress Engine.
</p>

<p>
    Then you’ll see a new window that will offer you to choose whether you want to automatically generate a Child Theme, or you would rather use Parent Theme.
</p>

 <figure class="img-polaroid">
        <img src="projects/cherry-wizard/img/images/3.png" alt="" >
</figure>

<p>
    A Child Theme affords you to customize it without worrying about occasionally damaging it. All the changes will be made and saved in the Child Theme.Installing a Child Theme is a safe way to do the further customization.
<p>

<p>
    When the Child Theme is generated you’ll see a window informing you that the theme was successfully installed.
</p>

 <figure class="img-polaroid">
        <img src="projects/cherry-wizard/img/images/4.png" alt="" >
</figure>




