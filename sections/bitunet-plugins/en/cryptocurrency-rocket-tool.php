<h3>Cryptocurrency Rocket Tool Plugin</h3>

<p>Cryptocurrency Rocket Tools plugin was specially developed for displaying cryptocurrency options online to provide visitors with the recent updates, rates, and prices. This plugin is compatible with all cryptocurrencies from <a href="https://www.cryptocompare.com/" target="_blank">Cryptocompare</a>. </p>


<h4>Installation</h4>
<ul class="marked-list">
<p><li>Unzip <strong>cryptocurrency-rocket-tools.zip</strong> folder;</li></p>
<p><li>Upload the <strong>cryptocurrency-rocket-tools</strong> folder to your <strong>/wp-content/plugins</strong> directory;</li></p>
<p><li>In your WordPress dashboard, head over the <strong>Plugins</strong> section;</li></p>
<li><p>activate Cryptocurrency Rocket Tools.</li></p>
</li>