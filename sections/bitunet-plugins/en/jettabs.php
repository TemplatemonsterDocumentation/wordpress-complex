<h3>JetTabs Plugin</h3>

<p><b>JetTabs</b> is a plugin that allows adding stylish tabs and accordion widgets with vertical and horizontal layouts and building content inside them using Elementor live page builder widgets. </p>

<p>The plugin makes it simple to create a template with Elementor and add it to the <b>Accordion</b> or <b>Tabs</b> widget. It helps organize content and style it up according to one’s needs and preferences. </p>

<p>While using JetTabs you’ll be astonished how easy You can create content and place it into well-structured tabs and accordion blocks, making Your web page look neat and classy at the same time.</p>

    <div class="alert alert-info">
        You can find the detailed documentation on JetTabs plugin <a href="http://documentation.zemez.io/wordpress/index.php?project=jettabs" target="_blank">here</a>.</div>

<h5>Widgets Overview</h5>

<ul class="marked-list">
<li><p><a href="https://jettabs.zemez.io/tabs/" target="_blank"><b>Tabs</b></a> - grasp the opportunity to create tabs and enrich them with all kinds of Elementor-built content using gorgeous Tabs widget, which makes it possible to organize content in a smart way and make it look way more stylish. </p></li>

<li><p><a href="https://jettabs.zemez.io/classic-accordion/" target="_blank"><b>Classic Accordion</b></a> - acordion blocks will become the milestones of your creative work if you’re into optimizing your webpage’s looks and want to create classy blocks with unfolding content created with Elementor. </p></li>

<li><p><a href="https://jettabs.zemez.io/image-accordion/" target="_blank"><b>Image Accordion</b></a> - explore the opportunity to enrich accordion blocks with vivid imagery. The widget is specially cut for building creative and eye-catching content, making it possible to place all good things at once in one place.</p></li></ul>

