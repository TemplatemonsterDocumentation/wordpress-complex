<h3>Cryptocurrency Price Ticker Widget</h3>

<p>Cryptocurrency Rocket Tools plugin displays current prices of crypto coins â€“ bitcoin, litecoin, ethereum, ripple, dash etc. </p>


<h4>Installation</h4>
<ul class="marked-list">
<p><li>Open WordPress admin panel and go to <strong>Plugins</strong></li></p>
<p><li>Add <strong>New</strong> and Search <strong>Cryptocurrency price ticker widget</strong>;</li></p>
<p><li>Click on <strong>Install</strong> button and activate the plugin.</li></p>


<h4>Creating Crypto Ticker Widget</h4>
<ul class="marked-list">
<p><li>Go to a <strong>Crypto Widget</strong> tab in sidebar of your WordPress admin dashboard;</li></p>
<p><li>Click on <strong>Add New</strong> and generate your first crypto price ticker shortcode by using all settings at that page;</li></p>
<p><li>You are good to go and use this shortcode anywhere inside your website to show Crypto Price Ticker Widget!</li></p>