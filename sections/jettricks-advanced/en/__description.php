<h3>Getting Started</h3>

<h5>This block uncovers the detailed process of preparing templates with Elementor live page builder to embed them into the <b>tabs</b> and <b>accordion</b> blocks created with JetTabs plugin. </h5>

<ol class="index-list">
<p><li>Navigate to <b>Pages > Add New</b> tab in the left column of the WordPress Dashboard. Click on <b>Edit with Elementor</b> button. You’ll be redirected to Elementor editor page.</li></p>

	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/start1.png">
  	</figure>

<p><li>When Elementor editor will open, you’ll see the block containing <b>Add New Section</b> button at the right part of the screen, where your future page’s draft is displayed.</li></p>

	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/start2.png">
  	</figure>

<p><li>Click on <b>Add New Section</b> to add the section, where you will place JetElements module.</li></p>

<p><li>Now you’re ready to add JetElements modules to the columns. Choose one of the elements you want to add to the page, and drag it to the column area.</li></p>

	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/start3.png">
  	</figure>

<p><li>In the left column you’ll see the element <b>settings</b>. You can manage <b>content</b>, <b>style</b>, or use <b>advanced</b> styling options from here.

<div class="alert alert-info">Keep in mind, that for different elements the options may vary. Each element has its own styling and content management settings.</div>

	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/start4.png">
  	</figure>

<ol class="marked-list">
<li><b>Edit Widget</b> - this option provides fast access to module editing block in the left column of the page editing screen.</li>
<li><b>Duplicate Widget</b> - use this option to duplicate the existing module with all its settings and content.</li>
<li><b>Remove Widget</b> - this option removes the module from the page.</li></ol>
</li></p>

	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/start5.png">
  	</figure>

<p><li>When everything is ready click the arrow on the right of the <b>UPDATE</b> button and here select <b>Save as Template</b> option.</li></p>

	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/start6.png">
  	</figure>

<p><li>A <b>Save Your Page to Library</b> popup window will appear. Here input the preferable name in the <b>Enter Template Name</b> field and then click <b>Save</b> button in order to save it for further using.</li></p>

	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/start7.png">
  	</figure>

<p><li>Create as many templates as You want to use for different tabs and accordion blocks.</li></p>

