<h3>Installation via Dashboard</h3>


<p>1. Navigate to WordPress Dashboard > Elementor > My Templates.</p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/1.png">
    </figure>
<p>2. Locate Import Templates button and click on it. </p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/2.png">
    </figure>

<p>3. Click Choose File button and select json file from your hard drive and click Import Now button.</p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/3.png">
    </figure>

<p>4. Your template will be added to the list. Click Edit with Elementor to start customizing. </p>
