<h3>Installation via Elementor</h3>


<p>1. Open Elementor editing page and locate Add Template button. Click on it.</p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/1.png">
    </figure>
<p>2. In the opened Library window navigate to My Templates.</p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/2.png">
    </figure>

<p>3. To import your template click corresponding icon.</p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/3.png">
    </figure>

<p>4. In the opened window you will be offered to either drag and drop your file or select it from your hard drive by clicking Select File button.</p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/3.png">
    </figure>


<p>5. Once you selected json file you will find it in end of the list in your Library.</p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/3.png">
    </figure>

<p>6. Click Insert button to embed it to your page.</p>
 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/3.png">
    </figure>

    <p>Now you are free to customize it in Elementor!</p>