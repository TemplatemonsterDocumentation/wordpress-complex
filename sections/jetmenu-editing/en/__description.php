<h3>Editing Menu with JetMenu</h3>

<h5>From this block you’ll learn how to create and edit a menu using JetMenu plugin. </h5>

<ol class="index-list">
<p><li>First, let’s navigate to <b>Appearance > Menus</b> tab on the left of WordPress Dashboard.


 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-editing/1.png">
  	</figure></li></p>

<p><li>Here you need to select the menu you want to edit from the menus dropdown list. Then click <b>Select</b> button.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-editing/2.png">
  	</figure></li></p>

<p><li>Locate JetMenu Settings block. Here you should enable JetMenu for current menu location option. Then click <b>Save</b> button to save the changes.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-editing/4.png">
  	</figure></li></p>
</li></p>

<p><li>Now let’s edit your existing menu with JetMenu plugin. Locate <b>Menu Structure</b> block and hover on one of the menu items. You’ll see <b>JetMenu</b> button on the right of the menu item title. Click on it to edit the item with JetMenu.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-editing/3.png">
  	</figure></li></p>
</li></p>

<p><li>A popup window will appear. It consists of several tabs: <b>Content, Icon, Badges</b> and <b>Misc</b>. Let’s overview each one of them.</li></p></ol>

<h6>Content</h6>

<ol class="index-list">
<p><li>Enable <b>Mega Submenu</b> option in order to create a submenu for the item.</li></p>

<p><li>After that click <b>Edit Mega Menu Item Content</b> button to open the JetMenu content editor.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-editing/16.png">
  	</figure></li></p>
</li></p>

<p><li>A new window will open. It is similar to the one you see when proceeding to edit with Elementor.</li></p>

<p><li>Click on <b>Add New Section</b> button to create a new section for the menu.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-editing/6.png">
  	</figure>

</li></p>
<p><li>Select the preferable section structure.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-editing/7.png">
  	</figure>

</li></p>

<p><li>Drop the content modules you want to add to the menu item (<b>image, video, button, text, icon, animated elements,</b> etc.). You can add the default Elementor content modules as well as JetElements ones.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-editing/10.png">
  	</figure>

</li></p>
<p><li>You can customize the module’s  content and style the same way you do it when creating content with Elementor. Please, feel free to check the <a href="https://wordpress.org/plugins/elementor/" target="_blank">Elementor</a> documentation and <a href="http://documentation.zemez.io/wordpress/index.php?project=jetelements" target="_blank">JetElements</a> documentation in order to learn how to do it.


 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-editing/11.png">
  	</figure>

</li></p>

<p><li>When everything is done, click <b>Save</b> button to save the applied changes.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-editing/12.png">
  	</figure>

</li></p>

<p><li>After that click on <b>Back</b> icon in the bottom left corner to return to the <b>JetMenu Content</b> popup window.</li></p>
<p><li>Don’t forget to click <b>Save</b> to save the changes.</li></p></ol>

<h6>Icon</h6>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-editing/13.png">
  	</figure>

<p>In this block you can set the JetMenu item icon and specify the icon color.</p>

<ol class="index-list">
<p><li>Click on <b>Menu Icon</b> block to view the list of all the available icons and select the one you need.</li></p>
<p><li>Then specify the icon color in the <b>Icon Color</b> block using color picker tool.</li></p></ol>

<h6>Badges</h6>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-editing/14.png">
  	</figure>

<ul class="marked-list">
<p><li>In order to use the badge for the menu item switch to <b>Badges</b> tab. Here you can specify the item badge title, badge color and badge background color.</li></p>
<p><li><b>Menu Badge</b> - here you need to fill in the title for the menu item badge.</li></p>
<p><li><b>Badge Color</b> - set the menu item badge color using color picker.</li></p>
<p><li><b>Badge Background Color</b> - here you can set the badge background color using color picker tool.</li></p></ul>

<h6>Misc</h6>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-editing/15.png">
  	</figure>

<p>In this block you can hide or show the menu item navigation label. </p>

<ol class="index-list">
<p><li>Enable <b>Hide Item Navigation Label</b> option in order to remove the navigation label from the menu item.</li></p>

<p><li>Click <b>Save</b> button in the bottom right corner to save the changes.</li></p></ol>

