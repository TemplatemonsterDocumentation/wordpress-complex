<h2>Cherry Shortcodes</h2>



<p>To use Cherry Tesimonials at its fullest try to apply settings to your testimonials with the help of shortcodes.
</p>

<p>A shortcode cal look on the backend like this:</p>


<p>[tm_testimonials type="list" sup_title="" title="" sub_title="" limit="3" orderby="date" order="DESC" category="" ids="0" content_length="55" divider="off" show_avatar="on" size="100" show_email="on" show_position="on" show_company="on" autoplay="7000" effect="slide" loop="on" pagination="on" navigation="on" slides_per_view_phone="1" slides_per_view_tablet="1" slides_per_view_laptop="1" slides_per_view="1" space_between_phone="15" space_between_tablet="15" space_between_laptop="15" space_between="15" template="default.tmpl" custom_class=""]</p>


<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Layout</dt>
            <dd>
                Pick the needed layout for testimonials: default or boxed
            </dd>
        </dl>
    </li>
</ul>
