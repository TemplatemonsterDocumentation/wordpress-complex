<h5>Добавить виджет</h5>

<p>Для того что бы добавить виджет на страницу шаблона, необходимо предварительно определится с его позиционированием выбрав одну из нескольких Widgets Areas. Затем нажать кнопку "+ Add a Widget", появившемся списке виджетов. После того, как вы добавили виджеты, можно назначить порядок их вывода нажав кнопку "Reorder".</p>

