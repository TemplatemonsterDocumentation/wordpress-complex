<h3>Settings</h3>

<p>Navigate to <b>Woostroid2 > Settings</b> to view the <b>JetThemeCore</b> settings and manage them. </p>

			<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/dashboard/settings12.png">
  	</figure>

<ul class="marked-list">
	<p><li><b>Locations Relations</b> - here you can manage the preferences for templates created with JetThemeCore and Elementor Pro attached to the same locations. There are several variations available: </p>
<ul class="marked-list">
	<li>show both, <b>Jet</b> before <b>Pro</b>;</li>
	<li>show both, <b>Pro</b> before <b>Jet</b>;</li>
	<li><b>Jet</b> overrides;</li>
	<li><b>Pro</b> overrides.</li></ul></li>

	<p><li><b>Prevent Pro locations registration</b> - enable this option if your header/footer disappear when JetThemeCore is active.</li></p>

	<p><li><b>Use nucleo-mini icon package</b> - enable this option in order to Elementor icon picker control. </li></p></ul>

