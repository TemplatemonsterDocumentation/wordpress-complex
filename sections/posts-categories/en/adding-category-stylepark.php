<h3>Adding a Category</h3>

<p>To add a category you need to do the following:</p>

<ol class="index-list">
    <li>Open the <strong>Posts</strong> tab.</li>
    <li>Click <strong>Categories</strong> to see the list of all categories.</li>
    <li>Enter new category name.</li>
    <li>Click <strong>Add New Category</strong>.</li>
    <li>Click <strong>Publish</strong>.</li>
</ol>

