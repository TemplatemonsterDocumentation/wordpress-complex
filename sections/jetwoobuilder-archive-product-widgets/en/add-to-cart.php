<h6>Add to Cart</h6>


<ul class="marked-list">
	<li><p><b>Typography</b> — turn the option on to view the typography settings for the button text.
		<ul class="marked-list">
			<li><b>Size</b> — define the font size of the button label text.</li>
			<li><b>Family</b> — here you can set the font family for the button label text.</li>
			<li><b>Weight</b> — in this block you can select the suitable font weight.</li>
			<li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</li>
			<li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li>
			<li><b>Line Height</b> — in this field you can set the height of the  text  line.</li>
			<li><b>Letter Spacing</b> — here you can set the space between letters.</li></ul>
	</p></li>
	<li><p><b>Button Width</b> — here you can define the width of the button.</p></li>
</ul>

<p>Switch from <b>Normal</b> to <b>Hover</b>, <b>Added</b> or <b>Loading</b> modes to set the color scheme and box shadow for the button when in normal state, on mouse hover or when the purchase is disabled for the customers.</p>

<ul class="marked-list">
	<li><p><b>Text Color</b> - here you can set the color of the button label text using the color picker.</p></li>

	<li><p><b>Background Color</b> - here you can set the background color to use for the button.</p></li>

	<li><p><b>Box Shadow</b> - click on the pencil icon to access the shadow option and apply the shadow for the button.</p>

	<p><b>Shadow Settings:</b></p>

		<ul class="marked-list">
			<li><b>color</b> — here you can set the color for the the shadow (use color picker tool to do it);</li>
			<li><b>blur</b> — in this block you can define the blur settings for the shadow;</li>
			<li><b>spread</b> — here you can set how large the shadow will be;</li>
			<li><b>horizontal</b> — here you can move the shadow horizontally (from right to left);</li>
			<li><b>vertical</b> — here you can move the shadow vertically (from top to bottom);</li>
			<li><b>position</b> — here you can apply the shadow to the inside of the button or to the outside of it.</li>
		</ul>
	</li>

	<li><p><b>Border Type</b> - here you can select one of the available border types to use for the button: none, solid, double, dotted, dashed or groove.</p>

		<ul class="marked-list">
			<li><b>Width</b> - here you can define the width of the border in px.</li>
			<li><b>Color</b> - here you can change the color of the borders.</li>
			<li><b>Border Radius</b> - here you can set the border radius for the chosen border to make the border angles more round.</li>
		</ul>
	</li>

	<li><p><b>Padding</b> - here you can set the padding values for the text placed inside the button.</p></li>
	<li><p><b>Margin</b> - here you can define the margins applied for the whole button contained in the column.</p></li>
</ul>

