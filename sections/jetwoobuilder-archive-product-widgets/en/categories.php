<h5>Categories</h5>

<p><b>Typography</b> - here you can edit the typography settings. They are the same as available for other JetWooBuilder widgets.</p>

<p>Switch between Normal and Hover modes in order to customize the appearance of both.</p>

<ul class="marked-list">
	<li><p><b>Background Type</b> - here you can specify if you want to use the classic or gradient background and change the background style with the available options (they are similar for all JetWooBuilder widgets).</p></li>

	<li><p><b>Color </b>- here you can specify the color to apply for the categories.</p></li>
	
	<li><p><b>Alignment</b> - here you can specify the needed alignment for the categories (left, center, right).</p></li>


</ul>