<article><h2>Price</h2>


<h6>
This widget makes it easy to embed the product price and sale price to the Archive Product page. One can also customize the prices along with the currency signs.
</h6>



<h4>Style</h4>

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/archive-price.png">
    </figure>

<h5>Price</h5>

<ul class="marked-list">
	<li><p><b>Typography</b> — turn the option on to view the typography settings for the product price.
		<ul class="marked-list">
			<li><b>Size</b> — define the font size of the product price.</li>
			<li><b>Family</b> — here you can set the font family for the product price.</li>
			<li><b>Weight</b> — in this block you can select the suitable font weight.</li>
			<li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</li>
			<li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li>
			<li><b>Line Height</b> — in this field you can set the height of the product price.</li>
			<li><b>Letter Spacing</b> — here you can set the space between the product price characters.</li>
		</ul>
	</p></li>


	<li><p><b>Color</b> - here you  can change the general color which is applied to the prices.</p></li>

	<li><p><b>Space Between Prices</b> - use the controls to set the space between the prices (regular and sale price) and make it responsive.</p></li>

	
</ul>

<p>Switch between <b>Regular</b> and <b>Sale</b> modes in order to customize both product prices.</p>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can change the color to use it for the price.</p></li>

	<li><p><b>Text Decoration</b> - here you can add the text decorations for the prices (none, line through, e.g., when the regular price is no more active, and underline to make the price stand out.</p></li>

	<li><p><b>Size</b> - here you can set the needed size for the prices to make them responsive.</p></li>

	<li><p><b>Font Weight</b> - here you can specify the font weight to use for the prices.</p></li>

	<li><p><b>Alignment</b> - here you can change the price alignment to the more preferable one.</p></li>
</ul>

<h6>Surrency Sign</h6>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can specify the color to use for the currency sign only.</p></li>

	<li><p><b>Size</b> - here you can set the size to use for the currency sign to make it responsive.</p></li>

	<li><p><b>Vertical Alignment</b> - here you can change the currency sign alignment to position it beside the price value.</p></li>
</ul>

<p>Switch between <b>Regular</b> and <b>Sale</b> mode in order to customize both sale and regular prices.</p>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can change the color of the currency sign.</p></li>

	<li><p><b>Size</b> - use the controls in order to set the size of the currency sign and make it responsive.</p></li>
</ul>
