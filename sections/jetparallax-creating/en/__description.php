<h3>Creating Parallax Effect</h3>

<h5>This block uncovers the detailed process of creating Parallax effect for the sections created with Elementor.</h5>

<p>Here is the sample of the section built with JetParallax effects.</p>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/parallax2.png">
    </figure>

<p><li>After You've opened <b>Jet Parallax</b> tab in <b>Layout settings</b> of the section You need to click <b>Add Item</b> button to add a new item with the Parallax effect.</li></p>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/par4.png">
    </figure>

<p><li>You can see the list of the available <b>Parallax settings</b> below.

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/par5.png">
    </figure>

<ul class="marked-list">
<p><li>Click on the newly created item and there click <b>Image</b> to add a new image from the media library or upload it from your hard drive.</li></p>
<p><li>To change the speed of the parallax effect you need to use the <b>Parallax Speed</b> control. Drag it leftwards to reduce the speed, or rightwards to increase it.</li></p>
<p><li>Select the parallax type from the <b>Parallax Type</b> dropdown list.
<ul class="marked-list">
<p><li><b>None</b> - select this parallax type not to use parallax effects for this element.</li>
<li><b>Scroll</b> - select this type to apply the parallax effect on scroll.</li>
<li><b>Mouse Move</b> - select this type to make the parallax effect respond to the mouse moves.</li></p></ul></li>

<p><li><b>z-index</b> - the option allows to move the object or parallax layer using z-axis, zoom it in on mouse move, etc, use 3D effect in animation.</li></p>

<p><li><b>Background X Position %</b> - define the item’s horizontal position in percents in relation to the background.</li></p>
<p><li><b>Background Y Position %</b> - define the item’s vertical position in percents in relation to the background.</li></p>
<p><li><b>Size</b> - here you can select the preferable size for the item (<b>Auto, Cover</b> or <b>Contain</b>).</p>

<p>Note, that you can add as many items with different parallax settings (speed, position), as you need.</p>

</li></ul>

<p><li>In order to change the <b>order of the parallax items</b> <b>drag</b> and <b>drop</b> the items up and down. The first item from the list will be shown above all the other items, and cover them. The last item from the list will be shown below all the other item layers.

<ul class="marked-list">
<p><li><b>Z-Index</b> - use this option if you want the parallax item to cover the section’s content.

</li></p></ul>
</li></p>

<p><li><b>Animation Property</b> option allows to set the specific type of animation. </p>

<p> Note, that the <b> Transform</b> animation property is highly recommended to use.</p>

<p>Here is the list of the available animation properties:

  <ul class="marked-list">
    <li><b>Background Position</b> - is the property, which allows the image to move within its occupied area.</li>
    <li><b>Transform</b> - allows the image to move around its occupied area.</li>
    <li><b>Transform 3D</b> - allows the image to move within its occupied plane.</li></ul>

<p><li>To view the changes, please, click <b>Save</b> button to save the changes and click <b>View Page</b> option in the bottom left corner to navigate to the page and see the results. </li></p></ol>




