<h3>Changelog</h3>

<h5>In this block you can view the latest updates for JetSmartFilters plugin.
 </h5>

<h4>Version 1.2.0 (January 25, 2019)</h4>

<ul class="marked-list">
<li>Added separate widget for Apply button;</li>
<li>Added support for Elementor Pro Posts widget;</li>
<li>Added support for Elementor Pro Archive widget;</li>
<li>Added new options for Pagination widget;</li>
<li>Added minor bug fixes and performance improvements.</li></ul>

<h4>Version 1.1.0 (November 30, 2018)</h4>

<ul class="marked-list">
<li>Added RU localization;</li>
<li>Added an ability to edit or disable prev/next controls in Pagination widget;</li>
<li>Added step, number format and suffix options for range and check range filters;</li>
<li>Added an ability to search by meta field in search filter;</li>
<li>Added an ability to run Elementor ready tiggers after appling filters;</li>
<li>Added an ability to filter the same query variable by multiple filters.</li>
<li>Added RU localization.</li></ul>


