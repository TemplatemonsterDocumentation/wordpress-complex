<h3>General Requirements</h3>

<h4>Software</h4>
<p>Before you start working with the WordPress template, you should download the required software. You can check the required software on the template preview page.</p>

<p>To upload the files to a hosting server, you will need an FTP manager like Total Commander, FileZilla, CuteFTP, etc.	</p>

<h4>Hosting</h4>
<p>Since WordPress CMS is a PHP/MySQL platform, you need to have the hosting server prepared for it.</p>
<p>In case you already have a hosting server, you need to check whether it is compatibile with WordPress hosting requirements or not. In other words, whether you can host a WordPress website with it.</p>
<p>This theme itself requires Apache or Nginx hosting servers.</p>
<p>To eliminate <strong>Fatal error: Maximum execution time of 30 seconds exceeded,</strong> modify the value of “max_execution_time” to a greater value say 600.</p>
<p>We also recommend to use the following <strong>configuration settings</strong>:


<ul class="marked-list">
	<li><p>50 MB of disk space;</p></li>
	<li><p>Memory limit per process: 64mb (128mb or more recommended).</p></li>
</li>
</ul>

<h4>PHP and MySQL</h4>
<p>Minimal required version of PHP is 5.2.4 and MySQL 5. PHP 5.2 is already not safe as contains critical vulnerabilities that can be used to harm your website. Some Theme extensions will not work with PHP 5.2 and require version 5.4 or later.</p>

<p>Recommended settings are:</p>

<ul class="marked-list">
	<li><p>PHP 5.4;</p></li>
	<li><p>MySQL 5.5 or later;</p></li>
	<li><p>mod_rewrite;</p></li>
	<li><p>php fopen;</p></li>
	<li><p>suPHP.</p></li>
</ul>

<p>You can also install WordPress on your PC or laptop through a local server. You can use the following software to create a local server: <strong>WAMP, AppServ, MAMP</strong>, etc. All of these support WordPress and can be installed as a regular software.</p>

