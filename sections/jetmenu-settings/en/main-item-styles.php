<h3>Main Menu Item Styles</h3>

<h5>Navigate to Main Menu Item Styles tab to access JetMenu styles settings for the Main Menu.  </h5>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/basic-settings/8.png">
  	</figure>


<ul class="marked-list">
<p><li><b>Top level menu typography</b> - enable this option in order to access the typography settings, such as <b>font size, font family,</b> etc.
<ul class="marked-list">
<li><b>Top level menu font size</b> - use controls to adjust the menu font size to your needs.</li>
<li><b>Top level menu font family</b> - select the font to use for the top level menu.</li>
<li><b>Top level menu font weight</b> - here you can specify the weight to apply to the font (from <b>1000</b> to <b>9000</b>).</li>
<li><b>Top level menu text transform</b> - here you can make all of the menu items <b>uppercase, lowercase, capitalize</b> or <b>normal</b>.</li>
<li><b>Top level menu font style</b> - here you can select the style of the menu items font (<b>normal, italic</b> or <b>oblique</b>).</li>
<li><b>Top level menu line height</b> - use the controls to specify the height of the menu text lines.</li>
<li><b>Top level menu letter spacing</b> - use the controls to set the preferable letter spacing for the menu text.</li>
<li><b>Top level menu subset</b> - here you can define, which subset you want to use for the menu (<b>latin, greek</b> or <b>cyrillic</b>).</li></ul></li></p>
<p><li><b>Show item Description</b> - enable this option in order to display the item decription in the cvase it is added for the menu item.</li></p>
<p><li><b>Top level menu description typography</b> - enable this option to change the Menu Description typography settings. The typography settings are similar to those of the the <b>Top level menu typography</b> block.</li></p></ul>

<p>There are three different modes allowing you to customize <b>Main Menu</b> in its normal state, when on hover and when active: <b>Default, Hover</b> and <b>Active</b>. Switch between them to apply the separate style settings to <b>Main Menu</b> when on hover or active.</p>

<ul class="marked-list">
<p><li><b>Item text color</b> - here you can specify the color to use it for the menu item text.</li></p>
<p><li><b>Item description color</b> - here you can specify the color to use it for the menu item description text.</li></p>
<p><li><b>Item icon color</b> - choose the item icon color using color picker tool.</li></p>
<p><li><b>Item drop-down arrow color</b> - here you can select the color for the arrow that opens drop-down menu.</li></p>
<p><li><b>Item background</b> - enable this option in order to access the item background settings. These are quite similar to the <b>Mega Menu Container Background</b> settings.</li></p>
<p><li><b>Item border</b> - enable this option to use the border for the menu item.</li></p>
<p><li><b>Item border style</b> - here you can select the style of the applied border (<b>solid, double, dotted, dashed</b>).</li></p>
<p><li><b>Item border width</b> - here you need to specify the border width in <b>px</b>.</li></p>
<p><li><b>First item border</b> - enable this option to add a border to the first menu item. You can as well change its border's width and color, as in the general border settings.</li></p>
<p><li><b>Last item border</b> - enable this option to add a border to the last menu item. You can as well change its border's width and color, as in the general border settings.</li></p>
<p><li><b>Item border color</b> - use color picker tool to set the preferable color for the applied border.</li></p>
<p><li><b>Item box shadow</b> - enable this option to use the shadow for the menu items. When enabled, the option will give you access to the shadow options, which are similar to <b>Main Menu Container</b> Shadow options.</li></p>
<p><li><b>Item border radius</b> - here you can apply the custom border radius for the menu items.</li></p>
<p><li><b>Item padding</b> - set the padding for the menu items in <b>px</b>.</li></p>
<p><li><b>Item margin</b> - here you can define the values to use for the margins in <b>px</b>.</li></p></ul>
