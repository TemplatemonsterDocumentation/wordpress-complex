<h3>Sub Menu Item Styles</h3>

<h5>Switch from customizing Simple Sub Panel to Mega Sub Panel by clicking on the corresponding tabs. </h5>

<h6>Simple Submenu Panel</h6>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/basic-settings/2.png">
  	</figure>


<ul class="marked-list">

<p><li><b>Panel width</b> - use the controls to define the preferable simple sub panel width.
<p><li><b>Panel background</b> - enable this option to access the extended background options for this block. They are quite similar to the <b>Mega Menu Container Background</b> settings.</li></p>

<p><li><b>Panel border</b> - enable this option in order to use the border for the Menu Sub Panel.
<ul class="marked-list">
<li><b>Panel border style</b> - here you can select the style for the sub panel border (<b>solid, double, dotted, dashed</b>).</li>
<li><b>Panel border width</b> - here you can specify the values for the border width of the Menu Sub Panel.</li>
<li><b>Panel border color</b> - here you can define the color of the sub panel using color picker tool.</li></ul></li></p>

<p><li><b>Panel box shadow</b> - enable this option in order to create the shadow around this element and access the shadow settings.
<ul class="marked-list">
<li><b>Panel - position of the horizontal shadow</b> -  here you can use the controls to set the position of the horizontal shadow.</li>
<li><b>Panel - position of the vertical shadow</b> - here you can use the controls to set the position of the vertical shadow.</li>
<li><b>Panel - shadow blur distance</b> - use the controls to define the extent of shadow blur.</li>
<li><b>Panel - shadow size</b> - here you can define the size of the dropped shadow.</li>
<li><b>Panel shadow color</b> - define the color of the shadow using color picker tool.</li>
<li><b>Panel shadow inset</b> - here you can enable the shadow inset for the menu container.</li></ul></li></p>

<p><li><b>Panel border radius</b> - in this block you can specify the value for the border radius.</li></p>
<p><li><b>Panel padding</b> - set the padding for the menu items in <b>px</b>.</li></p>
<p><li><b>Panel margin</b> - here you can define the values to use for the margins in <b>px</b>.</li></p>

<p><li><b>Panel shadow color</b> - here you can define the color of the panel shadow.</li></p>
<p><li><b>Panel shadow inset</b> - here you can enable the shadow inset by turning the option on.</li></p>


<p><li><b>Submenu typography</b> - enable this option to change the <b>Submenu typography</b> settings. The typography settings are similar to those of the the <b>Top Level Menu Typography</b> block.</li></p>
<p><li><b>Show Submenu Item Description</b> - here you can enable or disable item descriptions to hide them from being shown for submenu items.</li></p>
<p><li><b>Submenu descriptions typography</b> - enable this option to change the <b>Submenu Descriptions typography</b> settings. The typography settings are similar to those of the the <b>Top Level Menu Typography</b> block.</li></p>

<p>When customizing <b>Simple Sub Menu</b> styles you can switch from <b>Default</b> to <b>Hover</b> or <b>Active</b> mode to create several styles for the sub menu when on hover or active.</p>

<ul class="marked-list">
<p><li><b>Item text color</b> - here you can set the color to use it for the sub items text.</li></p>
<p><li><b>Item descriptions color</b> - here you can set the color to use it for the sub item description text.</li></p>
<p><li><b>Item icon color</b> - specify the color to use for the item icons.</li></p>
<p><li><b>Item drop-down arrow color</b> - specify the color to use for the item drop-down arrow symbols.</li></p>
<p><li><b>Submenu background</b> - enable this option in order to access the background options for the sub menu. These are quite similar to the <b>Mega Menu Container Background</b> settings.</li></p>

<p><li><b>Submenu border</b> - enable this option in order to use the border for the submenu.
<ul class="marked-list">
<li><b>Submenu border style</b> - here you can select the style for the submenu border (<b>solid, double, dotted, dashed</b>).</li>
<li><b>Submenu border width</b> - here you can specify the values for the border width of the sub menu.</li>
<li><b>Submenu border color</b> - here you can define the color of the submenu using color picker tool.</li></ul></li></p>

<p><li><b>Submenu box shadow</b> - enable this option in order to create the shadow around this element and access the shadow settings.
<ul class="marked-list">
<li><b>Submenu - position of the horizontal shadow</b> - here you can use the controls to set the position of the horizontal shadow.</li>
<li><b>Submenu - position of the vertical shadow</b> - here you can use the controls to set the position of the vertical shadow.</li>
<li><b>Submenu - shadow blur distance</b> - use the controls to define the extent of shadow blur.</li>
<li><b>Submenu - shadow size</b> - here you can define the size of the dropped shadow.</li>
<li><b>Submenu shadow color</b> - define the color of the shadow using color picker tool.</li>
<li><b>Submenu shadow inset</b> - here you can enable the shadow inset for the submenu.</li></ul></li></p>


<p><li><b>Item border radius</b> - in this block you can specify the value for the border radius.</li></p>
<p><li><b>Item padding</b> - set the padding for the submenu items in <b>px</b>.</li></p>
<p><li><b>Item margin</b> - here you can define the values to use for the margins in <b>px</b>.</li></p>

