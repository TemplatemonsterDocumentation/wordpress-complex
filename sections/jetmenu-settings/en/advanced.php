
<h3>Advanced Settings</h3>

<h5>In this block you can switch from customizing Icon to Badge or Drop-down Arrow. Just click on the corresponding tab to open the element settings. </h5>

<h6>Icon</h6>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/basic-settings/4.png">
  	</figure>


<p>From here you can customize <b>Top Level Icon</b> and <b>Sub Level Icon</b>. To proceed to customizing <b>Sub Level Icon</b> click on the corresponding block to open the settings.</p>

<ul class="marked-list">
<p><li><b>Icon size</b> - use the controls to set the preferable item icon size.</li></p>
<p><li><b>Icon margin</b> - here you can specify the values of the icon margins in <b>px</b>.</li></p>
<p><li><b>Icon position</b> - select the needed vertical position of the icon (<b>center, top, bottom</b>).</li></p>
<p><li><b>Icon horizontal position</b> - here you can specify the icon horizontal position (<b>center, left, right</b>).</li></p>
<p><li><b>Icon order</b> - use the controls to set the icon order.</li></p></ul>

<h6>Badge</h6>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/basic-settings/5.png">
  	</figure>


<p>From here you can customize <b>Top Level Badge</b> and <b>Sub Level Badge</b>. To proceed to customizing <b>Sub Level Badge</b> click on the corresponding block to open the settings.</p>

<ul class="marked-list">
<p><li><b>Badge typography</b> - enable this option to change the menu badge typography settings. The typography settings are similar to those of the the <b>Top Level Menu Typography</b> block.</li></p>
<p><li><b>Badge text color</b> - here you can specify the badge text color using color picker tool.</li></p>
<p><li><b>Badge background</b> - enable this option in order to access the badge background settings. These are quite similar to the <b>Mega Menu Container Background</b> settings.</li></p>

<p><li><b>Badge border</b> - enable this option to use the menu badge border.
<ul class="marked-list">
<li><b>Badge border style</b> - select, if you want to use a <b>solid</b>, a <b>dotted</b>, a <b>double</b> or the <b>dashed</b> border.</li>
<li><b>Badge border width</b> - here you can define the width of the border in <b>px</b>.</li>
<li><b>Badge border color</b> - set the preferable color for the border using color picker tool.</li></ul></li></p>

<p><li><b>Badge box shadow</b> - enable this option in order to use the shadow for the menu badge.
<ul class="marked-list">
<li><b>Badge - position of the horizontal shadow</b> - here you can use the controls to set the position of the horizontal shadow.</li>
<li><b>Badge - position of the vertical shadow</b> - here you can use the controls to set the position of the vertical shadow.</li>
<li><b>Badge - shadow blur distance</b> - use the controls to define the extent of shadow blur.</li>
<li><b>Badge - shadow size</b> - here you can define the size of the dropped shadow.</li>
<li><b>Badge shadow color</b> - define the color of the shadow using color picker tool.</li>
<li><b>Badge shadow inset</b> - here you can enable the shadow inset for the menu badge.</li></ul></li></p>

<p><li><b>Badge border radius</b> - in this block you can specify the value for the border radius.</li></p>
<p><li><b>Badge padding</b> - set the padding for the menu badge in <b>px</b>.</li></p>
<p><li><b>Badge margin</b> - here you can define the values to use for the margins in <b>px</b>.</li></p>
<p><li><b>Badge vertical position (may be overridden with order)</b> - here you need to specify the badge vertical position (<b>top, center</b> or <b>bottom</b>).</li></p>
<p><li><b>Badge horizontal position</b> - here you can define the badge horizontal position (<b>left, center, right</b>).</li></p>
<p><li><b>Badge order</b> - use controls to define the badge order.</li></p>
<p><li><b>Hide badge on mobile</b> - enable this option in order to hide the menu item badge on mobile devices.</li></p></ul>

