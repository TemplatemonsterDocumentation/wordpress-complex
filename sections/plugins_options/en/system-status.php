            </dt>
            	<dd>
            		This tool will install all the missing WooCommerce pages. Pages already defined and set up will not be replaced
            	</dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               Delete all WooCommerce tax rates
            </dt>
            	<dd>
            		This option will delete ALL of your tax rates, use with caution
            	</dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
               Reset Usage Tracking Settings
            </dt>
            	<dd>
            		This will reset your usage tracking settings, causing it to show the opt-in banner again and not sending any data
            	</dd>
        </dl>
    </li>
    <li>    
        <dl class="inline-term">
            <dt>    
