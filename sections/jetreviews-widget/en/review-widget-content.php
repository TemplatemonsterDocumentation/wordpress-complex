<h2>Review Widget Content Block</h2>

<h6>In this block you can edit the content displayed with JetReview plugin.</h6>

<h4>Content</h4>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/widg1.png">
  	</figure>

<ol class="index-list">
<p><li><b>Content Source</b> - here you can select, if you want to add content to the reviews widget manually, or use the post meta information (if it was previously added) to become the source of the widget’s content. </li></p>

<p><li>In the case you’ve selected <b>Content Source > Post Meta</b>, you’ll need to define the <b>Post ID</b> in order for the widget to get the information from it. Please, feel free to read <a href="https://zemez.io/wordpress/support/how-to/find-page-post-id/" target="_blank">How to Find Out Page / Post ID</a> tutorial to learn how to find out the post ID.</li></p>

<p><li>In the case you’ve selected <b>Manually Input</b> content source, you’ll need to fill in the following information.

<ul class="marked-list">
<p><li><b>Title</b> - here you can fill in the preferable <b>Review</b> widget’s title.</li></p>

<p><li><b>Add item</b> - click <b>Add Item</b> button to add the features which will be displayed in <b>Review</b> widget as the bars or rating categories.</li></p></ul></li></p>

<p><li>Each of the items has the following additional fields you’ll need to fill in.

<ul class="marked-list">
<p><li><b>Label</b> - here you need to name the rating category (e.g., <b>Design, Quality,</b> etc.).</li></p>

<p><li><b>Field Value</b> - here you need to input the value you want to assign to this rating category (e.g., 4).</li></p>

<p><li><b>Field Max</b> - here you need to specify the maximum value for the rating category (e.g., 5, in this case the item will display 4 from 5 possible rating points).</li></p>


<p><li><b>Summary Title</b> - here you need to fill in the title to use it for the review summary.</li></p>
<p><li><b>Summary Description</b> - here you have to fill in the summary text in the content field (you can also use <b>html</b> markup in <b>Text</b> editor, add media, etc.).</li></p>
<p><li><b>Summary Legend</b> - here you need to add the text to show up as the summary for the review.</li></p></ul></li></p></ol>

<h5>Fields Settings</h5>

<ul class="marked-list">
<p><li><b>Layout</b> - here you can select the preferable type of the rating to use it for the items with rating categories (you can set <b>Points, Percentage</b> or <b>Stars</b>).</li></p>

<p><li><b>Progressbar</b> - enable this option in order to display the rating in the form of a progressbar.

<div class="alert alert-info">Note, that the progressbar is available only for <b>Percentage</b> and <b>Points</b> layout types.</div>
</li></p>

<p><li><b>Values Position</b> - the rating value can be put above the progress bar or inside it.</li></p>

<p><li><b>Values Alignment</b> - in the case you’ve set <b>Values Position > Above Progressbar</b>, you can define if the value will be displayed on the left or on the right.</li></p>

<p><li><b>Label Suffix</b> - in the case you want to add additional text to the existing item label, input it in this field.</li></p></ul>

<h5>Summary Settings</h5>

<ul class="marked-list">
<p><li><b>Summary Results Block Position</b> - here you can select one of the following positions for the <b>Summary Results</b> block:
<ul class="marked-list">
<p><li>At right side of summary box:;<li></p>
<p><li>At bottom of summary box;</li></p>
<p><li>At left side of summary box;</li></p>
<p><li>At top of summary box.</li></p></ul></li></p></ul>

<h5>Results Block Width</h5>

<ul class="marked-list">
<p><li><b>Results Block Width</b> - in the case you’ve selected <b>Right</b> or <b>Left</b> block position, you’ll be able to define the Results block width using the corresponding control.</li></p>


<p><li><b>Summary Average Layout</b> - here you can specify the summary layout type (<b>Points, Percentage, Stars</b>).</li></p>

<p><li>In the case you’ve selected <b>Percentage</b> or <b>Points</b> layout type, you’ll be able to enable <b>Progressbar</b> and set the needed values position (<b>Above</b> or <b>Inside the progressbar</b>).</li></p></ul>


