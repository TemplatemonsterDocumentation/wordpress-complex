<h2>Review Widget Style Block</h2>

<h6>In this block you can change the style and layout which is used when displaying <b>Review</b> widget. </h6>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/widg2.png">
  	</figure>

<h5>Title</h5>

<ul class="marked-list">
<li><p><b>Color</b> - here you can change the color of the title color.</p></li>
<li><p><b>Typography</b> - turn the option on to view the typography settings.

<ul class="marked-list">
<li><p><b>Size</b> — define the font size of the title.</p></li>
<li><p><b>Family</b> — here you can set the font family for the title.</p></li>
<li><p><b>Weight</b> — in this block you can select the suitable font weight.</p></li>
<li><p><b>Transform</b> — here you can choose from the dropdown menu, if you want the title to be shown in uppercase, lowercase, capitalize or normal way.</p></li>
<li><p><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</p></li>
<li><p><b>Line Height</b> — in this field you can set the height of the slide title line.</p></li>
<li><p><b>Letter Spacing</b> — here you can set the space between letters.</p></li></ul>

</p></li>

<li><p><b>Background Color</b> - here you can select the color to use it for the title background.</p></li>

<li><p><b>Padding</b> - here you can set the preferable custom padding for the title. Fill in the values for the top, bottom, right and left padding in pixels to apply your custom padding.</p></li>

<li><p><b>Margin</b> - here you can apply your custom margins in pixels to set them for the title block.</p></li>

<li><p><b>Alignment</b> - here you can define the preferable title text alignment.</p></li>
<li><p><b>Border Type</b> - select the preferable border type to use it for the title.</p></li>

<li><p><b>Border Radius</b> - here you can define the value of the border radius to make the angles more smooth and round.</p></li>

