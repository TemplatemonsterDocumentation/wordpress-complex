<h5>Review</h5>

<ul class="marked-list">
<li><p><b>Review Gap</b> - here you can fix the appropriate gap between reviews for different screen resolutions.</p></li>
<li><p><b>Padding</b> - here you can set the preferable custom padding for the review. Fill in the values for the top, bottom, right and left padding in pixels to apply your custom padding.</p></li>
<li><p><b>Border Type</b> - select the preferable border type to use it for the review.</p></li>
<li><p><b>Border Radius</b> - here you can define the value of the border radius to make the angles more smooth and round.</p></li>
