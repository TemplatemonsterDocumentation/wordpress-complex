<h3>JetCompareWishlist Plugin</h3>


<p><b>JetCompareWishlist</b> plugin is a useful tool for adding comparison and wishlist fucntionality to e-Commerce pages built with Elementor.</p>

<p>With <b>JetCompareWishlist</b> plugin you can create comparison lists and wishlists and enable visitors to add desired products into them, showcasing the selected products with this plugin in the most amazing way.</p>

<p>It allows to use the comparison and wishlist functionality, and is perfect for:</p>
<ul class="marked-list">
<li>showcasing comparison lists and wishlists with desired products easily and quickly;</li>

<li>adding Compare and Wishlist buttons to the products on default WooCommerce templates as well as to custom Single and Archive WooCommerce templates;</li>

<li>comparing and viewing products and their features in a suitable form as a comparison table and wishlist.</li></ul>

