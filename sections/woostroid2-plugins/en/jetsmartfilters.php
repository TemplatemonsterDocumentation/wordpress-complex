<h3>JetSmartFilters Plugin</h3>

<p><b>JetSmartFilters</b> is a plugin that adds easy-to-use AJAX filters to the pages built with Elementor which contain the dynamic listings.</p>

<p>There are several types of filters:</p>
<ul class="marked-list">
<li><b>Checkboxes list</b> - this filter is made for displaying select options with the custom values of the properties of the products from which one is able to choose to filter the posts or products on the page.</li>
<li><b>Select</b> - this filter perfectly suits for displaying checkboxes with the custom values of the properties of the products from which one is able to choose to filter the posts or products on the page.</li>
<li><b>Range</b> - this filter provides an easy way for the visitors to select the range within which to look for the products or posts.</li>
<li><b>Check Range</b> - use this filter to allow the visitors select the ranges by which the items will be filtered. It adds the checklist of ranges containing the specific range values between which the products or posts will be selected.</li>
<li><b>Date Range</b> - this filter provides an easy way to filter the posts or products using the date they were added, or filter the events by the event date when it’s supposed to take place.</li>
<li><b>Radio</b> - use this filter widget to display the radio filter allowing to select from the options represented for the visitor’s choice</li>
<li><b>Search</b> - use this filter to allow the visitors to search for the matching results manually by inputting the needed words or phrases into the Search field.</li></ul>

<p>Each filter can be applied to the products or posts listing in order to get the results the visitor needs the most.</p>

<p><b>JetSmartFilters</b> plugin provides the 7 different widgets for applying filters. Every widget has extensive style and easy-to-use content settings.</p>

<div class="alert alert-info">
        Please, note, that in order to use <b>JetSmartFilters</b> you would also need <b>JetWooBuilder</b> plugin to showcase the product listings.</a></div>


