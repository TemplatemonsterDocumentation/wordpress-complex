<h2>Radio</h2>


<p>This filter type allows filtering the posts or products using the different sources, allowing to add the options via manual input using the values that are already added to the meta fields of the posts, as well as use the posts, taxonomies and custom fields as the select options. </p>

<ol class="index-list">
<p><li>In case you choose the Manual Input source type, you’ll have to set the Query Variable into the corresponding field to specify which field contains the data by which the filter will filter the publications or products. </li></p>

<p><li>Then you’ll have to manually add several options with different corresponding values and labels to show then as the select options. </li></p>

<p><li><b>Taxonomies</b> - in case you’ve selected this data source, you’ll be able to use the taxonomies or categories as filters for your posts or custom post types, products, etc.</li></p>

<p><li><b>Posts</b> - if you’ve selected this data source, you’ll be able to filter the posts using the related posts if there is a relation set for them. </li></p>

<p><li><b>Custom Fields</b> - in case you’ve got the repeaters in the custom post types, you can specify the Custom Fields data source and the specific Custom Field Key which will display the values from the repeating posts fields. </li></p></ul>
