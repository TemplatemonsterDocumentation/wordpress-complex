<article><h2>Range</h2>

<p>The Range filter type allows setting the certain range within which one has to query the prices.</p>

<ul class="marked-list">
<li><p><b>Query Variable</b> - fill in the needed meta field slug (this meta field will become the source of the range values). E.g., you can specify the _price meta key to allow working with the Price range.</p></li>

<li><p><b>Values prefix</b> - set the prefix to be shown before the values (e.g., the currency sign).</p></li>
<li><p><b>Min Value</b> - here you can specify the minimum value available for filtering.</p></li>
<li><p><b>Max Value</b> - here you can specify the maximum value available for filtering. </p></li>
<li><p><b>Get min/max dynamically</b> - in case youâ€™re working with prices, you can enable auto select for the minimum or maximum values. These values will be defined automatically depending on the actual min and max product prices.</p></li></ul></article>
