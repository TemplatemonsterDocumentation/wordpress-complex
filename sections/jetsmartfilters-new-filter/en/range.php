<p><li><b>Thousands separator</b> - here you can define the separator for thousands values (e.g. space).</p></li>
<p><li><b>Decimal separator</b> - here you can define the separator for decimal values.</p></li>
<p><li><b>Number of decimals</b> - here you can specify the number of decimals after the separator.</p></li>
<p><li><b>Min Value</b> - here you can specify the minimum value available for filtering.</p></li>
<p><li><b>Max Value</b> - here you can specify the maximum value available for filtering. </p></li>
<p><li><b>Step</b> - here you can set the value of step applying to the filter.</p></li>
<p><li><b>Get min/max dynamically</b> - in case youâ€™re working with prices, you can enable auto select for the minimum or maximum values. These values will be defined automatically depending on the actual min and max product prices.</p></li>
