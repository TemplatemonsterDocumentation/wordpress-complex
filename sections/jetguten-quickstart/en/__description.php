<h3>JetGuten Quick Start</h3>

<h5>From this block you can get the information on how to start working with JetGuten blocks using Gutenberg editor. </h5>

<ol class="index-list">
<p><li>First, let’s create a new page using Gutenberg editor. To do it navigate to <b>Pages > Add New</b> in admin panel. If you’re creating a new page from All Pages, click the dropdown arrow on Add New button and here select Gutenberg editor from the dropdown list. You’ll be redirected to Gutenberg page editor.

	  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/qs/qs1.png">
  	</figure>  </li></p>

<p><li>Add the title of the page, and start creating page’s structure using <b>Layout Elements</b>. You can locate the columns, spacers, separators, etc., in <b>Add Block > Layout Elements</b> section.

		  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/qs/qs2.png">
  	</figure>

  	Here you can separate the page’s content using columns (sometimes it is needed in the case you want to display several <b>Pricing Table</b> blocks or Banner blocks in a row. </li></p>
<p><li>To customize the number of columns click the block you want to edit and switch from <b>Document</b> to <b>Block</b> tab. Here use the control to change the number of columns to use (by default there are <b>2</b> columns).

	  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/qs/qs3.png">
  	</figure>  </li></p>
<p><li>Locate <b>JetGuten</b> blocks in <b>Add Block > Layout Elements</b>. Here you’ll find:
<ul class="marked-list">
<li>Pricing Table;</li>
<li>Countdown Timer;</li>
<li>Banner;</li>
<li>Circle Progress;</li>
<li>Animated Box;</li>
<li>Map;</li>
<li>Progress Bar;</li>
<li>Inline SVG;</li>
<li>Image Comparison.</li></ul></p>
<p>These blocks can be used for adding more content to the pages built with Gutenberg.

		  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/qs/qs4.png">
  	</figure>


</li></p>

<p><li>You can edit these blocks using the visual interface located on the right or click <b>Options</b> in the top right corner of the block and select <b>Edit as HTML</b>.

		  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/qs/qs6.png">

  	</figure> After that you’ll have the direct access to HTML of this block. To switch back to visual interface select <b>Edit Visually</b> option.

  </li></p>

<li><p>In order to save one of the already styled blocks added with JetGuten as a template click <b>Options</b> for the block and select <b>Add to Reusable Blocks</b>.

	  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/qs/qs8.png">
  	</figure>
  	</p></li>

<p><li>After that it will become available in <b>Add Block > Reusable</b> section. Then you'll have to add the name for the block and click Save button to save it. </li></p>

<p><li>When everything is done you can preview the changes using <b>Preview</b> button or click <b>Publish</b> to make the page live on your site. </li></p></ol>
