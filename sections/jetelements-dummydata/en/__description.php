<h3>JetElements Dummy Data Installation</h3>

<h5>From this block you'll learn how to install and use dummy data .json files to style JetElements in a smooth and quickly way! </h5>


<p><b>Dummy Data</b> is a JSON file which contains the styles to make JetElements widgets look like on demo.</p>

<p>Feel free to use dummy data package to style up JetElements widgets on your website! Proceed to reading this short instruction to find out how to apply dummy data styles.</p>

<ol class="index-list">
<p><li>Navigate to <b>jetelements/dummy-data</b> directory to see all the <b>.json</b> files available for uploading and installation.</li></p>

<p><li>Open WordPress Dashboard and proceed to <b>Elementor > My Library</b> tab on the left. Open it. </li></p>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/my-library.png">
    </figure>

<p><li>Click on <b>Import Templates</b> button in the top left corner to upload the JSON files to Elementor library.</li></p>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/import-files.png">
    </figure>

<p><li>Now navigate to <b>Pages > All Pages</b> tab and open one of the pages for editing. Click <b>Edit with Elementor</b> button to switch to Elementor editor. </li></p>


  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/5.png">
    </figure>

<p><li>Hover on the <b>Folder</b> icon in the bottom left corner to see the list of options. Select <b>Templates Library</b>. </li></p>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/templates-library.png">
    </figure>


<p><li>Switch to <b>My Templates</b> tab on the top of the popup window and select the template you want to use. That's it! </li></p></ol>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/my-templates.png">
    </figure>
