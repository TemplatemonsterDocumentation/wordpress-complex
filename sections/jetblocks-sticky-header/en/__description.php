<h3>Creating Sticky Header</h3>

<p>This block explains how to create a sticky header with JetBlocks.</p>

<h4>Making Header Section Sticky</h4>

    <figure class="img-polaroid">
        <img src="projects/crocoblock/img/core/sticky-header.png">
    </figure>


<ol class="index-list">
<p><li>First of all, let’s assume you’ve created a header template (from scratch or using one of the pre-made headers represented in JetThemeCore templates library). </li></p>

<p><li>Let’s click on the section’s settings and open <b>Advanced</b> section’s settings. </li></p>

<p><li>Open <b>Jet Sticky</b> block. Here you can access the section’s settings.</li></p>

<p><li>Let’s enable <b>Sticky Section</b> option.</li></p>

<p><li>Select the proper <b>Sticky Section Visibility</b> (here you can select for which devices you need to make the section sticky).</li></p>

<p><li><b>Z-Index</b> - here you can set the <b>Z-Index</b> to make the section overlay the existing content on your site.</li></p>

<p><li><b>Max Width</b> - here you can select the maximum width of the sticky section. Just drag the control to adjust the width according to your needs.</li></p></ol>

<h4>Sticky Section Style</h4>

    <figure class="img-polaroid">
        <img src="projects/crocoblock/img/core/sticky-header1.png">
    </figure>


<p><b>Margin</b></p>

<p>Here you can set the custom margins for the sticky section.</p>

<p><b>Padding</b></p>

<p>Here you can apply the custom paddings for the sticky section.</p>


<p><b>Background Type</b></p>

<p>Here you can choose the background to use for the sticky section.</p>

<p><b>Box Shadow</b></p>

<p>Use the shadow settings (such as <b>shadow color, horizontal, vertical, blur, spread, position</b>) in order to make the header stand out when it is shown above the content.</p>

<p><b>Transition Duration</b></p>

<p>Here you can specify the duration of the section’s transition.</p>
