<h2>Categories Grid</h2>

<h6>
This widget adds the WooCommerce Product Categories Grid layouts to the pages built with Elementor on WooCommerce-based websites.
</h6>

<div class="alert alert-info">
        Note, that before using this widget you need to have at least several WooCommerce Product Categories with the thumbnails and descriptions and the products assigned to them.
</div>

<h4>Content</h4>

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/cat-grid.png">
    </figure>


<ul class="marked-list">
	<li><p><b>Category Layout</b> - here you can change the layouts for the categories according to your needs (there are 5 different layout types). </p></li>

	<li><p><b>Columns</b> - here you can change the number of columns to use for the grid (for 1 to 6 columns) and make the columns responsive by changing their number for desktop, mobile and tablet devices. </p></li>

	<li><p><b>Equal Columns Height</b> - enable this option in order to make the height of the columns the same for all every column. </p></li>

	<li><p><b>Add gap between columns</b> - here you can set the gap distance between the columns.</p></li>

	<li><p><b>Add gap between rows</b> - here you can apply the gap distance between the grid rows. </p></li>

	<li><p><b>Categories number</b> - here you can set the number of categories to be shown. </p></li>

	<li><p><b>Hide empty</b> - enable this option in order to hide the empty categories from view. </p></li>

	<li><p><b>Hide Subcategories</b> - enable this option in order to hide the subcategories from view. </p></li>

	<li><p><b>Hide uncategorized</b> - here you can hide from view the uncategorized products. </p></li>

	<li><p><b>Show by</b> - use the dropdown menu in order to select the method by which the categories will be displayed (all categories, parent categories only, or category IDs to display the specific categories only).</p></li>

	<li><p><b>Set comma separated IDs list</b> - here you can set the list of category IDs to be displayed in the case youâ€™ve selected IDs in the Show by menu. </p></li>

	<li><p><b>Order by</b> - here you can set the ascending (ASC) or descending (DESC) order for the categories to be shown. </p></li>

	<li><p><b>Sort by</b> - here you need to specify the method using which the categories will be sorted (by name, ID or count). </p></li>

	<li><p><b>Show Categories Featured Image</b>- enable this option in order to enable the use of the featured image for the categories. </p></li>

	<li><p><b>Featured Image Size</b> - here you can select the size to apply for the featured image (the sizes often depend on the ones already available in the theme for customization). </p></li>

	<li><p><b>Show Categories Title</b> - here you can enable the title to be displayed for the categories. </p></li>

	<li><p><b>Show Products Count</b> - here you can enable displaying the count of the products related to this category. </p></li>

	<li><p><b>Count Before/After Text</b> - here you can define the text or brackets to use for displaying before/after the count.</p></li>

	<li><p><b>Show Categories Description</b> - enable this option in order to display the description for the category. </p></li>

	<li><p><b>Description Words Count</b> - here you can set the number of words to be shown in the description. </p></li>

	<li><p><b>Trimmed After Text</b> - here you can set the symbol or suspension dots to be shown after the description.</p></li></ul>

<h5>Carousel </h5>

<ul class="marked-list">
	<li><p><b>Enable Carousel</b> - enable this option in order to apply the categories carousel. </p></li>

	<li><p><b>Slides Minimal Height</b> - here you can define the minimum height for the carousel slides to use for different devices. </p></li>

	<li><p><b>Slides to Scroll</b> - here you can specify the number of slides to scroll. </p></li>

	<li><p><b>Show Arrows Navigation</b> -  enable this option in order to display the navigation arrows for the slides to allow the users switch the slides. </p></li>

	<li><p><b>Prev/Next Arrow Icon</b> - here you can specify the icons to use for the navigation. </p></li>

	<li><p><b>Show Dots Navigation</b> - enable this option to display the pagination dots for the slides to enable users switch between them. </p></li>

	<li><p><b>Pause on Hover</b> - enable this option to make the carousel or slider pause when one hovers on it. </p></li>

	<li><p><b>Autoplay</b> - enable this option to use the autoplay for the slides. </p></li>

	<li><p><b>Autoplay Speed</b> - here you can specify the speed used for the autoplay. </p></li>

	<li><p><b>Infinite Loop</b> - enable this option to make the slides switch automatically infinitely. </p></li>

	<li><p><b>Animation Speed</b> - here you can define the speed of the animated switching between the slides.</p></li></ul>

<h4>Style</h4>

<h5>Column </h5>

<ul class="marked-list">
	<li><p><b>Column Padding</b> - here you need to specify the padding values for the columns and make the paddings responsive. </p></li></ul>

<h5>Category Item</h5>

<ul class="marked-list">
	<li><p><b>Background Color</b> - here you can set the color to use for the category item background using color picker tool. </p></li>

	<li><p><b>Border Type</b> - here you can define the type of the border to apply for the item  (or apply no border, according to your needs). </p></li>

	<li><p><b>Width</b> - here you need to set the width of the border that will be shown in pixels. </p></li>

	<li><p><b>Color</b> - here you can customize the color of the border. </p></li>

	<li><p><b>Border Radius</b> - here you can set the border radius values to make the angles more smooth and round. </p></li>

	<li><p><b>Box Shadow</b> - here you can apply the shadow for the item. Click the Edit pencil option to open the additional shadow settings.</p></li>

	<li><p><b>Padding</b> - here you can define the padding values for the item. </p></li></ul>

