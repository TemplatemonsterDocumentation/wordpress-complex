<h2>Products List</h2>

<h6>
This widget adds the stylish listing of the WooCommerce products to the pages built with Elementor. </h6>
</h6>

<div class="alert alert-info">
        Please, make sure you've created the WooCommerce products with featured images and all the needed infotrmation including prices, descriptions, etc, in order to be able to showcase them using this widget.
</div>

