<h5>General</h5>

<ul class="marked-list">
	<p><li><b>Layout</b> - here you can specify the desirable layout for the product list (image on the left, on the right or on the top).</li></p>

	<p><li><b>Products Number</b> - here you can set the number of the products to display in the list.</li></p>

	<p><li><b>Query products by</b> - here you can specify the query method for the products (all, featured, sale, tag, category, specific ID). Note, that depending on the query method youâ€™ll need to provide the IDs, categories and tags.</li></p>

	<p><li><b>Order by</b> - here you need to set the order in which the products will be shown (date, price, sales, top rated, random).</li></p>

	<p><li><b>Show Products Title</b> - enable this option in order to display the title for the products in the list.</li></p>

	<p><li><b>Show Products Featured Image</b> - enable this option in order to display the featured image for the products in the list.</li></p>

	<p><li><b>Featured image Size</b> - here you can set the size for the featured image (depending on the sizes available for choosing in your theme).</li></p>

	<p><li><b>Show Product Categories</b> - enable this option to display the product categories.</li></p>

	<p><li><b>Show Product Price </b>- here you can display the product price or hide it.</li></p>

	<p><li><b>Show Product Rating </b>- enable this option to display the rating for the product of hide it from view.</li></p>

	<p><li><b>Show Add to Cart button</b> - enable this option to display Add to Cart button and allow the visitors make purchases using it.</li></p>

    <li><p><b>Use default Ajax Add to Cart styles</b> - this option enables default WooCommerce styles to 'Add to Cart' Ajax button ('Loading' and 'Added' statements).</p></li>

<p>In case <b>JetCompareWishlist</b> plugin is installed and actived there are such additional settings:</p>
<ul class="marked-list">
<li><b>Show Compare</b> - enable this option if you want to add a compare button to the products.</li>
<li><b>Compare Button Order</b> - here you can specify the order of the compare button.</li>
<li><b>Show Wishlist</b> - enable this option if you want to add a wishlist button to the products.</li>
<li><b>Wishlist Button Order</b> - here you can specify the order of the wishlist button.</li></ul>

<h5>Compare</h5>
<ul class="marked-list">
