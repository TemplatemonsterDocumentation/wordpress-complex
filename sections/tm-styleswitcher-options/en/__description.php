<h4>Additional options</h4>

<p><strong>Cherry Live Demo Mods Switcher </strong> plugin allows to switch between different skins and change your website appearance at will. The presets can be found in the customizer tab 'TM Style Switcher'. When choosing one of the registered preset be aware that all the unsaved customizer settings will be lost.</p>

