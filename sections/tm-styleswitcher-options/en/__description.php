<h2>Plugin Options</h2>

<h4>Activating TM Styleswitcher plugin</h4>

<p>Go to <strong>Plugins</strong> menu in your WordPress administration panel. Click on 'Activate' link.</p>

<h4>Plugin options</h4>

<p><strong>Cherry Live Demo Mods Switcher </strong> helps to save and import or export customizer settings such as sidebar position, header settings etc.</p>

<p>To find it you have to go to <strong> Customizer</strong>, and there you'll find <strong> TM Style Switcher</strong> tab.</p>

<p> There are such  options: </p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Export</dt>
            <dd>
                'Export' button generates and downloads customizer settings in one file
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Import</dt>
            <dd>
                'Import' button allows to choose one of the previously exported files with the customizer settings. Them the page will be reloaded with the new customizer settings
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Restore settings</dt>
            <dd>
                'Restore settings' button helps to jump to default customizer settings. All the settings that were previously set would be erased
            </dd>
        </dl>
    </li>
</ul>

