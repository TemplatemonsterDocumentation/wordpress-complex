<h3>Step 1. Theme Installation</h3>

<p>To use <b>WooCommerce QuickStart Kit</b> plugin you have to install Cherry Framework 5 theme. Let’s install the theme to your WordPress website.</p>


<ol class="index-list">
<p><li>Navigate to <b>Appearance > Themes</b> tab. Here click <b>Add New</b> button to add a new theme.</li></p>

	<figure class="img-polaroid"><img src="img/wookit/1.png" alt=""></figure>


<p><li>We’ll install <b>Monstroid 2 Lite</b> theme. Please, type in Monstroid in the <b>Search</b> field. Note, that you can as well use the other Cherry Framework  - based WooCommerce theme. </li></p>

<p><li>Hover on the newly found theme and click <b>Install</b> button. </li></p>

	<figure class="img-polaroid"><img src="img/wookit/2.png" alt=""></figure>

<p><li>Then click <b>Activate</b> to activate the theme. </li></p></ol>

	<figure class="img-polaroid"><img src="img/wookit/3.png" alt=""></figure>


