<h4>Popup Import</h4>

<ol class="index-list">
<p><li>To import a popup template navigate to <b>JetPopup > All Popups</b> block in the Dashboard. Here click <b>Import Popup</b> button located at the top of the page.


<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/14.png">
  	</figure></li></p>

<p><li>Browse the JSON template of the needed popup from your hard drive. Then click <b>Import Now</b> button. After that, Elementor editor will open and you'll be able to work with the newly imported popup template.</li></p></ol>
