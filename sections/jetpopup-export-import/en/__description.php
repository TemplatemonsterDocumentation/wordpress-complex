<h4>Popup Export</h4>


<ol class="index-list">
<p><li>In case you've created a popup template, yet you want to export it in order to use it on different site, please, navigate to <b>JetPopup > All Popups</b> block. Here you can see the full list of all the popup templates existing on your site. </li></p>

<p><li>Here hover at the popup template you want to export and click <b>Export Popup</b> option. The template will be saved in JSON format.

