
<h3>TM-Builder Row module settings</h3>

<p><strong>Power Builder Row Module Settings</strong> help users customize certain elements of this template on the fly.</p>

<p>
    This option offers you to manage your content alignment when you are working with columns. 

    You can reach Row module settings this way: 
    Go to Power Builder > Select the number of columns you need to display > Select the number of rows > Go to the Row module settings as showed on the picture below
</p>

<figure class="img-polaroid">
    <img src="img/plugins/tm-builder/rows.png">
</figure>

<p>
    A window will open offering you to choose custom row module settings
</p>

<figure class="img-polaroid">
    <img src="img/plugins/tm-builder/settings.png">
</figure>


<p>
    Here you have access to managing these settings:
</p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Make this row fullwidth</dt>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Use custom width</dt>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Use grid pagging</dt>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Set the custom padding</dt>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Keep custom padding on mobile</dt>
        </dl>
    </li>
</ul>      

<p>
    To reach vertical content alignment you need to go to Advanced Design Settings. The window must look as follows:
