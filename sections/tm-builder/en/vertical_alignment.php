<figure class="img-polaroid">
    <img src="img/plugins/tm-builder/alignment.png">
</figure>

<p>
    When establishing margings and padding you set the distance between elements that is constant. It will remain the same not dependind on the window size. 
    However, using column vertical alignment option you can set alignment that will change proportionally depending on the window size.
</p>

