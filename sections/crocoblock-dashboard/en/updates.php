<h3>Plugin Updates</h3>

<ul class="marked-list">
<p><li>Switch to <b>Plugins</b> tab. Here you can view the full list of <b>Jet plugins</b> available for installation and usage. </li></p>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/dashboard/plugins.png">
  	</figure>

<p><li><b>Check for Updates</b> - click this button in order to check the available updates for the plugins listed below.</p>

<p>If the updates are already available, you’ll see the notice on the right of the plugin which newer version is currently available for downloading. </p>

<div class="alert alert-info">
        Note, that you can also update the plugins in <b>Plugins > Installed Plugins</b> tab in WordPress Dashboard.</div></li>

<p><li><b>Check for New Plugins</b> - click this button in order to check if there are any new plugins have rolled out and are now available to be downloaded and used.</li></p>

<p><li><b>Install</b> - click this option to install the plugin that is available in your subscription, but hasn’t been installed on this site yet.</li></p>

<p><li><b>Documentation</b> - click this link to navigate to the plugin’s documentation.</li></p>

<p><li><b>Synchronize Templates Library</b> - click this option to synchronize the templates available in Magic Button (headers, footers, sections and pages).</li></p>

<div class="alert alert-info">You can also view the current plugin’s version and see if it matches the version installed on your site on the right of the plugin’s shortcut.</div>
