<h3>JetMenu Export/Import Options</h3>

<h5>This block uncovers the process of exporting and importing JetMenu plugin settings.
</h5>

<h3>Export Option</h3>

<p>Once you’ve styled up JetMenu items, you might need to export the settings to use them all over again for another site. To do it, please, follow the instruction below. </p>

<ol class="index-list">
<p><li>Navigate to JetMenu tab on the left of the Dashboard.

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/export/1.png">
    </figure></li></p>

<p><li>Scroll the page down and locate Export button. Click it to export the settings. </li></p>

<p><li>They will be saved in the form of .json file to your hard drive.

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/export/2.png">
    </figure></li></p>

<p><li>Now you can use them all over again. </li></p></ol>

<h3>Import Option</h3>

<ol class="index-list">
<p><li>As you’ve already seen, there is also Import button just on the right of Export button. To import the previously exported options, please, click it.

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/export/3.png">
    </figure></li></p>

<p><li>A new block will appear below it. Here you need to click Browse button and browse your hard drive for the .json file you’ve previously exported.

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/export/4.png">
    </figure></li></p>

<p><li>Click Go button.</li></p>

<p><li>If the options are successfully imported, you’ll see the “Options successfully imported. Page will be reloaded. “ green notice.</li></p></ol>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/export/5.png">
    </figure>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/export/6.png">
    </figure>

<h3>Reset/Save Option</h3>

<p>In the case you’ve changed some of the style settings, and you’re not satisfied with the results, you can reset the default menu style settings using Reset options.</p>

<ol class="index-list">
<p><li>You can find Reset button in the bottom right corner of the JetMenu settings page. Click it to reset your menu settings.</li></p>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/export/7.png">
    </figure>

<p><li>A notice will appear, informing you that all menu options will be changet back to the default ones.</li></p>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/export/8.png">
    </figure>

<p><li>Right beside the Reset button you can find Save button. Click it to save the changes for the menu style.</li></p></ol>


  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/export/9.png">
    </figure>
