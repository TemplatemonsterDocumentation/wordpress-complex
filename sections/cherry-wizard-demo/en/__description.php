<h3>Step 3. Demo Content Installation</h3>

<p>
    The next step will be to install demo content that comes with your theme. It usually adds to your theme the necessary content, such as images, posts and other sample data.
</p>

<p>A window will appear offering you to:</p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>append demo content to the existing content</dt>
            <dd>
                this way the new sample data will be added to the existing one;
            </dd>
        </dl>
    </li>

     <li>
        <dl class="inline-term">
            <dt>replace the existing content with demo content</dt>
            <dd>
                this option will delete your existing content and will install the new sample data instead of it;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>skip demo content installation</dt>
            <dd>
                choose this option if you don’t want to install demo content and you want to keep the already existing one.
            </dd>
        </dl>
    </li>
</ul>

 <figure class="img-polaroid">
        <img src="projects/cherry-wizard/img/images/11.png" alt="" >
</figure>

<p>
    If you’ve chosen to install the demo content, the next step will lead you to the <strong> Importing Sample Data</strong> window, where you’ll need to enter your password to start importing (it is the password you usually use when you go to Administration Panel).
</p>

 <figure class="img-polaroid">
        <img src="projects/cherry-wizard/img/images/12.png" alt="" >
</figure>

<p>
    After that you’ll be able to monitor the process of importing sample data.
    You’ll see the bars that will show the progress.
</p>

 <figure class="img-polaroid">
        <img src="projects/cherry-wizard/img/images/13.png" alt="" >
</figure>

<p>
    When the demo content will be successfully installed you’ll see the latter notice.
</p>

 <figure class="img-polaroid">
        <img src="projects/cherry-wizard/img/images/14.png" alt="" >
</figure>

<p>Now you can view your website or proceed to the further customization! </p>
