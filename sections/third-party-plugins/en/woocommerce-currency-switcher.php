<h3> WooCommerce Currency Switcher</h3>

<p>
	This advanced <a href="https://wordpress.org/plugins/woocommerce-currency-switcher/" target="_blank"> WooCommerce Currency Switcher plugin</a> adds a simple currency switcher widget to the website.
</p>

<figure class="img-polaroid"><img src="img/woocommerce/woocommerce-currency-switcher.png" alt=""></figure>
<h4>Installing WooCommerce Currency Switcher</h4>

<ol class="index-list">
	<li><p> Download WooCommerce Currency Swircher to your plugin directory or simply install it via WordPress admin interface.</p></li>
	<li><p> Activate the plugin using <b>Activate</b> option under the plugin on <b>Installed Plugins</b> page.</p></li>
	<li><p> To use Currency Switcher, please, navigate to <b>WooCommerce > Settings</b> tab on the left of the Dashboard and locate <b>Currency</b> tab in the top settings panel. Here you can manage currencies. To learn how to use Currency Switcher, please, proceed to reading <a href="https://currency-switcher.com/documentation/" target="_blank">plugin documentation</a>.</p></li>


</ol>

<div class="alert alert-info">
NOTE: You can use any two currencies in the free version of the plugin (by default, it is EUR and USD, but you can change them to your needed currencies). However, You can get a premium version in order to add more currencies to the switcher.
</div>

