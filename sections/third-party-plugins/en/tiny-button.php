<h3>Tiny MCE Small Button</h3>

<p>With the help of this <a href="#" target="_blank">TinyMCE Plugin</a> you can wrap selected text in a small tag.</p>


    <figure class="img-polaroid">
        <img src="img/third_party_plugins/tiny-button.png">
    </figure>

<p>To use Tiny MCE Small Button you have to select the text passage and wrap it into < small> </ small> tags. The text passage will become smaller.</p>

