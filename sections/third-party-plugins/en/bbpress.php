<h3>bbPress</h3>

<p>Have you ever been frustrated with forum or bulletin board software that was slow, bloated and always got your server hacked? <a href="https://wordpress.org/plugins/bbpress/" target="_blank"> bbPress</a>  is focused on ease of integration, ease of use, web standards, and speed.

We're keeping things as small and light as possible while still allowing for great add-on features through WordPress's extensive plugin system. What does all that mean? bbPress is lean, mean, and ready to take on any job you throw at it.
