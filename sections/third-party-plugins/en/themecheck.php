<h3>Theme Check</h3>

<p>The theme check plugin is an easy way to test your theme and make sure it's up to spec with the latest theme review standards. <a href="https://wordpress.org/plugins/theme-check/" target="_blank">WP Theme Check Plugin</a>. The tests are run through a simple admin menu. </p>

<h4>Installing the WP Instagram Widget plugin</h4>

<ol class="index-list">
	<li><p> Upload the wp-instagram-widget folder to the /wp-content/plugins/ directory</p></li>
	<li><p> Activate the plugin through the 'Plugins' menu in WordPress</p></li>
	<li><p> That's it!</p></li>
</ol>	
<p>
	You can also install he plugin right from the admin panel. For that you need to:</p>
<ol class="index-list">
	<li><p> Navigate to the "Plugins" section on the left navigation panel </p></li>
	<li><p> Click the "Add New" button at the top</p></li>
	<li><p> Type WP Instagram Widget in the search field </p></li>
	<li><p> Click "Install now" </p></li>
</ol>	

