<h3>BNE Testimonials</h3>

<p>Display testimonials on any page of your site with the help of this advanced <a href="https://wordpress.org/plugins/bne-testimonials/" target="_blank">BNE Testimonials plugin</a>. The testimonials can be displayed as a list or a slider and easily customized in accordance with the design of the website.</p>
<p>Thanks to an assortment of flexible adjustments you can add  title, featured image, tagline, website, message and custom taxonomy to arrange the testimonials in different groups. Use [bne_testimonials_list] & [bne_testimonials_slider] shortcodes to add the widget quickly and worry-free.</p>

