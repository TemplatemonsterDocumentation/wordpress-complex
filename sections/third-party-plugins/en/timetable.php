<h4>Installing</h4>
<p>
	After downloading the plugin, you should have motopress­timetable.zip file on your computer. This is a compressed file that contains all the plugin data. Follow the steps listed below to install it:
</p>
<ol class="index-list">
	<li><p> Log into your WordPress dashboard and go to Plugins > Add New.</p></li>
	<li><p> Click the Upload Plugin button.</p></li>
	<li><p> Click the Choose File button > find the plugin archive on your computer</p></li>
	<li><p> Click Install Now</p></li>
	<li><p> Click Activate</p></li>
</ol>	

	
