<h3>Timetable and Event Schedule</h3>

<p>This <a href="https://wordpress.org/plugins/mp-timetable/" target="_blank">Timetable and Event Schedule</a> plugin is an easy manageable timetable­building tool for constructing various kinds of event schedules (lessons, conferences, concerts, etc.) and featuring Upcoming events in the additional widget. It comes with clean modern interface and responsive design.

</p>

