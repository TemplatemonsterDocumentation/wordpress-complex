<h3>Shortcode Widget</h3>

<p>This <a href="#" target="_blank">Shortcode Widget</a> plugin adds a text-like widget that allows you to write shortcode in it. (just whats missing in the default text widget). To test the widget you can add the widget and use the shortcode “[shortcode_widget_test]”, it will display “It works” on the frontend and this will confirm the widget does work. </p>

