<h3>Restaurant Menu</h3>

<p><a href="https://wordpress.org/plugins/mp-restaurant-menu/">MotoPress Restaurant Menu</a> plugin is an easy-to-manage modern solution for building online menus of restaurants, cafes and other typical food establishments on any WordPress website. One great advantage of the plugin is a powerful eCommerce tool to help you sell food and drinks online without any technical help. Menus can be displayed in pages, posts, and sidebars using shortcodes which are a perfect tool for creating complex content with minimum efforts. Non-tech WordPress users will value the intuitive system of both shortcode settings and the entire menu instruments. Applying a simple set of operations you will be able to customize menu design, add and categorize unlimited menu items, create and feature/hide all needed tags and ingredients, choose whether to display particular items, switch on/off all needed settings for individual items and categories any time and much more. 

</p>

