<h3>JetBlocks Plugin</h3>

<p><b>JetBlocks</b> is a perfect tool providing an opportunity to create specific content perfectly fit for website's headers and footers.</p>

<p>The plugin makes it extra easy to add <b>authorization links</b>, <b>hamburger panels</b>, <b>login forms</b>, add and customize <b>site logo</b>, <b>nav menu</b>, <b>registration forms</b>, <b>search forms</b> and <b>WooCommerce cart</b> using the convenient widgets made specifically for Elementor. </p>

<p>The plugin allows to enjoy the stunning results in minutes, possesses multiple customization content and style settings. </p>

    <div class="alert alert-info">
        You can find the detailed documentation on JetBlocks plugin <a href="http://documentation.zemez.io/wordpress/index.php?project=jetblocks" target="_blank">here</a>.</div>


<h3>Widgets Overview</h3>

<ul class="marked-list">
<p><li>
    <b>Auth Links</b> - the widget allows adding the authorization links to the sections created with Elementor.
</li></p>

<p><li>
    <b>Hamburger Panel </b> - this widget allows adding a neat hamburger panel to the pages built with Elementor in order to make access to the needed template more easy for your visitors.

</li></p>
<p><li>
    <b>Login Form </b> - this widget allows to add and customize a login form to the pages built with Elementor.

</li></p>

<p><li>
    <b>Site Logo</b> - widget allows adding textual and image logo to the headers and footers created for the site pages.
</li></p>
<p><li>
    <b>Nav Menu</b> - this widget makes it easy to create a menu and place it in the website's header or footer built with Elementor.
</li></p>
<p><li>
    <b>Registration Form </b> - this widget allows adding stylish registration forms with multiple fields to the headers, footers and webpages built with Elementor.
</li></p>
<p><li>
    <b>Search</b> - add the search forms to the website's headers and footers whenever it is needed and style them up using multiple style settings.
</li></p>
<p><li>
    <b>WooCommerce Shopping Cart</b> - add the shopping cart to website's header or footer and customize its appearance in about few clicks.
</li></p>
</ul>
