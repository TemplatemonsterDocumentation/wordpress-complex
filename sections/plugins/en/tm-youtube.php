<h4>Installing TM Youtube Subscribe Widget plugin</h4>

<ol class="index-list">
	<li><p> Upload "TM Youtube Subscribe Widget" folder to the "/wp-content/plugins/" directory</p></li>
	<li><p> Activate the plugin through the "Plugins" menu in WordPress</p></li>
	<li><p> Navigate to the "Widgets" section to start customizing</p></li>
</ol>
