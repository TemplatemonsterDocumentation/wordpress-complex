<h3>TM Youtube Subscribe Widget Plugin</h3>

<p>This custom designed <a href="https://wordpress.org/plugins/tm-youtube-subscribe/">plugin</a> from TemplateMonster allows you add YouTube subscribe widget to the site. In this way, users will be able to navigate to your YouTube channel right from the homepage.  </p>
	
