<h3>Cherry Testimonials</h3>

<p>Unveil customers' feedback on your services to look more reliable to prospects. This easy-to-use <a href="https://wordpress.org/plugins/cherry-testi/" target="_blank">Cherry Testomonials</a> plugin will let you add testimonials to any post and page of your site via shortcodes. With Cherry Testimonials, you can prove your testimonials aren't fake and were written by real people. Besides the testimonial itself, it's possible to reveal a lot of details about its author. They include his name, photo, email, company he works in, his position, and link to the site of that company or his personal blog. Note that you can hide the email, position, company's name, and photo, if a customer doesn't want you to reveal any of that data.</p>

