<h3>TM WooCommerce Quick View</h3>

<p>
	<b>TM WooCommerce Quick View</b> plugin helps to add Quick View button to the blocks where WooCommerce products are listed. It makes the process of choosing the product much easier for the customers so that they can view the products much quicker.
</p>
<figure class="img-polaroid">
        <img src="img/plugins/quick-view.png">
  	</figure>

<h4>TM WooCommerce Quick View Location</h4>

<ol class=index-list>
<p><li>To access TM WooCommerce plugin open WordPress Dashboard and navigate to "WooCommerce > Settings" tab in the left column. </li></p>

<p><li>Now you need to open "Tm Quick View" tab. Here you'll see the settings of the plugin available for you.</li></p></ol>

<h4>TM WooCommerce Quick View Settings</h4>

<ul class=marked-list>
<p><li><b>Enable Quick View Button</b> - check this oprion to turn on the Quick View buttom to let the visitors view the product features without opening single product page.</li></p>

<p><li><b>Enable Product Navigation Mode</b> - here you can turn on navigation arrows to switch to previous and next products.</li></p></ul>
