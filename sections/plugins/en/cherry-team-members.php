<h4>Cherry Team Members Location</h4>

<p>To access Cherry Team Members plugin you need to log in to WordPress Dasboard. Locate "Team" tab in the left column. </p>

<p>Here you'll find 4 sub menus that will help you showcase your team members on the website's page.</p>

<ul class=marked-list>
<li><b>Team</b></li>
<li><b>Add New</b></li>
<li><b>Groups</b></li>
<li><b>Settings</b></li>

<h4>Team</h4>

<p>In the <b>Team</b> block you can view the persons from your team, their photos, positions and team groups. Click on <b>Edit</b> option to edit personal information about the team members. </p>

<h4>Add New</h4>

<p>Here you can add new persons to your team.</p>

<h5>Adding New Person</h5>

<ul class=marked-list>
<li><b>Title</b> - here you can add the title in the empty field.</li>
<li><b>Content Field</b> - here you can fill in all the necessary content you want to display.</li>
<li><b>Position</b> - fill in the person position in the required field under <b>Person Options</b> block.</li>
<li><b>Location</b> - add the name of the location for the person.</li>
<li><b>Phone Number </b> - define the phone number for the person.</li>
<li><b>Social Profiles</b> - here you can add social networks to contact the person.</li>
<li><b>Skills</b> - add the skills the team member possesses.</li>


