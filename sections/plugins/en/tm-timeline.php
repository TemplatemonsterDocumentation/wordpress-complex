<h4>Creating a timeline</h4>

<p>In order to create a timeline follow these steps:</p>

<ul class="marked-list">
	<li><p> Navigate to the “TM Timeline” -> “Add New” page in the left column of WordPress Dashboard.</p></li>
	<li><p> Create event posts. Specify the “Timeline Date” option for each of them.</p></li>
	<li><p> Create a tag and assign it to the event posts. List of all event posts is available on “TM Timeline” -> “Posts” page.</p></li>
	<li><p> Navigate to “TM Timeline” -> “Settings” page, fill in required options and hit the “Generate Shortcode” button.</p></li>
	<li><p> Copy the generated shortcode to your page or post.</p></li>
</ul>
