<h3>TM Timeline</h3>

<p><a href="https://wordpress.org/plugins/tm-timeline/" target="_blank">TM Timeline</a> plugin allows to showcase the most important events of your business in a chronological order, which can be a great addition to your ‘About’ page.
Tell your visitors the history of your company, and show the turning points you experienced along the way. The plugin has a simple interface and lets you build your timelines quickly.</p>

