<h3>Widget Importer & Exporter</h3>

<p>
	<a href="https://wordpress.org/plugins/widget-importer-exporter/">Widget Importer & Exporter</a> is useful for moving widgets from one WordPress site to another and backing up the widgets.
</p>

<h5>Importing</h5>

<p>
	Importing is a matter of uploading an export file created by the plugin. The results of an import are shown in a nicely formatted table with an explanation of what happened with each sidebar and widget.
	Importation takes into consideration the sidebars not existing in the current theme (widgets imported as Inactive), widgets that already exist in the same sidebar (widgets not duplicated) and the widgets that are not supported by the site (widgets not imported).
</p>

<h5>Exporting</h5>

<p>
	Widget Importer & Exporter can create an export file (in JSON format with .wie extension) out of currently active widgets. This file can be imported into other sites using this plugin or used to restore the widgets to the same site later.
</p>

