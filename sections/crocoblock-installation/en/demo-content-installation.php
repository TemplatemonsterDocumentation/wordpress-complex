<h3>Plugins and Content Installation</h3>

<p>This block uncovers the process of installing demos and plugins using <b>Jet Wizard</b> plugin. Please, read further in the case you want to use another theme instead of <b>Kava.</b>
</p>

<h4>Step 1</h4>

<ol class="index-list">
<li><p>First, let’s install <b>Jet Plugins Wizard</b> plugin. In order to do this open <b>Plugins > Add New</b> and here click <b>Upload Plugin</b> button at the top of the screen.</p></li>

	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/inst4.png">
  	</figure>

<li><p>Browse the <b>Jet Plugins Wizard</b> plugin’s <b>.zip</b> package, which you can download from your personal account. It is located right along with Kava theme in the <b>Quick Installation Pack</b> block.</p></li>

	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/inst5.png">
  	</figure>

<li><p>Click <b>Install Now</b> button. After the plugin is successfully installed click <b>Activate</b> link which is located right under the notice about plugin’s successful installation.</p></li></ol>

	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/inst6.png">
  	</figure>

<h4>Step 2</h4>


<ol class="index-list">
<li><p>Let’s activate <b>CrocoBlock subscription</b>. To do it one needs to retrieve the <b>License Key</b>. You can find it on your <a href="https://account.crocoblock.com/#purchase-details" target="_blank">Personal Account</a> page in <b>Purchase Details</b> block. Click <b>Manage License</b> link located under the <b>License Keys</b>.</p></li>

<li><p>Here you’ll see your license details. You need to copy the key from the <b>License</b> block. Please, make sure that you have copied it without any extra text which can be also shown in this field.</p></li>

<li><p>Let’s navigate to <b>Plugins Wizard</b> tab on the left of the Dashboard. Here You’ll see the <b>Installation Wizard</b> page where one needs to paste the key in order to activate the license. Then click <b>Activate License</b> button.</p>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/inst8.png">
  	</figure>

<div class="alert alert-info">
        When the license is active you’ll be able to proceed to plugins and demo content installation right away.
</div></li></ol>


<h4>Step 3</h4>

<ol class="index-list">
<li><p>Let’s choose the demo you want to use on your site. To do it navigate to <b>Plugins Wizard</b> in WordPress Dashboard.</p></li>

<li><p>On the <b>Select skin and start install</b> screen select the demo you want to use on your site.</p>

			<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/wizard1.png">
  	</figure>


<div class="alert alert-info">Note, that you can change it later anytime you want.</div>
</li>

<li><p>You can view the list of recommended plugins for each of demos in the <b>Recommended Plugins</b> listed on the right of each of the demos.</p></li>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/wizard2.png">
  	</figure>

<li><p>In order to view the demo before you make a decision to install it click <b>View Demo</b> button on the right of the demo you selected. The preview page will open. Here you can see how the demo will look on your site.</p></li>

<li><p>Click <b>Start Install</b> button in order to proceed with demo installation.</p></li></ol>

<h4>Step 4</h4>

<ol class="index-list">
<li><p>After you’ve picked the demo you want to install, <b>Jet Plugin Wizard</b> will open <b>Configure Plugins</b> page, which displays the lists of <b>Required Plugins</b>, <b>Recommended Plugins</b> and <b>Extra Plugins</b>.</p>

<ul class="marked-list">
<li><p><b>Required Plugins</b> - here you can find the minimum set of service plugins for your theme installation set by default.</p></li>
<li><p><b>Recommended Plugins</b> - here you can see the recommended set of basic plugins to display the template’s pages. The best option for your site's future configuration. If you will not install one or more plugins from this list, the specific sections of the template, for which these plugins are responsible, will not be displayed.</p></li>
<li><p><b>Extra Plugins</b> - here one can view the full list of plugins available for a template installation is recommended if you want to add additional functionality to your theme.</p></li></ul></li>

<li><p>Check or uncheck the plugins from the represented list in order to select which ones you want to be installed on your site. After that click <b>Next</b> button. The installation process will start.</p></li>

<li><p>When the plugins are installed the green notices will appear informing you that the plugins are already installed and active on your site. The blue progress bar will show how long it will take to install all the selected plugins.</p></li></ol>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/wizard3.png">
  	</figure>

<h4>Step 5</h4>

<ol class="index-list">
<li><p>It’s time to install demo content. After the plugins are installed you can see the We’re almost there page where one can choose if you want to:</p>

				<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/wizard4.png">
  	</figure>


<ul class="marked-list">
<li><p><b>Append demo content to my existing content</b> - the sample data will be added to the current content of your theme.</p></li>
<li><p><b>Replace my existing content with demo content</b> - your current data will be replaced by the new demo content (sample data). If you want to save the current content of your theme, please choose Skip Data Installation.</p></li>
<li><p><b>Skip demo content installation</b> - the sample data will not be installed on your theme and your current content will stay as it is.</p></li></ul></li>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/wizard5.png">
  	</figure>

<li><p>After you’ve chosen the most suitable option, click <b>Next</b> button. The demo content will be automatically installed on your site. The blue progress bars will indicate the percents of demo content which are accomplished.</p></li>

<li><p>After everything is done you’ll be redirected to <b>Congratulations! You’re all set! </b> page. Here you can:

<ul class="marked-list">
<li>view your site;</li>
<li>start editing pages;</li>
<li>navigate to documentation;</li>
<li>access the knowledge base;</li>
<li>navigate to CrocoBlock community;</li>
<li>switch the skin.</li>
</ul></p></li></ol>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/wizard6.png">
  	</figure>
