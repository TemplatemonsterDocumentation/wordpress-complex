<h5>Adding a Gallery </h5>

<p>
    To add a gallery  you need to navigate to the post or page and press the plugin button.
</p>

<figure class="img-polaroid">
    <img src="projects/<?php echo $project;?>/img/widgets/gallery-add-1.png">
</figure>

<p>
In a popup window you need to choose a gallery you want to add.
</p>
<figure class="img-polaroid">
    <img src="projects/<?php echo $project;?>/img/widgets/gallery-add-2.png">
</figure>

<p>
    After that a shortcode will be added to the editor. For example <strong>[tm-pg-gallery id="155"][/tm-pg-gallery]</strong> where id - identificator of the displayed gallery.  Display, Filters and Pagination are taken from the settings of the selected gallery. 
</p>
