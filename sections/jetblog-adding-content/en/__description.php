<h3>Adding Content with JetBlog</h3>

<h5>From this block you can learn how to add content with <b>JetBlog</b> addon to the pages built with Elementor.</h5>


<ol class="index-list">
<p><li>Navigate to <b>Pages > Add New</b> tab in the left column of the WordPress Dashboard. Click on <b>Edit with Elementor</b> button. You’ll be redirected to Elementor editor page.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/5.png">
  	</figure>

  	</li></p>

<li><p>When Elementor editor will open, you’ll see the block containing <b>Add New Section</b> button at the right part of the screen, where your future page’s draft is displayed.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/6.png">
  	</figure></p>

<p>Click on <b>Add New Section</b> to add the section, where you will place JetBlog module.</p></li>

<p><li>Now you’re ready to add JetBlog modules to the columns. Choose one of the elements you want to add to the page, and drag it to the column area.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/7.png">
  	</figure>

   	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/8.png">
  	</figure></li></p>

<p><li>In the left column you’ll see the module settings. You can manage content, style, or use advanced styling options from here.</p>

<p><div class="alert alert-info">Keep in mind, that for different elements the options may vary. Each element has its own styling and content management settings.</div>
</li></p>

</ol>
