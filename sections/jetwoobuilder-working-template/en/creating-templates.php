<h3>Creating Templates</h3>

<h5>This block uncovers the process of building the <b>Single and Archive Product</b> pages with <b>Elementor</b> using <b>JetWooBuilder</b> widgets. </h5>

<ol class="index-list">
<p><li>
  Once you’ve created a template and navigated to Elementor editing page by clicking on <b>Edit with Elementor</b> button, the standard <b>Elementor editor</b> opens.


</li></p>

<p><li>
  You can create any page structure you need by adding the sections, add columns to the sections, duplicate sections and columns, add templates you’ve already created, and use now only the widgets added by <b>JetWooBuilder</b>, but also any other widget you want to place to the template, which is available in the <b>Elements</b> block.
</li></p>

<p><li>
  To add a new section click <b>Add New Section</b> button. Click <b>Edit Section</b> option in order to set the content width, section background style, paddings and margins, etc.

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/templ/1.png">
    </figure>

        <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/templ/3.png">
    </figure>

</li></p>

<p><li>
  Select the preferable columns structure for the section. You can also drag the column borders in order to set the custom columns width. You can also edit the columns width, set the content position, etc, by editing the column.

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/templ/2.png">
    </figure>

</li></p>

<p><li>
  After that navigate to <b>Elements</b> list and here scroll down to see <b>JetWooBuilder</b> widgets.

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/templ/6.png">
    </figure>

</li></p>

<p><li>
  Drag and drop the widgets to the columns in order to place them on the page.

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/templ/7.png">
    </figure>

</li></p>

<p><li>
  After the page is built click <b>Publish</b> button to preserve the changes.

</li></p>

<p><li>
  Go back to the Dashboard by clicking <b>Exit to Dashboard</b> and here click <b>Products > All Products</b>.

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/templ/8.png">
    </figure>

        <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/templ/9.png">
    </figure>
</li></p>

<p><li>
  Click <b>Edit</b> link under the product to which you want to apply a template. Scroll a bit down and locate <b>JetWooBuilder Template Settings</b>.

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/templ/10.png">
    </figure>
</li></p>

<p><li>
  Here in the <b>Custom Template</b> select the newly created template from the dropdown list.

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/templ/12.png">
    </figure>
</li></p>

<p><li>
  Click <b>Publish</b> to save the changes.

</li></p></ol>




