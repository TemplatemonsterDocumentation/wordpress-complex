<h2>Single Tabs</h2>

<h6>
This widget provides the opportunity to place the reviews and product description into tabs, which can be opened on click. This widget allows to effectively use the space of the <b>Single Product</b> page.
</h6>

<div class="alert alert-info">
        Note, that the content displayed in the tabs should be added to the product previously to displaying it.
</div>

<h4>Style</h4>

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/single-tabs.png">
    </figure>

<h6>General</h6>

<ul class="marked-list">
	<li><p><b>Tabs Position</b> - here you can select the most preferable position for the tabs (top, left or right).</p></li>

	<li><p><b>Tabs Items Display</b> - here you can set the width to use for the Tab containers.</p></li>

	<li><p><b>Tabs Alignment</b> - in this block you can select the alignment to use for the tabs (right, left, center, stretch).</p></li>
</ul>

<h6>Tabs Nav</h6>

<ul class="marked-list">
	<li><p><b>Background Type</b> - here you can set the preferable background type to use for the tab controls (boxes). It can be <b>Classic</b> or <b>Gradient</b> background.</p>

<p><b>Classic Background Type</b></p>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can set  the specific color to use for the tab controls.</p></li>

	<li><p><b>Image</b> - click the image box and upload an image in order to use it as a background for the tab controls.</p></li>

	<li><p><b>Position</b> - here you can position the image used for the background according to your needs.</p></li>

	<li><p><b>Attachment</b> - here you can make the image fixed or allow it to be scrolled.</p></li>

	<li><p><b>Repeat</b> - here you can allow the image to be repeated horizontally, vertically, tiled or restrict repeating.</p></li>

	<li><p><b>Size</b> - select <b>Cover, Contain</b> or <b>Auto</b> image sizing in order to make it fitted into the tabs controls container.</p></li>

<p><b>Gradient Background Type</b></p>

	<li><p><b>Color</b> - add the first color to use for the gradient background of the tab controls.</p></li>

	<li><p><b>Location</b> - here you can change the location of the first gradient color.</p></li>

	<li><p><b>Second Color</b> - add the second color to use for the gradient background.</p></li>

	<li><p><b>Location</b> - here you can change the location of the second gradient color.</p></li>

</li>


	<li><p><b>Type</b> - here you can change the gradient location from linear to radial.</p></li>

	<li><p><b>Angle</b> - if the gradient type is lineer, you can change the angle to use for it.</p></li>

	<li><p><b>Position</b> - if the gradient is radial, here you  can change the position of its center to the preferable one.</p></li>

	<li><p><b>Padding</b> - here you can set the padding for the tabs content.</p></li>

	<li><p><b>Border type</b> - here you can change the border type to the one needed (solid, dotted, dashed, double, groove or none).</p></li>

	<li><p><b>Width</b> - here you can set the width of the border in px.</p></li>

	<li><p><b>Color</b> - here you can choose the color to use for the border.</p></li>

	<li><p><b>Border Radius</b> - here you can apply a radius for the border andles to make them more smooth.</p></li>
</ul>

<h6>Tabs Nav Item</h6>

<ul class="marked-list">
	<li><p><b>Typography</b> — turn the option on to view the typography settings for the tabs nav text.
		<ul class="marked-list">
			<li><b>Size</b> — define the font size of the  tabs nav text.</li>
			<li><b>Family</b> — here you can set the font family for the  tabs nav text.</li>
			<li><b>Weight</b> — in this block you can select the suitable font weight.</li>
			<li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</li>
			<li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li>
			<li><b>Line Height</b> — in this field you can set the height of the  tabs nav text.</li>
			<li><b>Letter Spacing</b> — here you can set the space between the  tabs nav text characters.</li>
		</ul>
	</p></li>

	<li><p><b>Item Text Alignment</b> - here you can change the text alignment in the tabs (left, center or right).</p></li>

	<li><p><b>Padding</b> - here you can apply the paddings for the tabs controls content.</p></li>

	<li><p><b>Margin</b> - here you can set the margins for the tabs nav elements.</p></li>

	<li><p><b>Border Type</b> - here you can set the type of the border to use (solid, dotted, double, dashed, groove or none).</p></li>

	<li><p><b>Border Width</b> - here you can specify the border width in px.</p></li>

	<li><p><b>Border Color</b> - here you can set the color of the borders to use.</p></li>

	<li><p><b>Border Radius</b> - here you can apply the radius for the border angles to make them more rounded and smooth.</p></li>
</ul>

<p>Switch between <b>Normal</b>, <b>Hover</b> and <b>Active</b> modes to change the appearance of the tabs on mouse hover, when they are opened or inactive.</p>

<ul class="marked-list">
	<li><p><b>Text Color</b> - here you can apply the borders to use for the tabs nav items.</p></li>

	<li><p><b>Background</b> - here you can set the color to use for the tab nav items.</p></li>

	<li><p><b>Box Shadow</b> - here you can set the shadow for each of the item nav boxes.</p></li>
</ul>

<h6>Tabs Content</h6>

<ul class="marked-list">
	<li><p><b>Background Type</b> - here you can set the preferable background type to use for the tab item content box. It can be <b>Classic</b> or <b>Gradient</b> background. The settings are similar to the ones for the backgrounds used for another JetWooBuilder elements.</p></li>

	<li><p><b>Padding</b> - here you can define the specific padding values to use for the tabs content.</p></li>

	<li><p><b>Border Type</b> - here you can specify the suiting border type to use for the tabs content (solid, dotted, double, dashed, groove or none).</p></li>

	<li><p><b>Width</b> - here you can set the width to use for the border in px.</p></li>

	<li><p><b>Color</b> - here you can specify the color to use for the border.</p></li>

	<li><p><b>Border Radius</b> - here you can apply the radius for the borders to make the angles more round and smooth.</p></li>
</ul>
