<h2>Single Content</h2>


<h6>
This widget is used to display the content which is added in the <b>Product content</b> block. It has several style settings and allows displaying the already created content.
</h6>

<div class="alert alert-info">
  Note, that in order to display the content using this widget You need to add the content to the product into the content field.
</div>

