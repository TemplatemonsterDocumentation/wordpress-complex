<h2>Single Rating</h2>

<h6>
This widget is invaluable when adding the product rating to the <b>Single Product</b> page and styling it up according to one’s vision.
</h6>

<div class="alert alert-info">
        Note, that in order to view how the style changes look, there must be at least 1 review added to one of the products.
</div>

<h4>Style</h4>

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/single-rating.png">
    </figure>

<ul class="marked-list">
	<li><p><b>Elements Display</b> - here you can specify if you want the stars and review link to be shown in line or as a block.</p></li>

	<li><p><b>Alignment</b> - here you can set the rating to be shown in the center, justified, or align it right or left.</b></p>
</li>

<h6>Stars</h6>

<p>Switch between <b>All</b> and <b>Rated</b> modes in order to customize the appearance of both.</p>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can change the color of the stars in the rating.</p></li>

	<li><p><b>Font Size</b> - in this block you can change the size of the stars.</p></li>

	<li><p><b>Margin</b> - set the custom margins to apply for the stars.</p></li>

<h6>Reviews Link</h6>

<ul class="marked-list">
	<li><p><b>Typography</b> — turn the option on to view the typography settings for the review link.
		<ul class="marked-list">
			<li><b>Size</b> — define the font size of the review link.</li>
			<li><b>Family</b> — here you can set the font family for the review link.</li>
			<li><b>Weight</b> — in this block you can select the suitable font weight.</li>
			<li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</li>
			<li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li>
			<li><b>Line Height</b> — in this field you can set the height of the review link.</li>
			<li><b>Letter Spacing</b></li> — here you can set the space between the review link characters.</li>
		</ul>
	</p></li>
</ul>

<p>Switch between <b>Normal</b> and <b>Hover</b> modes in order to make changes in the appearance of the <b>Review links</b> on mouse hover and in its normal state.</p>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can specify which color you want to apply for the review link.</p></li>

	<li><p><b>Margin</b> - in this block you can set the custom margins to use for the review link.</p></li>
</ul>
