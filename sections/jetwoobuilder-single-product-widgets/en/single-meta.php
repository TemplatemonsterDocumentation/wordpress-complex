<h6>SKU</h6>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can set the custom color to use it for the SKU unique product value.</p></li>

	<li><p><b>Typography</b> — turn the option on to view the typography settings for the product SKU text.
		<ul class="marked-list">
			<li><b>Size</b> — define the font size of the product SKU text.</p></li>
			<li><b>Family</b> — here you can set the font family for the product SKU text.</p></li>
			<li><b>Weight</b> — in this block you can select the suitable font weight.</p></li>
			<li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</p></li>
			<li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</p></li>
			<li><b>Line Height</b> — in this field you can set the height of the product SKU text.</p></li>
			<li><b>Letter Spacing</b> — here you can set the space between the product SKU text characters.</p></li>
		</ul>
	</p></li>

	<li><p><b>Margin</b> - here you can set the preferable margins to use for the SKU text and value.</p></li>

	<li><p><b>Alignment</b> - here you can set the preferable alignment to use for the content (left, center, right).</p></li>
</ul>

