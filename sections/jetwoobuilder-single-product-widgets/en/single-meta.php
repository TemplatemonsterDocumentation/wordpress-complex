<h2>Single Meta</h2>

<h6>
This widget embeds the SKU number, category and tag used for the product to the page, and allows changing the color, typography and alignment for this information.
</h6>

<div class="alert alert-info">
        Note, that before displaying the SKU using this widget one needs to add the <b>SKU</b> value in the <b>Product Data > Inventory</b>. You should also add the categories and the tags for the product before displaying them with the widget.
</div>

