<h2>Single Upsells</h2>

<h6>
This widget adds the products which may also be needed by the customer when he purchases the product shown on the <b>Single Product</b> page. The upsell products can be set in <b>Linked Products Upsells</b> field available in <b>Products > Product Data</b> block.
</h6>

