<h6>Title</h6>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can change the color of the widget title.</p></li>

	<li><p><b>Typography</b> — turn the option on to view the typography settings for the title text.
		<ul class="marked-list">
			<li><b>Size</b> — define the font size of the  title text.</li>
			<li><b>Family</b> — here you can set the font family for the title text.</li>
			<li><b>Weight</b> — in this block you can select the suitable font weight.</li>
			<li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</li>
			<li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li>
			<li><b>Line Height</b> — in this field you can set the height of the title text.</li>
			<li><b>Letter Spacing</b> — here you can set the space between the title text characters.</li>
		</ul>
	</p></li>

	<li><p><b>Margin</b> - here you can specify the margins to use for the widget title text.</p></li>

	<li><p><b>Alignment</b> - here you can specify the alignment of the widget title (left, center, right).</p></li>
</ul>
