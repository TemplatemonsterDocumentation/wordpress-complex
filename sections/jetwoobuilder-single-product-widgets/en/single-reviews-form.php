<h2>Single Reviews Form</h2>

<h6>
This widget allows displaying the reviews for the product along with the field for adding a new review. The Single Reviews Form widget is necessary when one wants the customers to be able to leave reviews.
</h6>

<div class="alert alert-info">
        Note, that at least 1 review needs to be added to the product for you to be able how the advanced appearance changes look.
</div>

