<h2>Single Related Products</h2>

<h6>
This widget makes it possible to display the related products list and style the colors and the typography for them. Note, that in the related products the widget displays the recently added products that have similar tags or categories.
</h6>

<h4>Style </h4>

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/single-related-products.png">
    </figure>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can change the color which is applied to the title of the widget.</p></li>

	<li><p><b>Typography</b> — turn the option on to view the typography settings for the related products title.
		<ul class="marked-list">
			<li><b>Size</b> — define the font size of the related products title.</li>
			<li><b>Family</b> — here you can set the font family for the related products title.</li>
			<li><b>Weight</b> — in this block you can select the suitable font weight.</li>
			<li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</li>
			<li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li>
			<li><b>Line Height</b> — in this field you can set the height of the related products title.</li>
			<li><b>Letter Spacing</b> — here you can set the space between the related products title characters.</li>
		</ul>
	</p></li>


	<li><p><b>Margin</b> - here you can set the custom margins to use for the title.</p></li>

	<li><p><b>Alignment</b> - here you can change the position of the title aligning it to the center, left or right.</p></li>
</ul>
