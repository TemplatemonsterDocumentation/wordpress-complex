<h2>Single Sale Badge</h2>

<h6>
This widget makes it simple to add the <b>Sale badge</b> to the product’s page. Note, that one should set the <b>Sale price</b> in <b>Products > Product Data Sale Price</b> field to enable this functionality.
</h6>

<div class="alert alert-info">
        Note, that to be able to view how the style settings affect the widget’s appearance one needs to add a <b>Sale price</b> for the product in the corresponding field via <b>Product Data > General</b>.
</div>

<h4>Style </h4>

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/single-sale-badge.png">
    </figure>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can change the color to use it for the Sale badge text.</p></li>

	<li><p><b>Background</b> - here you can change the color of the Sale badge background.</p></li>

	<li><p><b>Typography</b> — turn the option on to view the typography settings for the Sale badge text.
		<ul class="marked-list">
			<li><b>Size</b> — define the font size of the Sale badge text.</li>
			<li><b>Family</b> — here you can set the font family for the Sale badge text.</li>
			<li><b>Weight</b> — in this block you can select the suitable font weight.</li></li>
			<li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</li>
			<li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li>
			<li><b>Line Height</b> — in this field you can set the height of the Sale badge text.</li>
			<li><b>Letter Spacing</b> — here you can set the space between the Sale badge text characters.</li>
		</ul>
	</p></li>

	<li><p><b>Border Type</b> - here you can change the appearance of the Sale badge border by selecting one of the border types.</p></li>

	<li><p><b>Border Radius</b> - here you can change the border radius in order to make the angles more smooth and rounded.</p></li>

	<li><p><b>Box Shadow</b> - here you can apply the shadow to use for the badge.</p></li>

	<li><p><b>Padding</b> - here you can apply custom padding in order to use it for the element.</p></li>
</ul>
