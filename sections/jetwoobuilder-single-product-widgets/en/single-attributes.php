<article><h2>Single Attributes</h2>


<h6>
This widget is used to display the attributes which are added in the <b>Product > Product Data</b> in the <b>Attributes</b> block.
</h6>

<h4>Content</h4>

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/single-attributes-content.png">
    </figure>

<ul class="marked-list">
	<li><p><b>Title Text</b> - here you can input the text to use it as a title for the product attributes.</p></li>
	<li><p><b>Title Tag</b> - select the title heading tag to apply to the attributes heading. </p></li>
</ul>

<h4>Style</h4>

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/single-attributes-style.png">
    </figure>

<h6>Title</h6>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can change the color of the title text using the color picker tool. </p></li>
	<li><p><b>Typography</b> — turn the option on to view the typography settings for the title text.
		<ul class="marked-list">
			<li><b>Size</b> — define the font size of the title text.</li>
			<li><b>Family</b> — here you can set the font family for the title text.</li>
			<li><b>Weight</b> — in this block you can select the suitable font weight.</li>
			<li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</li>
			<li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li>
			<li><b>Line Height</b> — in this field you can set the height of the title text.</li>
			<li><b>Letter Spacing</b> — here you can set the space between the title text characters.</li>
		</ul>
	</p></li>
	<li><p><b>Margin</b> - here you can change the margins to use them for the title of the attributes.</p></li>

	<li><p><b>Padding</b> - input the padding values in order to use the custom paddings for the attributes title.</p></li>

	<li><p><b>Alignment</b> - here you can set the preferable (right, left, center) alignment for the title text.</p></li>
</ul>

<h6>Attributes Table</h6>

<ul class="marked-list">
	<li><p><b>Table Width</b> - here you can set the custom width for the attributes table to make it responsive.</p></li>
</ul>

<p><b>Title</b></p>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can set the color to use it for the attribute.</p></li>

	<li><p><b>Typography</b> — turn the option on to view the typography settings for the attribute text.
		<ul class="marked-list">
			<li><b>Size</b> — define the font size of the attribute text.</li>
			<li><b>Family</b> — here you can set the font family for the attribute text.</li>
			<li><b>Weight</b> — in this block you can select the suitable font weight.</li>
			<li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</li>
			<li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li>
			<li><b>Line Height</b> — in this field you can set the height of the attribute text.</li>
			<li><b>Letter Spacing</b> — here you can set the space between the attribute text characters.</li>
		</ul>
	</p></li>


<ul class="marked-list">
	<li><p><b>Background Color</b> - here you can set the needed background color of the attribute text. </p></li>

	<li><p><b>Border Type</b> - here you can set the border type you want to use for the attribute (solid, dotted, double, dashed, groove, none). The border settings are similar for different elements used in JetWooBuilder.</p></li>

	<li><p><b>Width</b> - here you can specify the width of the border in px.</p></li>

	<li><p><b>Color</b> - here you can change the color of the border.</p></li>

	<li><p><b>Border Radius</b> - here you can apply the border radius to make the angles more rounded and smooth.</p></li>

	<li><p><b>Padding</b> - input the padding values in order to use the custom paddings for the attributes.</p></li>

	<li><p><b>Alignment</b> - here you can set the preferable (right, left, center) alignment for the attributes.</p></li>
</ul>

<h6>Value </h6>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can change the color of the attribute value. </p></li>

	<li><p><b>Typography</b> — turn the option on to view the typography settings for the attribute value text.
		<ul class="marked-list">
			<li><b>Size</b> — define the font size of the attribute value text.</li>
			<li><b>Family</b> — here you can set the font family for the attribute value text.</li>
			<li><b>Weight</b> — in this block you can select the suitable font weight.</li>
			<li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</li>
			<li><b>Style</b> — in this block you can choose from the dropdown menu the style of the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li>
			<li><b>Line Height</b> — in this field you can set the height of the attribute value text.</li>
			<li><b>Letter Spacing</b> — here you can set the space between the attribute value text characters. </li>
		</ul>
	</p></li>


	<li><p><b>Background Color</b> - here you can change the background of the attribute value block.</p></li>

	<li><p><b>Border Type</b> - here you can set the border type you want to use for the attribute value (solid, dotted, double, dashed, groove, none). The border settings are similar for different elements used in JetWooBuilder.</p></li>

	<li><p><b>Width</b> - here you can specify the width of the border in px.</p></li>

	<li><p><b>Color</b> - here you can change the color of the border.</p></li>

	<li><p><b>Border Radius</b> - here you can set the needed values for the borders in order to make the border angles more round and smooth.</p></li>

	<li><p><b>Padding</b> - here you can set your own custom padding values in px.</p></li>

	<li><p><b>Alignment</b> - here you can specify the left, right or center alignment according to your needs.</p></li>
