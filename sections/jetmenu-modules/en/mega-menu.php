<h3>Mega Menu Module</h3>

<h5>From this block you’ll learn how to create a horizontal menu using Elementor only. </h5>

<h5>Adding Mega Menu Module</h5>


<ol class="index-list">
<p><li>First of all, please, navigate to <b>Appearance > Menus</b> tab on the left of the Dashboard and turn off <b>Enable JetMenu for current location</b> option. Then click <b>Save</b> button.</li></p>



  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-module/777.png">
    </figure>

<p><li>After that navigate to <b>Pages > Add new</b> on the left of the Dashboard and click <b>Edit with Elementor</b> option under the page where you want to add a horizontal menu.</li></p>


  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-module/888.png">
    </figure>

<p><li>Add a new section and select the preferable columns structure in order to drop there a horizontal menu.</li></p>



  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-module/999.png">
    </figure>

<p><li>Select <b>Mega Menu</b> module on the left and drop it to the content area.</li></p>


<p><li>In the newly opened <b>Content</b> block choose a menu you want to embed to the page from <b>Select Menu</b> dropdown.</li></p>


  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-module/yyy.png">
    </figure>

<p><li>Click <b>Save</b> button.</li></p></ol>

<div class="alert alert-info">Note, that you can change the menu style the same way you did it when customizing the simple menu style, using JetMenu style and content settings. To access them click <b>JetMenu</b> tab in the left column of the Dashboard.</div>


<p>You can also add content to menu items. To do it navigate to <b>Appearance > Menus</b> tab on the left of the Dashboard, select the menu to which you want to add content with Elementor, and hover on the menu item. Click <b>JetMenu</b> and edit menu content the same way as described <a href="http://documentation.zemez.io/wordpress/index.php?project=jetmenu&lang=en&section=jetmenu-editing" target="_blank">here</a>. </p>


<h4>Mega Menu Widget Style</h4>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-module/megamen-st.png">
    </figure>

<p>Open <b>Style</b> block. Here you can access different style settings available for JetMenu Mega Menu widget stylization. The options are much needed for creating stylish appearance for menu built with Elementor right from Elementor editor page.</p>

<h5>Menu Container</h5>

<ul class="marked-list">
<p><li><b>Menu Items Alignment</b> - here you can align the menu items to the right, left or center.</li></p>
<p><li><b>Background Type</b> - select Classic or Gradient background in order to use it for menu container. </li></p>

  <p><b>Classic Background Type</b></p>


    <ul class="marked-list">
      <li><b>Color</b> — here you can set background color for the menu using color picker tool.</li>
      <li><b>Image</b> — here you can choose the image you want to use as a background. Select the image from the media library or upload it to define it as a background in this block. When the image is uploaded, you’ll be able to define its position, alignment, turn on repeat and set the necessary size.</li></ul>

    <p><b>Gradient Background Type</b></p>

    <ul class="marked-list">
      <li><b>Color</b> — here you can set background color for the menu using color picker tool.</li>
      <li><b>Location</b> — here you can define the proportion for the first color in the gradient background.</li>
      <li><b>Second Color</b> — here you can set the second color to use in the gradient background.</li>
      <li><b>Location</b> — here you can define the proportion for the second color in the gradient background.</li>
      <li><b>Type</b> — define, if you want to use linear or radial type of the gradient.</li>
      <li><b>Angle</b> — in this block you can set the angle for the gradient (in degrees).</li></ul>

  </li>


  <li><p><b>Border Type</b> - here you can define the type of the border you need to use for the menu. </p></li>
  <li><p><b>Box Shadow</b> - enable this option if you want to access the shadow advanced settings for this module, and need to apply shadow for it.</p></li>
  <li><p><b>Border Radius</b> - here you can set the border radius for the menu.</p></li>
  <li><p><b>Padding</b> - here you can set the custom padding for the menu. Fill in the values for the top, bottom, right and left padding in pixels to apply your custom padding.</p></li>
  <li><p><b>Width</b> - here you can specify the width of the menu in px (use controls or set the preferable value in the corresponding block).</p></li></ul>

<h5>Main Menu Items</h5>

    <ul class="marked-list">
  <li><p><b>Typography(Description Typography)</b> - turn the option on to view the typography settings.</p>

    <ul class="marked-list">
      <li><b>Family</b> — here you can set the font family for the item text.</li>
      <li><b>Size</b> — define the font size of the text.</li>
      <li><b>Weight</b> — in this block you can select the suitable font weight.</li>
      <li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in uppercase, lowercase, capitalize or normal way.</li>
      <li><b>Style</b> — in this block you can choose from the dropdown menu the style for the font. It can be normal, italic (the characters look similar to handwriting) and oblique (the characters are slightly inclined to the right).</li>
      <li><b>Line Height</b> — in this field you can set the height of the text line.</li>
      <li><b>Letter Spacing</b> — here you can set the space between letters.</li></ul>

    <li><p><b>Item max width(%)</b> - here you can choose the spacing between items.</p></li>
    <li><p><b>Text Color(Description color)</b> - here you need to specify the color of the menu item text.</p></li>
    <li><p><b>Icon Color</b> - here you can set the menu icon color using color picker tool.</p></li>
    <li><p><b>Drop-down arrow color</b> - Here you can set the drop-down arrow color using picker tool.</p></li>
    <li><p><b>Background Type</b> - in this block you can choose, whether you want to use classic or gradient type for the menu. The customization options vary for each type.</p></li>
    <li><p><b>Border Type</b> - here you can define the type of the border you need to use for the menu.</p></li>
    <li><p><b>First Item Border Type</b> - here you can set the border type for the first item.</p></li>
    <li><p><b>Last Item Border Type</b> - here you can set the border type for the last item.</p></li>
    <li><p><b>Box Shadow</b> - enable this option if you want to access the shadow advanced settings for this module, and need to apply shadow for it.</p></li>
    <li><p><b>Padding</b> - here you can set the custom padding for the menu. Fill in the values for the top, bottom, right and left padding in pixels to apply your custom padding.</p></li>
    <li><p><b>Margin</b> - here you can define the margins for the menu.</p></li>
  </li></ul>



