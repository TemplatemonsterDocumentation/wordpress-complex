<h3>Vertical Mega Menu Widget</h3>

<div class="alert alert-info">Please, pay attention that this widget is named Custom Menu in the  JetMenu versions below 1.4.0</div>

<h5>From this block you’ll learn how to add the menu to the website’s page using <b>Custom Menu</b> module for Elementor. There are also multiple menu customization options for this content module. </h5>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/popup.png">
    </figure>

<h5>Adding and Customizing Vertical Mega Menu</h5>

<ol class="index-list">
<p><li>First, you need to open the page for editing with Elemenor. Add a new section and define its structure in order to place a menu there.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-module/1.png">
  	</figure>
</li></p>

<p><li>Locate <b>Vertical Mega Menu</b> content module.


 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-vert1.png">
  	</figure>

</li></p>

<p><li>Drag and drop it to the area where you want to add it. Right-click on it and select <b>Edit Widget</b> option.
</li></p></ol>

<h5>Content</h5>

<p>Here you need to specify the menu you want to display with <b>Vertical Mega Menu</b> module. Select the menu you want to add from the drop-down list of your existing menus.</p>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-vert2.png">
    </figure>

<h5>Settings</h5>

<ul class="marked-list">
<p><li><b>Sub menu position</b> - here you can specify if you want the sub menu items to be displayed on the right or on the left from the vertical menu main items.</li></p>

<p><li><b>Animation</b> - here you can select the type of animation to use for the sub items or mega menu content appearance.</li></p></ul>

<h5>Style</h5>

<h6>Main Menu / Mega Menu / Sub menu</h6>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-vert3.png">
    </figure>

<p><b>Main Menu</b>, <b>Mega Menu</b> and <b>Sub Menu</b> blocks contain the same customization features, which are described below.</p>

<ul class="marked-list">
<p><li><b>Main Menu Width / Mega Menu Width / Sub Menu Width</b> - here you can specify the width of the menu in <b>px</b> (use controls or set the preferable value in the corresponding block).</li></p>
<li><p><b>Background Type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the menu. The customization options vary for each type.

<p><b>Classic Background Type</b></p>

<ul class="marked-list">
<li><b>Color</b> — here you can set background color for the menu using color picker tool.</li>
<li><b>Image</b> — here you can choose the image you want to use as a background. Select the image from the <b>media library</b> or upload it to define it as a background in this block. When the image is uploaded, you’ll be able to define its position, alignment, turn on repeat and set the necessary size.</li></ul>

<p><b>Gradient Background Type</b></p>

<ul class="marked-list">
<li><b>Color</b> — here you can set background color for the menu using color picker tool.</li>
<li><b>Location</b> — here you can define the proportion for the first color in the gradient background.</li>
<li><b>Second Color</b> — here you can set the second color to use in the gradient background.</li>
<li><b>Location</b> — here you can define the proportion for the second color in the gradient background.</li>
<li><b>Type</b> — define, if you want to use linear or radial type of the gradient.</li>
<li><b>Angle</b> — in this block you can set the angle for the gradient (in degrees).</li></ul></li>

<p><li><b>Padding</b> - here you can set the custom padding for the menu. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>

<p><li><b>Margin</b> - here you can define the margins for the menu.</li></p>
<p><li><b>Border Radius</b> - here you can set the border radius for the menu.</li></p>
<p><li><b>Border Type</b> - here you can define the type of the border you need to use for the menu.</li></p>
<p><li><b>Box Shadow</b> - enable this option if you want to access the shadow advanced settings for this module, and need to apply shadow for it.</li></p></ul>

<h5>Main Menu Items / Sub Menu Items</h5>

<p><b>Main Menu Items</b> and <b>Sub Menu Items</b> blocks contain the same customization features, which are described below.</p>

<p>You can switch from customizing <b>Normal</b> to <b>Hover</b> style settings. Just click on the button <b>Hover</b> to proceed to customizing hover elements.</p>

<ul class="marked-list">
<p><li><b>Background Color</b> - here you can set the color of the menu item background using color picker tool.</li></p>
<p><li><b>Text Color</b> - here you need to specify the color of the menu item text.</li></p>
<p><li><b>Typography</b> - turn the option on to view the typography settings.

<ul class="marked-list">
<li><b>Size</b> — define the font size of the text.</li>
<li><b>Family</b> — here you can set the font family for the item text.</li>
<li><b>Weight </b>— in this block you can select the suitable font weight.</li>
<li><b>Transform</b> — here you can choose from the dropdown menu, if you want the text to be shown in <b>uppercase, lowercase, capitalize</b> or <b>normal</b> way.</li>
<li><b>Style</b> — in this block you can choose from the dropdown menu the style for the font. It can be <b>normal, italic</b> (the characters look similar to handwriting) and <b>oblique</b> (the characters are slightly inclined to the right).</li>
<li><b>Line Height</b> — in this field you can set the height of the text line.</li>
<li><b>Letter Spacing</b> — here you can set the space between letters.</li></p></ul></li>

<p><li><b>Padding</b> - here you can set the custom padding for the menu items. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Margin</b> - here you can define the margins for the menu items.</li></p>
<p><li><b>Border Radius</b> - here you can set the border radius for the menu items.</li></p>
<p><li><b>Border Type</b> - here you can define the type of the border you need to use for the menu items.</li></p>
<p><li><b>Box Shadow</b> - enable this option if you want to access the shadow advanced settings for this module, and need to apply shadow for it.</li></p>
<p><li><b>First item custom styles</b> - enable this option to access the first item border radius and border type settings.</li></p>
<p><li><b>Last item custom styles</b> - enable this option to access the last item border radius and border type settings.</li></p></ul>

<h6>Icon</h6>

<ul class="marked-list">
<p><li><b>Icon Color</b> - here you can set the menu icon color using color picker tool.</li></p>
<p><li><b>Icon Font Size</b> - use the controls to set the suitable icon size.</li></p>
<p><li><b>Icon Box Size</b> - here you can use the controls to set the icon box size without changing the size of the icon.</li></p>
<p><li><b>Border Type</b> - here you can define the type of the border you need to use for the menu icon.</li></p>
<p><li><b>Border Radius</b> - here you can set the border radius for the menu icon.</li></p>
<p><li><b>Margin</b> - here you can define the margins for the menu icon.</li></p>
<p><li><b>Box Shadow </b>- enable this option if you want to access the shadow advanced settings for this icon, and need to apply shadow for it.</li></p>
<p><li><b>Alignment</b> - select the suitable alignment for the icon (<b>top, center, bottom</b>).</li></p></ul>

<h6>Badge</h6>

<ul class="marked-list">
<p><li><b>Color</b> -  here you can set the menu badge color using color picker tool.</li></p>
<p><li><b>Background Type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the menu badge. The customization options vary for each type. The settings are quite similar to the Main menu background type ones.</li></p>
<p><li><b>Typography</b> - turn the option on to view the typography settings. The settings are quite similar to the <b>Main Menu</b> items typography settings.</li></p>
<p><li><b>Border Type</b> - here you can define the type of the border you need to use for the menu badge.</li></p>
<p><li><b>Border Radius</b> - here you can set the border radius for the menu badge.</li></p>
<p><li><b>Padding</b> - here you can set the custom padding for the menu badge. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Margin</b> - here you can define the margins for the menu badge.</li></p>
<p><li><b>Alignment</b> - select the suitable alignment for the badge (<b>top, center, bottom</b>).</li></p></ul>

<h6>Drop-down Icon</h6>

<ul class="marked-list">
<p><li><b>Icon</b> - click on this option to see the list of all the available icons and pick the one you want to use.</li></p>
<p><li><b>Offset</b> - here you can change the drop-down icon position.</li></p>
<p><li><b>Icon Color</b> - here you can set the menu drop-down icon color using color picker tool.</li></p>
<p><li><b>Icon Background Color</b> - here you need to specify the background color of the drop-down icon.</li></p>
<p><li><b>Icon Font Size</b> - change the size of the icon using controls or define it in px in the corresponding window.</li></p>
<p><li><b>Icon Box Size</b> - change the size of the icon box using controls or specify it in <b>px</b>.</li></p>
<p><li><b>Border Type</b> -  here you can define the type of the border you need to use for the menu drop-down icon.</li></p>
<p><li><b>Border Radius</b> - here you can set the border radius for the menu drop-down icon.</li></p>
<p><li><b>Box Shadow</b> - enable this option if you want to access the shadow advanced settings for this drop-down icon, and need to apply shadow for it.</li></p>
<p><li><b>Alignment</b> - select the suitable alignment for the drop-down icon (<b>top, center, bottom</b>).</li></p></ul>
