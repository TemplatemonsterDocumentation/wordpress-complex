<h3>Cherry Socialize Widgets</h3>

<p>
    Cherry Socialize plugin comes with the Cherry Socialize Instagram widget, which is used to setup and display Instagram feed on the website.

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/images/cherry-social2.png", alt = "" >
</figure>

<p>You can customize the widget by changing latter settings:</p>

	<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Title</dt>
            <dd>
				This property specifies the widget title
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Tagged photos</dt>
            <dd>
                Choose the content type here
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>My photos</dt>
            <dd>
                Choose the content type here
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Hashtag (enter without `#` symbol)</dt>
            <dd>
                Here you can set a hashtag to display the images, selected by this property
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Number of photos </dt>
            <dd>
                This property defines the number of images to show
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Photo size</dt>
            <dd>
                This property defines the size of the shown images from thumbnail to high
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Enable liking photos</dt>
            <dd>
                Enable / disable liking photos right from the webpage
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Caption</dt>
            <dd>
                Enable this option if you want to show captions
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Date</dt>
            <dd>
                Hide/Show date below the images
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Follow us</dt>
            <dd>
                Hide/Show Follow Us button
            </dd>
        </dl>
    </li>
</ul>

     <div class="alert alert-info">
        Be aware that your Instagram images will be saved to a cache for 1 hour by default settings. That's why your new photos wouldn't be shown immediately. Wait for an hour for the cache to be renewed and your new photos will appear.
    </div>
