<h5>Installing Cherry Socialize Plugin</h5>

<ul class = "marked list">
<li><p>Upload plugin to the /wp-content/plugins/ directory.</p></li>
<li><p>Activate Cherry Socialize plugin through the "Plugins" menu in WordPress.</p></li>
</ul>

 <p>You can edit it fom here: <strong> Administration Panel > Plugins > Cherry Socialize > Edit Plugin</strong></p>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/images/cherry-social1.png", alt = "" >
</figure>

<p>You can also use it right after installing! To do this go to <strong>Customizer</strong>, open <strong> Widgets</strong> tab and choose <strong> Add Widget > Cherry Socialize Instagram</strong> . That's it!</p>

