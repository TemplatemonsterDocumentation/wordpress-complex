<h5>General</h5>

<ul class="marked-list">
	<li><b>Background Color</b> - here you can set the needed color to use for the background of the products navigation.</li>

	<li><b>Border Type</b> - here you can define the type of the border to apply for the item  (or apply no border, according to your needs).</li>

	<li><b>Border Radius</b> - here you can set the border radius values to make the angles more smooth and round.</li>

	<li><b>Box Shadow</b> - here you can apply the shadow for the item. Click the Edit pencil option to open the additional shadow settings.</li>

	<li><b>Padding</b> - here you can define the padding values for the item.</li>

    <li><b>Margin</b> - here you can apply the specific margin values for the products navigation and make it responsive.</li></ul>


    <h5>Items</h5>

<ul class="marked-list">
	<li><b>Alignment</b> - here you can define the alignment type to apply for the item (top, center, bottom, stretch).</li>

