<h5>Logo</h5>

<ul class="marked-list">
<p><li><b>Logo Alignment</b> - here you can select the preferable alignment for the logo (left, centered or right).</li></p></ul>

<h5>Text</h5>

<ul class="marked-list">
<p><li><b>Color</b> - here you can specify the color of the logo text.</li></p>

<p><li><b>Typography</b> - turn the option on to view the typography settings for the text. The typography settings are similar to the other ones available for JetBlocks elements included into the widgets.</li></p>
</ul>
