<h3>Site Logo</h3>

<p>This widget allows adding textual and image logo to the headers and footers created for the site pages. </p>

<h4>Content</h4>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/logo.png">
  	</figure>

<ul class="marked-list">
<p><li><b>Logo Type</b> - here you can specify the type of logo you want to use on the page (image, text, or both). Please, note that the customization and content settings will depend on the logo type youâ€™ve selected.</li></p>

<p><li><b>Logo Text Form</b> - here you can set the source from where the text will be used (either the site name or your custom text).</li></p>

<p><li><b>Logo Image</b> - here you can set the image to be shown as a website logo.</li></p>

<p><li><b>Retina Logo Image</b> - here you can upload the image in retina quality to be shown for retina-ready devices.</li></p></ul>

<h5>Settings</h5>

<ul class="marked-list">
<p><li><b>Linked Logo</b> - enable this option if you want the logo to be linked to an URL.</li></p>

<p><li><b>Remove Link on Front Page</b> - here you can remove the link for the logo on front page (in the case it is linked).</li></p></ul>

