<h3>JetBlog Settings</h3>

<h5>This block will give you information on how to use <b>JetBlog</b> plugin settings. </h5>

<ol class="index-list">
<p><li>First, make sure that you’ve installed and activated <a href="https://wordpress.org/plugins/elementor/" target="_blank">Elementor</a> page builder before proceeding to JetElements installation. If you have problems with installing Elementor feel free to read the detailed <a href="https://docs.elementor.com/" target="_blank">Elementor documentation</a>.</li></p>

In order to give JetBlog access to YouTube videos one needs to set a YouTube API key in JetBlog settings. Let’s see how this can be done.

First, navigate to Elementor > JetBlog Settings tab on the left of the Dashboard.

Here input your YouTube API key. If you don’t have one, please, create it here.
