<h2>JetReviews Installation</h2>

<h6>This block uncovers the basic steps You need to accomplish to install <b>JetReviews</b> plugin. </h6>

<ol class="index-list">
<p><li>First, navigate to WordPress Dashboard. Here open <b>Plugins > Add New</b> tab and click <b>Upload</b> button located at the top of the opened page.</li></p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/install2.png">
  	</figure>

<p><li>Browse the archive containing <b>JetReviews</b> plugin and click <b>Install Now</b> button.

<div class="alert alert-info">Note, that before this step you need to unpack the package downloaded from the marketplace. It contains documentation and the very <b>.zip</b> archive you need to download in order to install JetReviews.</div></li></p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/install3.png">
  	</figure>

<p><li>After that click <b>Activate Plugin</b> button to activate the plugin on Your site.</li></p>

<p><li>Please, make sure that both <b>JetReviews</b> and <b>Elementor</b> plugins are active on Your site. To do it navigate to <b>Plugins > Installed Plugins</b> and view the list of installed and active plugins. </li></p></ol>





