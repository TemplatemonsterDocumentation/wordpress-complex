<h2>Adding Reviews via Dashboard</h2>

<h6>
From this block you can learn more about adding reviews using the meta boxes via WordPress Dashboard.
</h6>


<ol class="index-list">
<p><li>First, let’s navigate to <b>Elementor > JetReviews Settings</b> tab. </li></p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/rev1.png">
  	</figure>

<p><li>Here you need to enable the <b>Meta Box</b> for the regular <b>Posts</b> and <b>Pages</b> and for other custom post types, such as <b>Projects, Products</b>, etc. Then click <b>Save</b> button. </li></p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/rev2.png">
  	</figure>

<p><li>After that go to <b>Posts</b> and click <b>Edit</b> option under the post to which you want to add a review.</li></p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/rev5.png">
  	</figure>

<p><li>Scroll the post editing page down and locate the <b>JetReviews</b> block. </li></p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/rev6.png">
  	</figure>

<p><li>Here you need to fill in the following fields:

<ul class="marked-list">
<p><li><b>Title</b> - give the general title of Your <b>Review</b> block. </li></p>

<p><li><b>Summary Title</b> - add the title to describe the conclusion of your review in few words. </li></p>

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/rev7.png">
  	</figure>

<p><li><b>Summary Text</b> - there you can add an explanation (share your opinion about the product, service, etc.). </li></p>

<p><li><b>Summary Legend</b> - add the text which will show up above the summary value (e.g., <b>Excellent, Good, Poor</b>, etc.). </li></p>

</ul></li></p>

<p><li>In <b>Review Fields</b> click <b>Add New Field</b> option. Here you can add as many fields as there would be reviewed features (e.g., <b>Design, Efficiency</b>, etc.). </li></p>

<p><li>Each <b>Review field</b> has the following additional fields You’ll need to fill in in order to provide information for the post:

<ul class="marked-list">
<p><li><b>Label</b> - here you need to name the feature you want to mention in the review (e.g., <b>Quality, Design,</b> etc.). </li></p>


<p><li><b>Value</b> - here you need to define the value which you want to assign to this feature (e.g., 55).</li></p>


<p><li><b>Max Value</b> - here you need to set the maximum value (e.g., 100, in this case the value will be set as 55 from 100 points). </li></p></ul></li></p>

<p><li>After that click <b>Update</b> button to save the changes for the post. Well done! </p>

<p>Now we have a pre-made review meta for the post that can be added to the post using Review widget when choosing <b>Post Meta</b> in <b>Review > Content > Content Source</b> dropdown list. </li></p></ol>

