<h2>Enabling Reviews from Front-end</h2>

<h6>From this block you can learn more about the way of adding review form to your post with products and using its features.</h6>

<h5>Appending and customizing Review widget in Elementor</h5>

<ul class="index-list">
<p><li>First, letâ€™s open the post with products and click <b>Edit with Elementor</b> button.</li></p> 
<p><li>Then, find the <b>Review</b> widget in the elements panel and drag it to the post. Now proceed to customize the review form.</li></p>
<p><li>Select <b>Post Meta</b> content source in the <b>Content</b> section.</li></p>
<p><li>Input <b>post ID</b> in order to get data from the post you have chosen.</li>
<p>To define the appropriate <b>post ID</b> go to posts lists on your <b>WordPress Dashboard</b>, hover over the particular post and you will see the address line with the post ID, like post=2221, at the bottom left of the browser.</p>
<p><li>Fill in the <b>Get Data From Post</b> field with the ID and click <b>Update</b>.</li></p></p></ul>

<h5>Setting criteria fields</h5>
<ul class="index-list">
<p><li>Go to the default post editor, where you can see <b>JetReviews</b> fields. Click <b>Add New Field</b> button.</li></p>
<p><li>Type the <b>Title</b> of the review and set the <b>min</b> and <b>max</b> values for evaluation of the product. Then click <b>Update</b> and view your post.</li></p>
<p><li>Also you can customize review form in the <b>Style</b> block in Elementor.</p></li></p></ul>