<h2>TM WooCommerce Ajax Filters</h2>

<h5>
    <p>
    	The plugin can be used as an addition to WooCommerce plugin and allows to add various filters to the product pages. Additionally, the plugin offers AJAX pagination,  AJAX filter and AJAX layout switcher.
    </p>
</h5>

<h4>
AJAX filters
</h4>
<p>
Ajax filters can be displayed with the help of widgets that you can add to any area. One widget displays one filter.    
</p>
<p>
<strong>NOTE:</strong> Widgets are displayed in product page listing only and if filtering criteria match with the listed products.    
</p>


