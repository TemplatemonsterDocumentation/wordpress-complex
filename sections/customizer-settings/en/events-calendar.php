<h3>The Events Calendar</h3>


<p>Here you can configure Events Calendar</p>



<h5>General Theme</h5>

<p>Here you can customize the general theme settings and apply global configurations for the styling of The Events Calendar</p>


<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Accent Color</dt>
            <dd>
            	Select the Accent Color for the Events Calendar
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Featured Events Highlight Color</dt>
            <dd>
