<h5>Plugin functions</h5>
<p>Export/Import functions of TM Style Switcher plugin are presented in Export/Import Customizer tab 
</p>
    
    <figure class="img-polaroid">
        <img src="img/plugins/export-import.png" alt="" >
    </figure>

    <ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Export</dt>
            <dd>
                EXPORT button generates and downloads files in json format
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Import</dt>
            <dd>
                By pressing on IMPORT button starts the process of importing and installing your theme settings. At the end the page will be reloaded with new settings
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Restore Settings</dt>
            <dd>
                After first activation of the plugin you get a section with default theme settings in the base. By pressing on RESTORE SETTINGS plugin downloads default theme settings and reloads the page
            </dd>
        </dl>
    </li>
</ul>

<h5>Additional functions</h5>
<p>The plugin allows to implement the so-called Skin Switcher or Style Switcher using the export files.
</p>

<p>Every skin is presented in the form of a registered preset. The presets can be found in the tab of TM Style Switcher customizer. When tapping on a preset the settings from a json file, which were attached to the preset in the registration process, will be imported.</p>

