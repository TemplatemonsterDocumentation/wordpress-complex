<h2>Single Excerpt</h2>



<h6>
This widget is perfect for displaying the excerpt which is added in the <b>Product Short Description</b> field on the Product page.
</h6>

<div class="alert alert-info">
  Note, that previously to displaying the product description you need to enter the description for the product in the corresponding field on the <b>Product editing</b> page.
</div>

