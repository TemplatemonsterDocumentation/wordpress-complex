<article><h2>Single Images</h2>


<h6>
This widget makes it simple to add the product featured image to the product page and has stylization options available for setting up.
</h6>

<div class="alert alert-info">
	Note, that before using this widget one needs to upload the featured image for the product on the <b>Product editing page</b>.
</div>

<h4>Style</h4>

    <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/modules/single-images.png">
    </figure>

<h6>Main Image </h6>

<ul class="marked-list">
	<li><p><b>Images Block Width</b> - here you can change the width of the block where the image is shown in percentage for different devices.</p></li>

	<li><p><b>Margin</b> - here you can set the preferable margins to use for the image on different devices.</p></li>

	<li><p><b>Border type</b> - here you can set the border to use for the image (solid, dotted, dashed, double, groove or none).</p></li>

	<li><p><b>Width</b> - here you can set the custom width to apply for the border.</p></li>

	<li><p><b>Color</b> - set the needed color to use for the border.</p></li>

	<li><p><b>Box Shadow</b> - here you can apply the shadow for the image container. The shadow settings are similar for all of JetWooBuilder widgets and their stylization elements.</p></li>
</ul>

<h6>Thumbnails</h6>

<ul class="marked-list">
	<li><p><b>Thumbnails Width</b> - here you can apply the custom width for the product image thumbnails.</p></li>

	<li><p><b>Padding</b> - in this block you can set the preferable padding values to use for the image thumbnails.</p></li>

	<li><p><b>Border Type</b> - here you can set the border to use for the image thumbnails (solid, dotted, dashed, double, groove or none).</p></li>

	<li><p><b>Width</b> - here you can set the custom width to apply for the border.</p></li>

	<li><p><b>Color</b> - set the needed color to use for the border.</p></li>

	<li><p><b>Box Shadow</b> - here you can apply the shadow for the image thumbnails container. The shadow settings are similar for all of JetWooBuilder widgets and their stylization elements.</p></li>
</ul>
