<h2>Taxonomy Tiles</h2>

<h6>
This widget allows to display the taxonomy categories or tags for the WooCommerce products in the form of attractive tile layouts on the pages built with Elementor.
</h6>

<div class="alert alert-info">
        Note, that before using this widget you need to create WooCommerce products and then assign them to categories and tags (the number of tags and categories should be at least the same as the number of tiles). It is also necessary to set the thumbnail images for the WooCommerce categories and tiles, as well as descriptions.
</div>

<h4>Content</h4>

<h5>General</h5>

<ul class="marked-list">
	<li><p><b>Layout</b> - here you can set one of the available 9 layouts for the taxonomy tiles.</p></li>

	<li><p><b>Item Style</b> - select one of the styles available in the dropdown list.</p></li>

	<li><p><b>Min Height</b> - here you can set the minimum height to apply for the layout.</p></li>

	<li><p><b>Main Box Width</b> - here you can specify the width of the tiles main box.</p></li>

	<li><p><b>Show</b> - here you can select the taxonomy which will be displayed in the tiles (categories or tags).</p></li>

	<li><p><b>Hide Empty</b> - enable this option in order to hide the empty categories or tags from view.</p></li>

	<li><p><b>Hide Uncategorized</b> - enable this option in order to hide the uncategorized products from view.</p></li>

	<li><p><b>Show by IDs</b> - enable this option in order to be able to show the categories or tags by IDs.</p></li>

	<li><p><b>Exclude taxonomy by IDs</b> -  here you can specify which categories or tags you need to exclude using their IDs.</p></li>

	<li><p><b>Order by</b> - here you can set the ascending or descending order to apply for the categories or tags.</p></li>

	<li><p><b>Sort by</b> - here you can set the method by which the categories or tags will be sorted (name, count or IDs).</p></li>

	<li><p><b>Show Counts</b> - enable this option to show how many products are included into each of the categories or tags.</p></li>

	<li><p><b>Title Max Length</b> - here you can set the maximum length to apply it for the title of the category or tag.</p></li>

	<li><p><b>Description Length</b> - here you can specify the maximum length of the category or tag description.</p></li>

	<li><p><b>Count Before/After Text</b> - here you can set the brackets, symbols or text to be shown before and after the count of products.</p></li></ul>

<h4>Style</h4>

<h5>Box</h5>

<ul class="marked-list">
	<li><p><b>Gap Between Boxes</b> - here oyu can set the necessary gap distance between the tiles.</p></li>

	<li><p><b>Border Type</b> - here you can apply the needed border type for the content and set its width, color, as well as define the border None not to use any borders for the count.</p></li>

	<li><p><b>Border Radius</b> - here you can apply the custom border radius values to make the border angles more smooth.</p></li>

	<li><p><b>Box Shadow</b> - here you can apply the shadow for the box and customize its appearance according to your needs.</p></li>

	<li><p><b>Vertical / Horizontal Alignment</b> - here you can set the alignment for the box.</p></li>

	<li><p><b>Padding</b> - here you can set your custom paddings to use for the box and make them responsive.</p></li></ul>


