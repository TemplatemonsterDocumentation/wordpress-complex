	<li><p><b>Border Type</b> - here you can select the desirable border type to apply for the button or apply no border at all.</p></li>

	<li><p><b>Width </b>- here you can set the needed width for the border.</p></li>

	<li><p><b>Color </b>- here you can specify the color to apply for the border.</p></li>

