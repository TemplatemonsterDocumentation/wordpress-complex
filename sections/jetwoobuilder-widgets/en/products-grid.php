<h2>Products Grid</h2>

<h6>
This widget adds the WooCommerce products into attractive grid layouts to the pages built with Elementor.
</h6>

<div class="alert alert-info">
        Note, that before dusing this widget you need to create the WooCommerce products with featured images, descriptions and prices in place.
</div>

<h4>Content</h4>

<h5>General</h5>

<ul class="marked-list">
	<li><p><b>Product Layout</b> - here you can set one of 5 available grid layouts to apply for the products.</p></li>


	<li><p><b>Columns</b> - here you can change the number of columns to use for the grid (for 1 to 6 columns) and make the columns responsive by changing their number for desktop, mobile and tablet devices.</p></li>

	<li><p><b>Equal Columns Height</b> - enable this option in order to make the height of the columns the same for all every column.</p></li>

	<li><p><b>Add gap between columns</b> - here you can set the gap distance between the columns.</p></li>

	<li><p><b>Add gap between rows</b> - here you can apply the gap distance between the grid rows.</p></li>

	<li><p><b>Products number</b> - here you can set the number of categories to be shown.</p></li>

	<li><p><b>Query products by</b> - here you can specify the query method to apply for the products shown in the grod (all, featured, sale, tag, category, specific ID).</p></li>

	<li><p><b>Order by</b> - here you can set the needed product order in the grid.</p></li>

	<li><p><b>Show Products Title</b> - here you can enable the displaying of the products title.</p></li>

	<li><p><b>Show Products Featured Image</b> - enable this option in order to enable the use of the featured images for the products.</p></li>

	<li><p><b>Featured Image Size</b> - here you can select the size to apply for the featured image (the sizes often depend on the ones already available in the theme for customization).</p></li>

	<li><p><b>Show Badges</b> - enable this option to display the badges.</p></li>

	<li><p><b>Set Sale badge text</b> - here you can set the text which will be displayed inside the badge.</p></li>

	<li><p><b>Show Product Excerpt</b> - here you can enable displaying of the product description excerpt.</p></li>

	<li><p><b>Show Product Categories</b> - here you can define if the categories for the products will be displayed.</p></li>

	<li><p><b>Show Product Tags</b> - here you can enable the displaying of the product tags.</p></li>

	<li><p><b>Show product Price</b> - enable this option to make the price visible for the products.</p></li>

	<li><p><b>Show Product rating</b> - here you can enable showing the rating of the products.</p></li>

	<li><p><b>Show Add to Cart button</b> - enable this option to let the visitors purchase the products shown in the grid.</p></li></ul>


<h5>Carousel</h5>


<ul class="marked-list">
	<li><p><b>Enable Carousel</b> - enable this option in order to apply the categories carousel.</p></li>

	<li><p><b>Slides Minimal Height</b> - here you can define the minimum height for the carousel slides to use for different devices.</p></li>

	<li><p><b>Slides to Scroll</b> - here you can specify the number of slides to scroll.</p></li>

	<li><p><b>Show Arrows Navigation</b> -  enable this option in order to display the navigation arrows for the slides to allow the users switch the slides.</p></li>

	<li><p><b>Prev/Next Arrow Icon</b> - here you can specify the icons to use for the navigation.</p></li>

	<li><p><b>Show Dots Navigation</b> - enable this option to display the pagination dots for the slides to enable users switch between them.</p></li>

	<li><p><b>Pause on Hover</b> - enable this option to make the carousel or slider pause when one hovers on it.</p></li>

	<li><p><b>Autoplay</b> - enable this option to use the autoplay for the slides.</p></li>

	<li><p><b>Autoplay Speed</b> - here you can specify the speed used for the autoplay.</p></li>

	<li><p><b>Infinite Loop</b> - enable this option to make the slides switch automatically infinitely.
</p></li>
	<li><p><b>Animation Speed</b> - here you can define the speed of the animated switching between the slides.</p></li></ul>




<h4>Style</h4>

<h5>Column</h5>

<ul class="marked-list">
	<li><p><b>Column Padding</b> - here you need to specify the padding values for the columns and make the paddings responsive.</p></li></ul>

<h5>Product Item</h5>

<ul class="marked-list">
	<li><p><b>Background Color</b> - here you can set the color to use for the product item background using color picker tool.</p></li>

	<li><p><b>Border Type</b> - here you can define the type of the border to apply for the item  (or apply no border, according to your needs).</p></li>

	<li><p><b>Width</b> - here you need to set the width of the border that will be shown in pixels.</p></li>

	<li><p><b>Color</b> - here you can customize the color of the border.</p></li>

	<li><p><b>Border Radius</b> - here you can set the border radius values to make the angles more smooth and round.</p></li>

	<li><p><b>Box Shadow </b>- here you can apply the shadow for the item. Click the Edit pencil option to open the additional shadow settings.</p></li>

	<li><p><b>Padding</b> - here you can define the padding values for the item.</p></li></ul>

<h5>Product Thumbnail (Image)</h5>

<ul class="marked-list">
	<li><p><b>Border Type</b> - here you can set the border to apply for the thumbnail image (or use None to use no border at all).</p></li>

	<li><p><b>Width</b> - here you need to set the width for the border.</p></li>

	<li><p><b>Color </b>- here you can customize the color to apply for the border.</p></li>

	<li><p><b>Border Radius</b> - here you can set the radius for the border to make its angles more round.</p></li>

	<li><p><b>Box Shadow</b> - click Edit pencil option to apply a shadow for the thumbnail image and customize its settings.</p></li>

	<li><p><b>Margin </b>- here you can apply the specific margin values for the thumbnail and make it responsive.</p></li>

	<li><p><b>Padding</b> - here you can set the custom paddings for the thumbnail and make them responsive.</p></li></ul>

<h5>Title</h5>

<ul class="marked-list">
	<li><p><b>Typography</b> - here you can edit the typography settings. They are the same as available for other JetWooBuilder widgets.</p></li>

	<li><p><b>Background Color</b> - here you can set the background color to apply for the title.</p></li></ul>

<p>Switch between Normal and Hover modes in order to customize the title appearance on hover and when in its normal state.</p>

<ul class="marked-list">
	<li><p><b>Color</b> - here you can set the color to use for the title when on hover and in normal state.</p></li>


	<li><p><b>Alignment</b> - here you can change the title alignment on hover and in normal state.</p></li>

	<li><p><b>Padding</b> - here you can customize the padding values to apply the custom padding for the title and make it responsive.</p></li>

	<li><p><b>Margin </b> - here you can apply your custom margins for the title and make them responsive.</p></li>

	<li><p><b>Order</b> - here you can set the order of the title defining where it will be shown on the grid block.
</p></li></ul>
