<h3>Ajax Search Widget Content Block</h3>

<h6>In this block you can edit the content displayed with JetSearch plugin.</h6>

<h5>Search Form</h5>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/Screenshot_21.png">
    </figure>

<ul class="marked-list">
<li><p><b>Placeholder Icon</b> - here you can select the appropriate icon for the placeholder from thedrop-down list.</p></li>
<li><p><b>Placeholder Text</b> - here you can define the text for the placeholder.</p></li>
<li><p><b>Show Submit Button</b> - enable this option if you want the submit button appears.</p></li>
<li><p><b>Submit Button Label</b> - here you ca specify the label of the submit button.</p></li>
<li><p><b>Submit Button Icon</b> - here you can choose the needed icon for the submit button from the drop-down list.</p></li>
<li><p><b>Show Category List</b> - enable this option if you what to display the list wih categories.</p></li>
<li><p><b>Taxonomy</b> - here you can specify the particular taxonomy to look for the results only within it.</p></li>
<li><p><b>Select Placeholder</b> - here you can define the placeholder text for the category list.</p></li></ul>

<h5>Search Settings</h5>

<ul class="marked-list">
<li><p><b>Search in</b> - here you can select particular search areas. If nothing is selected in the option, the search will be made over the entire site.</p></li>
<li><p><b>A number of results displayed on one search page</b> - this option allows to limit the number of displayed search results on one search page.</p></li>
<li><p><b>A number of results displayed in one search query</b> - this option allows to specify the number of displayed search results in one search query.</p></li>
<li><p><b>Sort results by</b> - here you can indicate how to sort the results. They can be sorted by relevance, date, title, author, last modified and number of comments(descending).</p></li>
<li><p><b>Filter results by</b> - here you can set ascending or descending order for filter results.</p></li></ul>



<h5>Structured Data</h5>

<ul class="marked-list">
<p><li><b>Add Structured Data to Review Box</b> - enable this option in order to add the data markup for the review content and enable ranking and displaying it with Google.</li></p></ul>

<h5>Header Settings</h5>
<p>The header appears when there are at least 2 reviews.</p>
<ul class="marked-list">
<p><li><b>Average Layout</b> - here you can choose the preferable layout for the average rating (you can set <b>Points, Percentage</b> or <b>Stars</b>).</li></p>
<p><li><b>Progressbar</b> - enable this option in order to display the average rating in the form of a progressbar.</li></p>
<div class="alert alert-info">Note, that the progressbar is available only for <b>Percentage</b> and <b>Points</b> layout types.</div>
</li></p>
<p><li><b>Values Position</b> - here you can select the position of the average rating either inside or above the progress bar.</li></p></ul>
  
<h5>Fields Settings</h5>

<ul class="marked-list">
<p><li><b>Layout</b> - here you can select the preferable type of the rating to use it for the items with rating categories (you can set <b>Points, Percentage</b> or <b>Stars</b>).</li></p>
<p><li><b>Progressbar</b> - enable this option in order to display the rating in the form of a progressbar.

<div class="alert alert-info">Note, that the progressbar is available only for <b>Percentage</b> and <b>Points</b> layout types.</div>
</li></p>

<p><li><b>Values Position</b> - enable this option in order to display the rating in the form of a progressbar.</li></p>

<p><li><b>Values Alignment</b> - in the case you’ve set <b>Values Position > Above Progressbar</b>, you can define if the value will be displayed on the left or on the right.</li></p></ul>

<h5>Summary Settings</h5>

<ul class="marked-list">
<p><li><b>Summary Results Block Position</b> - here you can select one of the following positions for the <b>Summary Results</b> block:
<ul class="marked-list">
<p><li>At right side of summary box:;<li></p>
<p><li>At bottom of summary box;</li></p>
<p><li>At left side of summary box;</li></p>
<p><li>At top of summary box.</li></p></ul>
<p><li><b>Results Block Width</b> - here you can specify width of the the results block.</li></p>
<p><li><b>Summary Average Layout</b> - here you can choose the preferable layout for the summary average results (you can set Points, Percentage or Stars).</li></p>
<p><li><b>Progressbar</b> -  enable this option in order to display the summary results in the form of a progressbar.</li></p>
<div class="alert alert-info">Note, that the progressbar is available only for <b>Percentage</b> and <b>Points</b> layout types.</div>
<p><li><b>Values Position</b> - here you can select the position of the summary results either inside or above the progress bar.</li></p></li></li></p></ul>

<h5>Results Block Width</h5>

<ul class="marked-list">
<p><li><b>Results Block Width</b> - in the case you’ve selected <b>Right</b> or <b>Left</b> block position, you’ll be able to define the Results block width using the corresponding control.</li></p>


<p><li><b>Summary Average Layout</b> - here you can specify the summary layout type (<b>Points, Percentage, Stars</b>).</li></p>

<p><li>In the case you’ve selected <b>Percentage</b> or <b>Points</b> layout type, you’ll be able to enable <b>Progressbar</b> and set the needed values position (<b>Above</b> or <b>Inside the progressbar</b>).</li></p></ul>


