<h5>Top Level Items</h5>

<ul class="marked-list">
<p><li><b>Vertical Menu Width</b> - here you can set the custom responsive width to apply to the vertical menu in px or percents.</li></p>

<p><li><b>Vertical menu Alignment</b> - here you can position the vertical menu to the left, center or right side.</li></p>



















<p>Switch between <b>Normal</b>, <b>Hover</b> and <b>Active</b> modes in order to make the menu items react to the mouse hover, style them up to change their appearance when they are active or in their normal state.</p>

<ul class="marked-list">
<p><li><b>Background Color</b> - here you can change the color to use for the menu background according to your needs using the color picker tool.</li></p>

<p><li><b>Text Color</b> - here you can change the color of the menu items text.</li></p>

<p><li><b>Text Background Color</b> - here you can define the color to use for the items text background.</li></p>

<p><li><b>Dropdown Icon Color</b> - here you can set the color to use it for the dropdown arrow (or other icon you’ve set for the dropdown).</li></p>

<p><li><b>Typography</b> - turn the option on to view the typography settings for the text. The typography settings are similar to the other ones available for JetBlocks elements included into the widgets.</li></p>

<p><li><b>Padding</b> - here you can change the padding values to the custom ones in order to make the menu fully responsive.</li></p>

<p><li><b>Margin</b> - set your custom margins to make them responsive for different devices.</li></p>

<p><li><b>Border Type</b> - here you can specify the suiting border type to use for the element (solid, dotted, double, dashed, groove or none).</li></p>

<p><li><b>Width</b> - here you can set the width to use for the border in px.</li></p>
<p><li><b>Color</b> - here you can specify the color to use for the border.</li></p>
<p><li><b>Border Radius</b> - here you can apply the radius for the borders to make the angles more round and smooth.</li></p>



<p><li><b>Dropdown Icon Size</b> - here you can specify the size of the dropdown icon and make it responsive.</li></p>

<p><li><b>Gap Before Dropdown Icon</b> - here you can change the size of the gap set before the dropdown icon to make it fully responsive.</li></p></ul>

