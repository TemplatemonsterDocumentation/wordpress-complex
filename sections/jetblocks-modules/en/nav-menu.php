<p><li><b>Padding</b> - here you can add the custom paddings to use for the items in order to make them responsive.</li></p>

<p><li><b>Dropdown Icon Size</b> - here you can change the size of the dropdown icon to make it fully responsive and fit for your website’s style.</li></p>

<p><li><b>Gap Before Dropdown Icon</b> - here you can add a responsive gap to use for the dropdown icon.</li></p></ul>

