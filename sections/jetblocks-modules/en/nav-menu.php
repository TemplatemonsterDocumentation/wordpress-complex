<p><li><b>Box Shadow</b> - here you can set the shadow and customize its settings.</li></p>


<p><li><b>Gap before 1st Level Sub</b> - here you can add a gap to apply it before the 1st level of sub menu.</li></p>

<p><li><b>Gap before 2nd level sub</b> - here you can add a gap to apply it before the 2nd level of sub menu.</li></p></ul>

