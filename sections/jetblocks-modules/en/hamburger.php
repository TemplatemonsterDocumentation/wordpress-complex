<h3>Hamburger Panel</h3>

<p>This widget allows adding a neat hamburger panel to the pages built with Elementor in order to make access to the needed template more easy for your visitors.</p>

<p>Please, notice that You need to create at least one template in order to be able to display it in the open <b>Hamburger</b> area.</p>

<h4>Content</h4>

		<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/hamb.png">
  	</figure>

<ul class="marked-list">
<p><li><b>Icon</b> - here you can specify which icon you want to use for the Hamburger element.</li></p>

<p><li><b>Active Icon</b> - here you need to set the icon that shows up when the panel is open. By clicking on the icon the user will be able to close the Hamburger panel.</li></p>

<p><li><b>Close Icon</b> - here you need to set the icon by clicking on which the user will be able to close the Hamburger panel.</li></p>

<p><li><b>Label</b> - here you can set the Hamburger label text.</li></p>

<p><li><b>Toggle Alignment</b> - switch the toggle alignment to the one needed (right, center, left).</li></p>

<p><li><b>Choose Template</b> - here you’ll be able to select the template to use for the Hamburger panel.</li></p></ul>

<p>Please, note that You’re also able to edit the templates right from current Elementor editing page. To do it, please, open the Hamburger panel and click <b>Edit Template</b> button.</p>

<h5>Settings</h5>

<ul class="marked-list">
<p><li><b>Position</b> - here you can change the Hamburger element panel position (attach it to  the right or the the left side of the page).</li></p>

<p><li><b>Effect</b> - here you can select which effect you want to apply for the Hamburger panel (slide or fade).</li></p>

<p><li><b>Z Order</b> - here you can change the position of the Hamburger menu to be always on top (to stay on top of other elements that have the lower Z index value).</li></p></ul>

<h4>Style</h4>

