<h5>Panel</h5>

<ul class="marked-list">
<p><li><b>Panel Width</b> - here you can change the width of the opened Hamburger panel and make it responsive.</li></p>

<p><li><b>Background Type</b> - here you can set the preferable background type to use for the panel. It can be Classic or Gradient background.</li></p>
<p><b>Classic Background Type</b></p></ul>
<ul class="marked-list">
<p><li><b>Color</b> - here you can set the specific color to use for the panel.</li></p>
<p><li><b>Image</b> - click the image box and upload an image in order to use it as a background for the panel.</li></p>
<p><li><b>Position</b> - here you can position the image used for the background according to your needs.</li></p>
<p><li><b>Attachment</b> - here you can make the image fixed or allow it to be scrolled.</li></p>
<p><li><b>Repeat</b> - here you can allow the image to be repeated horizontally, vertically, tiled or restrict repeating.</li></p>
<p><li><b>Size</b> - select Cover, Contain or Auto image sizing in order to make it fitted into the panel container.</li></p></ul>
<p><b>Gradient Background Type</b></p>
<ul class="marked-list">
<p><li><b>Color</b> - add the first color to use for the gradient background of the panel.</li></p>
<p><li><b>Location</b> - here you can change the location of the first gradient color.</li></p>
<p><li><b>Second Color</b> - add the second color to use for the gradient background.</li></p>
<p><li><b>Location</b> - here you can change the location of the second gradient color.</li></p>
<p><li><b>Type</b> - here you can change the gradient location from linear to radial.</li></p>
<p><li><b>Angle</b> - if the gradient type is lineer, you can change the angle to use for it.</li></p>
<p><li><b>Position</b> - if the gradient is radial, here you can change the position of its center to the preferable one.</li></p>
<p><li><b>Padding</b> - here you can set the padding for the panel content.</li></p></ul>

<ul class="marked-list">
<p><li><b>Border type</b> - here you can change the border type to the one needed (solid, dotted, dashed, double, groove or none).</li></p>
<p><li><b>Width</b> - here you can set the width of the border in px.</li></p>
<p><li><b>Color</b> - here you can choose the color to use for the border.</li></p>
<p><li><b>Border Radius</b> - here you can apply a radius for the border andles to make them more smooth.</li></p>
<p><li><b>Box Shadow</b> - here you can apply a shadow and change  the shadow settings for the panel.</li></p>

<p><li><b>Padding</b> - here you can change the padding values to your custom ones according to your needs.</li></p></ul>

<h5>Close Button</h5>

<ul class="marked-list">
<p>Switch between <b>Normal</b> and <b>Hover</b> modes in order to change the style of the Close button when in is in its normal state and when it is on mouse hover.</p>

<p><li><b>Close Icon</b> - here you can customize the Close Icon appearance (Color, Background Type, Icon Size, Box Size, Border Type, Border Radius, Box Shadow).</li></p></ul>

<h5>Toggle</h5>

<p>Switch between <b>Normal</b> and <b>Hover</b> modes in order to change the styles of the toggle elements when it is in its normal state or when it is on mouse hover.</p>

<ul class="marked-list">
<p><li><b>Background Type</b>  - here you can set the preferable background type to use for the element. It can be Classic or Gradient background. The settings are similar for all of the JetBlocks widgets that have a background type feature.</li></p>

<p><li><b>Border Type</b> - here you can specify the suiting border type to use for the element (solid, dotted, double, dashed, groove or none).</li></p>

<p><li><b>Width</b> - here you can set the width to use for the border in px.</li></p>
<p><li><b>Color</b> - here you can specify the color to use for the border.</li></p>
<p><li><b>Border Radius</b> - here you can apply the radius for the borders to make the angles more round and smooth.</li></p>

<p><li><b>Box Shadow</b> - here you can set the shadow and customize its settings.</li></p>

<p><li><b>Padding</b> - here you can change the padding values to custom ones making them responsive.</li></p>

<p><li><b>Margin</b> - here you can set the margin values to custom ones making them responsive.</li></p></ul>

<h5>Icon Styles</h5>

<p>Switch between <b>Normal</b> and <b>Hover</b> modes in order to customize the Icon appearance in normal state or when on mouse hover.</p>

<ul class="marked-list">
<p><li><b>Toggle Icon</b> - click the <b>Pencil</b> icon to change the icon appearance using all the available settings (they are similar for all the icons possessing this feature in the JetBlocks widgets).</li></p></ul>

<h5>Label Styles</h5>

<p>Switch between <b>Normal</b> and <b>Hover</b> modes in order to customize the Label appearance in normal state or when on mouse hover.</p>

<ul class="marked-list">
<p><li><b>Label Color</b> - here you can change the label color using color picker tool.</li></p>

<p><li><b>Typography</b> - turn the option on to view the typography settings for the text. The typography settings are similar to the other ones available for JetBlocks elements included into the widgets.</li></p></ul>
