<h3>Importing and Exporting Skins</h3>

<p>From this block you learn how to import and export the ready new post types, taxonomies and listing items.
</p>

<h4>Exporting Skin</h4>

<ol class="index-list">
	<li><p>First, navigate to <b>JetEngine</b> block in WordPress Dashboard and here open <b>Skins Manager</b>. Then, click <b>Export Skin</b> button.

	  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/dashboard/exp1.png">
  	</figure></li></p></p></li>

	<li><p>In the <b>Export Settings</b> popup window you'll see the list of post types, taxonomies, meta boxes, listings and samplate content that exist on your site. Check the needed content you want to export to another site where you use JetEngine.

		  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/dashboard/exp2.png">
  	</figure></p></li>

	<li><p>Click <b>Save file</b> to save the exported <b>JSON</b> file on your hard drive.</p></li>
</ol>

<h4>Importing Skin</h4>

<ol class="index-list">
	<li><p>First, navigate to <b>JetEngine</b> block in WordPress Dashboard and here open <b>Skins Manager</b>. Then, click <b>Import Skin</b> button.

		  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/dashboard/imp1.png">
  	</figure></p></li>

	<li><p>Browse the <b>JSON</b> file which you want to import to your site in order to add your own previously exported post types, listings,taxonomy types, etc. Then click <b>Import Skin</b> button.</p></li>

	<li><p>After the process is successfully finished you'll see the green notice informing you of the content that was imported on your site.

		  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/dashboard/imp2.png">
  	</figure></p></li>
</ol>

