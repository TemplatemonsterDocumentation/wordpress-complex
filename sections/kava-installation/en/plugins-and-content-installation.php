<h4 id="cherry-wizard">Jet Plugin Wizard Installation</h4>


<p>

    1. After theme activation you will get a notice which offers you to install Jet Plugin Wizard.
        To continue plugins and demo contents installation with Jet Plugin Wizard just hit <strong>Begin installing plugin</strong> link.
        You will be transfered to the page where you can select the needed plugins.
</p>

 <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/4.png">
    </figure>

<p>2. Then please click on <strong>Install</strong> link.</p>

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/5.png">
    </figure>

<p>3. After Jet Plugin Wizard has been successfully installed and activated click on <strong>Return to the Dashboard</strong> link.
</p>

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/6.png">
