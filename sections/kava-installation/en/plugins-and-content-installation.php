<h4>Plugins Installation</h4>

<p>4. After installation and activation Jet Plugin Wizard you will get a notice which offers you to continue the plugins and demo contents installation with Jet Plugin Wizard. Then click on <strong>START INSTALL</strong> on the top of the page to start installation of the recommended plugins.</p>

        <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/7.png">
    </figure>


     <p> 5. On the Wizard Home page you can check if your server configurations meet <strong>WordPress minimal requirements</strong>. If any doesnt't meet you can continue the installation process, but it will take more time and can drive to bugs.</p>
        <p>If your server configurations meet <strong>WordPress minimal requirements</strong> please click on <strong>NEXT</strong> link.    </p>

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/8.png">
    </figure>

<p> 6. Click <strong> Start Install </strong> button to proceed with plugins installation.</p>
