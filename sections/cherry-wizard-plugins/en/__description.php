<h3>Step 2. Preparations. Plugins Installation</h3>

<p>
    The second step includes making all the necessary preparations and installing plugins. It is important to install all the needed plugins for your theme to make it work correctly and enjoy all of its advantages.
</p>

<div class="alert alert-info">
    Notice: the plugins are installed with the help of TM Wizard Plugin, which comes together with your theme, you don’t need to download or activate it separately. Just go straight to the plugins installation step.
</div>

<p>
    Right after the theme was installed you’ll see a new window offering you to select skin and start installation. Select the skin you prefer the most, view the list of recommended plugins and proceed further.
</p>

 <figure class="img-polaroid">
        <img src="projects/cherry-wizard/img/images/5.png" alt="" >
</figure>

<p>The next step will offer you to choose how you want your demo content to be installed. It can be installed in two ways:<p>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>full version</dt>
            <dd>
                contains the entire amount of sample data to be used in the template, it is recommended to install it if you have a premium hosting plan;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>lite version</dt>
            <dd>
                lite version - contains optimized amount of sample data with the smaller number of posts and images, it is recommended to use it if you have an entry-level hosting plan.
            </dd>
        </dl>
    </li>
</ul>

 <figure class="img-polaroid">
        <img src="projects/cherry-wizard/img/images/6.png" alt="" >
</figure>

<p>
    After that you’ll be led to <strong>Configure Plugins</strong> menu, where you’ll be able to view the list of plugins before installing them to your theme.
</p>

<p>The list usually contains such plugins categories: </p>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>required plugins</dt>
            <dd>
                the category contains the minimal list of service plugins needed for your theme;
            </dd>
        </dl>
    </li>

     <li>
        <dl class="inline-term">
            <dt>recommended plugins</dt>
            <dd>
                in the category you can view the recommended set of basic plugins needed to display the template’s pages. If you don’t install them, some sections of your website will not be displayed;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>extra plugins</dt>
            <dd>
                the category lists the plugins that are available to use together with your template and may be needed for the additional functionality of your theme.
            </dd>
        </dl>
    </li>
</ul>

