<h3>JetElements Templates</h3>

<h5>From this block you can learn how to use JetElelements templates to create pages.</h5>

<ol class="index-list">
<p><li>Log in to WordPress Dashboard. Navigate to <b>Pages > Add new</b> tab on the left.</p></li>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-templates/1.png">
    </figure>

<p><li>In the <b>Page Settings</b> block open <b>Layout options</b> and select <b>No Sidebar</b>. Select <b>Fullwidth</b> mode in the <b>Content layout</b> section.</p></li>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-templates/2.png">
    </figure>

<p><li>Click on <b>Edit with Elementor</b> button to start editing.</p></li>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-templates/3.png">
    </figure>


<p><li>Click on <b>Add Template</b> button in the right section where the page layout is displayed.</p></li>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-templates/4.png">
    </figure>

<p><li>Switch to <b>My templates</b> block in the top of the popup window.
Hover the one template you want to apply to the page. You’ll see several options available for it.</p></li>

<ul class="marked-list">
<li><b>Preview</b> — click on the option to open the preview page for the template in the new browser window.</li>
<li><b>Delete</b> — click this option to remove the template completely.</li>
<li><b>Export</b> — you can save the template in order to use in another theme using this option.</li>
<li><b>Insert</b> — click this button to apply this template to the page you’re editing.</li></ul>
</p></li>

<p><li>You can also access page templates using <b>Templates Library</b>. To do it, please, click on <b>Template</b> icon in the left bottom corner of Elementor editor page. </p></li>

<p><li>A dropdown menu allowing you to save your current template or navigate to <b>Templates Library</b> will appear.

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/jet-templates/6.png">
    </figure>

Click on <b>Templates Library</b> to access the templates.</p></li></ol>
