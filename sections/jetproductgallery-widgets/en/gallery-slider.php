<h5>General</h5>

<ul class="marked-list">
<p><li><b>Product id</b> - here you can select a certain product, images of which will be displayed in the module.</li></p>
<p><li><b>Enable Gallery</b> - use this option if you want to get an access to customized options of product images gallery.</li></p>
<p><li><b>Enable Zoom</b> - this option allows to apply zoom to product images.</li></p></ul>

<h5>Images</h5>

<ul class="marked-list">
<p><li><b>Image Size</b> - here you can choose from drop-down list the size of product images.</li></p>
<p><li><b>Thumbnails Image Size</b> - here you can set the size of thumbnails image.</li></p></ul>

<h5>Slider</h5>
<h5>Navigation</h5>

<ul class="marked-list">
<p><li><b>Show Navigation</b> - in this block you can add navigation to the sides of the slides to provide easy switching from slide to slide for the visitors. </li></p>
<p><li><b>Arrow Previous</b> - here you can select the type of the arrow icon, that switches slider module to previous slide.</li></p>
<p><li><b>Arrow Next</b> - here you can select the type of the arrow icon, that switches slider module to next slide.</li></p></ul>

<h5>Pagination</h5>

<ul class="marked-list">
<p><li><b>Show Pagination</b> - here you can enable pagination at the bottom of Galler Slider to provide smoother navigation from slide to slide.</li></p>
<p><li><b>Type</b> - here you can choose the type of pagination. It can be displayed as bullets or thumbnails.</li></p>
<p><li><b>Direction</b> - here you can set the direction of pagination (vertical or horizontal).</li></p>
<p><li><b>Position</b> - here you can set the position of pagination. It can be on the left or on the right side.</li></p></ul>

<h5>Gallery</h5>

<ul class="marked-list">
<p><li><b>Button Icon</b> - here you can choose the size of product images in gallery. </li></p>
<p><li><b>Show Caption</b> - enable this option that the caption of product images will be showed or not in the gallery.</li></p></ul>
<h5>Controls</h5>
<ul class="marked-list">
<p><li><b>Show Full Screen</b> - here you can specify if the product gallery will be showcased fullwidth.</li></p>
<p><li><b>Show Zoom</b> - here you can adjust zoom to the product gallery. </li></p>
<p><li><b>Show Share</b> - here it is available to display share option in the product gallery. </li></p>
<p><li><b>Show Counter</b> - here you can enable a counter in the product gallery. </li></p>
<p><li><b>Show Arrows</b> - here you can set if navigation arrows will be showed or not in the product gallery.</li></p></ul>

<h5>Video</h5>

<ul class="marked-list">
<p><li><b>Display Video In</b> - here you can select how the video will be showed: as a popup or in the content.</li></p>
<p><li><b>Aspect Ratio</b> - this option allows your video item to be scaled according to your needs.</li></p></ul>
<h5>Options</h5>
<ul class="marked-list">
<p><li><b>Autoplay</b> - enable this option if you want the video to play automatically.</li></p>
<p><li><b>Loop</b> - turn this option on if you donâ€™t want the video to stop (the start and end time options will cease to affect the video).</li></p></ul>
<h5>Overlay</h5>
<ul class="marked-list">
<p><li><b>Overlay Color</b> - here you can select the color of the overlay.</li></p></ul>
<h5>Play Button</h5>
<ul class="marked-list">
<p><li><b>Show Play Button</b> - this option allows to display play button.</li></p>
<p><li><b>Play Button Type</b> - here you can customize the icon design of the play button. There can be icon or image. </li></p>
<p><li><b>Icon</b> - here you can choose icon for play button. </li></p>
<p><li><b>Image</b> - here you can choose image for play button. </li></p></ul>


