<h5>General Settings</h5>

<ul class="marked-list">
<p><li><b>Product ID</b> - here you can specify a certain product, which images are to be displayed by the widget. </li></p>
<p><li><b>Enable Gallery</b> - use this option if you want to get an access to customization settings of product images gallery.</li></p>
<p><li><b>Enable Zoom</b> - this option allows applying zoom to product images.
</li></p></ul>

<h5>Images</h5>

<ul class="marked-list">
<p><li><b>Image Size</b> -  here you can choose from the drop-down list the size of product images. </li></p></ul>

<h5>Gallery</h5>

<ul class="marked-list">
<p><li><b>Button Icon</b> - here you can choose the size of product images in a gallery.
 </li></p>
<p><li><b>Show Caption</b> - enable this option in order for the caption of product images to be shown or hidden in the gallery.</li></p></ul>
<h5>Controls</h5>
<ul class="marked-list">
<p><li><b>Show Full Screen</b> - here you can specify if the product gallery is to be showcased fullwidth.
</li></p>
<p><li><b>Show Zoom</b> - here you can adjust the zoom to the product gallery.
</li></p>
<p><li><b>Show Share</b> -  here it is available to show share option in the product gallery.
</li></p>
<p><li><b>Show Counter</b> - here you can enable a counter in the product gallery.
</li></p>
<p><li><b>Show Arrows</b> - here you can set if navigation arrows are to be shown or hidden in the product gallery.
</li></p></ul>

<h5>Video</h5>

<p>You can add the video in the Single Product gallery by inserting video URL of the needed video from Youtube, Vimeo or self-hosted site in Single Product template >Jet Product Gallery Video settings. You can also apply a certain placeholder for the video. This option is available for all JetProductGallery widgets.</p>


<ul class="marked-list">
<p><li><b>Display Video In</b> - here you can select how the video is to be shown: as a popup or inside the content.
</li></p>
<p><li><b>Aspect Ratio</b> - this option allows your video item to be scaled according to your needs.</li></p></ul>
<h5>Options</h5>
<ul class="marked-list">
<p><li><b>Autoplay</b> -   enable this option if you want the video to play automatically.

</li></p>
<p><li><b>Loop</b> - turn this option on if you donâ€™t want the video to stop (the start and end time options will cease to affect the video).
</li></p></ul>
<h5>Overlay</h5>
<ul class="marked-list">
<p><li><b>Overlay Color</b> - here you can select the color of the overlay.</li></p></ul>
<h5>Play Button</h5>
<ul class="marked-list">
<p><li><b>Show Play Button</b> - this option allows to display the play button.
</li></p>
<p><li><b>Play Button Type</b> - here you can customize the icon design of the play button. There can be an icon or an image.
 </li></p>
<p><li><b>Icon</b> - here you can choose an icon for the play button.
 </li></p>
<p><li><b>Image</b> - here you can choose an image for the play button. </li></p></ul>



