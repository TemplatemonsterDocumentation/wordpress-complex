<h3>Getting Started with JetElements</h3>

<h5>This block uncovers the basic steps You need to accomplish to add JetElements widgets to your website’s page layout. </h5>

<ol class="index-list">
<p><li>Navigate to <b>Pages > Add New</b> tab in the left column of the <b>WordPress Dashboard</b>. Click on <b>Edit with Elementor</b> button. You’ll be redirected to <b>Elementor editor page</b>.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/start/1.png">
  	</figure>
</li></p>

<p><li>When Elementor editor will open, you’ll see the block containing <b>Add New Section</b> button at the right part of the screen, where your future page’s draft is displayed.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/start/2.png">
  	</figure>
</li></p>

<p><li>Click on <b>Add New Section</b> to add the section, where you will place JetElements widget. </li></p>

<p><li>Now you’re ready to add <b>JetElements widgets</b> to the columns. Choose one of the elements you want to add to the page, and drag it to the column area.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/start/3.png">
  	</figure>
</li></p>

<p><li>In the left column you’ll see the element settings. You can manage <b>content</b>, <b>style</b>, or use <b>advanced styling</b> options from here.
 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/start/5.png">
  	</figure>

</li></p>

    <div class="alert alert-info">Keep in mind, that for different elements the options may vary. Each element has its own styling and content management settings.</div>
<ul class="marked-list">
<p><li><b>Edit Widget</b> - this option provides fast access to widget editing block in the left column of the page editing screen. </li></p>
<p><li><b>Duplicate Widget</b> - use this option to duplicate the existing widget with all its settings and content. </li></p>
<p><li><b>Remove Widget</b> - this option removes the widget from the page. </li></p>

