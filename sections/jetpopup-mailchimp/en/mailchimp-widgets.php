<h5>Fields</h5>

<ul class="marked-list">
<p><li>E-mail label - here you can add the field label title to show above the field (this label should let the visitor know which information he has to input in the field). </li></p>
<p><li>E-mail placeholder - here you can change the placeholder text to show the example of the information that should be placed in the field.</li></p>
<p><li>E-mail size - here you can set the size of the field in percents (the 100% means that the full row will be occupied by the field, set 50% for several of fields to place both of them into one row, etc). </li></p></ul>

<p>By default, there is only one field available (it’s the e-mail field). However, you can add more of them to create multi-fields in the subscribe popup. Enable Use Additional Fields option to add more fields to the subscription form. Then hit + Add item button to add a new field. </p>

<ul class="marked-list">
<p><li>Field merge tag - copy the merge field tag from the Merge Field you want to add (e.g., in case you’ve got *|MERGE|* tag, you need to input MERGE in this field).</li></p>
<p><li>Label - here you can add the field label title to show above the field.</li></p>
<p><li>Placeholder - here you can change the placeholder text to show the example of the information that should be placed in the field.</li></p>
<p><li>Column width -  here you can set the size of the field in percents (100% is the full row, 50% is half of the row, etc.).</li></p></ul>

<h5>Submit Button </h5>

<ul class="marked-list">
<p><li>Submit text - here you can add the text to display for the Submit button.</li></p>
<p><li>Button size - herer you can set the size of the button (100% is the full row, 50% is half of the row, etc.).</li></p>
<p><li>Alignment - set the preferable alignment to use for the Submit button (left, center, right or justified). </li></p></ul>

