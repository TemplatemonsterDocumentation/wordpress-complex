<h3>MailChimp Widget</h3>

<p>From this block you can learn about using MailChimp JetPopup widget for Elementor. </p>

<p>The MailChimp widget can be found under the JetPopup widgets block along with the Popup Action Button widget. Drag and drop it to the popup template’s layout to add it to the template.
</p>


