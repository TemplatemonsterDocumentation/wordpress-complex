<ul class="marked-list">
<p><li>Columns gap - here you can set the gap space to apply between the columns of the fields. </li></p>
<p><li>Rows gap - here you can set the needed distance to apply between the rows. </li></p>
<p><li>Padding - here you can apply the custom padding values and make them fully responsive.</li></p>
<p><li>Border radius - here you can apply the border radius to make the angles more round. </li></p></ul>


<p>Switch between Normal and Error moder to customize the appearance of both. </p>

