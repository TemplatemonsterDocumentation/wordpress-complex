<h3>MailChimp API Settings</h3>


<p>First of all, before using <b>MailChimp</b> widget provided by <b>JetPopup</b>, you need to have a registered <b>MailChimp</b> account.</p>

<ol class="index-list">

<li><p>To connect MailChimp to JetPopup, you’ll have to log in to your <b>MailChimp account</b> and here click the dropdown where you can view the notifications, profile information, manage account settings, etc. Let’s navigate to <b>Account</b> block. </p>

<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/mch1.png">
</figure>
</li>

<li><p>Here proceed to <b>Extras > API Keys</b> block and click <b>Create a Key</b> button to create a new API key. </p>

<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/mch2.png">
</figure></li>

<li><p>Copy the generated <b>API</b> key now available under <b>API Key</b> title. </p></li>

<li><p>Now let’s log in to WordPress Dashboard of your site. Here open <b>JetPopup > Settings</b> block and paste the API key to the corresponding field. Click <b>Sync</b> option to synchronize the information about the lists connected to this MailChimp account and key. </p>

<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/mch3.png">
</figure></li>

<li><p>After that, in case you’ve already got a list at MailChimp, you’ll see it in the <b>MailChimp Lists</b> block. Each of the created lists will contain the following information:

<ul class="marked-list">
<li><b>Name</b> - the name of the list (you can have several of them, and you’ll be able to select one of the lists while working with JetPopup MailChimp widget and its functionality); </li>
<li><b>List ID</b> - the unique MailChimp list ID number;</li>
<li><b>Date created</b> - the information about the date when the current list was created;</li>
<li><b>Double Opt-in</b> - shows if the additional confirmation is needed for this list (this functionality is not supported at the moment);</li>
<li><b>Merge Fields</b> - these are the fields that can be set as additional in the MailChimp form added with the widget. </li></ul>

</p>
<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/mch4.png">
</figure>
</li>

<li><p>In case you don’t have the extra fields yet, follow the steps described below. If you already have all the needed extra <b>Merge Fields</b>, skip this information and proceed to <a href="http://documentation.zemez.io/wordpress/index.php?project=jetpopup&lang=en&section=jetpopup-mailchimp#mailchimp-widget" target="_blank">MailChimp Widget</a>
 documentation block. </p></li></ol>
