<h3>Adding Extra Merge Fields</h3>


<ol class="index-list">
	<li><p>To add additional <b>Merge Fields</b> to the subscribe form log in to your MailChimp account and here click <b>Lists</b> option in the top panel. </p>

	<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/mch5.png">
</figure>
</li>

	<li><p>Click the title of the list you already have (even if it’s empty at the moment) to manage its content. </p>

		<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/mch6.png">
</figure></li>

	<li><p>Click <b>Settings</b> dropdown and here open <b>List fields and *|MERGE|* tags</b> option. Now you can view the full list of the existing merge fields available for this list. Feel free to delete the ones you don’t need and create the ones you want to add. </p>

		<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/mch7.png">
</figure></li>

	<li><p>Click <b>Add a field</b> button to add a new merge field to the list of the ones already available. </p>

		<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/mch8.png">
</figure></li>

	<li><p>Select a field type to add from the list of field types available (e.g., text will do for adding a name field, etc.). Then click <b>Save changes</b> button. </p>

		<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/mch9.png">
</figure></li>

	<li><p>Replace the <b>Untitled</b> placeholder text with the actual Merge field title. Replace the default tag with the custom one. Then click <b>Save</b> changes. </p></li>

	<li><p>Once again open WordPress Dashboard and here access <b>JetPopup > Settings</b>. Click <b>Sync</b> to sync the existing MailChimp lists. </p>

		<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/mch10.png">
</figure></li>

	<li><p>Proceed to MailChimp lists and here click <b>Get Merge Fields</b> button to pull all the merge fields that are newly added to the list. After that click <b>Save</b> button. </p></li></ol>
