<h2>TM WoooCommerce Compare & Wishlist</h2>
<h5>
This <a href="https://wordpress.org/plugins/tm-woocommerce-compare-wishlist/">custom designed plugin</a> gives your store even more functionality. With its help visitors will be able to add various kinds of products to the wishlist and compare pages. The plugin actually combines two separate widgets and installs them with a single click of a mouse.
</h5>

<h3>Installation</h3>
<p>
    Installation won't take you much time or effort. Only 3 steps are required to integrate the plugin into your website. 
</p>
<ol class="index-list">
    <li><p> Upload "TM WooCommerce Compare Wishlist" folder to the "/wp-content/plugins/" directory</p></li>
    <li><p> Activate the plugin through the "Plugins" menu in WordPress</p></li>
    <li><p> Navigate to the "TM Compare" and "TM Wishlist" section of WooCommerce settings to start customizing </p></li>   
</ol>

