    </figure>
</li></p>

<p><li>
	Click on <b>Add New</b> button to upload <b>JetWooBuilder</b> from your hard drive.

		  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/2.png">
    </figure>
</li></p>
<p><li>
	Click on <b>Upload</b> button. It can be found at the top of <b>Plugins</b> page. Browse the path to <b>JetWooBuilder</b> plugin and start installation.

			  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/3.png">
    </figure>
</li></p>
<p><li>
	When <b>JetWooBuilder</b> plugin is uploaded and installed you need to activate it. Navigate to <b>Plugins</b> tab and locate <b>JetWooBuilder</b> plugin in the list of installed plugins. Click on <b>Activate</b> link under <b>JetWooBuilder</b>. As a result the plugin will become active.
</li></p>
</ol>
