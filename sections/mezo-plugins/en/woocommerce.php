<h3>WooCommerce Plugin</h3>

<p><b>WooCommerce plugin</b> is a plugin, which powers online store functionlaity. </p>

<p>The plugin is made for those people who enjoy exquisite animation effects and are willing to add them to the website pages to liven up the website. This plugin gives you full controll of the store from managing taxes and customer accounts to changing the design.</p>


<h5>Menu Items Overview</h5>

<ul class="marked-list">
<p><li>
    <b>orders</b> - this section allows you to see and manage all orders;
</li></p>

<p><li>
    <b>coupons</b> - in the Coupons sections site owners can create various kinds of discount campaigns and coupons to provide customers with the top of the line service;
</li></p>

<p><li>
    <b>reports</b> - reports section gives you statistics on sales, coupons, campaigns, etc;
</li></p>

<p><li>
    <b>settings</b> - configure the WooCommerce plugin from this section;
</li></p>

<p><li>
    <b>system status</b> - this section will become in hand during troubleshooting. The System Status Report gives you all information of the plugin setup and all potential conflicts;
</li></p>

<p><li>
    <b>add-ons</b> - in this section you can browse for some new free and premium extensions that will bring even more functionality to your site;
</li></p>

<p><li>
    <b>booster tools</b> - information about all available WooCommerce tools can be found in this section. The enabled tools will be displayed in the right column.
</li></p>

</ul>
