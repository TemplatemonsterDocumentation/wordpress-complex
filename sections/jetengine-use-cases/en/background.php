<h3>Dynamic Section Background</h3>

<p>From this block you'll learn how to set the dynamic thumbnail image as the section's background using JetEngine plugin and Elementor PRO.
</p>

<ol class="index-list">
	<li><p>First, let's create a new Taxonomy/Post type. In <b>Meta Fields</b> select the <b>Media</b> field type. Then save the changes. Note, that you can also add the <b>Media</b> meta field to the existing taxonomy or category using <b>Meta Boxes</b>.

	  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/grid/thumbnail.png">
  	</figure></li></p>

  </p></li>

	<li><p>Add the thumbnail image to the needed taxonomy in the Media field  from the media library or by uploading the image from your hard drive. Then click <b>Update</b> in order to save the changes.


		  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/grid/thumbnail2.png">
  	</figure></p></li>

	<li><p>Add at least one post to the newly created term taxonomy.

		  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/grid/thumbnail3.png">
  	</figure></p></li>

	<li><p>Navigate to <b>Elementor > My Templates</b> and here click <b>Add New</b> button. In Choose Template Type select <b>Archive</b> template. Add the title for the new template and click <b>Create Template</b> button to proceed. Please, note, that this option is available only for Elementor PRO.


			  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/grid/thumbnail6.png">
  	</figure></p></li>

  </p></li>

	<li><p>Add a new section and navigate to <b>Edit Section > Style</b> settings. In <b>Background</b> block select <b>Classic</b> background type and click <b>Dynamic</b> option for the Image istead of selecting one from media library. </p></li>
	<li><p>Select <b>JetEngine > Custom Image</b> from the newly opened dropdown.

		  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/grid/thumbnail4.png">
  	</figure></p></li>

	<li><p>Open <b>Settings</b> block located in the bottom left corner of the editing page and open <b>Preview Settings</b>. Here select the archive you want to display in <b>Preview Dynamic Content as</b> dropdown.

		  	 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/grid/thumbnail5.png">
  	</figure></p></li>

	<p><li>After that type in the post / taxonomy title in the field below and click <b>Apply and Preview</b> button.</li></p></ol>

