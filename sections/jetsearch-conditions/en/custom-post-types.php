<h3>Enabling Ajax Search for Custom Post Types</h3>

<ul class="index-list">

<li><p>First, open the page where you want to add the JetSearch functionality with Elementor page builder, find <b>Ajax Search</b> widget and drop it into a new section.</p></li>
<li><p>After that, click <b>Content block > Search Settings</b>.

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/search-settings.png">
    </figure></li></p>


<li><p>Click on <b>Search In</b> empty field and you’ll see the dropdown showcasing the post types you’ve already got on your site (e.g., <b>Posts</b> and <b>Pages</b> are the default post types, and if you’ve added more post types with JetEngine or Pods plugins, you’ll see them right away). Also, in case you’ve got WooCommerce products, you’ll be able to set the search for the <b>Products</b> post type.</p>

<p>After you’ve set the post type, the Search plugin will automatically look for the needed results only in the post type you’ve defined. If nothing is selected in the option, the search will be applied to the entire site.</p></li>
<li><p>Click <b>Update</b> button in order not to lose the changes.</p></li></li></ul>

