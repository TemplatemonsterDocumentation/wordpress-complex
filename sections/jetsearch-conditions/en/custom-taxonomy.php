<h3>Enabling Ajax Search for Custom Taxonomy</h3>

<ul class="index-list">

<li><p>First, choose the page to which you want to add a search form and click <b>Edit with Elementor</b> button.</p></li>

<li><p>Drag <b>Ajax Search</b> widget to the page and <b>Content</b> block will automatically appear in the left-hand sidebar.</p></li>
<li><p>Enable <b>Show Category List</b> option.</p></li>
<li><p>Select the taxonomy which terms you want to display to help the visitors single out the results. Also select a placeholder text to display.

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/taxonomy.png">
    </figure></li></p>

<li><p>As a result, one additional block will be added to the search field, showcasing the dropdown which allows specifying the terms as additional conditions for the search. Now, on front-end, it is possible to click the dropdown and choose the needed terms within which you want to look for the results.
