<h3>Enabling Ajax Search for Custom Taxonomy</h3>

<h5>With JetSearch plugin every element of search results is customizable. It is possible to single out the most important search results and make it search only among the specific taxonomies.<h5>

<h5>Let’s explore how to enable the search for the specific taxonomy in order to limit the search results. </h5>

<ul class="index-list">
<li><p>Open the page to which you need to add the search form and go to the Elementor editor.</p></li>

<li><p>Next, drop the <b>Ajax Search</b> widget to the page. After that, you will see <b>Content</b> block in the left menu.</p></li>
<li><p>Now, enable the <b>Show Category List</b> option using the toggle.</p></li>
<li><p>Then, choose the taxonomy from where you want to pull the terms for displaying.</p></li>
<li><p>Now you can fill in the needed placeholder text to give the visitors a clue of what’s this all about.</p></li>


<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/taxonomy.png">
    </figure></li></p>

<li><p>Consequently, one additional block is included in the search field, so now visitors can specify within which terms they want to make the search.</p></li>
<li><p>Lastly, on the live site, you can check it and click the dropdown, choosing the needed terms for the sought elements.</p></li>

