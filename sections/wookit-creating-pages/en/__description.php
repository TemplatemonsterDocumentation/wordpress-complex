<h3>Step 3. Creating WooCommerce Shop Pages</h3>


<p>Now that weâ€™ve set <b>WooCommerce</b> plugin, the next step is to create the pages, which are necessary for your store.</p>

<p>Here is the list of pages that are required in order to make your website work as it is supposed.</p>

<ul class="marked-list">
<p><li><b>Shop page</b> - just create the new page and type in <b>Shop</b> in its title field. Then click <b>Publish</b> button to publish it.</li></p>

<li><p><b>Cart page</b> - this page will showcase the products added to cart. Create a new page from the Dashboard. Type in <b>Cart</b> in its title field. Then add the following shortcode in its content field.<p>

<p>[woocommerce_cart]</p>

</li></p>

<li><p><b>Checkout page</b> - this page shows the shipping and payment options. You can create it using another WooCommerce shortcode.</p>

<p>[woocommerce_checkout]</p></li></p>

<li><p><b>My Account page</b> - this page displays the information about each customer, their orders. To create it you need to create a new page and paste the following shortcode to its content field.</p>

<p>[woocommerce_my_account]</p></li></p>

<p>You can also create these pages automatically. To do it navigate to <b>WooCommerce > Status</b> tab on the left of the Dashboard. Here switch to <b>Tools</b> block and locate <b>Create default WooCommerce pages</b> option. Click <b>Create pages</b> button next to this option.</p>

	<figure class="img-polaroid"><img src="img/wookit/13.png" alt=""></figure>

<p>After that the pages will be created automatically and set in the <b>WooCommerce > Settings</b> as the default ones.</p>

