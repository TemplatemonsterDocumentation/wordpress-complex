<h3>Submission Form</h3>
<p>
    <strong>[tm_re_submission_form] </strong>shortcode adds submission form to the page. 
</p>
<p>
    <strong>NOTE:</strong> To change the main page for displaying properties you need to navigate to the plugin Settings section and choose the page to which you want to add the form.
</p>

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/widgets/shortcodes-7.png">
