<p>
    You can check detailed information about adding categories at
    <a href="http://codex.wordpress.org/Manage_Categories_SubPanel">
        WordPress Codex.
    </a>
</p>

<h5>Post Formats</h5>

<p>
    Post formats are used to customize the available post options and markup. You can choose a post format on the post
    editing screen.
</p>

<figure class="img-polaroid"><img src="img/wordpress/post_format.png" alt=""></figure>

<p>
    You can check detailed information on post formats at
    <a href="http://codex.wordpress.org/Post_Formats">Post Formats Page</a>
    .
</p>

<p>Check the list of available posts formats with description and settings below.</p>


<h6 id="post_format_standard">Standard Post Format</h6>

<p>Standard post format has the following fields:</p>

<ul class="marked-list">
    <li><strong>Post title</strong> - post title.</li>
    <li><strong>Post content</strong> - post content.</li>
    <li><strong>Categories</strong> - post category(ies).</li>
    <li><strong>Tags</strong> - post tags.</li>
    <li><strong>Featured Image</strong> - post featured image.</li>
    <li>Other fields that can be enabled in <strong>Screen Options</strong> in the top right corner.</li>
</ul>

<figure class="img-polaroid"><img src="img/wordpress/post_format_standard.png" alt=""></figure>


<h6 id="post_format_audio">Audio</h6>

<p><strong>Audio format</strong> is used to publish audio content.</p>

<figure class="img-polaroid"><img src="img/wordpress/post_format_audio.png" alt=""></figure>

<p>You can edit the following fields in the media gallery file settings:</p>

<ul class="marked-list">
    <li><strong>URL</strong> - audio file direct URL.</li>
    <li><strong>Title </strong> - audio track name.</li>
    <li><strong>Artist </strong> - audio track performer.</li>
    <li><strong>Album </strong> - audio track album.</li>
    <li><strong>Description </strong> - audio track description.</li>
</ul>

<figure class="img-polaroid"><img src="img/wordpress/post_format_audio_additional.png" alt=""></figure>

<h6 id="post_format_gallery">Gallery</h6>

<p>The main feature of this post format is the possibility to add an unlimited number of images.</p>

<p><strong>Adding a gallery:</strong></p>

<p>Click<strong> Add Media</strong>.</p>

<figure class="img-polaroid"><img src="img/wordpress/add_media.png" alt=""></figure>

<p>Drag files or click <strong>Select Files</strong> to add images from the local computer.</p>

<figure class="img-polaroid"><img src="img/wordpress/insert-media.png" alt=""></figure>

<p>After the images are uploaded, you can find them in Media Library. Click <strong>Uploaded to this post</strong> to
   view the images.</p>

<figure class="img-polaroid"><img src="img/wordpress/uploaded-images.png" alt=""></figure>

<p>After editing, you can close the media library. The uploaded images will be added automatically.</p>


<p>Gallery post format has the same fields that the standard post format has (e.g. title, content, category, etc).</p>


