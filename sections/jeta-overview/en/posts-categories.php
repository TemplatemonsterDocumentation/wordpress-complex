<h5>Creating a Blog Post</h5>

<p>To add a blog post, you need to do the following:</p>

<ol class="index-list">
    <li>Open the <strong>Posts</strong> tab.</li>
    <li>Click <strong>Add New</strong>.</li>
    <li>Add content to the post.</li>
</ol>

<p>
    You can check detailed information about adding posts at
    <a href="http://codex.wordpress.org/Posts_Add_New_SubPanel">WordPress Codex</a>
</p>

<h5>Adding a Category</h5>

<p>To add a category you need to do the following:</p>

<ol class="index-list">
    <li>Open the <strong>Posts</strong> tab.</li>
    <li>Click <strong>Categories</strong> to see all categories.</li>
    <li>Enter new category name.</li>
    <li>Click <strong>Add New Category</strong>.</li>
    <li>Click <strong>Publish</strong>.</li>
</ol>

