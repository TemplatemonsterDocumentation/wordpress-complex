<h3>Jet Data Importer</h3>

<p><b>JetTricks</b> is a plugin that allows adding different visual effects without ever needing to add a single line of code.</p>

<p>The plugin is made for those people who enjoy exquisite animation effects and are willing to add them to the website pages to liven up the website.</p>

<p>Use <b>JetTricks</b> to add Parallax to different widgets, create unfolding sections, sticky columns and View More buttons.</p>

    <div class="alert alert-info">
        You can find the detailed documentation on JetTricks plugin <a href="http://documentation.zemez.io/wordpress/index.php?project=jettricks" target="_blank">here</a>.</div>

<h5>Functionality Overview</h5>

<ul class="marked-list">
<p><li>
    <a href="https://jettricks.zemez.io/parallax-element/" target="_blank"><b>Parallax Element</b></a> functionality enables you to add parallax effect for versatile widgets and change parallax speed, enable or disable parallax for different devices, etc.
</li></p>

<p><li>
    <a href="https://jettricks.zemez.io/sticky-column/" target="_blank"><b>Sticky Column</b></a> is an additional functionality which makes it possible to make the columns "sticky" when one scrolls the section up and down.
</li></p>
<p><li>
    <a href="https://jettricks.zemez.io/unfold/" target="_blank"><b>Unfold</b></a> widget is used to make the content added to the widget hidden or displayed in a beautiful way.
</li></p>

<p><li>
    <a href="https://jettricks.zemez.io/show-more/" target="_blank"><b>View More</b></a> widget allows displayind sections and higing them under the cut stylishly, making the page's content more visually compact.
</li></p>
<p><li>
    <a href="https://jettricks.zemez.io/hotspots/" target="_blank"><b>Hotspots</b></a> widget adds hotspots to the images to mark the important details and comment anything shown on the image.
</li></p>
</ul>
