<h3>General Settings</h3>

<h5>General Settings</h5>

<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/18.png">
  	</figure>

<ul class="marked-list">
          <li><b>Title</b> – define the popup template’s title.</li>
          <li><b>Status</b> – set the popup template’s status (draft, pending review, private, published).</li></ul>

<h5>JetPopup Settings</h5>

<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/19.png">
  	</figure>

<ul class="marked-list">
          <li><b>Animation</b> – here you can set how the popup will be animated on entrance.</li>
          <li><b>Open event</b> – here you can adjust when the popup should be displayed (on page load, inactivity time after, page scrolled or try exit). To learn more about opening events, please, navigate <a href="http://documentation.zemez.io/wordpress/index.php?project=jetpopup&lang=en&section=jetpopup-events#triggers" target="_blank" rel="noopener">here</a>.</li>
          <li><b>Show once</b> – enable this option to show the popup once.</li></ul>

<h5>JetPopup Conditions</h5>

<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/20.png">
  	</figure>
<ul class="marked-list">
          <li><b>General</b> – entire site, singular, archive. For more information, please, navigate <a href="http://start/wordpress-complex/index.php?project=jetpopup&lang=en&section=jetpopup-events#conditions" target="_blank" rel="noopener">here</a>. </li></ul>
