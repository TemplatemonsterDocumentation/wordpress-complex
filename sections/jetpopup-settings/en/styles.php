<h5>Popup Container</h5>

<ul class="marked-list">
          <li><b>Width</b> – here you can define the width of the popup template.</li>
          <li><b>Custom Height</b> – enable this option to set the popup container’s height.</li>
          <li><b>Horizontal Position</b> – here you can position the template using the X axis (to the left, in the center or to the right). </li>
          <li><b>Vertical Position</b> – here you can definite popup container’s position vertically (top, center or bottom.</li>
          <li><b>Background Type</b> – here you can set the background of the popup container. You can choose either Classic or Gradient background type.

<p>Classic Background Type</p>
<ul class="marked-list">
<li><b>Color</b> — here you can set background color for the background using color picker tool.</li>
<li><b>Image</b> — here you can choose the image you want to use as a background. Select the image from the media library or upload it to define it as a background in this block. When the image is uploaded, you’ll be able to define its position, alignment, turn on repeat and set the necessary size.</li></ul>

<p>Gradient Background Type</p>


<ul class="marked-list">
<li><b>Color</b> — here you can set background color for the module using color picker tool.</li>
<li><b>Location</b> — here you can define the proportion for the first color in the gradient background.</li>
<li><b>Second Color</b> — here you can set the second color to use in the gradient background.</li>
<li><b>Location</b> — here you can define the proportion for the second color in the gradient background.</li>
<li><b>Type</b> — define, if you want to use linear or radial type of the gradient.</li>
<li><b>Angle</b> — in this block you can set the angle for the gradient (in degrees).</li></ul></li>

<p></p>
          <li><b>Padding</b> – type in the custom padding values to add a responsive padding for the block.</li>
          <li><b>Margin</b> – fill in the custom margin values to add a responsive margin around the block.</li>
          <li><b>Border Radius</b> – adjust the radius of the popup container’s borders to make the angles more round and smooth.</li>
          <li><b>Border Type</b> – here you can define the needed border type for the block. It can be solid, double, dotted, dashed or groove.</li>
          <li><b>Box Shadow</b> – enable this option if you want to access the shadow advanced settings for the block, and need to apply shadow to it.</li></ul>

<h5>Close Button</h5>

<ul class="marked-list">
	<li><b>Use Close Button</b> - here you can enable the usage of the Close button to let the visitors close the popup manually.</li>
          <li><b>Icon</b> – here you can select one of the icons, which will be displayed on close button.</li>
          <li><b>Icon Transform</b> – here you can position the icon that should close the popup.</li>
          <li><b>Normal/ Hover</b> – switch between the modes to change the Close button style in its normal state and when one hovers the button.</li>
          <li><b>Icon Styles</b> – enable this option to style the icon.</li>
          <li><b>Font Color</b> – here you can set the color of the icon.</li>
          <li><b>Background Type</b> – here you can specify the color to apply for the icon background</li>
          <li><b>Icon Size</b> – here you can choose the icon’s size.</li>
          <li><b>Box Size</b> – here you can specify the icon box’s size.</li>
	<li><b>Border Type</b> – here you can define the needed border type for the block. It can be solid, double, dotted or dashed.</li>
          <li><b>Width</b> – here you can fill the width of the border.</li>
          <li><b>Color</b> – here you can select the color of the border using Color picker tool.</li>
          <li><b>Border Radius</b> – here you can set the border radius for the chosen border to make the border angles more round.</li>
          <li><b>Box Shadow</b> – enable this option if you want to access the shadow advanced settings for the box, and need to apply shadow to it.</li></ul>

<h5>Popup Overlay</h5>
<ul class="marked-list">
           <li><b>Background Type</b> – here you can choose the background for the popup’s template (classic or gradient) and set the advanced settings for each types.</li></ul>













