<h2>Social Networks Login</h2>

<p>From this block you'll learn how to enable loging in using social networks, e.g. FaceBook, Google+, etc. </p>

<h3>Adding Social Networks Login</h3>

<p>To give the visitors an opportunity to log in using social network accounts you'll need to install an additional <a href="https://wordpress.org/plugins/wordpress-social-login/" target="_blanc">WordPress Social Login</a> plugin.

<p>Navigate to WordPress Dashboard and locate "Plugins" tab. Open it to find the installed plugin in the list of available plugins. You need to activate it. </p>

<p>For more information on how to use Social Networks Login plugin, please, navigate to <a href="http://miled.github.io/wordpress-social-login/documentation.html" target="_blanc"> plugin documentation</a>. Here you'll learn how to get an API key, etc. </p>

<figure class="img-polaroid">
