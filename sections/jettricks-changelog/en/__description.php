<h3>Changelog</h3>

<h5>In this block you can view the latest updates for JetTricks plugin.
 </h5>

<h4>Version 1.1.6 (December 28, 2018)</h4>
<ul class="marked-list">
<li>Added responsive Closed Height option in Unfold widget;</li>
<li>Added Show on Init option in Hotspots widget.</li>


<h4>Version 1.1.5 (November 23, 2018)</h4>
<ul class="marked-list">
<li>Added compatibility with WPML String Translation plugin;</li>
<li>Added RU localization.</li></ul>

<h4>Version 1.1.4 (October 31, 2018)</h4>
<ul class="marked-list">
<li>Added an ability to filter JetTricks settings before render.</li></ul>


<h4>Version 1.1.3 (September 18, 2018)</h4>
<ul class="marked-list">
<li>Added ‘None’ option in ‘Tooltip Trigger’ to display tooltips always in the Hotspots widgets;</li>
<li>Added ‘Text Alignment’ control for tooltip in the Hotspots widget;</li>
<li>Added multiple performance improvements and bug fixes;</li>
<li>Fixed not working “Back to default” button in the Box-Style Group Control;</li>
<li>Fixed sticky column on mobile when setting Sticky On is [desktop, tablet].</li></ul>


<h4>Version 1.1.2 (July 12, 2018)</h4>
<ul class="marked-list">
<li>Added Satellite widget extension;</li>
<li>Added new Tooltip widget extension;</li>
<li>Added new Section Particles widget extension.</li></ul>


<h4>Version 1.1.0 (July 5, 2018)</h4>
<ul class="marked-list">
<li>Added 3 new effects: Satellite Element, Section Particles and Tooltip;</li>
<li>Improved compatibility with Elementor v.2.1.</li></ul>
