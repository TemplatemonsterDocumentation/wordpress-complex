<h2>Switcher</h2>

<h6>
<b>Switcher</b> widget is a perfect asset for you if you need to display two different content blocks and to show then under different circumstances.
</h6>

<div class="alert alert-info">
        Please, before starting to use the widget make sure you have at least two templates created with Elementor on your site.
</div>

<h4>Content</h4>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/vertacc1.png">
    </figure>

<h5>Items</h5>

<p>Switch between Enable and Disable modes to specify the template shown when the switcher is enabled and the one template shown when the switcher is disabled.</p>

<ul class="marked-list">
<p><li><b>Label</b> - here you can set the custom name for the Disabled / Enabled switcher mode.</li></p>

<p><li><b>Template</b> - here you have to select the template to be shown when the switcher is active or disabled.</li></p></ul>

<h5>Settings</h5>

<ul class="marked-list">
<p><li><b>Initial State</b> - here you can make the Switcher show up in active mode or when it is disabled right after the page loads.</li></p>

<p><li><b>Switcher Preset</b> - here you can select one of the existing switcher presets available in dropdown list to apply it for the switcher.</li></p>

<p><li><b>Show Effect</b> - here you can select the effect to apply for the template when one uses switcher. The effects are available in the dropdown list.</li></p></ul>

<h4>Style</h4>

<h5>Switcher Control</h5>

<ul class="marked-list">
<p><li><b>Width</b> - here you can specify the width of the switcher and make it responsive.</li></p>

<p><li><b>Height</b> - here you can set the height of the switcher and make it responsive.</li></p>

<p><li><b>Handler Width</b> - here you can set the specific width of the handler control and make it responsive.</li></p>

<p><li><b>Margin</b> - here you can set the specific margin to use for the switcher block and make it responsive.</li></p>

<p><li><b>Border Type</b> - here you can apply the neede border type for the switcher or apply no border at all. When the border is applied you’ll be able to set its width and color.</li></p>

<p><li><b>Border Radius</b> - here you can specify the border radius values to make the border angles more smooth and round.</li></p>

<p><li><b>Box Shadow</b> - here you can enable the shadow for the switcher and customize its appearance using the available options.</li></p></ul>

<h5>Handler</h5>

<ul class="marked-list">
<p><li><b>Handler Offset </b>- here you can apply the custom offset for the handler control making it positioned inside the switcher field.</li></p></ul>



<h5>Labels</h5>

<ul class="marked-list">
<p><li><b>Typography</b> - here you can edit the typography settings. They are the same as available for other JetTabs widgets.</li></p></ul>


<h5>States</h5>

<p>Switch between Disable and Enable modes in order to customize the appearance of both.</p>

<ul class="marked-list">
<p><li><b>Control Color</b> - here you can specify the necessary color to use for the switcher control field.</li></p>

<p><li><b>Handler Color</b> - here you can specify the color to apply for the handler control.</li></p>

<p><li><b>Disable Label Color</b> - here you can define the color to apply for the Disable label.</li></p>

<p><li><b>Enable Label Color</b> - here you can specify the color to use for the Enable label.</li></p></ul>

<h5>Switcher Content</h5>

<ul class="marked-list">
<p><li><b>Background Type</b> - here you can select whether you want to use the the classic or the gradient background type and customize the one selected. The customization settings are the same for all JetTabs widgets.</li></p>

<p><li><b>Padding</b> - here you can specify the padding values for the switcher content field and make them responsive.</li></p>

<p><li><b>Border type</b> - here you can apply a border and set its type for the content shown inside the switcher. In the case the border is selected you’ll be able to edit its width and color.</li></p>

<p><li><b>Border Radius</b> - here you can set the custom border radius values to make the border angles more smooth and round.</li></p>

<p><li><b>Box Shadow</b> - here you can enable the shadow for the switcher content and customize its appearance using the available options.</li></p></ul>
