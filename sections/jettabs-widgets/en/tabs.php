<article><h2>Tabs</h2>


<h6>
<b>Tabs</b> widget is quite useful when creating tabs and enriching them with all kinds of Elementor-built content. It makes possible to organize content in a smart way and make it look way more stylish.

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/tabs-preview.png">
    </figure>

</h6>

<h4>Content</h4>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/tabs1.png">
    </figure>

<h6>Items</h6>

<ul class="marked-list">
<li><p><b>Add Item</b> - click <b>Add Item</b> button to add more tabs to the widget.</p></li>


<ul class="marked-list">
<li><p><b>Active</b> - enable this option in order to make the item active.</p></li>
<li><p><b>Icon</b> - here you can select the icon to apply it to the tab.</p></li>
<li><p><b>Label</b> - type in the item label to apply it.</p></li>
<li><p><b>Choose Template</b> - here you need to choose the template to embed it inside the tab.</p></li></ul>

<div class="alert alert-info">Check <a href="http://documentation.zemez.io/wordpress/index.php?project=jettabs&lang=en&section=jettabs-start" target="_blank">Getting Started</a> to learn more about creating templates.</div>

<h6>Settings</h6>

<ul class="marked-list">
<li><p><b>Show Effect</b> - here you can select one of the effects to apply it to the tabs when they show (<b>fade, zoom, move up, fall</b>).</p></li></ul>

<h4>Style</h4>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/tabs2.png">
    </figure>

<h6>General</h6>

<ul class="marked-list">
<li><p><b>Tabs Position</b> - here you can change the tabs position to <b>top, left</b> or <b>right</b>.</p></li></ul>

<h6>Tabs Control</h6>

<ul class="marked-list">
<li><p><b>Tabs Alignment</b> - here you can set the tabs alignment and make it different for different screen resolutions.</p></li></ul>

<p><b>Control Wrapper Style</b>

<ul class="marked-list">
<li><p><b>Background Type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the items. The customization options vary for each type. The settings are similar to the common background type settings for other <b>JetTabs</b> widgets.</p></li>
<li><p><b>Padding</b> - here you can set the preferable custom padding. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</p></li>
<li><p><b>Margin</b> - input the values for the margins in the required fields.</p></li>
<li><p><b>Border Type</b> - here you can select the needed type of the border to use for the item.</p></li>
<li><p><b>Border Radius</b> - here you can set the border radius to make the angles more smooth and round.</p></li>
<li><p><b>Box Shadow</b> - here you can apply the shadow for the button and change the shadow’s settings, such as <b>color, blur, spread</b> and <b>position</b>.</p></li></ul>

<p><b>Control Style</b>

<p>Switch between <b>Normal, Hover</b> or <b>Active</b> modes to customize the controls text and icon according to your needs when the tab is active, or in the normal state.</p>

<ul class="marked-list">
<li><p><b>Text Color</b> - here you can change the tab label text color to the one you want.</p></li>
<li><p><b>Typography</b> - turn the option on to view the typography settings. The typography settings are similar for all the <b>JetTabs</b> widgets and blocks.</p></li>
<li><p><b>Icon Color</b> - here you can change the color of the icons to be shown in the tabs.</p></li>
<li><p><b>Icon Size</b> - here you need to customize the size of the icons and make it responsive.</p></li>
<li><p><b>Background Type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the tabs. The customization options vary for each type. The settings are similar to the common background type settings for other <b>JetTabs</b> widgets.</p></li>
<li><p><b>Border Type</b> - here you can select the needed type of the border to use for the item.</p></li>
<li><p><b>Padding</b> - here you can set the preferable custom padding. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</p></li>
<li><p><b>Margin</b> - input the values for the margins in the required fields.</p></li>
<li><p><b>Border Radius</b> - here you can set the border radius to make the angles more smooth and round.</p></li>
<li><p><b>Box Shadow</b> - here you can apply the shadow for the tab and change the shadow’s settings, such as <b>color, blur, spread</b> and <b>position</b>.</p></li></ul>

<h6>Tabs Content</h6>

<ul class="marked-list">
<li><p><b>Background Type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the tabs content. The customization options vary for each type. The settings are similar to the common background type settings for other <b>JetTabs</b> widgets.</p></li>
<li><p><b>Padding</b> - here you can set the preferable custom padding. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</p></li>
<li><p><b>Border Type</b> - here you can select the needed type of the border to use for the item content.</p></li></p></li>
<li><p><b>Border Radius</b> - here you can set the border radius to make the angles more smooth and round.</p></li>
<li><p><b>Box Shadow</b> - here you can apply the shadow for the tab content and change the shadow’s settings, such as <b>color, blur, spread</b> and <b>position</b>.</p></li></ul>

<h4>Advanced</h4>

<p>The <b>Advanced</b> settings block provides opportunity to change the elements style, background and borders. The settings are similar for all of the <b>JetTabs</b> widgets.</p>

<p>Feel free to check the detailed description of these settings in the <b>Accordion</b> widget block.</p>
