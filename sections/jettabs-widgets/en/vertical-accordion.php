<h2>Vertical Accordion</h2>

<h6>
<b>Vertical Accordion</b> widget helps in creating Accordion blocks which arrange the content built with Elementor into the vertical items, which can be opened and closed with a click.
</h6>

<h4>Content</h4>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/vertacc1.png">
    </figure>

<h6>Items</h6>

<ul class="marked-list">
<p><li><b>Add Item</b> - click <b>Add Item</b> button to add more items to the <b>Vertical Accordion</b> widget.</li></p></ul>

<ul class="marked-list">
<p><li><b>Active</b> - enable <b>Active</b> option in order to make the item active.</li></p>
<p><li><b>Icon</b> - here you can select the icon to use for the item from the available icons list.</li></p>
<p><li><b>Active Icon</b> - here you need to select the icon that will show up when the item becomes active.</li></p>
<p><li><b>Label</b> - here you need to type in the text label  for the item in the corresponding field.</li></p>
<p><li><b>Choose Template</b> - here you need to select one of the saved templates to embed it into the item’s content.</li></p></ul>

<div class="alert alert-info">Check <a href="http://documentation.zemez.io/wordpress/index.php?project=jettabs&lang=en&section=jettabs-start" target="_blank">Getting Started</a> to learn more about creating templates.</div>


<h6>Settings</h6>

<ul class="marked-list">
<li><p><b>Show Effect</b> - here you can select one of the effects to apply it to the accordion items when they show (<b>fade, zoom, move up, fall</b>).</p></li></ul>

