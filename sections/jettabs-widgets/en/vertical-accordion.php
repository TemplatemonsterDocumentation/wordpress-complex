<h6>Accordion Container </h6>

<ul class="marked-list">
<p><li><b>Height</b> - here you can set the needed container height and make it responsive to fit for different screens and devices. </li></p>
<p><li><b>Background Type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the whole accordion container. The customization options vary for each type. The settings are similar to the common background type settings for other <b>JetTabs</b> widgets.</li></p>
<p><li><b>Border Type</b> - here you can select the needed type of the border to use for the whole accordion container.</li></p>
<p><li><b>Border Radius</b> - here you can set the border radius to make the angles more smooth and round.</li></p>
<p><li><b>Box Shadow</b> - here you can apply the shadow for the accordion container and change the shadow’s settings, such as <b>color, blur, spread</b> and <b>position</b>. </li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p></ul>

<h6>Toggle</h6>

<ul class="marked-list">
<p><li><b>Padding</b> - here you can set the preferable custom padding. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Margin</b> - input the values for the margins in the required fields.</li></p>
<p><li><b>Background Type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the accordion toggle. The customization options vary for each type. The settings are similar to the common background type settings for other <b>JetTabs</b> widgets.</li></p>
<p><li><b>Border Type</b> - here you can select the needed type of the border to use for the accordion toggle.</li></p>
<p><li><b>Border Radius</b> - here you can set the border radius to make the angles more smooth and round.</li></p>
<p><li><b>Box Shadow</b> - here you can apply the shadow for the accordion toggle and change the shadow’s settings, such as <b>color, blur, spread</b> and <b>position</b>. </li></p></ul>

<h6>Toggle Control</h6>

<ul class="marked-list">
<p><li><b>Width</b> - here you can set the width of the toggle and make it responsive. </li></p></ul>

<p>Switch between <b>Normal, Hover</b> or <b>Active</b> modes to customize the toggle control according to your needs when the block is active, or in the normal state.</p>

<ul class="marked-list">
<p><li><b>Text Color</b> - here you can set the color to use it for the toggle label text.</li></p>
<p><li><b>Typography</b> - turn the option on to view the typography settings. The typography settings are similar for all the <b>JetTabs</b> widgets and blocks.</li></p>
<p><li><b>Icon</b> - click the icon options block to view all of them and change the <b>icon color, background,</b> etc.</li></p>

<p><li><b>Color</b> - here you can change the color of the icon using Color Picker tool.</li></p>
<p><li><b>Background Type</b> - here you can select the needed icon background type (the settings are similar to the ones for the other JatTabs widgets blocks background types).</li></p>
<p><li><b>Icon Size</b> - here you can select the icon size in order for icon to fit. The settings are responsive.</li></p>
<p><li><b>Box Size</b> - here you can select the needed icon box size. The settings are responsive.</li></p>
<p><li><b>Border Type</b> - here you can select the needed type of the border to use for the icon box. You’ll also be able to set the <b>border color, width</b> and <b>border radius</b>.</li></p>
<p><li><b>Box Shadow</b> - here you can apply a shadow for the icon box.</li></p>

<p><li><b>Background Type</b> - here you can select the needed toggle control background type (the settings are similar to the ones for the other <b>JatTabs</b> widgets blocks background types).</li></p>

<p><li><b>Border Type</b> - here you can select the needed type of the border to use for the toggle control. You’ll also be able to set the border <b>color, width</b> and <b>border radius</b>.</li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Border Radius</b> - here you can set the border radius to make the angles more smooth and round.</li></p>
<p><li><b>Box Shadow</b> - here you can apply a shadow for the toggle controls.</li></p>
<p><li><b>Icon Margin</b> - her you can apply your custom margins for the icons on toggles and make them responsive.</li></p>
<p><li><b>Label Direction</b> - here you can set the needed orientation of the toggle text labels (<b>vertical</b> or <b>horizontal</b>).</li></p>
<p><li><b>Content Alignment</b> - here you can set the preferable toggle elements alignment inside the toggle controls (<b>start, center, end, space between</b>) and make it responsive.</li></p>
<p><li><b>Icon Alignment</b> - here you can set the suitable icon alignment in the toggle control and make it responsive.</li></p>
<p><li><b>Label Alignment</b> - here you can set the preferable label alignment inside the toggle control and make it responsive.</li></p></ul>

<h6>Toggle Content</h6>

<ul class="marked-list">
<p><li><b>Background Type</b> - here you can select the needed toggle content background type (the settings are similar to the ones for the other <b>JatTabs</b> widgets blocks background types).</li></p>

<p><li><b>Margin</b> - input the values for the margins in the required fields.</li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Border Type</b> - here you can select the needed type of the border to use for the toggle content block. You’ll also be able to set the border <b>color, width</b> and <b>border radius</b>.</li></p>
<p><li><b>Border Type</b> - here you can select the needed type of the border to use for the toggle content block. You’ll also be able to set the <b>border color, width</b> and <b>border radius</b>.</li></p>
<p><li><b>Border Radius</b> - here you can set the border radius to make the angles more smooth and round.</li></p>
<p><li><b>Box Shadow</b> - here you can apply a shadow for the toggle content block.</li></p></ul>

<h4>Advanced</h4>

<p>The <b>Advanced</b> settings block provides opportunity to change the elements style, background and borders. The settings are similar for all of the <b>JetTabs</b> widgets.</p>

<p>Feel free to check the detailed description of these settings in the <b>Accordion</b> widget block.</p>

