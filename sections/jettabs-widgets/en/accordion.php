<h6>Items</h6>

<ul class="marked-list">
<p><li><b>Add Item</b> - click <b>Add Item</b> button to add the new toggle item to the <b>Accordion</b> block.</li></p>

<p><li><b>Active</b> - enable this option to make the toggle active in the <b>Accordion</b>. </li></p>

<p><li><b>Icon</b> - here you can set the icon to be used as the major item icon. </li></p>

<p><li><b>Active Icon</b> - select one of the icons from the dropdown in order to set it as the <b>Active</b> toggle item. </li></p>

<p><li><b>Label</b> - type in the preferable name for the item toggle to use. </li></p>

<p><li><b>Choose Template</b> - select one of the existing previously saved templates to place inside the <b>Accordion</b> toggle. </li></p>

<h6>Settings</h6>

<ul class="marked-list">
<p><li><b>Collapsible</b> - enable Collapsible option in order to make the <b>Accordion</b> items collapsible. </li></p>

