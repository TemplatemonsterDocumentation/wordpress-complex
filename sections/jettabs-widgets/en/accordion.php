<h6>Accordion Container </h6>


<ul class="marked-list">
<p><li><b>Background Type</b> — in this block you can choose, whether you want to use classic or gradient type for the <b>Accordion container</b>. The customization options vary for each type.
<p><b>Classic Background Type</b></p>
  <ul class="marked-list">
    <li><b>Color</b> — here you can set background color for the <b>Accordion container</b> using color picker tool.</li>
    <li><b>Image</b> — here you can choose the image you want to use as a background. Select the image from the media library or upload it to define it as a background in this block. When the image is uploaded, you’ll be able to define its position, alignment, turn on repeat and set the necessary size.</li>
    <li><b>Opacity</b> — here you can define the value for the opacity of the background.</li>
  </ul>
<p><b>Gradient Background Type</b></p>
  <ul class="marked-list">
    <li><b>Color</b> — here you can set background color for the Accordion container using color picker tool.</li>
    <li><b>Location</b> — here you can define the proportion for the first color in the gradient background.</li>
    <li><b>Second Color</b> — here you can set the second color to use in the gradient background.</li>
    <li><b>Location</b> — here you can define the proportion for the second color in the gradient background.</li>
    <li><b>Type</b> — define, if you want to use linear or radial type of the gradient.</li>
    <li><b>Angle</b> — in this block you can set the angle for the gradient (in degrees).</li>
    <li><b>Opacity</b> — here you can change background opacity, where 1 is 100% opacity, and 0 — is 0% opacity.</li></ul></li></p>


<p><li><b>Border Type</b> - here you can define the type of the border you need to use for the element.</li></p>
<p><li><b>Width</b> - here you can set the border width value in px.</li></p>
<p><li><b>Color</b> - use the color picker tool to select the needed color for the border.</li></p>
<p><li><b>Border Radius</b> - here you can set the border radius to make the angles more smooth and round.</li></p>
<p><li><b>Box Shadow</b> -  here you can apply the shadow for the Accordion container and change the shadow’s settings, such as color, blur, spread and position.</li></p>

<p><li><b>Padding</b> - here you can set the preferable custom padding. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p></ul>

