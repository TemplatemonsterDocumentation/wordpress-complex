<h4>Advanced </h4>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/clacc3.png">
    </figure>

<p>The <b>Advanced</b> settings block provides opportunity to change the elements style, background and borders. The settings are similar for all of the <b>JetTabs</b> widgets.</p>

<h6>Element Style<</h6>

<ul class="marked-list">
<p><li><b>Margin</b> — in this block you can input the values (in pixels) to define the margins for the module you're customizing.</li></p>
<p><li><b>Padding</b>— here you can set the custom padding for the widget. Fill in the values for the top, bottom, right and left padding in pixels to apply your custom padding.</li></p>
<p><li><b>Z-index</b>— input the value of the z-index to use it for the widget.</li></p>
<p><li><b>Entrance Animation</b>- choose from the dropdown menu one of the styles for the widget to appear on the page.</li></p>
<p><li><b>Animation Duration</b> — here you can define the time during which the widget will appear on the page. It can be normal, slow or fast. Choose the one you need from the dropdown menu.</li></p>
<p><li><b>Animation Delay (ms)</b> — input the value (in ms) for the animation delay.</li></p>
<p><li><b>CSS Classes</b> — here you can define the CSS class for the widget.</li></p></ul>

<h6>Background</h6>

<p>Here you can switch from customizing <b>Normal</b> to <b>Hover</b> background style settings. Just click on the button <b>Hover</b> to proceed to customizing hover elements. </p>

<ul class="marked-list">
<p><li><b>Background Type</b> — in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the module background. The customization options vary for each type.</li></p></ul>

<p><b>Classic Background Type</b></p>

<ul class="marked-list">
<p><li><b>Color</b> — here you can set background color for the background using color picker tool.</li></p>
<p><li><b>Image</b> — here you can choose the image you want to use as a background. Select the image from the media library or upload it to define it as a background in this block. When the image is uploaded, you’ll be able to define its position, alignment, turn on repeat and set the necessary size.</li></p></ul>

<p><b>Gradient Background Type</b></p>

<ul class="marked-list">
<p><li><b>Color</b> — here you can set background color for the module using color picker tool.</li></p>
<p><li><b>Location</b> — here you can define the proportion for the first color in the gradient background.</li></p>
<p><li><b>Second Color</b> — here you can set the second color to use in the gradient background.</li></p>
<p><li><b>Location</b> — here you can define the proportion for the second color in the gradient background.</li></p>
<p><li><b>Type</b> — define, if you want to use linear or radial type of the gradient.</li></p>
<p><li><b>Angle</b> — in this block you can set the angle for the gradient (in degrees).</li></p></ul>

<h6>Border </h6>

<p>Here you can switch from customizing <b>Normal</b> to <b>Hover</b> border style settings. Just click on the button <b>Hover</b> to proceed to customizing hover elements. </p>

<p><b>Border Type</b> — here you can select the type of the border from the dropdown menu. Here is the list of available border types:</p>
<ul class="marked-list">
<li>none;</li>
<li>solid;</li>
<li>doubled;</li>
<li>dotted;</li>
<li>dashed.</li></ul>
<p><b>Border Radius</b> — here you can select the radius for the applied border (in pixels). </p>
<p><b>Box Shadow</b> — enable this option if you want to access the shadow advanced settings for this module, and need to apply shadow for it. </p>

<p><b>Shadow Settings:</b></p>
<ul class="marked-list">
<li><b>color</b> — here you can set the color for the the shadow (use color picker tool to do it);</li>
<li><b>blur</b> — in this block you can define the blur settings for the shadow;</li>
<li><b>spread</b> — here you can set how large the shadow will be;</li>
<li><b>horizontal</b> — here you can move the shadow horizontally (from right to left);</li>
<li><b>vertical</b> — here you can move the shadow vertically (from top to bottom);</li>
<li><b>position</b> — here you need to choose, whether you want the shadow to outline the module, or to be inset. </li></ul>




