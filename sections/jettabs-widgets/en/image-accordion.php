<h4>Content</h4>

  <figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/inst/imacc1.png">
    </figure>

<h6>Items</h6>

<ul class="marked-list">
  <p><li><b>Add Item</b> - click <b>Add Item</b> button to add more items to <b>Image Accordion</b>.</li></p></ul>

<ul class="marked-list">
<p><li><b>Image</b> - select the image you want to use for the <b>Image Accordion</b> item from the media library or upload it from your hard drive.</li></p>
<p><li><b>Title</b> - here you need to specify the title for the image.</li></p>
<p><li><b>Description</b> - in this block you need to define the description text for the image.</li></p>
<p><li><b>Button Text</b> - here you can set the text to be shown as the button label.</li></p>
<p><li><b>Link</b> - in this field you can define the link to use it for the button. You can also define, if the link opens in new window, or has nofollow value.</li></p></ul>

<h6>Settings</h6>

<ul class="marked-list">
<p><li><b>Orientation</b> - here you can change the <b>Image Accordion</b> orientation to <b>vertical</b> or <b>horizontal</b>.</li></p>
<p><li><b>Active Size (%)</b> - here you can change the size of the active position of the accordion item in percents (it shows the percentage which the active image occupies of the full Image Accordion block).</li></p>
<p><li><b>Animation Duration</b> - here you can set the needed animation duration in ms.</li></p>
<p><li><b>Easing</b> - here you can select the type of easing to apply to the Image Accordion items (<b>Circ, Back, Expo, Cubic, Quint, Sine</b>).</li></p></ul>

