<h6>Container</h6>

<ul class="marked-list">
<p><li><b>Height (px)</b> - here you can set the height of the <b>Image Accordion</b> widget in px and make it responsive.</li></p>
<p><li><b>Background Type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for <b>Image Accordion</b>. The customization options vary for each type. The settings are similar to the common background type settings for other <b>JetTabs</b> widgets.</li></p>
<p><li><b>Border Type</b> - here you can select the needed type of the border to use for the <b>Image Accordion</b>.</li></p>
<p><li><b>Border Radius</b> - here you can set the border radius to make the angles more smooth and round.</li></p>
<p><li><b>Box Shadow</b> - here you can apply the shadow for the <b>Image Accordion</b> widget and change the shadow’s settings, such as <b>color, blur, spread</b> and <b>position</b>.</li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>

<h6>Item</h6>

<ul class="marked-list">
<p><li><b>Item Gutter</b> - here you can set the space gutter between the items shown in accordion, and make it responsive.</li></p>
<p><li><b>Background Type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the item. The customization options vary for each type. The settings are similar to the common background type settings for other <b>JetTabs</b> widgets.</li></p>
<p><li><b>Border Type</b> - here you can select the needed type of the border to use for the item.</li></p>
<p><li><b>Border Radius</b> - here you can set the border radius to make the angles more smooth and round.</li></p>
<p><li><b>Box Shadow</b> - here you can apply the shadow for the <b>Image Accordion</b> item and change the shadow’s settings, such as <b>color, blur, spread</b> and <b>position</b>.</li></p></ul>

<h6>Content</h6>

<ul class="marked-list">
<p><li><b>Background Type</b> - in this block you can choose, whether you want to use <b>classic</b> or <b>gradient</b> type for the content. The customization options vary for each type. The settings are similar to the common background type settings for other <b>JetTabs</b> widgets.</li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Text Alignment</b> - here you can set the <b>right, left</b> and <b>centered</b> alignment for the textual content.</li></p></ul>

<h6>Title</h6>

<ul class="marked-list">
<p><li><b>Color</b> - here you can set the color to use it for the item title.</li></p>
<p><li><b>Typography</b> - turn the option on to view the typography settings. The typography settings are similar for all the <b>JetTabs</b> widgets and blocks.</li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Margin</b> - input the values for the margins in the required fields.</li></p>
<p><li><b>Alignment</b> - here you can set the alignment to use it for the title (<b>center, right, left</b>).</li></p></ul>

<h6>Description</h6>

<ul class="marked-list">
<p><li><b>Color</b> - here you can set the color to use it for the item description text.</li></p>
<p><li><b>Typography</b> - turn the option on to view the typography settings. The typography settings are similar for all the <b>JetTabs</b> widgets and blocks.</li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Margin</b> - input the values for the margins in the required fields.</li></p>
<p><li><b>Alignment</b> - here you can set the alignment to use it for the description text (<b>center, right, left</b>).</li></p></ul>

<h6>Action Button</h6>

<ul class="marked-list">
<p><li><b>Alignment</b> - here you need to select the button alignment (<b>left, right, center</b>) and make it responsive.</li></p></ul>

<p>Switch between <b>Normal</b> or <b>Hover</b> mode to customize the button according to your needs when the button is active, or in the normal state.</p>

<ul class="marked-list">
<p><li><b>Background Color</b> - here you can set the suitable color to use ir for the button background.</li></p>
<p><li><b>Text Color</b> - here you should select the color to apply it to the button label text.</li></p>
<p><li><b>Typography</b> - turn the option on to view the typography settings. The typography settings are similar for all the <b>JetTabs</b> widgets and blocks.</li></p>
<p><li><b>Padding</b> - here you can set the preferable custom padding. Fill in the values for the <b>top, bottom, right</b> and <b>left</b> padding in pixels to apply your custom padding.</li></p>
<p><li><b>Margin</b> - input the values for the margins in the required fields.</li></p>
<p><li><b>Border Radius</b> - here you can set the border radius to make the angles more smooth and round.</li></p>
<p><li><b>Border Type</b> - here you can select the needed type of the border to use for the item.</li></p>
<p><li><b>Box Shadow</b> - here you can apply the shadow for the button and change the shadow’s settings, such as <b>color, blur, spread</b> and <b>position</b>.</li></p>

<h6>Content Order and Alignment</h6>

<p>In this block you can change the order in which the <b>title</b>, <b>description</b> and <b>button</b> are shown.</p>

<h4>Advanced</h4>

<p>The <b>Advanced</b> settings block provides opportunity to change the elements style, background and borders. The settings are similar for all of the <b>JetTabs</b> widgets.</p>

