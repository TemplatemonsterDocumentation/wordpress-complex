<h3>JetDesignKit Installation</h3>

<h5>From this block, you can learn how to install JetDesignKit plugin using WordPress Dashboard.</h5>

<h5>Please make sure you have <b>Elementor</b> and <b>JetElements</b> plugins installed!</h5>

<ol class="index-list">


	<p><li>First, make sure that you’ve installed and activated <a href="https://wordpress.org/plugins/elementor/" target="_blank">Elementor</a> page builder and <a href="https://documentation.zemez.io/wordpress/index.php?project=jetelements" target="_blank">JetElements</a> plugin before proceeding to JetDesignKit installation. If you have problems installing Elementor feel free to read the detailed <a href="https://docs.elementor.com/" target="_blank">Elementor documentation</a>. If you need assistance with JetElements plugin installation, please refer to <a href="https://documentation.zemez.io/wordpress/index.php?project=jetelements&lang=en&section=jetelements-installation" target="_blank">JetElements documentation</a>.</li></p></p>


<p><li>Log in to WordPress Dashboard using Your login credentials and navigate to <b>Plugins</b> tab in the left column.

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/1.png">
    </figure></li></p>


<p><li>Click on <b>Add new</b> button to upload JetDesignKit plugin from Your hard drive.

<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/2.png">
    </figure></li></p>

<p><li> Click <b>Upload</b> button, which can be found at the top of <b>Plugins</b> page. Browse the path to <b>JetDesignKit.zip</b> plugin package and start the installation process.

	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/3.png">
  	</figure>

</li></p>
<p><li>When JetDesignKit plugin is uploaded and installed you’ll need to activate it. Navigate to <b>Plugins</b> tab and locate JetDesignKit plugin in the list of installed plugins. Click on <b>Activate</b> link under JetDesignKit. As a result the plugin will become active.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/4.png">
  	</figure>
</li></p>

</ol>
