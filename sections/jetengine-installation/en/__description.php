<h3>JetEngine Installation</h3>

<h5>From this block you can learn how to install <b>JetEngine</b> plugin using WordPress Dashboard. </h5>

<ol class="index-list">
<p><li>First, make sure that you’ve installed and activated <a href="https://wordpress.org/plugins/elementor/" target="_blank">Elementor</a> page builder before proceeding to JetEngine installation. If you have problems with installing Elementor feel free to read the detailed <a href="https://docs.elementor.com/" target="_blank">Elementor documentation</a>.</li></p>

<p><li>Log in to WordPress Dashboard using Your login credentials and navigate to <b>Plugins</b> tab in the left column.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/1.png">
  	</figure></li></p>

<p><li>Click on <b>Add new</b> button to upload <b>JetEngine</b> plugin from Your hard drive.
</li></p>

<p><li>Click on <b>Upload</b> button, which can be found at the top of <b>Plugins</b> page. Browse the path to <b>JetEngine.zip</b> plugin package and start the installation process.

 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/2.png">
  	</figure></li></p>

<p><li>When <b>JetEngine</b> plugin is uploaded and installed you’ll need to activate it. Click <b>Activate</b> button to make it active on the site.
 	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/3.png">
  	</figure>
</li></p>


 <p><li>Now you can locate <b>JetEngine</b> plugin in the list of installed plugins.

  	<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/installation/4.png">
  	</figure></li></p></ol>



