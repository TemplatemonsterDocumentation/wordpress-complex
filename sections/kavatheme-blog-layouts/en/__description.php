<h3>Blog Layouts</h3>

<h5>This section contains information on how to change the blog layouts using Customizer Blog settings. </h5>

<h5>Changing Blog Layouts</h5>



<ol class="index-list">
    <li><p>First, navigate to your WordPress Dashboard. Here open <b>Appearance > Customize</b> tab.</p></li>

    <figure class="img-polaroid"><img src="img/wordpress/blog1.png" alt=""></figure>

    <li><p>Make sure that You're currently customizing the page which displays blog posts. If not, please, open <b>Homepage Settings</b> and here check <b>Your homepage displays > Your latest posts</b>.</p></li>

        <figure class="img-polaroid"><img src="img/wordpress/blog2.png" alt=""></figure>

    <li><p>After that return to Customizer's main panel and open <b>Blog Settings</b>.</p></li>

        <figure class="img-polaroid"><img src="img/wordpress/blog4.png" alt=""></figure>

    <li><p>In <b>Layout</b> dropdown select one of the layouts you want to apply for the blog page:</p>

<ul class="marked-list">
<li>listing;</li>
<li>grid;</li>
<li>masonry;</li>
<li>justify;</li>
<li>creative.</li></ul></li>

    <figure class="img-polaroid"><img src="img/wordpress/blog3.png" alt=""></figure>

    <li><p>In <b>Style</b> dropdown select one of the 10 available styles for the layout. Each layout has 10 different styles.</p></li>

    <li><p>In <b>Sidebar</b> dropdown specify where you want to position the sidebar: on right side, on left side or no sidebar at all.</p></li>

    <li><p>Choose the navigation type you want to apply: <b>pagination</b> or <b>navigation</b>.</p></li>

    <li><p>Select the needed sticky label type: <b>font icon, text label, text with icon</b>.</p></li></ol>
<ul class="marked-list">
    <li><p><b>Show post author</b> - enable this option in order to display the author of the publications.</p></li>

    <li><p><b>Show publish date</b> - here you can enable the displaying of the publish date.</p></li>

    <li><p><b>Show categories</b> - here you can enable the categories for displaying.</p></li>

    <li><p><b>Show tags</b> - here you can enable the tags for displaying.</p></li>

    <li><p><b>Show comments</b> - here you can enable the comments to be displayed.</p></li>

    <li><p><b>Show Excerpt</b> - here you can enable the post excerpts to be shown.</p></li>

    <li><p><b>Excerpt Words Count</b> - here you can specify the length of the post excerpt in words.</p></li>

    <li><p><b>Read More button type</b> - here you can sen specify the type of the Read more button (Text, Icon, Text and Icon).</p></li>

    <li><p><b>Read more button text</b> - here you can select the text to be displayed on the Read more button.</p></li></ol>
