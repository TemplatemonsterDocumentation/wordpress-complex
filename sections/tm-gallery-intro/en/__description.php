<h2>TM Photo Gallery</h2>

<h5>
    <p>
    	Use this plugin to add various kinds of photo galleries to your site with a single click of a mouse. You just need to create a gallery and add it to any page or post you like. It will be a perfect solution for blogs or online stores to create compelling portfolios and presentations. TM Photo gallery allows you to create:
    </p>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Sets</dt>
            <dd>
				Contain albums with photos and separate images
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Albums</dt>
            <dd>
				Contain photos only
            </dd>
        </dl>
    </li> 
    <li>
        <dl class="inline-term">
            <dt>Separate not categorized images</dt>
        </dl>
    </li>
</ul>

	<p>
		After the installation you will see TM Photo Gallery icon in the left sidebar of your admin panel. 
	</p>

</h5>


