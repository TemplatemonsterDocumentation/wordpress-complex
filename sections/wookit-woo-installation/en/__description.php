<h3>Step 2. WooCommerce Installation</h3>

<ol class="index-list">
<p><li>Let’s install <b>WooCommerce</b> plugin. To do it navigate to <b>Plugins > Add New</b> tab on the left of the Dashboard.</li></p>

	<figure class="img-polaroid"><img src="img/wookit/4.png" alt=""></figure>


<p><li>Type in <b>WooCommerce</b> in the search box on the right.</li></p>
<p><li>Click <b>Install now</b> button on the right of the plugin. Then click <b>Activate</b>.</li></p>
<p><li>Now that <b>WooCommerce</b> plugin is active, you can start working with its settings.</li></p>
<p><li>On the Dashboard you can see the <b>WooCommerce Setup Wizard</b> block. Here click <b>Run the Setup Wizard</b>.</li></p></ol>

	<figure class="img-polaroid"><img src="img/wookit/5.png" alt=""></figure>

<h4>Store Setup</h4>

	<figure class="img-polaroid"><img src="img/wookit/6.png" alt=""></figure>

<ol class="index-list">
<p><li>The first step is called <b>Store Setup</b>. You’ll see the following block, where you’ll need to indicate the country where your store is based, set your store’s address, set the currency you want to use and define, what type of product you want to sell (physical products, digital products or both).</li></p>
<p><li>You can check or uncheck <b>Allow WooCommerce to collect non-sensitive diagnostic data and usage information</b> option.</li></p>
<p><li>When you’re ready click <b>Let’s go!</b> button below the block.</li></p></ol>

<h4>Payment</h4>

	<figure class="img-polaroid"><img src="img/wookit/7.png" alt=""></figure>

<ol class="index-list">
<p><li><b>WooCommerce</b> can accept both online and offline payments. On this screen you can select the payment methods you’re going to use for your online store, such as <b>Stripe, Paypal,</b> or you can disable these methods to use <b>Offline Payments</b>.</li></p>
<p><li>When everything is set, please, click <b>Continue</b> button.</li></p></ol>

<h4>Shipping</h4>

	<figure class="img-polaroid"><img src="img/wookit/8.png" alt=""></figure>

<ol class="index-list">
<p><li>In this block you need to select, if you want to use live rates, flat rates or free shipping to ship the products to your customers.</li></p>
<p><li>Here you can also define the weight unit to use and the units to use for the product dimensions.</li></p>
<p><li>After that click <b>Continue</b> to proceed to the next setup step.</li></p></ol>

<h4>Extras</h4>

	<figure class="img-polaroid"><img src="img/wookit/9.png" alt=""></figure>

<ol class="index-list">
<p><li>In this block you can enable <b>Automated Taxes</b>. Enable this option to automatically calculate and charge the correct rate of tax for each time a customer checks out.</li></p>
<p><li>Then click <b>Continue</b> to proceed.</li></p></ol>

<h4>Activate</h4>

	<figure class="img-polaroid"><img src="img/wookit/10.png" alt=""></figure>

<ol class="index-list">
<p><li>In the case you use automatic taxes, you need to activate <b>Jetpack</b> first. Click <b>Connect with Jetpack</b> button to do it.</li></p>
<p><li>When the connection is finally established click <b>Finish setting up your store</b> to navigate to <b>Ready!</b> block.</li></p></ol>

<h4>Ready!</h4>

	<figure class="img-polaroid"><img src="img/wookit/11.png" alt=""></figure>

<ol class="index-list">
<p><li>Now that you’ve set up <b>WooCommerce</b> plugin, you can subscribe to tips and product updates using your email address.</li></p>
<p><li>Fill in your email address in the corresponding field to subscribe.</li></p>
<p><li>You can also create a product to show it on your page. Click <b>Create a product</b> button to do it.</li></p>
<p><li>In the case you want to import products, hit <b>Import products</b> button.</li></p></ol>
