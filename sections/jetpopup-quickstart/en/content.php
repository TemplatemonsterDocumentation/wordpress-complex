<h3>Popup Action Button</h3>

<p>From this block, you can learn more about adding a <b>Yes/No</b> functionality to the popups using <b>Popup Action Button</b>.</p>

<h4>Popup Action Button Overview</h4>

<p>This widget is made for creating buttons that add <b>Yes/No</b> action to popup templates. It is easily customizable and provides one with all the needed means for adding different types of actions.</p>

<h4>Content</h4>

<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/9.png">
</figure>

<h5>Settings</h5>

<ul class="marked-list">
<p><li><b>Action Type</b> – here you can select the type of popup: <b>Link, Leave Page, Close Popup, Close Constantly</b>. It depends on what action you want to implement with this button (usually such buttons are added below the text blocks with questions, e.g., "Are you sure you want to leave this page?"). </li></p>

<p><li><b>Button text</b> – add the appropriate label to use for the button title.</li></p>
<p><li><b>Button link</b> – here you can add the link.
<ul class="marked-list">
<li><b>Open in new window</b> – the link will be opened in a new window.</li>
<li><b>Add nofollow</b> – the link will not be followed to another resource.</li></ul>

</li></p>
</ul>

<h4>Style</h4>
<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/10.png">
</figure>
<h5>General</h5>

<ul class="marked-list">
<p><li><b>Alignment</b> – here you can set the Action Button alignment. It can be positioned to the left, in the center or to the right.</li></p>
<p><li><b>Add Icon</b> – this option allows to add an icon to the button.
<ul class="marked-list">
<li><b>Icon</b> – here you can choose one of the icons, which will be displayed on the popup.</li>
<li><b>Icon Position</b> – here you can place the icon <b>before</b> or <b>after</b> the text.</li>
<li><b>Icon Margin</b> – set the responsive space around the icon.</li>
<li><b>Typography</b> – here you can adjust popup’s typography settings.</li>
<li><b>Family</b> – select the needed popup’s typography style.</li>
<li><b>Size</b> – set the popup’s size.</li>
<li><b>Weight</b> – choose the popup’s weight. There are number designation and normal, bold types.</li>
<li><b>Transform</b> – here you can choose the text’s appearance. It can be uppercase, lowercase, capitalize and normal.</li>
<li><b>Style</b> – here you can choose the text’s style. There are normal, italic and oblique.</li>
<li><b>Decoration</b> – here it is available to decorate the poup’s text. The text can be underlined, overlined or with line through.</li>
<li><b>Line-Height</b> – set the popup’s height.</li>
<li><b>Letter Spacing</b> – set the space between the letters in the popup’s text.</li>
<li><b>Padding</b> – here you can set the popup’s padding.</li>
<li><b>Border Radius</b> – here you can set the radius of the popup’s borders.</li>
<li><b>Normal</b> – here you can adjust the normal popup’s settings.</li>
<li><b>Background Color</b> – choose the color of the normal popup’s background.</li>
<li><b>Text Color</b> – choose the color of the normal popup’s text.</li>
<li><b>Border Type</b> – select the type of the popup’s borders. There are solid, double, dotted, dashed and groove type.</li>
<li><b>Width</b> – set the width of the popup’s borders.</li>
<li><b>Color</b> – select the color of the popup’s borders.</li>
<li><b>Box Shadow</b> – Use the shadow settings (such as shadow color, horizontal, vertical, blur, spread, position) in order to make the normal popup stand out when it is shown above the content.</li>
<li><b>Hover</b> – here you can adjust the hover popup’s settings.</li>
<li><b>Background Color</b> – select the color of the hover popup’s background.</li>
<li><b>Text Color</b> – select the color of the hover popup’s text.</li>
<li><b>Box Shadow</b> – Use the shadow settings (such as shadow color, horizontal, vertical, blur, spread, position) in order to make the hover popup stand out when it is shown above the content.</li></ul>

<h4>Advanced</h4>


<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/11.png">
</figure>

<h4>Element Style</h4>

<ul class="marked-list">
<li><b>Margin</b> — in this block you can input the values (in pixels) to define the margins for the module you're customizing.</li>
<li><b>Padding</b>— here you can set the custom padding for the module. Fill in the values for the top, bottom, right and left padding in pixels to apply your custom padding.</li>
<li><b>Z-index</b>— input the value of the z-index to use it for the module.</li>
<li><b>Entrance Animation</b>- choose from the dropdown menu one of the styles for the module to appear on the page.</li>
<li><b>Animation Duration</b> — here you can define the time during which the module will appear on the page. It can be normal, slow or fast. Choose the one you need from the dropdown menu.</li>
<li><b>Animation Delay (ms)</b> — input the value (in ms) for the animation delay.</li>
<li><b>CSS Classes</b> — here you can define the CSS class for the module.</li></ul>

<h4>Background</h4>

<p>Here you can switch from customizing Normal to Hover background style settings. Just click on the button Hover to proceed to customizing hover elements. </p>

<ul class="marked-list">
<li><b>Background Type</b> — in this block you can choose, whether you want to use classic or gradient type for the module background. The customization options vary for each type.</li></ul>

<h4>Classic Background Type</h4>


<ul class="marked-list">
<li><b>Color</b> — here you can set a background color for the background using color picker tool.</li>
<li><b>Image</b> — here you can choose the image you want to use as a background. Select the image from the media library or upload it to define it as a background in this block. When the image is uploaded, you’ll be able to define its position, alignment, turn on repeat and set the necessary size.</li></ul>

<h4>Gradient Background Type</h4>

<ul class="marked-list">
<li><b>Color</b> — here you can set a background color for the module using color picker tool.</li>
<li><b>Location</b> — here you can define the proportion for the first color in the gradient background.</li>
<li><b>Second Color</b> — here you can set the second color to use in the gradient background.</li>
<li><b>Location</b> — here you can define the proportion for the second color in the gradient background.</li>
<li><b>Type</b> — define, if you want to use a linear or a radial type of the gradient.</li>
<li><b>Angle</b> — in this block you can set the angle for the gradient (in degrees).</li></ul>

<h4>Border </h4>

<p>Here you can switch from customizing Normal to Hover border style settings. Just click on the button Hover to proceed to customizing hover elements. </p>
<ul class="marked-list">
<li><b>Border Type</b> — here you can select the type of the border from the dropdown menu. Here is the list of available border types:
<ul class="marked-list">
<li>none;</li>
<li>solid;</li>
<li>doubled;</li>
<li>dotted;</li>
<li>dashed.</li></ul></li>
<li><b>Border Radius</b> — here you can select the radius for the applied border (in pixels). </li>
<li><b>Box Shadow</b> — enable this option if you want to access the shadow advanced settings for this module, and need to apply shadow for it. </li></ul>

<p><b>Shadow Settings:</b></p>
<ul class="marked-list">
<li><b>color</b> — here you can set the color for the shadow (use color picker tool to do it);</li>
<li><b>blur</b> — in this block you can define the blur settings for the shadow;</li>
<li><b>spread</b> — here you can set how large the shadow will be;</li>
<li><b>horizontal</b> — here you can move the shadow horizontally (from right to left);</li>
<li><b>vertical</b> — here you can move the shadow vertically (from top to bottom);</li>
<li><b>position</b> — here you need to choose, whether you want the shadow to outline the module, or to be inset. </li></ul>
