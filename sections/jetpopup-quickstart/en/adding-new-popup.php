<h3>Adding New Popups</h3>

<p>From this block, youâ€™ll learn how to create a new popup from scratch using JetPopup plugin and Elementor widgets.</p>

<ol class="index-list">
<p><li>Open WordPress Dashboard and locate JetPopup tab on the left. Click <b>Add New Popup</b> in order to create a new popup. You can also create a new popup from <b>All Popups</b> block, where you can view the list of existing popup templates. Here click <b>Add New Popup</b> button.

<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/3.png">
  	</figure></li></p>

<p><li>On the <b>Add New Popup</b> page enter the title to use for the popup template. Then click <b>Edit with Elementor</b> button.

<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/4.png">
</figure>
</li></p>

<p><li>After you're redirected to Elementor live page builder for customizing the new popup you'll see the default popup block where you can add a new section, or use one of the previously created templates. To add a new section click <b>Add New Section</b> button. Click <b>Edit Section</b> option in order to set the content width, section background style, paddings and margins, etc.

<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/5.png">
</figure>
</li></p>

<p><li>Select the preferable columns structure for the section. You can also drag the column borders in order to set the custom columns width. Feel free to edit the columns width, set the content position, etc, by editing the column.

<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/6.png">
</figure>
</li></p>

<p><li>Add any widgets to the layout you've created to make the popup template fit your needs.</li></p>

<p><li>In order to add <b>Yes/No</b> functionality you need to use <a href="http://documentation.zemez.io/wordpress/index.php?project=jetpopup&lang=en&section=jetpopup-quickstart#action-button" target="_blank" rel="noopener">Popup Action Button</a> widget.

<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/7.png">
</figure>
</li></p>


<p><li>After you've created the needed content, you can proceed to managing <a href="http://documentation.zemez.io/wordpress/index.php?project=jetpopup&lang=en&section=jetpopup-settings#" target="_blank" rel="noopener">Popup Settings</a> which are located in the bottom left corner of the editing page.

<figure class="img-polaroid">
        <img src="projects/jetpopup/img/presets/8.png">
</figure>
 </li></p>


<p><li>After the page is built click Publish button to preserve the changes.</li></p>
</ol>


