<h3>Using Presets</h3>

<p>From this block youâ€™ll learn how to apply presets to popups to use the specific popup layout when creating a new popup. </p>

<ol class="index-list">
<p><li>JetPopup plugin provides the number of default presets that can help in creating popups with specific layouts. In order to select a preset, please, navigate  to <b>JetPopup > All Popups</b> in WordPress Dashboard and here click <b>Popup Presets</b> button located at the top of the page. </li></p>

<p><li>You'll see <b>Select Presets</b> block with the number of different preset variations. Choose one of the presets to use when creating a new popup.</li></p>

<p><li>Click <b>Create Popup</b> button at the bottom of <b>Presets</b> block to proceed to creating new popup template.</li></p>

<p><li>After that you'll see the selected preset opened in Elementor editing window. Here you can customize its appearance, fill in in with other widgets available in Elementor, change its appearance and style, make changes in JetPopup Settings, etc.</li></p></ol>


