<h3>Video</h3>
<p>Embed any videos from any source with the help of this module. They can be added to any section or row of the builder.  
</p>

<h4>Module Options</h4>
	<ul class="marked-list">
		<li>
            <dl class="inline-term">
                <dt>Video MP4/URL</dt>
                <dd>
    				Upload your desired video in .MP4 format, or type in the URL to the video you would like to display.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Video Webm</dt>
                <dd>
    				Upload the .WEBM version of your video here. All uploaded videos should be in both .MP4 .WEBM formats to ensure maximum compatibility in all browsers.
                </dd>
            </dl>
        </li>

        <li>
            <dl class="inline-term">
                <dt>Image Overlay URL</dt>
                <dd>
                    If you would like to define a custom video thumbnail image, which will be placed over the standard video interface with a custom play button to create a cleaner and more stylized appearance, then you can choose to do so using the Image Overlay field.
                    This field allows you to either upload your own custom image, or let Power Builder automatically generate one from your video URL. Most of the video providers support this options, such as Youtube and Vimeo. Simply click the "Generate From Video" button and let Power Builder take care of the rest!
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Disable on</dt>
                <dd>
                    This will turn thumbnails on and off.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Admin Label</dt>
                <dd>
                    By default, your video module will appear with a label that reads ‘Video’ in the builder. The Admin Label allows you to change this label for easy identification.
                </dd>
            </dl>
        </li>
	</ul>        