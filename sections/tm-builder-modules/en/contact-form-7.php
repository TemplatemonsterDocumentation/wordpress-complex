<h3>Contact Form 7</h3>
<p>
    Power builder makes it possible to integrate Contact Form 7 on your webpage.
    In the case you don't have a plugin you'll need to install <a href="https://wordpress.org/plugins/contact-form-7/" target="_blank">Contact Form 7</a>.
</p>
<p>
    To integrate it on your page with the help of Power Builder you have to follow these steps:
</p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
                <dt>Open Administration panel</dt>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Find 'Posts' tab</dt>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Choose 'Add new' in the 'Post' menu</dt>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Choose 'Power Builder to edit a newly created post. Choose the number of columns to be displayed</dt>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Click on 'Contact Form 7' module</dt>
            </dl>
        </li>
</ul>


<figure class="img-polaroid">
    <img src="img/builder/contact-form-7.png">
</figure>

<p>
    A <strong> General Settings</strong> form will open offering you to choose a contact form that has been created previously. 
</p>
    
<p>
    Choose one of the contact forms from a drop-down list.
</p>

<figure class="img-polaroid">
    <img src="img/builder/contact-form-7.1.png">
</figure>
 
 <p>
    You can switch to <strong> Custom CSS</strong> to edit contact form manually.
</p>

 <p>
    Save all the progress. You can view your contact form on your webpage now.
</p>